#!/bin/bash
echo "=== Exporting Measure Dust Optical Depth Result ==="

source /workspace/scripts/task_utils.sh

# Take final screenshot as evidence
take_screenshot /tmp/task_final.png || true

# Export results dynamically via Python
python3 << 'PYEOF'
import json
import os
import time

task_start = 0
if os.path.exists("/tmp/task_start_time.txt"):
    with open("/tmp/task_start_time.txt") as f:
        try:
            task_start = int(f.read().strip())
        except:
            pass

out_file = "/home/ga/AstroImages/optical_depth/output/optical_depth_report.txt"
file_exists = os.path.exists(out_file)
file_created = False
content = ""

if file_exists:
    mtime = os.path.getmtime(out_file)
    if mtime >= task_start:
        file_created = True
    # Read the content generated by the agent safely
    with open(out_file, 'r', errors='ignore') as f:
        content = f.read()

app_running = os.system("pgrep -f 'astroimagej\|aij\|AstroImageJ' > /dev/null") == 0

res = {
    "task_start": task_start,
    "task_end": int(time.time()),
    "file_exists": file_exists,
    "file_created_during_task": file_created,
    "file_content": content,
    "app_was_running": app_running
}

with open("/tmp/task_result.json", "w") as f:
    json.dump(res, f)
PYEOF

# Fix permissions
chmod 666 /tmp/task_result.json

echo "=== Export complete ==="