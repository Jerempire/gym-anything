#!/usr/bin/env python3
"""
Verifier for tax_bracket_piecewise_viz task.

Scoring (100 points total):
  - File created during task: 15 pts
  - Piecewise function logic (If commands + Bracket values): 25 pts
  - Marginal rates found: 15 pts
  - Income slider present: 15 pts
  - Text annotations present: 15 pts
  - Axes scaled appropriately: 15 pts

Pass threshold: 70 points
"""

import json
import tempfile
import os
import logging

logger = logging.getLogger(__name__)

PASS_THRESHOLD = 70

def verify_tax_bracket_viz(traj, env_info, task_info):
    """Verify the Tax Bracket Visualization task."""
    
    # Use copy_from_env to retrieve the JSON result generated by the export script
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "copy_from_env unavailable"}

    metadata = task_info.get('metadata', {})
    
    # Retrieve result file
    try:
        tmp = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
        tmp.close()
        try:
            copy_from_env("/tmp/task_result.json", tmp.name)
            with open(tmp.name, 'r', encoding='utf-8') as f:
                result = json.load(f)
        finally:
            try:
                os.unlink(tmp.name)
            except OSError:
                pass
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Error retrieving result: {e}"}

    score = 0
    feedback_parts = []
    subscores = {}

    # Criterion 1: File created during task (15 pts)
    if result.get('file_found', False) and result.get('file_created_during_task', False):
        score += 15
        subscores['file_created'] = True
        feedback_parts.append("File created (+15)")
    else:
        subscores['file_created'] = False
        feedback_parts.append("File not found or old (0/15)")

    # Criterion 2: Piecewise function logic (25 pts)
    # Check for usage of If command AND presence of specific bracket numbers
    num_if = result.get('num_if_commands', 0)
    brackets_found = result.get('bracket_values_found', [])
    required_brackets = [11600, 47150, 100525] # Key boundaries
    
    brackets_match_count = sum(1 for b in required_brackets if b in brackets_found)
    
    if num_if > 0 and brackets_match_count >= 2:
        score += 25
        subscores['piecewise_logic'] = True
        feedback_parts.append(f"Piecewise function logic found ({brackets_match_count}/3 key brackets) (+25)")
    elif num_if > 0:
        score += 10
        subscores['piecewise_logic'] = "partial"
        feedback_parts.append("If() command used but missing key bracket values (11600, 47150) (+10)")
    else:
        subscores['piecewise_logic'] = False
        feedback_parts.append("No If() command found for piecewise function (0/25)")

    # Criterion 3: Marginal rates found (15 pts)
    # Check if rates like 0.12, 0.22 are in the file
    rates_found = result.get('rates_found', [])
    if len(rates_found) >= 2:
        score += 15
        subscores['rates_found'] = True
        feedback_parts.append("Marginal tax rates found (+15)")
    else:
        subscores['rates_found'] = False
        feedback_parts.append("Marginal tax rates (e.g. 0.12, 0.22) not found (0/15)")

    # Criterion 4: Slider present (15 pts)
    if result.get('num_sliders', 0) > 0:
        score += 15
        subscores['slider'] = True
        feedback_parts.append("Slider found (+15)")
    else:
        subscores['slider'] = False
        feedback_parts.append("No numeric slider found (0/15)")

    # Criterion 5: Text annotation (15 pts)
    if result.get('num_texts', 0) > 0:
        score += 15
        subscores['text'] = True
        feedback_parts.append("Text annotation found (+15)")
    else:
        subscores['text'] = False
        feedback_parts.append("No text annotations found (0/15)")
        
    # Criterion 6: Axis Scaling (15 pts)
    # xMax should be large (e.g. > 150,000) for tax brackets
    x_max = result.get('axis_x_max', 0)
    if x_max > 100000:
        score += 15
        subscores['axes'] = True
        feedback_parts.append(f"Axes scaled correctly (xMax: {x_max:.0f}) (+15)")
    elif x_max > 1000:
        score += 5
        subscores['axes'] = "partial"
        feedback_parts.append(f"Axes partially scaled (xMax: {x_max:.0f}) (+5)")
    else:
        subscores['axes'] = False
        feedback_parts.append(f"Axes default/small (xMax: {x_max:.0f}), expected > 100,000 (0/15)")

    passed = score >= PASS_THRESHOLD

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts),
        "subscores": subscores
    }