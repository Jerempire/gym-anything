#!/usr/bin/env python3
"""
Verifier for joint_dynamics_profiling task.

Uses a multi-criteria scoring system based on the data extracted by export_result.sh.
Criteria:
1. CSV exists & is new (15 pts)
2. >= 100 data rows (15 pts)
3. Valid columns present (15 pts)
4. Joint range >= 45° in >= 2 joints (10 pts)
5. Velocity data consistent with positions (10 pts)
6. JSON report exists, new, has all fields (15 pts)
7. JSON cross-consistent with CSV (10 pts)
8. JSON violations internally consistent (10 pts)

Pass Threshold: 70/100
"""

import os
import json
import logging
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_joint_dynamics_profiling(traj, env_info, task_info):
    copy_from_env = env_info.get("copy_from_env")
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Environment copy function not available."}

    # Retrieve the result JSON generated by export_result.sh
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix=".json")
    temp_file.close()
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, "r") as f:
            result = json.load(f)
    except FileNotFoundError:
        return {"passed": False, "score": 0, "feedback": "Result file missing — export script failed to run."}
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback = []
    analysis = result.get("analysis", {})

    # ==========================================
    # Criterion 1: CSV exists & is new (15 pts)
    # ==========================================
    if result.get("csv_exists") and result.get("csv_is_new"):
        score += 15
        feedback.append("CSV file created after task start (+15)")
    elif result.get("csv_exists"):
        feedback.append("CSV file exists but is stale (not created during task)")
    else:
        feedback.append("CSV file not found")

    # ==========================================
    # Criterion 2: >= 100 data rows (15 pts)
    # ==========================================
    row_count = analysis.get("row_count", 0)
    if row_count >= 100:
        score += 15
        feedback.append(f"CSV contains {row_count} data rows (>= 100 required) (+15)")
    elif row_count >= 10:
        score += int(15 * (row_count / 100.0))
        feedback.append(f"CSV contains {row_count} data rows (partial credit)")
    else:
        feedback.append(f"CSV contains only {row_count} data rows (insufficient data)")

    # ==========================================
    # Criterion 3: Valid columns present (15 pts)
    # ==========================================
    if analysis.get("has_req_cols", False):
        score += 15
        feedback.append("CSV contains all 19 required kinematic columns (+15)")
    else:
        feedback.append("CSV is missing required time, position, velocity, or acceleration columns")

    # ==========================================
    # Criterion 4: Motion Range (10 pts)
    # ==========================================
    j_ranges = analysis.get("j_ranges", [])
    joints_over_45 = sum(1 for r in j_ranges if r >= 45.0)
    if joints_over_45 >= 2:
        score += 10
        feedback.append(f"Trajectory motion ranges sufficient ({joints_over_45} joints ≥ 45°) (+10)")
    elif joints_over_45 == 1:
        score += 5
        feedback.append(f"Trajectory motion ranges limited ({joints_over_45} joint ≥ 45°) (+5)")
    else:
        feedback.append("Trajectory motion ranges insufficient (waypoint delta too small)")

    # ==========================================
    # Criterion 5: Velocity Consistency (10 pts)
    # ==========================================
    if analysis.get("vel_consistent", False):
        score += 10
        feedback.append("Reported velocities are physically consistent with position differences (+10)")
    else:
        feedback.append("Velocity values appear fabricated or physically inconsistent with positions")

    # ==========================================
    # Criterion 6: JSON exists, new, has fields (15 pts)
    # ==========================================
    json_data = analysis.get("json_data", {})
    req_fields = [
        "total_samples", "total_waypoints", "trajectory_duration_s",
        "max_velocity_degs", "max_acceleration_degs2", "velocity_violations",
        "acceleration_violations", "compliant"
    ]
    
    if result.get("json_exists") and result.get("json_is_new"):
        has_all_fields = all(f in json_data for f in req_fields)
        if has_all_fields:
            score += 15
            feedback.append("JSON report complete with all required fields (+15)")
        else:
            missing = [f for f in req_fields if f not in json_data]
            score += 5
            feedback.append(f"JSON report exists but missing fields: {', '.join(missing)} (+5)")
    else:
        feedback.append("JSON report not found or is stale")

    # ==========================================
    # Criterion 7: Cross Consistency (10 pts)
    # ==========================================
    # CSV metadata vs JSON reporting
    if result.get("json_exists") and result.get("json_is_new") and result.get("csv_exists") and result.get("csv_is_new"):
        try:
            # Check samples match
            samples_match = abs(int(json_data.get("total_samples", -1)) - row_count) <= 5
            # Check max velocity match
            csv_max_v = analysis.get("max_vel", 0.0)
            rep_max_v = float(json_data.get("max_velocity_degs", -1.0))
            vel_match = abs(csv_max_v - rep_max_v) < max(0.05 * csv_max_v, 1.0)
            
            if samples_match and vel_match:
                score += 10
                feedback.append("JSON report values cross-validated against raw CSV data (+10)")
            else:
                feedback.append(f"JSON report values inconsistent with CSV data (samples: {samples_match}, vel: {vel_match})")
        except Exception as e:
            feedback.append(f"Cross-consistency check failed due to parsing error: {e}")
    else:
        feedback.append("Cross-consistency check skipped (missing required files)")

    # ==========================================
    # Criterion 8: Violations Logic Consistency (10 pts)
    # ==========================================
    if result.get("json_exists") and result.get("json_is_new"):
        try:
            v_viol = int(json_data.get("velocity_violations", 0))
            a_viol = int(json_data.get("acceleration_violations", 0))
            compliant = bool(json_data.get("compliant", False))
            
            logic_correct = False
            if v_viol == 0 and a_viol == 0 and compliant:
                logic_correct = True
            elif (v_viol > 0 or a_viol > 0) and not compliant:
                logic_correct = True
                
            if logic_correct:
                score += 10
                feedback.append("JSON compliance status logically consistent with violations (+10)")
            else:
                feedback.append("JSON compliance status contradicts reported violations")
        except Exception as e:
            feedback.append(f"Violations logic check failed: {e}")
    else:
        feedback.append("Violations logic check skipped (missing JSON)")

    # Final Evaluation
    passed = score >= 70

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }