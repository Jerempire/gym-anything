#!/usr/bin/env python3
import json
import logging
import os
import tempfile
import re

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

TASK_NAME = "configure_local_onion_service"

def verify_configure_local_onion_service(traj, env_info, task_info):
    """
    Verify the configuration of the local onion service.
    
    The generation of a valid 56-character v3 `.onion` address by Tor daemon 
    is the ultimate cryptographic proof that the user successfully applied 
    the directory permissions and `torrc` modifications.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "copy_from_env unavailable"}

    tmp = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    tmp.close()
    try:
        try:
            copy_from_env(f"/tmp/{TASK_NAME}_result.json", tmp.name)
            with open(tmp.name, 'r', encoding='utf-8-sig') as f:
                result = json.load(f)
        except Exception as e:
            logger.error(f"Failed to read result: {e}")
            return {"passed": False, "score": 0, "feedback": f"Result file not found: {e}"}
    finally:
        if os.path.exists(tmp.name):
            os.unlink(tmp.name)

    logger.info(f"Result: {json.dumps(result, indent=2)}")

    score = 0
    feedback_parts = []
    
    # Check torrc Configured (20 pts)
    has_dir = result.get('has_hidden_service_dir', False)
    has_port = result.get('has_hidden_service_port', False)
    if has_dir and has_port:
        score += 20
        feedback_parts.append("torrc Configured (20/20)")
    elif has_dir or has_port:
        score += 10
        feedback_parts.append("torrc partially configured (10/20)")
    else:
        feedback_parts.append("torrc NOT configured (0/20)")

    # Check Directory Permissions (10 pts)
    dir_exists = result.get('dir_exists', False)
    dir_perms = result.get('dir_perms', "")
    if dir_exists and dir_perms == "700":
        score += 10
        feedback_parts.append("Directory exists with 700 permissions (10/10)")
    elif dir_exists:
        feedback_parts.append(f"Directory exists but wrong permissions: {dir_perms} (0/10)")
    else:
        feedback_parts.append("Directory does not exist (0/10)")

    # Check Hostname Generated (30 pts) [Required Gate]
    hostname_exists = result.get('hostname_exists', False)
    onion_address = result.get('onion_address', "")
    valid_onion = False
    
    # v3 onion addresses are exactly 56 base32 characters + .onion
    if hostname_exists and re.match(r'^[a-z2-7]{56}\.onion$', onion_address):
        valid_onion = True
        score += 30
        feedback_parts.append("Valid v3 onion hostname generated by Tor daemon (30/30)")
    else:
        feedback_parts.append("Valid v3 onion hostname NOT generated (0/30)")

    # Check Visited in Browser (20 pts)
    onion_visited = result.get('onion_visited', False)
    if onion_visited:
        score += 20
        feedback_parts.append("Onion address visited in browser (20/20)")
    else:
        feedback_parts.append("Onion address NOT visited in browser (0/20)")

    # Check Bookmarked Correctly (20 pts)
    onion_bookmarked = result.get('onion_bookmarked', False)
    bookmark_title = result.get('bookmark_title', "")
    expected_title = task_info.get('metadata', {}).get('expected_bookmark_title', 'Local Drop Site Test')
    
    if onion_bookmarked and bookmark_title == expected_title:
        score += 20
        feedback_parts.append("Onion address bookmarked with correct title (20/20)")
    elif onion_bookmarked:
        score += 10
        feedback_parts.append(f"Onion address bookmarked but wrong title: {bookmark_title} (10/20)")
    else:
        feedback_parts.append("Onion address NOT bookmarked (0/20)")

    # Gate verification: passing requires 60+ points AND the valid dynamic onion hostname must be generated.
    passed = (score >= 60) and valid_onion

    return {
        "passed": passed,
        "score": min(score, 100),
        "feedback": " | ".join(feedback_parts)
    }