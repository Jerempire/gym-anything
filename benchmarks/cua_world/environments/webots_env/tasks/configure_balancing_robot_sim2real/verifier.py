#!/usr/bin/env python3
"""
Verifier for configure_balancing_robot_sim2real task.

A control systems engineer calibrates a Segway robot in Webots to match hardware specs.
Checks:
  - File exists and was created during task: 10 points
  - Main chassis mass == 45.5: 20 points
  - centerOfMass == [0, 0, 0.15]: 25 points
  - Encoders resolution == 0.001534: 25 points
  - Gyro noise == 0.005: 20 points

Pass threshold: 70 points AND must have centerOfMass and resolution configured.
"""

import json
import re
import tempfile
import os
import logging

logger = logging.getLogger(__name__)

def verify_configure_balancing_robot_sim2real(traj, env_info, task_info):
    """
    Parse the exported .wbt file to ensure all 5 Sim2Real constraints are met.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function unavailable"}

    metadata = task_info.get('metadata', {})
    output_path = metadata.get('output_path', '/home/ga/Desktop/segway_calibrated.wbt')
    
    expected_mass = str(metadata.get('expected_mass', 45.5))
    expected_resolution = str(metadata.get('expected_resolution', 0.001534))
    expected_noise = str(metadata.get('expected_noise', 0.005))
    # Coordinates in WBT might have varying spaces, e.g., "0 0 0.15"
    
    score = 0
    feedback_parts = []
    
    # 1. Read JSON result generated by export_result.sh
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    result_data = {}
    try:
        copy_from_env('/tmp/task_result.json', temp_json.name)
        with open(temp_json.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        logger.warning(f"Could not read result json: {e}")
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    file_exists = result_data.get('file_exists', False)
    file_created = result_data.get('file_created_during_task', False)

    if not file_exists:
        return {
            "passed": False,
            "score": 0,
            "feedback": f"Output file not found at {output_path}. You must save the world.",
            "details": {"file_exists": False}
        }

    # 2. Copy and read the .wbt file
    wbt_file = tempfile.NamedTemporaryFile(delete=False, suffix='.wbt')
    wbt_content = None
    try:
        copy_from_env(output_path, wbt_file.name)
        with open(wbt_file.name, 'r', errors='replace') as f:
            wbt_content = f.read()
    except Exception as e:
        logger.warning(f"Could not copy .wbt file: {e}")
    finally:
        if os.path.exists(wbt_file.name):
            os.unlink(wbt_file.name)

    if not wbt_content or len(wbt_content) < 100:
        return {"passed": False, "score": 0, "feedback": "Saved world file is empty or corrupted."}

    # Award points for saving valid file
    score += 10
    if not file_created:
        feedback_parts.append("File existed before task, but checking contents anyway.")
    else:
        feedback_parts.append("Correctly saved new world file.")

    # Find the main SEGWAY_ROBOT segment to avoid matching wheel physics
    robot_start = wbt_content.find('DEF SEGWAY_ROBOT Robot')
    if robot_start == -1:
        # Fallback if renamed
        robot_start = 0
    
    robot_segment = wbt_content[robot_start:]
    
    # 3. Check Mass (20 pts)
    # The main physics node is typically at the end of the robot definition
    # Look for a physics Physics node that contains mass 45.5
    has_com = False
    has_res = False

    mass_match = re.search(r'physics\s+Physics\s*\{[^}]*?mass\s+([\d.]+)', robot_segment, re.DOTALL)
    if mass_match:
        actual_mass = mass_match.group(1)
        if actual_mass == expected_mass:
            score += 20
            feedback_parts.append("Chassis mass correctly set to 45.5")
        else:
            feedback_parts.append(f"Chassis mass is {actual_mass}, expected 45.5")
    else:
        # Fallback check
        if re.search(r'mass\s+' + re.escape(expected_mass), robot_segment):
            score += 20
            feedback_parts.append("Chassis mass set to 45.5")
        else:
            feedback_parts.append("Chassis mass of 45.5 not found")

    # 4. Check Center of Mass (25 pts)
    # Match centerOfMass 0 0 0.15 or centerOfMass [ 0 0 0.15 ]
    com_match = re.search(r'centerOfMass\s+(?:\[\s*)?([\d.-]+)\s+([\d.-]+)\s+([\d.-]+)(?:\s*\])?', robot_segment)
    if com_match:
        x, y, z = com_match.group(1), com_match.group(2), com_match.group(3)
        if float(x) == 0.0 and float(y) == 0.0 and float(z) == 0.15:
            score += 25
            has_com = True
            feedback_parts.append("centerOfMass correctly set to [0, 0, 0.15]")
        else:
            feedback_parts.append(f"centerOfMass is [{x}, {y}, {z}], expected [0, 0, 0.15]")
    else:
        feedback_parts.append("centerOfMass field not found")

    # 5. Check Encoder Resolution (25 pts)
    # The world has 2 PositionSensors. Count how many have resolution 0.001534
    res_matches = re.findall(r'resolution\s+([\d.]+)', robot_segment)
    correct_res_count = sum(1 for val in res_matches if float(val) == 0.001534)
    
    if correct_res_count >= 2:
        score += 25
        has_res = True
        feedback_parts.append("Both encoder resolutions correctly set to 0.001534")
    elif correct_res_count == 1:
        score += 12
        feedback_parts.append("Only one encoder resolution set to 0.001534 (missed one wheel)")
    else:
        feedback_parts.append("Encoder resolutions of 0.001534 not found")

    # 6. Check Gyro Noise (20 pts)
    # Check within the Gyro node or globally if unique
    gyro_noise_match = re.search(r'noise\s+([\d.]+)', robot_segment)
    if gyro_noise_match:
        actual_noise = gyro_noise_match.group(1)
        if float(actual_noise) == 0.005:
            score += 20
            feedback_parts.append("Gyro noise correctly set to 0.005")
        else:
            feedback_parts.append(f"Gyro noise is {actual_noise}, expected 0.005")
    else:
        feedback_parts.append("Gyro noise field not found")

    # Threshold Check
    passed = (score >= 70) and has_com and has_res

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }