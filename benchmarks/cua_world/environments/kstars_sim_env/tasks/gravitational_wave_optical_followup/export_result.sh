#!/bin/bash
echo "=== Exporting gravitational_wave_optical_followup results ==="

source /workspace/scripts/task_utils.sh

take_screenshot /tmp/task_final.png

FINAL_RA=$(indi_getprop -1 "Telescope Simulator.EQUATORIAL_EOD_COORD.RA" 2>/dev/null | tr -cd '0-9.' | head -c 10)
FINAL_DEC=$(indi_getprop -1 "Telescope Simulator.EQUATORIAL_EOD_COORD.DEC" 2>/dev/null | sed 's/[^0-9.\-]//g' | head -c 10)
if [ -z "$FINAL_RA" ]; then FINAL_RA="-1"; fi
if [ -z "$FINAL_DEC" ]; then FINAL_DEC="-999"; fi

TASK_START=$(cat /tmp/task_start_time.txt 2>/dev/null || echo "0")
BASE_DIR="/home/ga/Images/kilonova_search"

# We use Python to analyze the actual FITS files generated by the agent.
# This calculates the true RMS of the R-band images to compare against the agent's report.
FITS_INFO=$(python3 -c "
import os, json, glob
import numpy as np

files = []
base = '$BASE_DIR'
task_start = $TASK_START

try:
    from astropy.io import fits as pyfits
except ImportError:
    pyfits = None

for root, _, filenames in os.walk(base):
    for filename in filenames:
        if filename.lower().endswith('.fits') or filename.lower().endswith('.fit'):
            fpath = os.path.join(root, filename)
            try:
                stat = os.stat(fpath)
                mtime = stat.st_mtime
                size = stat.st_size
                
                # Default info
                filt = ''
                exptime = -1
                rms = -1.0
                
                if pyfits and mtime > task_start and size > 2048:
                    try:
                        with pyfits.open(fpath) as hdul:
                            h = hdul[0].header
                            filt = str(h.get('FILTER', '')).strip().upper()
                            exptime = float(h.get('EXPTIME', -1))
                            data = hdul[0].data
                            if data is not None and ('R' in filt or filt == '4'):
                                rms = float(np.std(data))
                    except Exception:
                        pass
                        
                files.append({
                    'name': filename,
                    'dir': os.path.basename(root),
                    'filter': filt,
                    'exptime': exptime,
                    'size': size,
                    'mtime': mtime,
                    'true_rms': rms
                })
            except Exception:
                pass

print(json.dumps(files))
" 2>/dev/null || echo "[]")

REPORT_PATH="/home/ga/Documents/gcn_response.txt"
REPORT_EXISTS="false"
REPORT_MTIME=0
REPORT_B64=""

if [ -f "$REPORT_PATH" ]; then
    REPORT_EXISTS="true"
    REPORT_MTIME=$(stat -c %Y "$REPORT_PATH" 2>/dev/null || echo "0")
    REPORT_B64=$(cat "$REPORT_PATH" | base64 -w 0 2>/dev/null || echo "")
fi

REPORT_EXISTS_PY=$([ "$REPORT_EXISTS" = "true" ] && echo "True" || echo "False")

python3 - << PYEOF
import json

result = {
    "task_start": $TASK_START,
    "timestamp": $(date +%s),
    "final_ra": "$FINAL_RA",
    "final_dec": "$FINAL_DEC",
    "fits_files": $FITS_INFO,
    "report_exists": $REPORT_EXISTS_PY,
    "report_mtime": $REPORT_MTIME,
    "report_b64": "$REPORT_B64"
}

with open('/tmp/task_result.json', 'w') as f:
    json.dump(result, f, indent=2)
print("Result written to /tmp/task_result.json")
PYEOF

echo "=== Export complete ==="