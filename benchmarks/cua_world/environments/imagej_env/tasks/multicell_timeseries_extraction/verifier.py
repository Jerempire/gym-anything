#!/usr/bin/env python3
"""
Verifier for multicell_timeseries_extraction task.

Verification Criteria:
1. Files (CSV and ROI zip) must exist and be created during the task.
2. ROI zip must contain at least 3 ROIs.
3. CSV must contain time-series data:
   - Rows approx 51 (matching Mitosis frames).
   - Columns match ROI count.
   - Values must be numeric and varying (real data).
"""

import json
import tempfile
import os
import logging
from typing import Dict, Any

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_multicell_timeseries(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]) -> Dict[str, Any]:
    """
    Verify the output of the time-series extraction task.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}

    # Retrieve the summary JSON generated by export_result.sh
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/multicell_timeseries_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": f"Failed to retrieve task results: {str(e)}. Did you save the files to the correct location?"
        }
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    # Initialize scoring
    score = 0
    feedback = []
    
    # Check 1: Files created (max 20 pts)
    if result_data.get("csv_exists") and result_data.get("csv_created_after_start"):
        score += 10
        feedback.append("CSV file created successfully.")
    else:
        feedback.append("CSV file missing or pre-existing.")

    if result_data.get("rois_exists") and result_data.get("rois_created_after_start"):
        score += 10
        feedback.append("ROI file created successfully.")
    else:
        feedback.append("ROI file missing or pre-existing.")

    # Check 2: ROI Count (max 20 pts)
    roi_count = result_data.get("roi_count", 0)
    if roi_count >= 3:
        score += 20
        feedback.append(f"ROI count sufficient: {roi_count} ROIs.")
    else:
        feedback.append(f"ROI count insufficient: {roi_count} (Found) vs 3 (Required).")

    # Check 3: Time Series Integrity (max 30 pts)
    # Mitosis sample has 51 frames. 
    # ImageJ results table usually has header + data.
    # Rows should be close to 51 (sometimes 52 with header, or exactly 51 depending on export).
    # Allow range 45-60 to account for off-by-one or extra header/summary rows.
    row_count = result_data.get("row_count", 0)
    if 45 <= row_count <= 60:
        score += 30
        feedback.append(f"Time-series length correct ({row_count} rows).")
    else:
        feedback.append(f"Time-series length incorrect. Expected ~51 rows for 51 frames, got {row_count}. Did you use Multi-Measure on the stack?")

    # Check 4: Data Validity (max 30 pts)
    # Check columns. If 3 ROIs, we expect at least 3 data columns + Label column.
    col_count = result_data.get("col_count", 0)
    if col_count >= roi_count: 
        score += 10
        feedback.append("Column count matches or exceeds ROI count.")
    else:
        feedback.append(f"Column count ({col_count}) seems low for {roi_count} ROIs.")
        
    if result_data.get("data_looks_numeric") and result_data.get("data_varies"):
        score += 20
        feedback.append("Data appears to be valid numeric time-series measurements.")
    else:
        feedback.append("Data validation failed: content appears non-numeric or static (variance=0).")

    passed = (score >= 70)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }