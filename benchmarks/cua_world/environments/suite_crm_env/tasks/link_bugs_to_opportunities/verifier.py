#!/usr/bin/env python3
"""
Verifier for link_bugs_to_opportunities task.

Verification Criteria:
1. Relationship Metadata: Many-to-Many metadata injected into 'relationships' table.
2. Junction Table: The actual '_c' junction table physically exists in DB.
3. Record Linkage: Target bug ID and opportunity ID are paired in a row within the junction table.
4. VLM Trajectory: Agent workflow visual validation (Studio usage, Subpanel linking).
5. Anti-gaming: Relationship count must have increased since task start.
"""

import json
import os
import tempfile
import logging

try:
    from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm
    VLM_AVAILABLE = True
except ImportError:
    VLM_AVAILABLE = False

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_link_bugs(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Retrieve the results
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result JSON: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    score = 0
    feedback_parts = []
    
    metadata_exists = result.get('relationship_metadata_exists', False)
    table_exists = result.get('junction_table_exists', False)
    records_linked = result.get('records_linked', False)
    initial_count = result.get('initial_relationship_count', 0)
    current_count = result.get('current_relationship_count', 0)
    rel_type = result.get('relationship_type', '')

    # Criterion 1: Check Relationship Metadata & Anti-Gaming (30 points)
    if metadata_exists and "many-to-many" in rel_type.lower():
        if current_count > initial_count:
            score += 30
            feedback_parts.append("Many-to-Many relationship created correctly in metadata")
        else:
            feedback_parts.append("WARNING: Relationship found but count did not increase (possible cheating)")
    else:
        feedback_parts.append("Many-to-Many relationship metadata not found")

    # Criterion 2: Physical Junction Table Verification (30 points)
    if table_exists:
        score += 30
        feedback_parts.append(f"Physical junction table '{result.get('junction_table_name')}' generated by Studio")
    elif metadata_exists:
        feedback_parts.append("Metadata exists, but junction table failed to deploy (did agent click 'Save & Deploy'?)")

    # Criterion 3: Record Linkage (25 points)
    # The ultimate test: are the specific records physically linked?
    if records_linked:
        score += 25
        feedback_parts.append("Target Bug and Opportunity records successfully linked")
    elif table_exists:
        feedback_parts.append("Junction table ready, but records were not linked in the UI")

    # Criterion 4: VLM Visual Verification (15 points)
    vlm_passed = False
    if VLM_AVAILABLE and traj:
        try:
            frames = sample_trajectory_frames(traj, n=5)
            final = get_final_screenshot(traj)
            images = frames + [final] if final else frames
            
            prompt = (
                "You are evaluating a CRM administration task in SuiteCRM. "
                "The agent's goal was to 1) Use the 'Studio' admin tool to create a relationship, "
                "and 2) Navigate to an Opportunity and use a subpanel to link a Bug.\n"
                "Review the images and answer:\n"
                "1. Is the SuiteCRM Admin/Studio interface visible in any frame?\n"
                "2. Is the detail view of an Opportunity with a 'Bugs' subpanel or selection popup visible?\n"
                "Respond strictly with JSON format: {\"studio_used\": true/false, \"subpanel_used\": true/false}"
            )
            
            vlm_response = query_vlm(images=images, prompt=prompt)
            if vlm_response and vlm_response.get("parsed"):
                parsed = vlm_response["parsed"]
                if parsed.get("studio_used") and parsed.get("subpanel_used"):
                    score += 15
                    vlm_passed = True
                    feedback_parts.append("VLM visual verification passed")
                else:
                    feedback_parts.append("VLM found missing visual steps (Studio or Subpanel not seen)")
        except Exception as e:
            logger.warning(f"VLM verification error: {e}")
            # Do not punish heavily for VLM system failure if DB is perfect
            if metadata_exists and table_exists and records_linked:
                score += 15
                vlm_passed = True
                feedback_parts.append("VLM skipped but database state is perfect")

    # Final Evaluation
    key_criteria_met = (metadata_exists and table_exists and records_linked)
    passed = (score >= 85 and key_criteria_met)

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }