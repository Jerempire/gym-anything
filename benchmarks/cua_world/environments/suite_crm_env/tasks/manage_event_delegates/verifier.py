#!/usr/bin/env python3
import os
import json
import tempfile
import logging

from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_manage_event_delegates(traj, env_info, task_info):
    """
    Verify that the event was created, fields were correct, and existing contacts 
    were linked via many-to-many relationship (rather than creating duplicates).
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    expected_location = metadata.get('expected_location', 'The Capital Grille, Chicago')
    expected_budget = metadata.get('expected_budget', '12500')
    
    # 1. Read JSON result generated by export_result.sh
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/manage_event_delegates_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 2. Verify Event Creation and Anti-Gaming (Timestamp check)
    event_found = result.get('event_found', False)
    event_created_time = result.get('event_created_time', 0)
    task_start = result.get('task_start', 0)
    
    if not event_found:
        return {
            "passed": False,
            "score": 0,
            "feedback": "Event 'VIP Executive Dinner 2026' was not found in the database."
        }
        
    if event_created_time > 0 and event_created_time < task_start:
        return {
            "passed": False,
            "score": 0,
            "feedback": "Event existed before task started (anti-gaming violation)."
        }
    
    score += 20
    feedback_parts.append("Event created")
    
    # 3. Verify Details (Location & Budget)
    location = result.get('location', '')
    budget = result.get('budget', '')
    
    details_ok = True
    if 'capital grille' in location.lower():
        score += 10
    else:
        details_ok = False
        feedback_parts.append(f"Location mismatch: got '{location}'")
        
    try:
        budget_float = float(budget.replace(',', ''))
        expected_budget_float = float(expected_budget.replace(',', ''))
        if abs(budget_float - expected_budget_float) < 1.0:
            score += 10
        else:
            details_ok = False
            feedback_parts.append(f"Budget mismatch: got '{budget}'")
    except ValueError:
        details_ok = False
        feedback_parts.append(f"Budget mismatch: got '{budget}'")
        
    if details_ok:
        feedback_parts.append("Event details correct")
        
    # 4. Verify Linked Contacts (Many-to-Many Relationship)
    c1_linked = result.get('c1_linked', False)
    c2_linked = result.get('c2_linked', False)
    c3_linked = result.get('c3_linked', False)
    duplicates_created = result.get('duplicates_created', False)
    
    if duplicates_created:
        feedback_parts.append("WARNING: Agent created duplicate contacts instead of linking existing ones")
        # Do not award points for improperly linked duplicates
    else:
        links = 0
        if c1_linked:
            score += 20
            links += 1
        if c2_linked:
            score += 20
            links += 1
        if c3_linked:
            score += 20
            links += 1
            
        feedback_parts.append(f"Contacts linked correctly: {links}/3")
        
    # 5. Supplementary VLM check for trajectory behavior
    try:
        frames = sample_trajectory_frames(traj, n=4)
        final = get_final_screenshot(traj)
        if frames and final:
            vlm_res = query_vlm(
                images=frames + [final],
                prompt="Did the agent interact with the SuiteCRM interface to select/link Contacts for an Event? Answer ONLY with JSON: {'workflow_visible': true/false}"
            )
            if vlm_res and getattr(vlm_res, 'get', lambda x, y: y)('parsed', {}).get('workflow_visible', False):
                feedback_parts.append("VLM verified visual trajectory")
    except Exception as e:
        logger.warning(f"VLM verification skipped or failed: {e}")
    
    # Pass condition: Event exists and at least two contacts are correctly linked (80 points)
    passed = score >= 80 
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }