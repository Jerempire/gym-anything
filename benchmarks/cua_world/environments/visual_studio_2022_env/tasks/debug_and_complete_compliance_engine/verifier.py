"""
Verifier for debug_and_complete_compliance_engine task.

Scoring (100 points):
  - All 12 tests pass:                                    20 pts
  - Bug 1 fixed (severity labels corrected):              10 pts
  - Bug 2 fixed (date subtraction direction):             10 pts
  - Bug 3 fixed (integer division):                       10 pts
  - Stub 1 implemented (Wilson score interval):           12 pts
  - Stub 2 implemented (expedited reporting flags):       10 pts
  - Compliance report generated by running app:           10 pts
  - Build Release 0 errors:                               10 pts
  - Debug report exists and documents bugs:                8 pts

Pass threshold: 60 points
Build gate: if build_errors > 0, score capped at 40
"""

import json
import os
import re
import shutil
import tempfile
import logging

logger = logging.getLogger(__name__)

RESULT_PATH      = "C:\\Users\\Docker\\debug_and_complete_compliance_engine_result.json"
REPORT_GEN_PATH  = "C:\\Users\\Docker\\source\\repos\\ComplianceReporter\\src\\ComplianceReporter.Engine\\ReportGenerator.cs"
STATS_PATH       = "C:\\Users\\Docker\\source\\repos\\ComplianceReporter\\src\\ComplianceReporter.Engine\\StatisticsHelper.cs"
REPORT_AGG_PATH  = "C:\\Users\\Docker\\source\\repos\\ComplianceReporter\\src\\ComplianceReporter.Engine\\ReportAggregator.cs"
COMPLIANCE_REPORT = "C:\\Users\\Docker\\Documents\\compliance_report.txt"
DEBUG_REPORT     = "C:\\Users\\Docker\\Documents\\debug_report.txt"


def _has(pattern, text, flags=re.IGNORECASE | re.DOTALL):
    return bool(re.search(pattern, text, flags))


def verify_debug_and_complete_compliance_engine(traj, env_info, task_info):
    copy_from_env = env_info.get("copy_from_env")
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "copy_from_env unavailable"}

    tmp = tempfile.mkdtemp(prefix="verify_compliance_")
    try:
        # --- Step 1: Read export result JSON ---
        result = {}
        json_local = os.path.join(tmp, "result.json")
        try:
            copy_from_env(RESULT_PATH, json_local)
            with open(json_local, encoding="utf-8-sig") as f:
                result = json.load(f)
        except FileNotFoundError:
            return {"passed": False, "score": 0,
                    "feedback": "Result JSON not found — export script may not have run"}
        except Exception as e:
            return {"passed": False, "score": 0, "feedback": f"Cannot read result JSON: {e}"}

        # --- Anti-gaming gate ---
        if not result.get("any_file_modified", False):
            return {"passed": False, "score": 0,
                    "feedback": "No Engine source files were modified — no work detected"}

        # --- Step 2: Independently read source files ---
        def _read_remote(remote, name):
            local = os.path.join(tmp, name)
            try:
                copy_from_env(remote, local)
                with open(local, encoding="utf-8-sig") as f:
                    return f.read()
            except Exception:
                return ""

        report_gen_src = _read_remote(REPORT_GEN_PATH, "ReportGenerator.cs")
        stats_src      = _read_remote(STATS_PATH, "StatisticsHelper.cs")
        report_agg_src = _read_remote(REPORT_AGG_PATH, "ReportAggregator.cs")

        score = 0
        fb    = []

        # ── Criterion 1: All 12 tests pass (20 pts) ──────────────────────────
        all_pass     = result.get("all_tests_passed", False)
        tests_passed = result.get("tests_passed", 0)
        tests_failed = result.get("tests_failed", 0)

        if all_pass and tests_passed >= 12:
            score += 20
            fb.append(f"All {tests_passed} tests pass (+20)")
        elif tests_passed >= 10:
            score += 12
            fb.append(f"{tests_passed}/12 tests pass, {tests_failed} failing (+12)")
        elif tests_passed >= 7:
            score += 6
            fb.append(f"{tests_passed}/12 tests pass, {tests_failed} failing (+6)")
        else:
            fb.append(f"Only {tests_passed}/12 tests pass, {tests_failed} failing (0/20)")

        # ── Criterion 2: Bug 1 — Severity labels fixed (10 pts) ──────────────
        if report_gen_src:
            g3_match = re.search(r'ctcaeGrade\s*>=\s*3\)\s*return\s*"(\w[\w-]*)"', report_gen_src)
            g2_match = re.search(r'ctcaeGrade\s*>=\s*2\)\s*return\s*"(\w[\w-]*)"', report_gen_src)
            if g3_match and g2_match:
                g3_label = g3_match.group(1)
                g2_label = g2_match.group(1)
                if g3_label == "Severe" and g2_label == "Moderate":
                    score += 10
                    fb.append("Bug 1: Severity labels corrected (Grade 3=Severe, Grade 2=Moderate) (+10)")
                elif g3_label != "Moderate" or g2_label != "Severe":
                    score += 5
                    fb.append("Bug 1: Severity labels changed but not standard CTCAE mapping (+5)")
                else:
                    fb.append("Bug 1: Severity labels still swapped (Grade 3=Moderate) (0/10)")
            else:
                # Method restructured — check via export result
                if result.get("severity_bug_fixed", False):
                    score += 10
                    fb.append("Bug 1: Severity classification fixed (export confirms) (+10)")
                else:
                    fb.append("Bug 1: Cannot determine severity fix status (0/10)")
        else:
            if result.get("severity_bug_fixed", False):
                score += 10
                fb.append("Bug 1: Severity fixed (export confirms) (+10)")
            else:
                fb.append("Bug 1: Severity labels still swapped (0/10)")

        # ── Criterion 3: Bug 2 — Date subtraction fixed (10 pts) ─────────────
        if report_gen_src:
            date_fixed = _has(r"eventDate\s*-\s*enrollmentDate", report_gen_src)
            date_buggy = _has(r"enrollmentDate\s*-\s*eventDate", report_gen_src)
            if date_fixed and not date_buggy:
                score += 10
                fb.append("Bug 2: Date subtraction corrected (eventDate - enrollmentDate) (+10)")
            elif not date_buggy:
                score += 5
                fb.append("Bug 2: Original reversed subtraction removed but new pattern unclear (+5)")
            else:
                fb.append("Bug 2: Date subtraction still reversed (enrollmentDate - eventDate) (0/10)")
        else:
            if result.get("date_bug_fixed", False):
                score += 10
                fb.append("Bug 2: Date fixed (export confirms) (+10)")
            else:
                fb.append("Bug 2: Date subtraction still reversed (0/10)")

        # ── Criterion 4: Bug 3 — Integer division fixed (10 pts) ─────────────
        if stats_src:
            has_cast = _has(r"\(double\)\s*eventCount|\(double\)\s*totalPatients", stats_src)
            has_multiply = _has(r"1\.0\s*\*\s*eventCount|eventCount\s*\*\s*1\.0", stats_src)
            has_convert = _has(r"Convert\.ToDouble|\.0\s*/", stats_src)
            if has_cast or has_multiply or has_convert:
                score += 10
                fb.append("Bug 3: Integer division fixed with explicit cast/conversion (+10)")
            elif not _has(r"return\s+eventCount\s*/\s*totalPatients\s*;", stats_src):
                score += 5
                fb.append("Bug 3: Division expression changed but cast pattern unclear (+5)")
            else:
                fb.append("Bug 3: Integer division still present (eventCount / totalPatients) (0/10)")
        else:
            if result.get("int_div_bug_fixed", False):
                score += 10
                fb.append("Bug 3: Integer division fixed (export confirms) (+10)")
            else:
                fb.append("Bug 3: Integer division still present (0/10)")

        # ── Criterion 5: Stub 1 — Wilson score interval implemented (12 pts) ─
        if report_agg_src:
            wilson_has_nie = _has(
                r"CalculateWilsonScoreInterval[\s\S]{0,400}NotImplementedException",
                report_agg_src
            )
            wilson_has_sqrt = _has(r"Math\.Sqrt", report_agg_src)
            wilson_has_z_sq = _has(r"zScore\s*\*\s*zScore|z\s*\*\s*z|Math\.Pow.*zScore", report_agg_src)

            if not wilson_has_nie and wilson_has_sqrt:
                score += 12
                fb.append("Stub 1: Wilson score interval implemented with Math.Sqrt (+12)")
            elif not wilson_has_nie:
                score += 6
                fb.append("Stub 1: Wilson NotImplementedException removed but Math.Sqrt missing (+6)")
            else:
                fb.append("Stub 1: Wilson score interval still throws NotImplementedException (0/12)")
        else:
            if result.get("wilson_stub_removed", False):
                score += 12 if result.get("wilson_has_sqrt", False) else 6
                fb.append(f"Stub 1: Wilson implemented (export confirms) (+{12 if result.get('wilson_has_sqrt', False) else 6})")
            else:
                fb.append("Stub 1: Wilson still not implemented (0/12)")

        # ── Criterion 6: Stub 2 — Expedited flags implemented (10 pts) ───────
        if report_agg_src:
            flags_has_nie = _has(
                r"DetectExpeditedReportingFlags[\s\S]{0,400}NotImplementedException",
                report_agg_src
            )
            flags_has_grade = _has(r"CtcaeGrade\s*>=\s*4|Grade\s*>=\s*4", report_agg_src)
            flags_has_outcome = _has(r'"Death"|"Life-Threatening"', report_agg_src)
            flags_has_onset = _has(r"15|Days\s*<=", report_agg_src)

            if not flags_has_nie and flags_has_grade and flags_has_outcome and flags_has_onset:
                score += 10
                fb.append("Stub 2: Expedited flags implemented with grade + outcome + onset filtering (+10)")
            elif not flags_has_nie:
                score += 5
                fb.append("Stub 2: NotImplementedException removed but filter logic unclear (+5)")
            else:
                fb.append("Stub 2: Expedited flags still throws NotImplementedException (0/10)")
        else:
            if result.get("flags_stub_removed", False):
                score += 10 if result.get("flags_has_filter", False) else 5
                fb.append(f"Stub 2: Flags implemented (export confirms) (+{10 if result.get('flags_has_filter', False) else 5})")
            else:
                fb.append("Stub 2: Flags still not implemented (0/10)")

        # ── Criterion 7: Compliance report generated (10 pts) ─────────────────
        report_exists = result.get("compliance_report_exists", False)
        report_has_content = (
            result.get("report_has_severity", False) or
            result.get("report_has_expedited", False)
        )
        if report_exists and report_has_content:
            score += 10
            fb.append("Compliance report generated with expected sections (+10)")
        elif report_exists:
            score += 5
            fb.append("Compliance report file exists but missing expected sections (+5)")
        else:
            fb.append("Compliance report not generated (agent may not have run the app) (0/10)")

        # ── Criterion 8: Build gate (10 pts) ─────────────────────────────────
        build_success = result.get("build_success", False)
        build_errors  = result.get("build_errors", 999)

        if build_success and build_errors == 0:
            score += 10
            fb.append("Build Release: OK — 0 errors (+10)")
        else:
            if score > 40:
                score = 40
                fb.append(f"BUILD FAILED ({build_errors} errors) — score capped at 40")
            else:
                fb.append(f"BUILD FAILED ({build_errors} errors)")

        # ── Criterion 9: Debug report (8 pts) ─────────────────────────────────
        debug_exists = result.get("debug_report_exists", False)
        if debug_exists:
            mentions = sum([
                result.get("debug_mentions_severity", False),
                result.get("debug_mentions_date", False),
                result.get("debug_mentions_division", False),
            ])
            if mentions >= 2:
                score += 8
                fb.append(f"Debug report documents {mentions}/3 bug categories (+8)")
            elif mentions >= 1:
                score += 4
                fb.append(f"Debug report documents {mentions}/3 bug categories (+4)")
            else:
                score += 2
                fb.append("Debug report exists but doesn't describe specific bugs (+2)")
        else:
            fb.append("No debug report written (0/8)")

        passed = score >= 60
        return {
            "passed": passed,
            "score": min(score, 100),
            "feedback": " | ".join(fb)
        }

    except Exception as e:
        logger.exception("Verification error")
        return {"passed": False, "score": 0, "feedback": f"Verification error: {e}"}
    finally:
        shutil.rmtree(tmp, ignore_errors=True)
