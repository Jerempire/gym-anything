#!/usr/bin/env python3
"""
Verifier for schedule_meeting_from_request task.
Evaluates if the agent correctly parsed the text file and scheduled the meeting
with the dynamic date and parameters.
"""

import json
import logging
import os
import tempfile
from datetime import datetime

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_schedule_meeting_from_request(traj, env_info, task_info):
    """
    Verify the meeting was scheduled correctly.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve the result file generated by export_result.sh
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result file: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # Initialize scoring
    score = 0
    feedback_parts = []
    
    # 1. Event Existence (20 pts)
    if not result.get("event_found"):
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "No event named 'Systems Infrastructure Check' found in the calendar."
        }
    
    score += 20
    feedback_parts.append("Event created with correct title")
    
    actual = result.get("event_details", {})
    expected = result.get("expected_details", {})
    
    # 2. Date and Time Verification (35 pts total)
    # Expected start format: "YYYY-MM-DD HH:MM:SS"
    exp_start_str = f"{expected['date']} {expected['start_time']}"
    act_start_str = actual.get('start', "")
    
    date_correct = False
    time_correct = False
    
    if act_start_str:
        try:
            exp_dt = datetime.strptime(exp_start_str, "%Y-%m-%d %H:%M:%S")
            act_dt = datetime.strptime(act_start_str, "%Y-%m-%d %H:%M:%S")
            
            # Check Date (20 pts)
            if exp_dt.date() == act_dt.date():
                score += 20
                date_correct = True
                feedback_parts.append(f"Correct Date ({exp_dt.date()})")
            else:
                feedback_parts.append(f"Wrong Date: expected {exp_dt.date()}, got {act_dt.date()}")
            
            # Check Time (15 pts) - Allow +/- 5 minutes tolerance
            diff_seconds = abs((exp_dt - act_dt).total_seconds())
            if diff_seconds <= 300:
                score += 15
                time_correct = True
                feedback_parts.append("Correct Start Time")
            else:
                feedback_parts.append(f"Wrong Start Time: expected {exp_dt.time()}, got {act_dt.time()}")
                
        except ValueError:
            feedback_parts.append("Could not parse date/time format")
    else:
        feedback_parts.append("Start time missing")

    # 3. Duration Verification (15 pts)
    # Allow small tolerance (e.g. 1.45 - 1.55 hours)
    act_duration = actual.get('duration', 0.0)
    exp_duration = expected.get('duration', 1.5)
    
    if abs(act_duration - exp_duration) < 0.1:
        score += 15
        feedback_parts.append("Correct Duration (90 min)")
    else:
        feedback_parts.append(f"Wrong Duration: expected {exp_duration}h, got {act_duration}h")

    # 4. Location Verification (15 pts)
    act_loc = actual.get('location', "")
    exp_loc = expected.get('location', "")
    
    if exp_loc.lower() in str(act_loc).lower():
        score += 15
        feedback_parts.append(f"Correct Location ({exp_loc})")
    else:
        feedback_parts.append(f"Wrong Location: expected '{exp_loc}', got '{act_loc}'")

    # 5. Attendees Verification (15 pts)
    # Check if both expected attendees are present in the actual list
    act_attendees = [a.lower() for a in actual.get('attendees', [])]
    exp_attendees = expected.get('attendees', [])
    
    found_attendees = 0
    for exp_a in exp_attendees:
        # Check partial match (e.g. "Luis Fernandez" in "Luis Fernandez (User)")
        if any(exp_a.lower() in act_a for act_a in act_attendees):
            found_attendees += 1
            
    if found_attendees == len(exp_attendees):
        score += 15
        feedback_parts.append("All attendees added")
    elif found_attendees > 0:
        score += 7
        feedback_parts.append(f"Some attendees missing ({found_attendees}/{len(exp_attendees)} found)")
    else:
        feedback_parts.append("No correct attendees found")

    # Final Pass Check
    # Must have at least Date and Title correct to pass
    passed = (date_correct and score >= 70)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }