#!/usr/bin/env python3
"""
Verifier for batch_update_event_location task.
Reads the JSON result generated by export_result.sh inside the container.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_batch_update_event_location(traj, env_info, task_info):
    """
    Verifies that the agent correctly updated the location of specific events
    without modifying others or deleting data.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy result file
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    # Error check
    if result.get("error"):
        return {"passed": False, "score": 0, "feedback": f"Verification script error: {result['error']}"}
    
    if not result.get("baseline_found"):
         return {"passed": False, "score": 0, "feedback": "Setup failed: Baseline not found"}

    score = 0
    max_score = 100
    feedback = []
    
    # 1. Old location cleared (20 pts)
    remaining = result.get("remaining_board_room_count", -1)
    if remaining == 0:
        score += 20
        feedback.append("Success: No events remain in 'Board Room'.")
    else:
        feedback.append(f"Fail: {remaining} events still have location 'Board Room'.")

    # 2. Target events updated correctly (25 pts each = 50 pts)
    targets = result.get("target_updates", {})
    target_names = ["Investor Update Preparation", "Budget Committee Meeting"]
    
    for name in target_names:
        if name in targets:
            info = targets[name]
            if info["correct"]:
                score += 25
                feedback.append(f"Success: '{name}' updated correctly.")
            else:
                feedback.append(f"Fail: '{name}' has location '{info['location']}' (expected 'Executive Conference Room - 3rd Floor').")
        else:
            feedback.append(f"Fail: '{name}' was deleted or not found.")

    # 3. Total count preserved (10 pts)
    if not result.get("total_count_changed", True):
        score += 10
        feedback.append("Success: Total event count unchanged.")
    else:
        diff = result.get("count_diff", 0)
        feedback.append(f"Fail: Event count changed by {diff} (possible deletion/duplication).")

    # 4. No collateral damage (10 pts)
    damage = result.get("collateral_damage", [])
    if len(damage) == 0:
        score += 10
        feedback.append("Success: No other events were modified.")
    else:
        feedback.append(f"Fail: {len(damage)} other events were incorrectly modified.")

    # 5. Data preservation (10 pts)
    if result.get("data_preserved", False):
        score += 10
        feedback.append("Success: Event details (attendees/time) preserved.")
    else:
        feedback.append("Fail: Some event details (attendees/time) were altered.")

    passed = (score >= 70)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }