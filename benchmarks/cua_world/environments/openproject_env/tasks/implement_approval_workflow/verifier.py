#!/usr/bin/env python3
"""
Verifier for implement_approval_workflow task.
Checks if the status was created, workflow configured, and work package updated.
"""

import json
import tempfile
import os
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def verify_implement_approval_workflow(traj, env_info, task_info):
    """
    Verify the OpenProject approval workflow implementation.
    
    Scoring Criteria:
    1. Status "Awaiting Approval" exists (25 pts)
    2. Workflow transition configured correctly (35 pts)
    3. Target Work Package moved to new status (40 pts)
    
    The export_result.sh script runs a Rails runner command inside the container
    to inspect the database directly.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve the result JSON generated by export_result.sh
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load verification result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 1. Check Status Existence (25 pts)
    if result.get('status_exists', False):
        score += 25
        feedback_parts.append("Status 'Awaiting Approval' created")
    else:
        feedback_parts.append("Status 'Awaiting Approval' NOT found")

    # 2. Check Workflow Configuration (35 pts)
    if result.get('workflow_configured', False):
        score += 35
        feedback_parts.append("Workflow transition configured")
    else:
        feedback_parts.append("Workflow transition missing or incorrect")

    # 3. Check Work Package Status (40 pts)
    if result.get('wp_moved', False):
        score += 40
        feedback_parts.append("Work package successfully transitioned")
    else:
        feedback_parts.append("Work package not in 'Awaiting Approval' status")

    # Anti-gaming warning (does not necessarily zero score, but important context)
    details = result.get('details', [])
    if details:
        feedback_parts.append(f"Details: {'; '.join(details)}")

    passed = (score == 100)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }