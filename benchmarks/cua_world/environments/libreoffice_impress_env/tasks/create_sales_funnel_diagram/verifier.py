#!/usr/bin/env python3
"""
Verifier for Create Sales Funnel Diagram task.

Verification Strategy:
1. Programmatic Check (Primary):
   - Reads the analysis JSON generated by export_result.sh (which used odfpy inside the container).
   - Checks for slide count, specific text content, and geometric arrangement (funnel shape).
2. VLM Check (Secondary):
   - Looks at the final screenshot to confirm visual appearance (colors, arrangement).
"""

import json
import logging
import os
import tempfile
from gym_anything.vlm import query_vlm, get_final_screenshot

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_sales_funnel(traj, env_info, task_info):
    """
    Verify the sales funnel diagram creation.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Load Programmatic Analysis
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    analysis = result.get("analysis", {})
    funnel_data = analysis.get("funnel_analysis", {})
    
    # CRITERION 1: File Modification & Slide Creation (15 pts)
    if result.get("file_modified", False):
        score += 5
    if analysis.get("slide_count", 0) >= 2:
        score += 10
        feedback_parts.append("Slides created")
    else:
        feedback_parts.append("Missing slides")

    # CRITERION 2: Text Content (30 pts)
    # Expecting: Inquiries, MQL, SQL, Closed
    text_matches = funnel_data.get("text_matches", [False]*4)
    found_count = sum(text_matches)
    score += (found_count / 4) * 30
    feedback_parts.append(f"Data points found: {found_count}/4")

    # CRITERION 3: Geometric Logic (30 pts)
    # Check Y-sorting (order) and Width tapering
    if funnel_data.get("is_sorted_y", False):
        score += 15
        feedback_parts.append("Correct vertical order")
    else:
        feedback_parts.append("Incorrect vertical order")
        
    if funnel_data.get("is_tapered_width", False):
        score += 15
        feedback_parts.append("Correct funnel shape (tapered)")
    else:
        feedback_parts.append("Shape is not tapered/funnel-like")

    # CRITERION 4: VLM Verification (25 pts)
    # Check for visual confirmation of colored shapes and diagram structure
    final_screenshot = get_final_screenshot(traj)
    vlm_score = 0
    if final_screenshot:
        prompt = """
        Analyze this presentation slide.
        1. Is there a 'Funnel' diagram visible (inverted pyramid or stacked trapezoids)?
        2. Are there 4 distinct sections/shapes?
        3. Do the sections have different colors?
        4. Is the text 'Inquiries', 'MQL', 'SQL', 'Closed' visible?
        
        Respond with JSON: {"is_funnel": bool, "four_sections": bool, "distinct_colors": bool, "text_visible": bool}
        """
        try:
            vlm_res = query_vlm(prompt=prompt, image=final_screenshot)
            parsed = vlm_res.get("parsed", {})
            if parsed.get("is_funnel"): vlm_score += 10
            if parsed.get("four_sections"): vlm_score += 5
            if parsed.get("distinct_colors"): vlm_score += 5
            if parsed.get("text_visible"): vlm_score += 5
        except Exception as e:
            logger.warning(f"VLM check failed: {e}")
            # Fallback: give partial credit if programmatic checks were strong
            if score > 50: vlm_score = 15
    
    score += vlm_score
    feedback_parts.append(f"Visual check: {vlm_score}/25")

    # Final Pass Determination
    # Must have at least 2 correct text items and created slides
    passed = score >= 65 and found_count >= 2 and analysis.get("slide_count", 0) >= 2

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }