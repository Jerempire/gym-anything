#!/usr/bin/env python3
"""Verifier for Synchronous HL7-to-REST task."""

import json
import tempfile
import os
import re

def verify_synchronous_hl7_to_rest_ack_mapping(traj, env_info, task_info):
    """
    Verifies the synchronous facade channel.
    
    Criteria:
    1. Channel exists and is running (10 pts)
    2. Mock API received the JSON request with correct data (25 pts)
    3. Channel returned a valid HL7 ACK (15 pts)
    4. The ACK contains the specific UUID generated by the API (25 pts)
    5. Source is configured correctly (implied by 3 & 4) (25 pts)
    """
    
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy result file
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 1. Channel Status (10 pts)
    if result.get('channel_exists') and result.get('channel_status') in ['STARTED', 'DEPLOYED', 'Running']:
        score += 10
        feedback_parts.append("Channel created and running.")
    else:
        feedback_parts.append("Channel not found or not running.")

    # 2. API Receipt (25 pts)
    if result.get('api_received_request'):
        score += 25
        feedback_parts.append("Mock API received the transformed JSON request.")
    else:
        feedback_parts.append("Mock API did not receive request (check HTTP Sender URL or transformation).")

    # 3. & 4. ACK Verification
    ack = result.get('raw_ack_response', '')
    expected_uuid = result.get('generated_uuid', '')
    
    # Check if it looks like HL7
    if "MSH|" in ack and "MSA|" in ack:
        score += 15
        feedback_parts.append("Received valid HL7 ACK response.")
        
        # Check for UUID in MSA segment
        # MSA|AA|<uuid>|
        # Regex to find MSA segment content
        msa_match = re.search(r'MSA\|([^|]+)\|([^|]+)\|', ack)
        if msa_match:
            status = msa_match.group(1) # Should be AA
            msg_id_or_uuid = msa_match.group(2) # Should be UUID
            
            if status == 'AA':
                feedback_parts.append("ACK status is AA.")
            else:
                feedback_parts.append(f"ACK status is {status} (expected AA).")

            if expected_uuid and expected_uuid in ack:
                score += 50 # 25 for logic + 25 implied config
                feedback_parts.append(f"SUCCESS: Found correct API UUID ({expected_uuid}) in ACK.")
            else:
                feedback_parts.append(f"FAIL: Expected UUID {expected_uuid} not found in ACK. Got: {msg_id_or_uuid}")
                feedback_parts.append("Did you configure the Response Transformer to map the API response?")
        else:
            feedback_parts.append("Could not parse MSA segment in response.")
    else:
        feedback_parts.append("Did not receive a valid HL7 ACK. (Check Source 'Response' setting - likely set to None or default instead of Destination).")

    return {
        "passed": score >= 75,
        "score": score,
        "feedback": " ".join(feedback_parts)
    }