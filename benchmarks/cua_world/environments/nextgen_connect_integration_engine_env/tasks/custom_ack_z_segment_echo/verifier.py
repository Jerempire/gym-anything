#!/usr/bin/env python3
"""
Verifier for Custom ACK Z-Segment Echo Task.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_custom_ack_z_segment_echo(traj, env_info, task_info):
    """
    Verify the agent configured the channel to echo a dynamic transaction ID in a custom ZAK segment.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Retrieve the result JSON generated by export_result.sh
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result file: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # 2. Score Calculation
    score = 0
    feedback_parts = []
    
    # Parse sections
    dynamic = result.get("dynamic_verification", {})
    persistence = result.get("file_persistence", {})
    status = result.get("channel_status", {})

    # Criterion 1: Channel Listener Active (10 pts)
    if dynamic.get("connected"):
        score += 10
        feedback_parts.append("✅ Channel is listening on port 6661.")
    else:
        feedback_parts.append(f"❌ Could not connect to port 6661: {dynamic.get('error')}")

    # Criterion 2: Custom Response Structure (ZAK segment present) (30 pts)
    if dynamic.get("zak_found"):
        score += 30
        feedback_parts.append("✅ Response contained custom ZAK segment.")
    else:
        # Check if we got a response at all
        resp = dynamic.get("received_response")
        if resp:
            feedback_parts.append(f"❌ Received response but missing ZAK segment. Got: {resp[:50]}...")
        else:
            feedback_parts.append("❌ No response received from channel.")

    # Criterion 3: Dynamic ID Echo (Core Logic) (50 pts)
    # This proves they didn't just hardcode "TX-SAMPLE-999" but actually parsed the message
    if dynamic.get("id_echoed"):
        score += 50
        feedback_parts.append(f"✅ Correctly echoed dynamic ID: {dynamic.get('sent_id')}")
    else:
        if dynamic.get("zak_found"):
             feedback_parts.append(f"❌ ZAK segment found but ID did not match. Expected {dynamic.get('sent_id')}.")
        
    # Criterion 4: File Persistence (10 pts)
    if persistence.get("file_saved"):
        score += 10
        feedback_parts.append("✅ Message was saved to file.")
    else:
        feedback_parts.append("❌ No new file found in output directory.")

    # Check Channel Status (Bonus/Confirmation)
    if status.get("deployed"):
        feedback_parts.append("(Channel is reported as STARTED by API)")

    # 3. Final Determination
    # Pass threshold is 90 because the dynamic echo is the main point of the task.
    # If they get everything but the echo (10+30+10 = 50), they fail.
    # If they get everything (100), they pass.
    passed = score >= 90
    
    return {
        "passed": passed,
        "score": score,
        "feedback": "\n".join(feedback_parts)
    }