#!/bin/bash
echo "=== Setting up Transcribe Complaint Task ==="

source /workspace/scripts/task_utils.sh

# 1. Ensure ArkCase is ready and port-forward is active
ensure_portforward
wait_for_arkcase

# 2. Install ReportLab for PDF generation
if ! python3 -c "import reportlab" 2>/dev/null; then
    echo "Installing reportlab..."
    pip3 install reportlab > /dev/null 2>&1
fi

# 3. Create the PDF Form on the Desktop
PDF_PATH="/home/ga/Desktop/complaint_form_2025-001.pdf"
mkdir -p /home/ga/Desktop

cat > /tmp/generate_pdf.py << 'EOF'
from reportlab.lib.pagesizes import LETTER
from reportlab.pdfgen import canvas
import sys

def create_form(path):
    c = canvas.Canvas(path, pagesize=LETTER)
    width, height = LETTER
    
    # Header
    c.setFont("Helvetica-Bold", 24)
    c.drawString(50, height - 50, "CITY MUNICIPAL COMPLAINT FORM")
    
    c.setLineWidth(2)
    c.line(50, height - 60, width - 50, height - 60)
    
    # Form Details
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, height - 100, "Reference Number:")
    c.setFont("Helvetica", 12)
    c.drawString(200, height - 100, "2025-001")
    
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, height - 130, "Date of Incident:")
    c.setFont("Helvetica", 12)
    c.drawString(200, height - 130, "February 14, 2025")
    
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, height - 160, "Complainant Name:")
    c.setFont("Helvetica", 12)
    c.drawString(200, height - 160, "Eleanor Rigby")
    
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, height - 190, "Subject:")
    c.setFont("Helvetica", 12)
    c.drawString(200, height - 190, "Construction Noise Violation")
    
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, height - 220, "Location:")
    c.setFont("Helvetica", 12)
    c.drawString(200, height - 220, "44 Rice Street, City Center")
    
    # Description Box
    c.setFont("Helvetica-Bold", 12)
    c.drawString(50, height - 260, "Incident Details:")
    
    c.rect(50, height - 400, width - 100, 130, stroke=1, fill=0)
    
    text = c.beginText(60, height - 280)
    text.setFont("Helvetica", 11)
    lines = [
        "Ongoing construction work with jackhammers is disrupting funeral services",
        "at the church. This is the third occurrence this week despite city ordinance",
        "44-B prohibiting noise during service hours. We have asked the foreman",
        "to stop multiple times but they refuse to comply."
    ]
    for line in lines:
        text.textLine(line)
    c.drawText(text)
    
    # Footer
    c.setFont("Helvetica-Oblique", 10)
    c.drawString(50, 50, "Form generated by Municipal Intake System v2.1")
    
    c.save()

if __name__ == "__main__":
    create_form(sys.argv[1])
EOF

python3 /tmp/generate_pdf.py "$PDF_PATH"
chown ga:ga "$PDF_PATH"
echo "PDF created at $PDF_PATH"

# 4. Ensure 'Eleanor Rigby' exists in ArkCase
echo "Checking/Creating person 'Eleanor Rigby'..."
# Search first
EXISTING_PERSON=$(arkcase_api GET "people?q=Eleanor%20Rigby")
PERSON_ID=$(echo "$EXISTING_PERSON" | python3 -c "import sys, json; data=json.load(sys.stdin); print(data[0]['id'] if data and len(data)>0 else '')" 2>/dev/null)

if [ -z "$PERSON_ID" ]; then
    echo "Creating Eleanor Rigby..."
    # Create Person payload
    PAYLOAD='{
        "objectType": "PEOPLE",
        "firstName": "Eleanor",
        "lastName": "Rigby",
        "email": "erigby@church.org",
        "businessPhone": "555-0199"
    }'
    RESPONSE=$(arkcase_api POST "people" "$PAYLOAD")
    PERSON_ID=$(echo "$RESPONSE" | python3 -c "import sys, json; print(json.load(sys.stdin).get('id', ''))" 2>/dev/null)
fi

echo "Eleanor Rigby Person ID: $PERSON_ID"
echo "$PERSON_ID" > /tmp/expected_person_id.txt

# 5. Record start time
date +%s > /tmp/task_start_time.txt
# Record initial complaint count
INITIAL_COUNT=$(arkcase_api GET "plugin/complaint" | python3 -c "import sys, json; print(len(json.load(sys.stdin)))" 2>/dev/null || echo "0")
echo "$INITIAL_COUNT" > /tmp/initial_complaint_count.txt

# 6. Reset UI state
pkill -f firefox 2>/dev/null || true
DISPLAY=:1 wmctrl -c "Mozilla Firefox" 2>/dev/null || true

# Take initial screenshot
take_screenshot /tmp/task_initial.png

echo "=== Setup Complete ==="

