#!/usr/bin/env python3
"""
Verifier for generate_case_priority_report task.
"""

import json
import csv
import os
import tempfile
import logging
import shutil

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_generate_case_priority_report(traj, env_info, task_info):
    """
    Verifies the CSV report generated by the agent.
    """
    # 1. Setup and Retrieve Files
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    temp_dir = tempfile.mkdtemp()
    agent_csv_path = os.path.join(temp_dir, "agent_report.csv")
    ground_truth_path = os.path.join(temp_dir, "ground_truth.json")
    metadata_path = os.path.join(temp_dir, "metadata.json")

    try:
        # Copy files from container
        # Note: We assume export_result.sh put these in /tmp/export_data/
        copy_from_env("/tmp/export_data/agent_report.csv", agent_csv_path)
        copy_from_env("/tmp/export_data/ground_truth.json", ground_truth_path)
        copy_from_env("/tmp/export_data/metadata.json", metadata_path)

        # Load Metadata
        with open(metadata_path, 'r') as f:
            metadata = json.load(f)

        # Load Ground Truth
        with open(ground_truth_path, 'r') as f:
            ground_truth = json.load(f) # List of dicts {case_number, title, priority}

    except Exception as e:
        shutil.rmtree(temp_dir)
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task data: {str(e)}"}

    # 2. Verify Output Existence and Timestamp
    score = 0
    feedback = []
    
    if not metadata.get("output_exists", False):
        shutil.rmtree(temp_dir)
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Report file 'priority_report.csv' was not found."
        }
    
    score += 10
    feedback.append("Report file created.")

    if not metadata.get("file_created_during_task", False):
        feedback.append("WARNING: File timestamp indicates it was not created during this task session.")
        # We don't fail immediately, but this is suspicious
    else:
        feedback.append("File created during task window.")

    # 3. Verify CSV Content
    try:
        with open(agent_csv_path, 'r', encoding='utf-8', errors='replace') as f:
            # Detect header
            sample = f.read(1024)
            f.seek(0)
            has_header = csv.Sniffer().has_header(sample)
            
            reader = csv.reader(f)
            rows = list(reader)
            
        if not rows:
            shutil.rmtree(temp_dir)
            return {"passed": False, "score": score, "feedback": "CSV file is empty."}

        # Check Header
        header = [h.lower().strip() for h in rows[0]]
        required_cols = ["case number", "title", "priority"]
        
        # Simple header check
        header_score = 0
        if all(any(req in h for h in header) for req in required_cols):
            header_score = 10
            feedback.append("CSV header is correct.")
        else:
            feedback.append(f"CSV header missing required columns. Found: {rows[0]}")
        
        score += header_score
        
        # If header exists, skip it for data check
        data_rows = rows[1:] if has_header else rows
        
        # 4. Verify Data Rows
        # Create lookup map from ground truth
        gt_map = {item['case_number']: item for item in ground_truth}
        
        correct_cases = 0
        
        for row in data_rows:
            if len(row) < 3:
                continue
            
            # Simple heuristic matching: assume order is CaseNum, Title, Priority OR use header index if robust
            # Let's assume the agent followed instructions: "Case Number,Title,Priority"
            # But we can be flexible if we found headers
            
            case_num = row[0].strip()
            title = row[1].strip()
            priority = row[2].strip()
            
            # Find corresponding ground truth
            if case_num in gt_map:
                gt_item = gt_map[case_num]
                
                # Verify Title (Fuzzy match)
                title_ok = gt_item['title'].lower() in title.lower() or title.lower() in gt_item['title'].lower()
                
                # Verify Priority (Exact match, case-insensitive)
                priority_ok = gt_item['priority'].lower() == priority.lower()
                
                if title_ok and priority_ok:
                    correct_cases += 1
                    # 25 points per correct case
                    score += 25
                else:
                    feedback.append(f"Case {case_num}: Incorrect details. Got Title='{title}', Priority='{priority}'.")
            else:
                # Hallucination or wrong case number
                pass

        feedback.append(f"Correctly identified {correct_cases}/{len(ground_truth)} cases.")
        
        # Penalty for hallucinations (extra rows that aren't in GT)
        if len(data_rows) > len(ground_truth):
            score -= 5 * (len(data_rows) - len(ground_truth))
            feedback.append("Penalty applied for extra/incorrect rows.")

    except Exception as e:
        feedback.append(f"Error parsing CSV: {str(e)}")

    # Cleanup
    shutil.rmtree(temp_dir)

    # Final Score Calculation
    # Max possible: 10 (exists) + 10 (header) + 75 (3 * 25 data) = 95
    # Let's normalize to 100 if perfect (add 5 bonus for perfection)
    if score >= 95:
        score = 100
        
    passed = score >= 85

    return {
        "passed": passed,
        "score": max(0, min(100, score)),
        "feedback": " ".join(feedback)
    }