#!/usr/bin/env python3
"""
Verifier for generate_case_audit_report task.

Verifies:
1. Report file existence and creation time.
2. Structure of the report (Header, Sections).
3. Accuracy of aggregated metrics (Total, Active, Closed) against Ground Truth.
4. Accuracy of list content (High Priority Active cases, All cases).
"""

import json
import os
import re
import tempfile
import logging
from typing import Dict, Any, List

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def parse_report(content: str) -> Dict[str, Any]:
    """Parses the user's text report into a structured format for comparison."""
    parsed = {
        "header_found": False,
        "generated_by": None,
        "total_cases": None,
        "active_cases": None,
        "closed_cases": None,
        "high_priority_active": [],
        "all_cases_table": []
    }
    
    lines = content.splitlines()
    if not lines:
        return parsed

    # 1. Header Check
    if "CASE AUDIT REPORT" in content and "=====" in content:
        parsed["header_found"] = True

    # 2. Extract Metrics
    for line in lines:
        if "Generated by:" in line:
            parsed["generated_by"] = line.split(":", 1)[1].strip()
        elif "Total Cases:" in line:
            try: parsed["total_cases"] = int(re.search(r'\d+', line).group())
            except: pass
        elif "Active Cases:" in line:
            try: parsed["active_cases"] = int(re.search(r'\d+', line).group())
            except: pass
        elif "Closed Cases:" in line:
            try: parsed["closed_cases"] = int(re.search(r'\d+', line).group())
            except: pass

    # 3. Extract High Priority Section
    # Look for content between "HIGH PRIORITY ACTIVE CASES:" and "ALL CASES:"
    try:
        section_pattern = r"HIGH PRIORITY ACTIVE CASES:(.*?)ALL CASES:"
        high_prio_match = re.search(section_pattern, content, re.DOTALL)
        if high_prio_match:
            high_prio_text = high_prio_match.group(1)
            # Find lines like "- CaseNum: Title"
            for line in high_prio_text.splitlines():
                line = line.strip()
                if line.startswith("-"):
                    # Extract roughly "- <Number>: <Title>"
                    parts = line.lstrip("- ").split(":", 1)
                    if len(parts) == 2:
                        parsed["high_priority_active"].append({
                            "number": parts[0].strip(),
                            "title": parts[1].strip()
                        })
    except Exception as e:
        logger.warning(f"Error parsing high priority section: {e}")

    # 4. Extract Table Rows
    # Look for lines containing pipes "|"
    try:
        if "ALL CASES:" in content:
            table_start = content.index("ALL CASES:")
            table_lines = content[table_start:].splitlines()
            for line in table_lines:
                if "|" in line and "Case Number" not in line and "---" not in line:
                    # It's a data row
                    cols = [c.strip() for c in line.split("|") if c.strip()]
                    # Expecting 5 columns: Number, Title, Status, Priority, Assignee
                    if len(cols) >= 4: # Allow some leniency
                        parsed["all_cases_table"].append({
                            "number": cols[0],
                            "title": cols[1],
                            "status": cols[2].upper(),
                            "priority": cols[3],
                            "assignee": cols[4] if len(cols) > 4 else ""
                        })
    except Exception as e:
        logger.warning(f"Error parsing table section: {e}")

    return parsed

def verify_audit_report(traj, env_info, task_info):
    """
    Main verification function.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load Result JSON from Container
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # Basic Checks
    report_exists = result.get("report_exists", False)
    created_during = result.get("report_created_during_task", False)
    content = result.get("report_content", "")
    ground_truth = result.get("ground_truth_cases", [])

    if not report_exists:
        return {"passed": False, "score": 0, "feedback": "Report file case_audit_report.txt not found."}
    
    if not created_during:
        return {"passed": False, "score": 0, "feedback": "Report file was not created/modified during the task session."}

    # Analyze Ground Truth
    gt_total = len(ground_truth)
    gt_active = sum(1 for c in ground_truth if c.get('status') == 'ACTIVE')
    gt_closed = sum(1 for c in ground_truth if c.get('status') == 'CLOSED')
    
    gt_high_active_ids = {
        c.get('caseNumber') for c in ground_truth 
        if c.get('status') == 'ACTIVE' and c.get('priority') == 'High'
    }

    # Parse User Report
    parsed = parse_report(content)
    
    score = 0
    feedback = []

    # Scoring Criteria
    
    # 1. Structure (10 pts)
    if parsed["header_found"] and parsed["generated_by"] == "arkcase-admin":
        score += 10
        feedback.append("Report format correct.")
    else:
        feedback.append("Report format incorrect or missing header info.")

    # 2. Metrics Accuracy (30 pts)
    metrics_score = 0
    if parsed["total_cases"] == gt_total: metrics_score += 10
    if parsed["active_cases"] == gt_active: metrics_score += 10
    if parsed["closed_cases"] == gt_closed: metrics_score += 10
    score += metrics_score
    feedback.append(f"Metrics Score: {metrics_score}/30")
    
    if metrics_score < 30:
        feedback.append(f"Expected Counts - Total:{gt_total}, Active:{gt_active}, Closed:{gt_closed}")
        feedback.append(f"Reported Counts - Total:{parsed['total_cases']}, Active:{parsed['active_cases']}, Closed:{parsed['closed_cases']}")

    # 3. High Priority Identification (20 pts)
    # Check if all reported high priority cases are actually high priority active
    reported_hp_ids = set()
    for item in parsed["high_priority_active"]:
        # Find matching case in GT to verify number
        # We assume the user wrote the case number correctly
        reported_hp_ids.add(item["number"])
    
    # Intersection over Union for scoring or simple Recall
    # Here we demand exact match for full points
    if reported_hp_ids == gt_high_active_ids:
        score += 20
        feedback.append("High Priority Active cases correctly identified.")
    elif reported_hp_ids.issubset(gt_high_active_ids) and len(reported_hp_ids) > 0:
        score += 10
        feedback.append("Some High Priority cases identified, but list incomplete.")
    else:
        feedback.append("High Priority Active cases list is incorrect or empty.")

    # 4. All Cases Table (40 pts)
    # Check if every ground truth case appears in the table
    # We check if the Case Number exists in the reported table rows
    reported_table_ids = {row["number"] for row in parsed["all_cases_table"]}
    gt_ids = {c.get('caseNumber') for c in ground_truth}
    
    # Calculate overlap
    found_ids = reported_table_ids.intersection(gt_ids)
    
    if len(gt_ids) > 0:
        table_accuracy = len(found_ids) / len(gt_ids)
        points = int(table_accuracy * 40)
        score += points
        feedback.append(f"Case Table Completeness: {int(table_accuracy*100)}% ({points}/40 pts)")
    else:
        # Fallback if GT is empty for some reason (shouldn't happen with setup script)
        score += 0
        feedback.append("No ground truth cases found to verify table.")

    passed = (score >= 60 and metrics_score >= 20)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }