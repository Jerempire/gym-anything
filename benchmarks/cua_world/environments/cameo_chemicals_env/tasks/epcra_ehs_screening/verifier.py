#!/usr/bin/env python3
"""
Verifier for epcra_ehs_screening task.
Verifies the content of the CSV report generated by the agent.
"""

import json
import csv
import os
import tempfile
import logging
import io

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_epcra_ehs_screening(traj, env_info, task_info):
    """
    Verify the EPCRA screening report CSV.
    
    Scoring:
    - 10 pts: File exists and is a valid CSV
    - 10 pts: File created during task (anti-gaming)
    - 16 pts per chemical (5 chemicals = 80 pts total for content)
      - 8 pts for correct EHS Status (YES/NO)
      - 8 pts for correct TPQ value
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load metadata and ground truth
    metadata = task_info.get('metadata', {})
    expected_path = metadata.get('expected_path', '/home/ga/Desktop/epcra_screening_report.csv')
    ground_truth = metadata.get('ground_truth', {})

    # Load task result metadata (from export_result.sh)
    task_result = {}
    temp_result_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result_json.name)
        with open(temp_result_json.name, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read task result metadata: {e}"}
    finally:
        if os.path.exists(temp_result_json.name):
            os.unlink(temp_result_json.name)

    # Check existence
    if not task_result.get('output_exists', False):
        return {"passed": False, "score": 0, "feedback": "Output CSV file not found on Desktop."}

    # Check timestamp (Anti-gaming)
    if not task_result.get('file_created_during_task', False):
        return {"passed": False, "score": 0, "feedback": "Output file exists but was not created/modified during the task."}

    score = 20  # Base score for file existence + timestamp
    feedback_parts = ["File created successfully"]

    # Retrieve and parse the CSV content
    temp_csv = tempfile.NamedTemporaryFile(delete=False, suffix='.csv')
    try:
        copy_from_env(expected_path, temp_csv.name)
        
        with open(temp_csv.name, 'r', encoding='utf-8', errors='replace') as f:
            content = f.read()
            
        # Parse CSV
        reader = csv.reader(io.StringIO(content))
        header = next(reader, None)
        
        if not header:
            return {"passed": False, "score": score, "feedback": "CSV file is empty or missing headers."}

        # Normalize headers
        header = [h.strip().lower() for h in header]
        # Basic header check (allow some flexibility)
        if not any('chemical' in h for h in header) or not any('status' in h for h in header):
             feedback_parts.append("Warning: CSV headers do not match expected format (Chemical Name, EHS Status, TPQ).")

        rows = list(reader)
        
        # Map rows by chemical name (fuzzy match)
        row_map = {}
        for row in rows:
            if not row: continue
            name = row[0].strip().lower()
            # Normalize common variations
            if "sulfuric" in name: name = "sulfuric acid"
            if "phosgene" in name: name = "phosgene"
            if "chlorine" in name: name = "chlorine"
            if "toluene" in name: name = "toluene"
            if "acetone" in name: name = "acetone"
            row_map[name] = row

        # Evaluate each chemical
        chemicals_correct = 0
        
        for chem_name, truth in ground_truth.items():
            chem_key = chem_name.lower()
            if chem_key not in row_map:
                feedback_parts.append(f"Missing entry for {chem_name}")
                continue
                
            row = row_map[chem_key]
            if len(row) < 3:
                feedback_parts.append(f"Incomplete data for {chem_name}")
                continue

            # Check Status (Column 2)
            actual_status = row[1].strip().upper()
            expected_status = truth['ehs'].upper()
            
            # Check TPQ (Column 3)
            actual_tpq = row[2].strip()
            # Normalize TPQ (remove 'lbs', commas, spaces)
            actual_tpq_clean = ''.join(c for c in actual_tpq if c.isdigit())
            expected_tpq_clean = ''.join(c for c in truth['tpq'] if c.isdigit())
            
            chem_score = 0
            
            # Status Check
            if actual_status == expected_status or (expected_status == "YES" and "YES" in actual_status) or (expected_status == "NO" and "NO" in actual_status):
                chem_score += 8
            else:
                feedback_parts.append(f"{chem_name}: Wrong Status (Expected {expected_status}, got {actual_status})")

            # TPQ Check
            tpq_correct = False
            if expected_status == "NO":
                # For NO, accept N/A, -, blank, or 0
                if actual_tpq.upper() in ["N/A", "NA", "-", "", "NONE"]:
                    tpq_correct = True
            else:
                # For YES, match the number
                if actual_tpq_clean == expected_tpq_clean and actual_tpq_clean != "":
                    tpq_correct = True
            
            if tpq_correct:
                chem_score += 8
            else:
                feedback_parts.append(f"{chem_name}: Wrong TPQ (Expected {truth['tpq']}, got {actual_tpq})")
            
            score += chem_score
            if chem_score == 16:
                chemicals_correct += 1

    except Exception as e:
        return {"passed": False, "score": score, "feedback": f"Error parsing CSV content: {e}"}
    finally:
        if os.path.exists(temp_csv.name):
            os.unlink(temp_csv.name)

    # Final verdict
    # Pass threshold: 82 points (must get at least 4/5 chemicals completely correct + file checks)
    passed = score >= 82
    
    return {
        "passed": passed,
        "score": score,
        "feedback": "; ".join(feedback_parts)
    }