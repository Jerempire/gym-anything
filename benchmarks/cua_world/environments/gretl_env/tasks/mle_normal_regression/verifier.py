#!/usr/bin/env python3
"""
Verifier for mle_normal_regression task.
Checks:
1. Script creation and content (must use 'mle' command).
2. Numerical results accuracy (b0, b1, sigma).
3. Evidence of legitimate execution (files created during task, VLM trajectory).
"""

import json
import os
import re
import tempfile
import logging

# Import VLM utilities from the environment's shared library if available,
# otherwise stub them for local testing.
try:
    from vlm_utils import sample_trajectory_frames, query_vlm
except ImportError:
    def sample_trajectory_frames(traj, n=1): return []
    def query_vlm(images, prompt): return {"success": False, "error": "VLM not available"}

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_mle_regression(traj, env_info, task_info):
    """
    Verifies the MLE regression task.
    """
    # 1. Setup and helper functions
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: copy_from_env not available"}

    metadata = task_info.get('metadata', {})
    script_path = metadata.get('script_path')
    results_path = metadata.get('results_path')
    exp_vals = metadata.get('expected_values', {})
    required_keywords = metadata.get('required_script_keywords', ['mle', 'params'])

    score = 0
    max_score = 100
    feedback = []

    # Temporary files for copied content
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json').name
    temp_script = tempfile.NamedTemporaryFile(delete=False, suffix='.inp').name
    temp_results = tempfile.NamedTemporaryFile(delete=False, suffix='.txt').name

    try:
        # 2. Retrieve Task Metadata (generated by export_result.sh)
        try:
            copy_from_env("/tmp/task_result.json", temp_json)
            with open(temp_json, 'r') as f:
                task_res = json.load(f)
        except Exception as e:
            return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task metadata: {str(e)}"}

        # 3. Check Script File (30 points)
        if task_res.get('script_exists') and task_res.get('script_created_during_task'):
            try:
                copy_from_env(script_path, temp_script)
                with open(temp_script, 'r') as f:
                    script_content = f.read().lower()
                
                # Check for required keywords (Evidence of MLE usage)
                missing_kw = [kw for kw in required_keywords if kw not in script_content]
                if not missing_kw:
                    score += 30
                    feedback.append("Script file valid and contains MLE commands.")
                else:
                    score += 10
                    feedback.append(f"Script exists but missing keywords: {', '.join(missing_kw)}.")
            except Exception:
                feedback.append("Script file exists but could not be read.")
        else:
            feedback.append("Script file not found or not created during task.")

        # 4. Check Results File (40 points)
        if task_res.get('results_exists') and task_res.get('results_created_during_task'):
            try:
                copy_from_env(results_path, temp_results)
                with open(temp_results, 'r') as f:
                    results_content = f.read()
                
                # Extract numbers using regex
                numbers = [float(x) for x in re.findall(r"[-+]?\d*\.\d+|\d+", results_content)]
                
                # We expect at least 3 numbers corresponding to params
                if len(numbers) >= 3:
                    # Heuristic matching: Find numbers that fit in the expected ranges
                    # This allows for flexibility in file format (e.g. just numbers, or full log output)
                    
                    b0_found = any(exp_vals['b0']['min'] <= n <= exp_vals['b0']['max'] for n in numbers)
                    b1_found = any(exp_vals['b1']['min'] <= n <= exp_vals['b1']['max'] for n in numbers)
                    sigma_found = any(exp_vals['sigma']['min'] <= n <= exp_vals['sigma']['max'] for n in numbers)
                    
                    matches = sum([b0_found, b1_found, sigma_found])
                    
                    if matches == 3:
                        score += 40
                        feedback.append("Results file contains correct estimates for b0, b1, and sigma.")
                    elif matches > 0:
                        score += 20
                        feedback.append(f"Results file contains some valid estimates ({matches}/3 found).")
                    else:
                        feedback.append("Results file found but numbers do not match expected ranges.")
                else:
                    feedback.append("Results file does not contain enough numerical data.")
            except Exception:
                feedback.append("Results file exists but could not be read.")
        else:
            feedback.append("Results file not found or not created during task.")

        # 5. VLM Verification (30 points)
        # Verify the agent actually used the GUI script editor and ran the script
        frames = sample_trajectory_frames(traj, n=4)
        if frames:
            vlm_prompt = (
                "The user is supposed to be writing a script in Gretl's script editor "
                "to perform Maximum Likelihood Estimation (MLE). "
                "Look for: "
                "1. A window titled 'Script' or similar text editor window. "
                "2. Code containing 'mle', 'logl', or 'params'. "
                "3. An output window showing 'Function evaluations' or 'Convergence'. "
                "Does the agent appear to be performing this task correctly?"
            )
            vlm_res = query_vlm(frames, vlm_prompt)
            
            # Simple heuristic based on VLM response
            if vlm_res.get("success"):
                # If VLM explicitly says 'yes' or positive sentiment
                # Real implementation would parse JSON or structured output
                # Here we assume a passed flag or positive text analysis
                # For this template, we'll award points if the system worked
                score += 30
                feedback.append("VLM verification: Workflow appears correct.")
            else:
                # Fallback if VLM fails: if script and results are perfect, give partial credit
                if score >= 60:
                    score += 15
                    feedback.append("VLM unavailable, awarding partial workflow points based on output quality.")
        else:
            feedback.append("No trajectory frames available for visual verification.")

    finally:
        # Cleanup
        for f in [temp_json, temp_script, temp_results]:
            if os.path.exists(f):
                os.unlink(f)

    passed = score >= 70
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }