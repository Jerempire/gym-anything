#!/usr/bin/env python3
"""
Verifier for ttest_food_expenditure_group task.

Strategy:
1. Parse the agent's output text file for the t-statistic and p-value.
2. Compare against Ground Truth generated by gretlcli in export_result.sh.
3. Verify the existence and validity of the boxplot image.
4. Use VLM to confirm the workflow (Gretl UI usage) and visualization content.
"""

import json
import os
import re
import base64
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def parse_t_statistic(text_content):
    """
    Attempts to extract the t-statistic from Gretl output.
    Looks for patterns like "t-statistic = 5.92" or "t(38) = 5.92".
    """
    if not text_content:
        return None
    
    # Regex for t-statistic (flexible format)
    # Matches "t-statistic = 5.92" or "t = 5.92" or similar
    match = re.search(r"t(?:-statistic)?\s*(?:=\s*|\()\s*(-?[\d\.]+)", text_content, re.IGNORECASE)
    if match:
        try:
            return float(match.group(1))
        except ValueError:
            pass
    return None

def verify_ttest_food_expenditure_group(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback = []
    
    # --- Criterion 1: Results Text File Exists & Created During Task (20 pts) ---
    if result.get("txt_exists") and result.get("file_created_during_task"):
        score += 20
        feedback.append("Results text file created.")
    elif result.get("txt_exists"):
        score += 10
        feedback.append("Results file exists but timestamp check failed (possible pre-existing file).")
    else:
        feedback.append("Results text file missing.")

    # --- Criterion 2: Statistical Accuracy (40 pts) ---
    # Decode contents
    try:
        agent_text = base64.b64decode(result.get("txt_content_b64", "")).decode('utf-8', errors='ignore')
        gt_text = base64.b64decode(result.get("gt_content_b64", "")).decode('utf-8', errors='ignore')
    except Exception:
        agent_text = ""
        gt_text = ""

    agent_t = parse_t_statistic(agent_text)
    gt_t = parse_t_statistic(gt_text)
    
    accuracy_passed = False
    
    if gt_t is not None:
        if agent_t is not None:
            # Check tolerance (e.g. +/- 0.1)
            if abs(agent_t - gt_t) < 0.1:
                score += 40
                feedback.append(f"T-statistic matches ground truth ({agent_t} vs {gt_t}).")
                accuracy_passed = True
            elif abs(abs(agent_t) - abs(gt_t)) < 0.1:
                # Allow sign flip (depends on group order 0-1 vs 1-0)
                score += 40
                feedback.append(f"T-statistic magnitude matches ground truth (|{agent_t}| vs |{gt_t}|).")
                accuracy_passed = True
            else:
                feedback.append(f"T-statistic incorrect. Expected ~{gt_t}, got {agent_t}.")
        else:
            feedback.append("Could not parse t-statistic from agent output.")
    else:
        # Fallback if GT generation failed (shouldn't happen)
        feedback.append("Ground truth generation failed. Skipping statistical accuracy check.")
        # Grant partial points if output looks like a t-test
        if "test" in agent_text.lower() and "mean" in agent_text.lower():
            score += 20
            feedback.append("Output resembles a t-test, but exact value verification failed.")

    # --- Criterion 3: Boxplot Image Exists (20 pts) ---
    if result.get("img_exists"):
        size = result.get("img_size", 0)
        if size > 1000: # Arbitrary small limit to ensure not empty
            score += 20
            feedback.append("Boxplot image saved.")
        else:
            score += 5
            feedback.append("Boxplot image exists but is suspiciously small.")
    else:
        feedback.append("Boxplot image missing.")

    # --- Criterion 4: VLM / Anti-Gaming (20 pts) ---
    # We verify the agent didn't just write a fake file by checking logic
    # If accuracy passed, we are fairly confident. 
    # We add points if the screenshot shows Gretl or plot.
    
    # Simple check: if we have accurate stats + a plot file + text file, we assume workflow was valid.
    # To be more robust, we could check the screenshot path if available.
    if accuracy_passed and result.get("img_exists"):
        score += 20
        feedback.append("Workflow verified by output consistency.")
    elif accuracy_passed:
        score += 10
        feedback.append("Workflow partially verified (missing plot).")
    
    passed = score >= 80
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }