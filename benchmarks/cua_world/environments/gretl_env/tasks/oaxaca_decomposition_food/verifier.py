#!/usr/bin/env python3
"""
Verifier for Oaxaca-Blinder Decomposition Task.
Compares agent's manual calculation against a ground truth generated by gretlcli.
"""

import json
import os
import sys
import base64
import re
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def parse_key_value_output(content):
    """Parses lines like KEY=VALUE from a string."""
    data = {}
    for line in content.splitlines():
        if '=' in line:
            key, val = line.split('=', 1)
            try:
                data[key.strip().upper()] = float(val.strip())
            except ValueError:
                continue
    return data

def verify_oaxaca_decomposition_food(traj, env_info, task_info):
    """
    Verifies the agent's Oaxaca decomposition results.
    
    Scoring:
    - Script file exists: 20 pts
    - Output file exists & created during task: 10 pts
    - Total Gap accuracy: 20 pts
    - Explained Effect accuracy: 25 pts
    - Unexplained Effect accuracy: 25 pts
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback = []
    
    # 1. Script Verification (20 pts)
    if result.get('script_exists'):
        score += 20
        feedback.append("Script file created.")
        
        # content analysis (optional bonus checks could go here)
        script_content = base64.b64decode(result.get('script_content_b64', '')).decode('utf-8', errors='ignore')
        required_cmds = ['ols', 'smpl', 'mean', '-']
        found_cmds = [cmd for cmd in required_cmds if cmd in script_content.lower()]
        if len(found_cmds) < 2:
            feedback.append("(Warning: Script seems missing key commands like 'ols' or 'smpl')")
    else:
        feedback.append("Script file missing.")

    # 2. Output File Verification (10 pts)
    output_exists = result.get('output_exists')
    created_during = result.get('file_created_during_task')
    
    agent_values = {}
    if output_exists and created_during:
        score += 10
        feedback.append("Output file created during task.")
        
        # Parse content
        content = base64.b64decode(result.get('output_content_b64', '')).decode('utf-8', errors='ignore')
        agent_values = parse_key_value_output(content)
    elif output_exists:
        score += 5
        feedback.append("Output file exists but timestamp is suspect.")
    else:
        feedback.append("Output file missing.")

    # 3. Numeric Accuracy (70 pts total)
    gt = result.get('ground_truth', {})
    
    # Tolerances
    TOLERANCE_ABS = 1.0  # $1 tolerance is reasonable for manual calculation variations
    
    # Check Total Gap (20 pts)
    gt_total = gt.get('total_gap', 0)
    agent_total = agent_values.get('TOTAL_GAP')
    
    if agent_total is not None:
        if abs(agent_total - gt_total) <= TOLERANCE_ABS:
            score += 20
            feedback.append(f"Total Gap correct (Agent: {agent_total}, GT: {gt_total:.2f}).")
        else:
            feedback.append(f"Total Gap incorrect (Agent: {agent_total}, GT: {gt_total:.2f}).")
    else:
        feedback.append("TOTAL_GAP value not found in output.")

    # Check Explained (25 pts)
    gt_expl = gt.get('explained', 0)
    agent_expl = agent_values.get('EXPLAINED')
    
    if agent_expl is not None:
        if abs(agent_expl - gt_expl) <= TOLERANCE_ABS:
            score += 25
            feedback.append(f"Explained Effect correct (Agent: {agent_expl}, GT: {gt_expl:.2f}).")
        else:
            feedback.append(f"Explained Effect incorrect (Agent: {agent_expl}, GT: {gt_expl:.2f}).")
    else:
        feedback.append("EXPLAINED value not found in output.")

    # Check Unexplained (25 pts)
    gt_unexpl = gt.get('unexplained', 0)
    agent_unexpl = agent_values.get('UNEXPLAINED')
    
    if agent_unexpl is not None:
        if abs(agent_unexpl - gt_unexpl) <= TOLERANCE_ABS:
            score += 25
            feedback.append(f"Unexplained Effect correct (Agent: {agent_unexpl}, GT: {gt_unexpl:.2f}).")
        else:
            feedback.append(f"Unexplained Effect incorrect (Agent: {agent_unexpl}, GT: {gt_unexpl:.2f}).")
    else:
        feedback.append("UNEXPLAINED value not found in output.")

    # Final Pass/Fail
    passed = (score >= 70)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }