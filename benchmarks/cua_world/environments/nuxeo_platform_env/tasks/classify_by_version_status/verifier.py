#!/usr/bin/env python3
"""
Verifier for classify_by_version_status task.

Task Requirements:
- Product-Specs (v0.2) -> Drafts
- User-Guide (v1.0) -> Released
- Marketing-Flyer (v2.0) -> Released
- Internal-Memo (v0.5) -> Drafts
- Holding-Area Root -> Empty (except folders)

Verification:
- Uses /tmp/task_result.json generated by export_result.sh (API query).
- Also checks VLM trajectory to ensure the agent didn't just use API scripts (optional, but good practice).
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_classify_by_version_status(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy result file
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    if result.get('error'):
        return {"passed": False, "score": 0, "feedback": f"Export script error: {result['error']}"}

    score = 0
    feedback_parts = []
    
    docs = result.get('documents', {})
    
    # ----------------------------------------------------------------
    # Criterion 1: Product-Specs (0.2) -> Drafts (25 pts)
    # ----------------------------------------------------------------
    spec_loc = docs.get('Product-Specs', {}).get('parent_folder')
    if spec_loc == 'Drafts':
        score += 25
        feedback_parts.append("Product-Specs (v0.2) correctly in Drafts")
    else:
        feedback_parts.append(f"Product-Specs (v0.2) in {spec_loc} (Expected: Drafts)")

    # ----------------------------------------------------------------
    # Criterion 2: User-Guide (1.0) -> Released (25 pts)
    # ----------------------------------------------------------------
    guide_loc = docs.get('User-Guide', {}).get('parent_folder')
    if guide_loc == 'Released':
        score += 25
        feedback_parts.append("User-Guide (v1.0) correctly in Released")
    else:
        feedback_parts.append(f"User-Guide (v1.0) in {guide_loc} (Expected: Released)")

    # ----------------------------------------------------------------
    # Criterion 3: Marketing-Flyer (2.0) -> Released (25 pts)
    # ----------------------------------------------------------------
    flyer_loc = docs.get('Marketing-Flyer', {}).get('parent_folder')
    if flyer_loc == 'Released':
        score += 25
        feedback_parts.append("Marketing-Flyer (v2.0) correctly in Released")
    else:
        feedback_parts.append(f"Marketing-Flyer (v2.0) in {flyer_loc} (Expected: Released)")

    # ----------------------------------------------------------------
    # Criterion 4: Internal-Memo (0.5) -> Drafts (25 pts)
    # ----------------------------------------------------------------
    memo_loc = docs.get('Internal-Memo', {}).get('parent_folder')
    if memo_loc == 'Drafts':
        score += 25
        feedback_parts.append("Internal-Memo (v0.5) correctly in Drafts")
    else:
        feedback_parts.append(f"Internal-Memo (v0.5) in {memo_loc} (Expected: Drafts)")

    # ----------------------------------------------------------------
    # Bonus/Penalty: Clean Root
    # ----------------------------------------------------------------
    # If any document is still in 'Holding-Area' (root), we subtract points or fail?
    # The scoring says 25pts per doc. If a doc is in root, it got 0 for that doc.
    # The "root_clean" check in JSON can be used for feedback.
    if not result.get('root_clean', True):
        feedback_parts.append("Warning: Holding Area root still contains files")

    # Final Evaluation
    passed = (score == 100)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": ". ".join(feedback_parts)
    }