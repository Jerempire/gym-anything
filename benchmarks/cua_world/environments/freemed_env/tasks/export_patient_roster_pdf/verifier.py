#!/usr/bin/env python3
"""
Verifier for export_patient_roster_pdf task.

Checks:
1. File exists, was created during task, and has data (>0 bytes)
2. PDF Content Check: Uses pdfminer to extract text and verifies real patient names
   from the DB are present in the PDF document.
3. PDF Metadata Check: Verifies the PDF was generated by a browser engine (Mozilla/cairo/Skia)
   to prevent gaming via Python scripts querying the DB.
4. VLM Trajectory: Verifies UI workflow (navigating to patient list, opening print dialog).
"""

import os
import json
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_export_patient_roster(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    # 1. Read JSON results from container
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read task result: {e}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    output_exists = result.get('output_exists', False)
    file_created = result.get('file_created_during_task', False)
    size_bytes = result.get('output_size_bytes', 0)
    db_patients = result.get('db_patients', [])

    if output_exists and size_bytes > 1000:
        score += 15
        feedback_parts.append("PDF output exists and is non-empty")
    else:
        feedback_parts.append("PDF output missing or empty")
        return {"passed": False, "score": score, "feedback": " | ".join(feedback_parts)}

    if file_created:
        score += 10
        feedback_parts.append("File created during task window")
    else:
        feedback_parts.append("File appears to be old (timestamp check failed)")

    # 2. Extract and analyze PDF
    temp_pdf = tempfile.NamedTemporaryFile(delete=False, suffix='.pdf')
    extracted_text = ""
    pdf_is_browser_generated = False
    
    try:
        copy_from_env("/tmp/exported_roster.pdf", temp_pdf.name)
        
        # We use pdfminer (available in the host Python environment per spec)
        try:
            from pdfminer.high_level import extract_text
            from pdfminer.pdfparser import PDFParser
            from pdfminer.pdfdocument import PDFDocument
            
            # Extract Text
            extracted_text = extract_text(temp_pdf.name)
            
            # Extract Metadata
            with open(temp_pdf.name, 'rb') as fp:
                parser = PDFParser(fp)
                doc = PDFDocument(parser)
                info = doc.info[0] if doc.info else {}
                
                creator = info.get('Creator', b'').decode('utf-8', 'ignore').lower() if isinstance(info.get('Creator'), bytes) else str(info.get('Creator', '')).lower()
                producer = info.get('Producer', b'').decode('utf-8', 'ignore').lower() if isinstance(info.get('Producer'), bytes) else str(info.get('Producer', '')).lower()
                
                browser_signatures = ['mozilla', 'firefox', 'cairo', 'skia', 'chromium', 'webkit']
                if any(sig in creator or sig in producer for sig in browser_signatures):
                    pdf_is_browser_generated = True
                    
        except ImportError:
            logger.warning("pdfminer not found, fallback to basic text extraction")
            # Fallback if pdfminer is missing somehow
            import subprocess
            proc = subprocess.run(['strings', temp_pdf.name], capture_output=True, text=True)
            extracted_text = proc.stdout
            
            if 'cairo' in extracted_text.lower() or 'mozilla' in extracted_text.lower():
                pdf_is_browser_generated = True

    except Exception as e:
        logger.error(f"Error processing PDF: {e}")
    finally:
        if os.path.exists(temp_pdf.name):
            os.unlink(temp_pdf.name)

    # Score Metadata Check
    if pdf_is_browser_generated:
        score += 15
        feedback_parts.append("PDF metadata indicates browser generation")
    else:
        feedback_parts.append("PDF metadata does not indicate browser generation (potential script abuse)")

    # Score Text/Patient Extraction Match
    matches = 0
    extracted_text_lower = extracted_text.lower()
    for patient_name in db_patients:
        # Check if "First Last" is in text
        if patient_name.lower() in extracted_text_lower:
            matches += 1
        else:
            # Fallback: check if "Last, First" or both names are closely present
            parts = patient_name.split()
            if len(parts) >= 2:
                if (parts[1].lower() + ", " + parts[0].lower()) in extracted_text_lower:
                    matches += 1
                elif parts[0].lower() in extracted_text_lower and parts[1].lower() in extracted_text_lower:
                    matches += 1

    if matches >= 3:
        score += 35
        feedback_parts.append(f"Found {matches} actual patient names in PDF (>=3 required)")
    elif matches > 0:
        score += 15
        feedback_parts.append(f"Found only {matches} patient names in PDF")
    else:
        feedback_parts.append("No patient names found in PDF text")

    # 3. VLM Trajectory Verification
    vlm_score = 0
    try:
        from gym_anything.vlm import sample_trajectory_frames, query_vlm
        
        frames = sample_trajectory_frames(traj, n=6)
        prompt = """Review these screenshots from an agent's trajectory.
The agent was asked to export a patient roster as a PDF using the browser's "Print to PDF" functionality.
Did the agent:
1. Navigate to a patient list or report in the FreeMED application?
2. Open the browser's Print dialog?
3. Interact with the system's "Save As" file dialog?

Respond with a JSON object containing:
{
  "navigated_to_list": true/false,
  "opened_print_dialog": true/false,
  "interacted_with_save_dialog": true/false
}
"""
        vlm_result = query_vlm(images=frames, prompt=prompt)
        
        if vlm_result and "parsed" in vlm_result:
            parsed = vlm_result["parsed"]
            if parsed.get("navigated_to_list"): vlm_score += 10
            if parsed.get("opened_print_dialog"): vlm_score += 10
            if parsed.get("interacted_with_save_dialog"): vlm_score += 5
            
        score += vlm_score
        feedback_parts.append(f"VLM trajectory visual verification: +{vlm_score} pts")
    except Exception as e:
        logger.warning(f"VLM verification failed: {e}")
        # Give partial credit if script checking passed but VLM crashed
        if matches >= 3 and pdf_is_browser_generated:
            score += 25
            feedback_parts.append("VLM failed but programmatic checks passed strongly")

    passed = score >= 75
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }