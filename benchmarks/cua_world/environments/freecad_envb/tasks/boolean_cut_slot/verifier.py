#!/usr/bin/env python3
import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_boolean_cut_slot(traj, env_info, task_info):
    """
    Verifies the FreeCAD Boolean Cut task.
    
    Scoring Criteria:
    1. File creation/validity (15 pts)
    2. Anti-gaming timestamp check (5 pts)
    3. 'Cut' object presence in file (20 pts)
    4. Geometric properties:
       - Bounding box dimensions (10 pts)
       - Face count (indicates topology change) (15 pts)
       - Volume accuracy (25 pts)
       - Volume reduction check (10 pts)
    """
    
    # 1. Setup and retrieve data
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}

    # Load metadata
    meta = task_info.get('metadata', {})
    expected_vol_min = meta.get('expected_volume_min', 24400)
    expected_vol_max = meta.get('expected_volume_max', 26200)
    full_cyl_vol = meta.get('full_cylinder_volume', 28274)
    
    # Retrieve result JSON from container
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except FileNotFoundError:
        return {"passed": False, "score": 0, "feedback": "No result file generated by export script"}
    except json.JSONDecodeError:
        return {"passed": False, "score": 0, "feedback": "Result file corrupted or empty"}
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Error reading result: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # Initialize scoring
    score = 0
    feedback = []
    
    # Extract data
    file_exists = result.get('file_exists', False)
    created_during = result.get('file_created_during_task', False)
    file_size = result.get('file_size_bytes', 0)
    geo = result.get('geometry_analysis', {})
    
    # --- Check 1: File Existence & Validity (20 pts) ---
    if file_exists and file_size > 2000:
        score += 15
        feedback.append("File exists and is valid size.")
        
        if created_during:
            score += 5
            feedback.append("File created during task window.")
        else:
            feedback.append("Warning: File timestamp indicates it was not created during this session.")
    else:
        feedback.append("File missing or too small (empty).")
        return {"passed": False, "score": 0, "feedback": " ".join(feedback)}

    # --- Check 2: Cut Object Presence (20 pts) ---
    if geo.get('cut_object_present'):
        score += 20
        feedback.append("Cut object found in document.")
    else:
        feedback.append("No explicit 'Cut' object found.")

    # --- Check 3: Bounding Box (10 pts) ---
    # Expected: ~30x30x40
    bx = geo.get('bbox_x', 0)
    bz = geo.get('bbox_z', 0)
    if (29.0 < bx < 31.0) and (39.0 < bz < 41.0):
        score += 10
        feedback.append(f"Dimensions correct (Z={bz:.1f}mm).")
    else:
        feedback.append(f"Dimensions incorrect (X={bx:.1f}, Z={bz:.1f}).")

    # --- Check 4: Volume Accuracy (25 pts) ---
    vol = geo.get('volume', 0)
    if expected_vol_min < vol < expected_vol_max:
        score += 25
        feedback.append(f"Volume correct ({vol:.0f} mm³).")
    else:
        feedback.append(f"Volume {vol:.0f} mm³ out of range ({expected_vol_min}-{expected_vol_max}).")

    # --- Check 5: Face Count (15 pts) ---
    # Cylinder has 3 faces. Slotted cylinder should have at least 7.
    faces = geo.get('face_count', 0)
    if faces >= 7:
        score += 15
        feedback.append(f"Topology correct ({faces} faces).")
    else:
        feedback.append(f"Topology simple ({faces} faces) - cut may not have penetrated.")

    # --- Check 6: Volume Reduction (10 pts) ---
    # Ensure we actually removed material from a full cylinder
    if 0 < vol < (full_cyl_vol - 100):
        score += 10
        feedback.append("Material was successfully removed.")
    else:
        feedback.append("Volume matches full cylinder (no cut performed).")

    # Final result
    passed = (score >= 65) and (geo.get('cut_object_present', False) or faces >= 7) and (expected_vol_min < vol < expected_vol_max)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }