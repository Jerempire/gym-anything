#!/usr/bin/env python3
"""
Verifier for create_heatsink_array task.

Verification Strategy:
1. Programmatic Geometry Check (Primary):
   - Reads 'geometry_report.json' generated by FreeCADCmd inside the container.
   - Verifies Bounding Box (50x30x18mm).
   - Verifies Volume (~9000mm³).
   - Verifies Feature Tree contains "LinearPattern".

2. Visual Verification (VLM):
   - Checks screenshot for heat sink visual characteristics (parallel fins).
"""

import json
import os
import tempfile
import logging
from gym_anything.vlm import query_vlm, get_final_screenshot

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_create_heatsink_array(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    expected_bbox = metadata.get('expected_bbox', [50.0, 30.0, 18.0])
    bbox_tol = metadata.get('bbox_tolerance', 2.0)
    expected_vol = metadata.get('expected_volume', 9000.0)
    vol_tol_percent = metadata.get('volume_tolerance', 0.2)  # 20%

    score = 0
    feedback_parts = []
    
    # ------------------------------------------------------------------
    # 1. Fetch Basic Result Metadata
    # ------------------------------------------------------------------
    task_result = {}
    with tempfile.NamedTemporaryFile(delete=True, suffix='.json') as f:
        try:
            copy_from_env("/tmp/task_result.json", f.name)
            f.seek(0)
            task_result = json.load(f)
        except Exception as e:
            return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {e}"}

    if not task_result.get("output_exists", False):
        return {"passed": False, "score": 0, "feedback": "No output file found at expected location."}

    if not task_result.get("file_created_during_task", False):
        feedback_parts.append("Warning: File timestamp indicates it wasn't created during this task session.")
        # We don't fail immediately, but it's suspicious.

    # ------------------------------------------------------------------
    # 2. Fetch Geometry Analysis Report
    # ------------------------------------------------------------------
    geo_report = {}
    with tempfile.NamedTemporaryFile(delete=True, suffix='.json') as f:
        try:
            copy_from_env("/tmp/geometry_report.json", f.name)
            f.seek(0)
            geo_report = json.load(f)
        except Exception as e:
            feedback_parts.append(f"Could not read geometry report: {e}")
            geo_report = {"valid_file": False}

    # ------------------------------------------------------------------
    # 3. Score Geometric Properties (65 points total)
    # ------------------------------------------------------------------
    if geo_report.get("valid_file"):
        score += 10
        feedback_parts.append("Valid FreeCAD file.")

        # Bounding Box Check (30 pts)
        actual_bbox = geo_report.get("bbox", [0, 0, 0])
        bbox_match = True
        dims = ["Length", "Width", "Height"]
        # Allow axes to be swapped, so we sort dimensions
        sorted_expected = sorted(expected_bbox)
        sorted_actual = sorted(actual_bbox)
        
        for i in range(3):
            if abs(sorted_actual[i] - sorted_expected[i]) > bbox_tol:
                bbox_match = False
                feedback_parts.append(f"Dimension mismatch: Expected ~{sorted_expected[i]}, got {sorted_actual[i]:.1f}")
        
        if bbox_match:
            score += 30
            feedback_parts.append("Bounding box dimensions correct.")
        else:
            # Partial credit for being close
            if all(abs(sorted_actual[i] - sorted_expected[i]) < 10 for i in range(3)):
                score += 10
                feedback_parts.append("Dimensions roughly correct.")

        # Volume Check (15 pts)
        actual_vol = geo_report.get("volume", 0)
        min_vol = expected_vol * (1.0 - vol_tol_percent)
        max_vol = expected_vol * (1.0 + vol_tol_percent)
        
        if min_vol <= actual_vol <= max_vol:
            score += 15
            feedback_parts.append(f"Volume correct ({actual_vol:.1f} mm³).")
        else:
            feedback_parts.append(f"Volume incorrect: {actual_vol:.1f} (Expected {expected_vol}).")

        # Feature Check (10 pts)
        if geo_report.get("has_linear_pattern"):
            score += 10
            feedback_parts.append("LinearPattern feature detected.")
        else:
            feedback_parts.append("LinearPattern feature NOT detected (check failed).")
            
    else:
        feedback_parts.append("File is not a valid FreeCAD document.")

    # ------------------------------------------------------------------
    # 4. VLM Visual Verification (35 points)
    # ------------------------------------------------------------------
    final_screenshot = get_final_screenshot(traj)
    if final_screenshot:
        prompt = """
        You are verifying a FreeCAD task. The user was asked to create a 'plate-fin heat sink'.
        
        Look at the image and check for:
        1. A rectangular base plate.
        2. Multiple parallel vertical fins (thin walls) sticking up from the base.
        3. The fins should look evenly spaced.
        
        Does the object look like a heat sink?
        Return JSON: {"looks_like_heatsink": bool, "has_parallel_fins": bool, "confidence": float}
        """
        
        vlm_res = query_vlm(prompt=prompt, image=final_screenshot)
        
        if vlm_res.get("success"):
            parsed = vlm_res.get("parsed", {})
            if parsed.get("looks_like_heatsink") or parsed.get("has_parallel_fins"):
                score += 35
                feedback_parts.append("Visual verification passed.")
            else:
                score += 5 # Pity points for screenshot existence
                feedback_parts.append("Visual verification failed: Does not look like a heat sink.")
        else:
            feedback_parts.append("VLM query failed.")
    else:
        feedback_parts.append("No screenshot available for visual verification.")

    # ------------------------------------------------------------------
    # Final Result
    # ------------------------------------------------------------------
    # Key criteria: Must have decent dimensions (bbox match) or valid file + visual confirmation
    # Threshold: 60 points
    
    passed = score >= 60
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback_parts)
    }