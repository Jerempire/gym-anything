#!/usr/bin/env python3
"""
Verifier for Setup Apparel Product Type task.

Checks the JSON output generated by the Drush introspection script.
Scoring is based on the existence and correct configuration of:
1. Attributes (Color, Size) and their values
2. Variation Type (Apparel) linked to attributes
3. Product Type (Apparel) linked to variation type
4. Product (Logo Tee) with multiple variations and correct price
"""

import json
import logging
import os
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_setup_apparel_product_type(traj, env_info, task_info):
    """
    Verify the Apparel product infrastructure setup.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    expected_price = metadata.get('expected_price', 24.99)
    min_variations = metadata.get('min_variations', 4)

    # Load result from container
    try:
        temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
        try:
            copy_from_env("/tmp/task_result.json", temp_result.name)
            with open(temp_result.name, 'r') as f:
                result = json.load(f)
        finally:
            os.unlink(temp_result.name)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result file: {e}"}

    score = 0
    feedback = []
    
    if result.get('error'):
        return {"passed": False, "score": 0, "feedback": f"Introspection script error: {result['error']}"}

    # 1. Verify Attributes (30 pts)
    # Color (15 pts)
    color_data = result.get('attributes', {}).get('color', {})
    if color_data.get('exists'):
        # Check values
        values = [v.lower() for v in color_data.get('values', [])]
        required_colors = ['black', 'white', 'navy']
        matching_colors = [c for c in required_colors if c in values]
        
        if len(matching_colors) == 3:
            score += 15
            feedback.append("Attribute 'Color' created with all correct values.")
        else:
            score += 10
            feedback.append(f"Attribute 'Color' exists but missing values (found: {color_data.get('values')}).")
    else:
        feedback.append("Attribute 'Color' NOT found.")

    # Size (15 pts)
    size_data = result.get('attributes', {}).get('size', {})
    if size_data.get('exists'):
        values = [v.lower() for v in size_data.get('values', [])]
        required_sizes = ['s', 'm', 'l', 'xl']
        matching_sizes = [s for s in required_sizes if s in values]
        
        if len(matching_sizes) == 4:
            score += 15
            feedback.append("Attribute 'Size' created with all correct values.")
        else:
            score += 10
            feedback.append(f"Attribute 'Size' exists but missing values (found: {size_data.get('values')}).")
    else:
        feedback.append("Attribute 'Size' NOT found.")

    # 2. Verify Variation Type (20 pts)
    var_type = result.get('variation_type', {})
    if var_type.get('exists'):
        if var_type.get('has_color_field') and var_type.get('has_size_field'):
            score += 20
            feedback.append("Variation Type 'Apparel' created and linked to both attributes.")
        elif var_type.get('has_color_field') or var_type.get('has_size_field'):
            score += 10
            feedback.append("Variation Type 'Apparel' exists but is missing one attribute linkage.")
        else:
            score += 5
            feedback.append("Variation Type 'Apparel' exists but attributes are NOT linked.")
    else:
        feedback.append("Variation Type 'Apparel' NOT found.")

    # 3. Verify Product Type (15 pts)
    prod_type = result.get('product_type', {})
    if prod_type.get('exists'):
        if prod_type.get('variation_type_id') == 'apparel':
            score += 15
            feedback.append("Product Type 'Apparel' created and uses 'Apparel' variation type.")
        else:
            score += 5
            feedback.append("Product Type 'Apparel' exists but uses wrong variation type.")
    else:
        feedback.append("Product Type 'Apparel' NOT found.")

    # 4. Verify Product and Variations (35 pts)
    product = result.get('product', {})
    if product.get('exists'):
        if product.get('type') == 'apparel':
            score += 10
            feedback.append("Product 'Urban Electronics Logo Tee' created with correct type.")
            
            # Check variations
            vars_data = product.get('variations', [])
            var_count = len(vars_data)
            
            if var_count >= min_variations:
                score += 15
                feedback.append(f"Product has {var_count} variations (>= {min_variations}).")
                
                # Check price
                prices = [v.get('price', 0) for v in vars_data]
                correct_prices = [p for p in prices if abs(p - expected_price) < 0.01]
                
                if len(correct_prices) == len(prices):
                    score += 10
                    feedback.append(f"All variations have correct price ${expected_price}.")
                elif len(correct_prices) > 0:
                    score += 5
                    feedback.append(f"Some variations have correct price (found {len(correct_prices)}/{len(prices)}).")
                else:
                    feedback.append(f"Variations have wrong price (expected {expected_price}, got {prices}).")
            else:
                score += 5
                feedback.append(f"Product exists but has too few variations ({var_count}/{min_variations}).")
        else:
            feedback.append(f"Product found but has wrong type: {product.get('type')}.")
    elif product.get('is_fuzzy_match'):
        feedback.append(f"Product with similar title '{product.get('title_found')}' found (check failed exact match).")
    else:
        feedback.append("Product 'Urban Electronics Logo Tee' NOT found.")

    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }