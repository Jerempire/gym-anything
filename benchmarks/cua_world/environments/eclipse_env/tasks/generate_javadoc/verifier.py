#!/usr/bin/env python3
"""Verifier for generate_javadoc task."""

import json
import tempfile
import os
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_generate_javadoc(traj, env_info, task_info):
    """
    Verify that JavaDoc was generated correctly.
    
    Criteria:
    1. index.html exists and was created during task (20 pts)
    2. Package structure exists (15 pts)
    3. All 5 Class HTML files exist (25 pts)
    4. Content validation (author/version/methods present) (30 pts)
    5. VLM verification of UI workflow (10 pts)
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    # 1. Load basic result JSON
    try:
        tmp_res = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
        tmp_res.close()
        copy_from_env("/tmp/task_result.json", tmp_res.name)
        with open(tmp_res.name, 'r') as f:
            result = json.load(f)
        os.unlink(tmp_res.name)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {e}"}

    output_dir = result.get('output_dir_used', '/home/ga/javadoc-output')

    # Criterion 1: Index file (20 pts)
    if result.get('index_exists') and result.get('created_during_task'):
        score += 20
        feedback_parts.append("index.html created successfully")
    elif result.get('index_exists'):
        score += 10
        feedback_parts.append("index.html exists but timestamp check failed")
    else:
        feedback_parts.append("index.html not found")

    # Criterion 2: Package structure (15 pts)
    if result.get('pkg_text_exists'): score += 5
    if result.get('pkg_math_exists'): score += 5
    if result.get('pkg_coll_exists'): score += 5
    
    # Criterion 3: Class files count (25 pts)
    # 5 points per class file found (max 25)
    class_count = result.get('class_files_count', 0)
    class_score = min(25, class_count * 5)
    score += class_score
    feedback_parts.append(f"Found {class_count}/5 class HTML files")

    # Criterion 4: Content Validation (30 pts)
    # We need to read a file to ensure it's actual JavaDoc, not empty files
    content_score = 0
    
    def read_remote_file(path):
        try:
            t = tempfile.NamedTemporaryFile(delete=False)
            t.close()
            copy_from_env(path, t.name)
            with open(t.name, 'r', errors='ignore') as f:
                data = f.read()
            os.unlink(t.name)
            return data
        except:
            return ""

    string_utils_path = f"{output_dir}/com/devutils/text/StringUtils.html"
    su_content = read_remote_file(string_utils_path)
    
    if len(su_content) > 200:
        # Check for standard JavaDoc elements
        if "StringUtils" in su_content: content_score += 5
        if "isEmpty" in su_content: content_score += 5  # Method check
        if "reverse" in su_content: content_score += 5  # Method check
        if "DevUtils Team" in su_content: content_score += 5 # Author tag
        if "1.0.0" in su_content or "Since" in su_content: content_score += 5 # Since tag
        if "Generated by javadoc" in su_content or "<!-- Generated by javadoc" in su_content: content_score += 5
        
        score += content_score
        feedback_parts.append(f"Content validation: {content_score}/30 pts")
    else:
        feedback_parts.append("Content validation failed (StringUtils.html missing or empty)")

    # Criterion 5: VLM Verification (10 pts)
    try:
        import sys
        sys.path.insert(0, '/workspace/utils')
        from eclipse_verification_utils import vlm_verify_eclipse_task
        
        vlm_res = vlm_verify_eclipse_task(
            traj, env_info,
            task_description="Generate JavaDoc using the Eclipse Wizard",
            checklist_items=[
                "Eclipse 'Generate Javadoc' wizard window is visible",
                "Destination directory is configured to /home/ga/javadoc-output",
                "Finish button was clicked in the wizard",
                "Console output shows 'Javadoc generation complete' or similar"
            ]
        )
        if vlm_res and vlm_res.get('vlm_passed'):
            score += 10
            feedback_parts.append("VLM: Workflow confirmed")
        else:
            feedback_parts.append("VLM: Workflow verification failed/unavailable")
            
    except Exception as e:
        logger.warning(f"VLM error: {e}")

    # Final scoring logic
    passed = score >= 60 and result.get('index_exists')
    
    return {
        "passed": passed,
        "score": min(100, score),
        "feedback": " | ".join(feedback_parts)
    }