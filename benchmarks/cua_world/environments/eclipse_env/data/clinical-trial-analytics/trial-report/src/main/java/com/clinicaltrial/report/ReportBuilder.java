package com.clinicaltrial.report;

import com.clinicaltrial.model.TrialSummary;

/**
 * Builds comprehensive clinical trial reports from analysis summaries.
 */
public class ReportBuilder {

    private final SummaryFormatter formatter;

    public ReportBuilder() {
        this.formatter = new SummaryFormatter();
    }

    /**
     * Generates a full report string including header, formatted summary,
     * and a validation section that checks data integrity.
     */
    public String buildReport(TrialSummary summary) {
        StringBuilder sb = new StringBuilder();
        sb.append("CLINICAL TRIAL ANALYSIS REPORT\n");
        sb.append("Generated by Clinical Trial Analytics Platform v1.0\n");
        sb.append("=".repeat(50)).append("\n\n");

        sb.append(formatter.formatForSubmission(summary));
        sb.append("\n");

        sb.append("DATA VALIDATION\n");
        sb.append("-".repeat(30)).append("\n");
        sb.append("Trial ID present: ").append(summary.getTrialId() != null).append("\n");
        sb.append("Sample size > 0:  ").append(summary.getSampleSize() > 0).append("\n");
        sb.append("CI valid:         ").append(summary.getCiLower() < summary.getCiUpper()).append("\n");

        return sb.toString();
    }

    /**
     * Returns just the sample size from a summary, used for quick validation.
     */
    public int extractSampleSize(TrialSummary summary) {
        return summary.getSampleSize();
    }
}
