#!/usr/bin/env python3
"""
Verifier for Generate Embedded Widget Code task.

Scoring (100 points):
- File exists: 10 pts
- Contains iframe tag: 10 pts
- Correct widget module (VisitsSummary): 20 pts
- Correct widget action (getEvolutionGraph): 20 pts
- Correct Site ID (1): 10 pts
- Auth token included (valid format): 30 pts

Pass threshold: 70 points AND Auth token included.
"""

import json
import logging
import os
import tempfile
import re
from typing import Dict, Any

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_generate_embedded_widget_code(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]) -> Dict[str, Any]:
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    expected_module = metadata.get('expected_module', 'VisitsSummary')
    expected_action = metadata.get('expected_action', 'getEvolutionGraph')
    
    try:
        temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
        try:
            copy_from_env("/tmp/widget_code_result.json", temp_result.name)
            with open(temp_result.name, 'r') as f:
                result = json.load(f)
        finally:
            if os.path.exists(temp_result.name):
                os.unlink(temp_result.name)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {e}"}

    score = 0
    feedback = []
    
    file_exists = result.get('file_exists', False)
    content = result.get('file_content', '')
    task_start = result.get('task_start_timestamp', 0)
    file_mtime = result.get('file_modified_time', 0)

    # CRITERION 1: File Existence (10 pts)
    if file_exists and len(content) > 0:
        score += 10
        feedback.append("File created successfully")
    else:
        return {"passed": False, "score": 0, "feedback": "Output file not found or empty"}

    # Anti-gaming: File creation time
    if file_mtime < task_start:
        feedback.append("WARNING: File modification time is before task start (pre-existing file?)")
        # We don't fail immediately but note it, though usually this suggests gaming.
        # For this task we'll allow it if content is correct, but typically we'd penalize.

    # CRITERION 2: Iframe Tag (10 pts)
    if "<iframe" in content.lower():
        score += 10
        feedback.append("Contains iframe tag")
    else:
        feedback.append("Missing <iframe> tag")

    # CRITERION 3: Correct Module (20 pts)
    # Pattern looks like: module=VisitsSummary
    if f"module={expected_module}" in content:
        score += 20
        feedback.append(f"Correct widget module ({expected_module})")
    else:
        feedback.append(f"Incorrect widget module (expected {expected_module})")

    # CRITERION 4: Correct Action (20 pts)
    # Pattern looks like: action=getEvolutionGraph
    if f"action={expected_action}" in content:
        score += 20
        feedback.append(f"Correct visualization graph ({expected_action})")
    else:
        feedback.append(f"Incorrect visualization action (expected {expected_action})")

    # CRITERION 5: Site ID (10 pts)
    if "idSite=1" in content or "idsite=1" in content:
        score += 10
        feedback.append("Correct Site ID")
    else:
        feedback.append("Missing or incorrect Site ID")

    # CRITERION 6: Auth Token (30 pts) - CRITICAL
    # Look for token_auth= followed by 32 hex chars
    token_match = re.search(r'token_auth=([a-f0-9]{32})', content, re.IGNORECASE)
    has_token = False
    
    if token_match:
        token = token_match.group(1)
        # We assume if it matches the pattern [a-f0-9]{32} it's likely valid generated by UI.
        # Checking against "anonymous" or empty
        if token != "anonymous":
            score += 30
            has_token = True
            feedback.append("Auth token included")
        else:
            feedback.append("Token is 'anonymous' (checkbox likely not checked)")
    else:
        feedback.append("No auth token found in URL")

    # Final logic
    passed = (score >= 70) and has_token
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }