#!/usr/bin/env python3
"""
Verifier for Compare Weekend vs Weekday Sales task.
"""

import json
import os
import tempfile
import logging
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_compare_weekend_weekday_sales(traj, env_info, task_info):
    """
    Verifies that the agent created a Weekend vs Weekday analysis.
    
    Criteria:
    1. Workbook 'Weekend_Analysis.dva' exists and was created during the task.
    2. The workbook contains the calculated field logic (CASE/IF "Weekend").
    3. VLM: Trajectory shows usage of Calculation Editor.
    4. VLM: Final screenshot shows a Donut/Pie chart with correct title.
    """
    
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    # 1. File-based Verification (via export_result.ps1)
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Copy the JSON result generated by the PowerShell script
        copy_from_env("C:\\Windows\\Temp\\task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            file_result = json.load(f)
    except Exception as e:
        logger.error(f"Failed to read task result: {e}")
        file_result = {}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)
            
    # Score File Existence and Validity (40 pts)
    if file_result.get("output_exists", False):
        score += 10
        feedback_parts.append("Workbook file saved.")
        
        if file_result.get("file_created_during_task", False):
            score += 10
            feedback_parts.append("File created during task session.")
        else:
            feedback_parts.append("Warning: File timestamp indicates it was not created in this session.")
            
        if file_result.get("contains_calculation_logic", False):
            score += 20
            feedback_parts.append("Calculation logic (CASE/Weekend) found in workbook.")
        else:
            feedback_parts.append("Could not confirm calculation logic in file.")
    else:
        feedback_parts.append("Workbook 'Weekend_Analysis.dva' not found.")

    # 2. VLM Verification (60 pts)
    # Use trajectory to verify process and final result
    frames = sample_trajectory_frames(traj, n=4)
    final_screen = get_final_screenshot(traj)
    
    if not frames or not final_screen:
        return {"passed": False, "score": score, "feedback": "No visual evidence available."}
        
    all_images = frames + [final_screen]
    
    prompt = """
    You are verifying an Oracle Analytics Desktop task.
    The user was asked to:
    1. Create a calculated column 'Day Type' to distinguish Weekend vs Weekday.
    2. Create a Donut or Pie chart showing Sales by Day Type.
    3. Title the chart "Revenue Share: Weekend vs Weekday".
    
    Review the screenshots provided.
    
    Check for:
    1. **Calculation Editor**: Do you see a screen where the user is writing a formula (e.g., CASE WHEN...)?
    2. **Chart Type**: Is there a Donut or Pie chart visible in the final or near-final frames?
    3. **Title/Labels**: Does the chart have the title "Revenue Share: Weekend vs Weekday" or show labels like "Weekend"/"Weekday"?
    
    Return JSON:
    {
        "calculation_editor_seen": true/false,
        "donut_or_pie_chart_present": true/false,
        "correct_title_or_labels": true/false,
        "confidence": "high/medium/low"
    }
    """
    
    try:
        vlm_resp = query_vlm(images=all_images, prompt=prompt)
        vlm_result = vlm_resp.get("parsed", {})
        
        if vlm_result.get("calculation_editor_seen", False):
            score += 20
            feedback_parts.append("VLM: Calculation editor usage observed.")
        
        if vlm_result.get("donut_or_pie_chart_present", False):
            score += 20
            feedback_parts.append("VLM: Donut/Pie chart detected.")
            
        if vlm_result.get("correct_title_or_labels", False):
            score += 20
            feedback_parts.append("VLM: Correct title or data labels observed.")
            
    except Exception as e:
        logger.error(f"VLM check failed: {e}")
        feedback_parts.append("VLM verification failed.")

    # Final logic
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback_parts)
    }