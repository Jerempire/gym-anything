#!/usr/bin/env python3
import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_botanical_timelapse(traj, env_info, task_info):
    """
    Verifies the complex multi-stage media extraction task.
    Uses multiple signals including embedded ffprobe data, trajectory VLM checks, and anti-gaming file timestamps.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    # 1. Read metadata produced by export_result.sh
    temp_meta = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/export_metadata.json", temp_meta.name)
        with open(temp_meta.name, 'r') as f:
            meta = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read exported metadata: {e}"}
    finally:
        if os.path.exists(temp_meta.name):
            os.unlink(temp_meta.name)

    # 2. Read manifest generated by agent
    temp_manifest = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    manifest = None
    try:
        copy_from_env("/tmp/manifest.json", temp_manifest.name)
        with open(temp_manifest.name, 'r') as f:
            manifest = json.load(f)
    except Exception:
        pass  # Handled below in Manifest criterion
    finally:
        if os.path.exists(temp_manifest.name):
            os.unlink(temp_manifest.name)

    # Variables extraction
    task_start = meta.get('task_start_time', 0)
    master_mtime = meta.get('master_mtime', 0)
    web_mtime = meta.get('web_mtime', 0)
    manifest_mtime = meta.get('manifest_mtime', 0)
    frames = meta.get('frames', [])
    master_info = meta.get('master_info', {})
    web_info = meta.get('web_info', {})

    # --- Criterion 1: Frames Extraction & Variance (20 pts) ---
    num_frames = len(frames)
    if 11 <= num_frames <= 14:
        score += 10
        feedback_parts.append(f"Frames count correct ({num_frames})")
    elif num_frames > 0:
        score += 5
        feedback_parts.append(f"Frames partially extracted ({num_frames})")
    else:
        feedback_parts.append("No frames extracted")

    # Anti-gaming: Ensure frames aren't just duplicates of the exact same image
    sizes = set(f.get('size', 0) for f in frames)
    if len(sizes) > 1:
        score += 10
        feedback_parts.append("Frames show size variance (successfully captured timeline)")
    elif num_frames > 0:
        feedback_parts.append("Frames lack variance (possible duplication)")

    # --- Criterion 2: Master Clip Validations (15 pts) ---
    master_streams = master_info.get('streams', [])
    master_format = master_info.get('format', {})
    if master_streams:
        v_stream = next((s for s in master_streams if s.get('codec_type') == 'video'), {})
        a_stream = next((s for s in master_streams if s.get('codec_type') == 'audio'), None)
        dur = float(master_format.get('duration', 0))
        
        c2_score = 0
        if 14.0 <= dur <= 16.0: c2_score += 5
        if v_stream.get('codec_name', '').lower() == 'h264': c2_score += 5
        if a_stream is not None: c2_score += 5
        
        score += c2_score
        feedback_parts.append(f"Master clip criteria: {c2_score}/15 points")
    else:
        feedback_parts.append("Master clip missing or invalid")

    # --- Criterion 3: Web Clip Format & Container Validations (15 pts) ---
    web_streams = web_info.get('streams', [])
    web_format = web_info.get('format', {})
    if web_streams:
        v_stream = next((s for s in web_streams if s.get('codec_type') == 'video'), {})
        width = v_stream.get('width', 0)
        height = v_stream.get('height', 0)
        codec = v_stream.get('codec_name', '').lower()
        fmt_name = web_format.get('format_name', '').lower()
        
        c3_score = 0
        if codec == 'vp8': c3_score += 5
        if width == 640 and height == 360: c3_score += 5
        if 'webm' in fmt_name or 'matroska' in fmt_name: c3_score += 5
        
        score += c3_score
        feedback_parts.append(f"Web clip format criteria: {c3_score}/15 points")
    else:
        feedback_parts.append("Web clip missing or invalid")

    # --- Criterion 4: Web Clip Audio Stripping Validation (15 pts) ---
    if web_streams:
        a_stream = next((s for s in web_streams if s.get('codec_type') == 'audio'), None)
        if a_stream is None:
            score += 15
            feedback_parts.append("Web clip audio successfully stripped")
        else:
            feedback_parts.append("Web clip still contains audio")

    # --- Criterion 5: Manifest JSON (10 pts) ---
    if manifest and isinstance(manifest, dict):
        has_keys = all(k in manifest for k in ['project', 'extracted_frame_count', 'master_clip', 'web_clip', 'web_clip_has_audio'])
        if has_keys:
            score += 10
            feedback_parts.append("Manifest JSON is valid and complete")
        else:
            score += 5
            feedback_parts.append("Manifest JSON is missing required keys")
    else:
        feedback_parts.append("Manifest JSON missing or invalid")

    # --- Criterion 6: Anti-gaming Timestamp Check (10 pts) ---
    if task_start > 0:
        valid_mtimes = 0
        if master_mtime >= task_start: valid_mtimes += 1
        if web_mtime >= task_start: valid_mtimes += 1
        if manifest_mtime >= task_start: valid_mtimes += 1
        
        if valid_mtimes >= 2:
            score += 10
            feedback_parts.append("Files created chronologically during task")
        else:
            feedback_parts.append("Files predated task start (possible gaming)")

    # --- Criterion 7: VLM Trajectory Process Verification (15 pts) ---
    vlm_points = 0
    query_vlm = env_info.get('query_vlm')
    if query_vlm:
        try:
            from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot
            frames_imgs = sample_trajectory_frames(traj, n=4)
            final_img = get_final_screenshot(traj)
            images = frames_imgs + [final_img] if final_img else frames_imgs
            
            if images:
                prompt = "Analyze the sequence of screenshots. The agent was asked to perform video transcoding, clipping, and frame extraction. Did the agent open a terminal/command prompt to run commands (like ffmpeg), OR use a media application (like VLC)? Reply with valid JSON containing a single boolean key 'evidence_of_work'."
                
                res = query_vlm(images=images, prompt=prompt)
                res_str = str(res).lower()
                
                if 'true' in res_str or "'evidence_of_work': true" in res_str:
                    vlm_points = 15
                    feedback_parts.append("VLM verified processing tool usage")
                else:
                    feedback_parts.append("VLM did not detect evidence of video processing")
        except Exception as e:
            logger.warning(f"VLM verification error: {e}")
            vlm_points = 15
            feedback_parts.append("VLM bypassed due to execution error")
    else:
        vlm_points = 15
        feedback_parts.append("VLM bypassed (not available)")
    score += vlm_points

    # Success conditions
    passed = score >= 70

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }