#!/bin/bash
echo "=== Setting up legacy camera stream relay task ==="

# Record task start time
date +%s > /tmp/task_start_time.txt

# Clean up any existing VLC or ffmpeg processes to ensure a clean state
killall vlc 2>/dev/null || true
killall cvlc 2>/dev/null || true
killall ffmpeg 2>/dev/null || true
sleep 2

# Remove any old artifacts
rm -f /home/ga/start_relay.sh
rm -f /home/ga/Videos/archive.mp4

# Create the VLC Stream Output (sout) Cheatsheet
mkdir -p /home/ga/Documents
cat > /home/ga/Documents/vlc_streaming_cheatsheet.txt << 'EOF'
=== VLC Stream Output (sout) Cheat Sheet ===

VLC's stream output module allows you to transcode and route media to various destinations.

1. Basic Syntax:
   cvlc <input_stream> --sout '#<module1>{<options>}:<module2>{<options>}'

2. Transcode Module:
   transcode{vcodec=...,vb=...,acodec=...,ab=...}
   - vcodec: video codec (e.g., h264, mp2v)
   - vb: video bitrate in kbps (e.g., 800)
   - acodec: audio codec (e.g., mp4a for AAC, mp3)
   - ab: audio bitrate in kbps (e.g., 128)

3. Duplicate Module (Routes a stream to multiple destinations simultaneously):
   duplicate{dst=<dest1>,dst=<dest2>}

4. Standard Output (std) Module (For saving directly to a file):
   std{access=file,mux=mp4,dst=/path/to/output.mp4}

5. RTP/RTSP Output Module (For hosting an active network stream):
   rtp{sdp=rtsp://:8554/stream_name}

EXAMPLE 1 - Transcode and save to a file:
cvlc input.mp4 --sout '#transcode{vcodec=h264,vb=1000,acodec=mp4a,ab=128}:std{access=file,mux=mp4,dst=output.mp4}'

EXAMPLE 2 - Read from UDP and duplicate to two different files:
cvlc udp://@127.0.0.1:1234 --sout '#duplicate{dst=std{access=file,mux=mp4,dst=file1.mp4},dst=std{access=file,mux=mp4,dst=file2.mp4}}'

Note: When chaining modules inside --sout, use a colon (:) to connect sequential steps like transcode to duplicate.
EOF

chown ga:ga /home/ga/Documents/vlc_streaming_cheatsheet.txt

# Start the dummy legacy MPEG-2 UDP camera stream
# We use the pre-existing sample_video.mp4 generated by the environment and loop it infinitely
echo "Starting legacy UDP camera stream..."
su - ga -c "ffmpeg -re -stream_loop -1 -i /home/ga/Videos/sample_video.mp4 -c:v mpeg2video -b:v 2M -c:a mp2 -b:a 128k -f mpegts udp://127.0.0.1:5000 > /tmp/legacy_cam.log 2>&1 &"

# Ensure stream has started
sleep 3
if ! pgrep -f "udp://127.0.0.1:5000" > /dev/null; then
    echo "WARNING: Failed to start simulated UDP camera stream."
else
    echo "UDP stream is active on udp://@127.0.0.1:5000"
fi

# Take initial state screenshot (showing a clean desktop)
DISPLAY=:1 scrot /tmp/task_initial.png 2>/dev/null || true

echo "=== Setup complete ==="