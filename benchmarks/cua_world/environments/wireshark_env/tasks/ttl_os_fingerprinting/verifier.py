#!/usr/bin/env python3
"""
Verifier for ttl_os_fingerprinting task.
Scores the task based on the JSON report generated by export_result.sh.
"""

import json
import tempfile
import os
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_ttl_os_fingerprinting(traj, env_info, task_info):
    """
    Verify the TTL Fingerprinting task.
    
    Scoring:
    - Report CSV exists & created during task: 10 pts
    - IP Completeness (>=90%): 15 pts
    - TTL Mode Accuracy (>=90%): 20 pts
    - Initial TTL Mapping (>=90%): 15 pts
    - Hop Count Calc (>=90%): 10 pts
    - OS Classification (>=90%): 10 pts
    - Summary File Exists: 5 pts
    - Summary Content (Total Count): 5 pts
    - Summary Content (OS Counts): 5 pts
    - Summary Content (Min/Max Hop): 5 pts
    
    Total: 100 pts
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy result file
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 1. File Existence & Timing (10 pts)
    if result.get('report_exists') and result.get('report_created_during_task'):
        score += 10
        feedback_parts.append("CSV report created")
    elif result.get('report_exists'):
        score += 5
        feedback_parts.append("CSV report exists but timestamp outdated")
    else:
        feedback_parts.append("CSV report missing")
        return {"passed": False, "score": 0, "feedback": " | ".join(feedback_parts)}

    # Get verification data
    v_data = result.get('verification_data', {})
    metrics = v_data.get('metrics', {})
    summary_checks = v_data.get('summary_checks', {})
    
    if not v_data.get('csv_valid', False):
        return {"passed": False, "score": score, "feedback": "CSV file invalid or empty"}

    # 2. Completeness (15 pts)
    completeness = metrics.get('completeness', 0.0)
    if completeness >= 0.9: score += 15
    elif completeness >= 0.7: score += 10
    elif completeness >= 0.5: score += 5
    feedback_parts.append(f"Completeness: {completeness:.1%}")

    # 3. TTL Accuracy (20 pts)
    ttl_acc = metrics.get('ttl_accuracy', 0.0)
    if ttl_acc >= 0.9: score += 20
    elif ttl_acc >= 0.7: score += 15
    elif ttl_acc >= 0.5: score += 10
    feedback_parts.append(f"TTL Acc: {ttl_acc:.1%}")

    # 4. Initial TTL (15 pts)
    init_acc = metrics.get('initial_ttl_accuracy', 0.0)
    if init_acc >= 0.9: score += 15
    elif init_acc >= 0.7: score += 10
    elif init_acc >= 0.5: score += 5
    
    # 5. Hop Count (10 pts)
    hop_acc = metrics.get('hop_count_accuracy', 0.0)
    if hop_acc >= 0.9: score += 10
    elif hop_acc >= 0.7: score += 7
    elif hop_acc >= 0.5: score += 4
    
    # 6. OS Class (10 pts)
    os_acc = metrics.get('os_classification_accuracy', 0.0)
    if os_acc >= 0.9: score += 10
    elif os_acc >= 0.7: score += 7
    elif os_acc >= 0.5: score += 4
    feedback_parts.append(f"OS Acc: {os_acc:.1%}")

    # 7. Summary File (20 pts total)
    if result.get('summary_exists'):
        score += 5
        if summary_checks.get('total_count_correct'):
            score += 5
            feedback_parts.append("Summary Total correct")
        
        os_match_ratio = summary_checks.get('os_counts_match', 0.0)
        if os_match_ratio >= 0.8:
            score += 5
            feedback_parts.append("Summary OS Counts correct")
            
        if summary_checks.get('min_max_hop_correct'):
            score += 5
            feedback_parts.append("Summary Min/Max correct")
    else:
        feedback_parts.append("Summary file missing")

    passed = score >= 60 and completeness >= 0.8
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }