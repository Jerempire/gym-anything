#!/usr/bin/env python3
"""
Verifier for TCP Options Negotiation Analysis task.
Checks the generated CSV and summary text file against ground truth generated by tshark.
"""

import json
import os
import sys
import tempfile
import csv
import logging
import shutil

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_tcp_options_negotiation_analysis(traj, env_info, task_info):
    """
    Verify the TCP options analysis task.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Define paths
    metadata = task_info.get('metadata', {})
    ground_truth_dir = metadata.get('ground_truth_dir', '/var/lib/wireshark_ground_truth')
    
    # Create temp directory for analysis
    with tempfile.TemporaryDirectory() as temp_dir:
        # 1. Fetch Task Result JSON
        try:
            copy_from_env("/tmp/task_result.json", os.path.join(temp_dir, "task_result.json"))
            with open(os.path.join(temp_dir, "task_result.json"), 'r') as f:
                task_result = json.load(f)
        except Exception as e:
            return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task metadata: {str(e)}"}

        # 2. Fetch Ground Truth Files
        # We need to copy them from the container because they were generated in setup_task.sh
        gt_files = {
            "syn_count.txt": "syn_count",
            "ground_truth_syns.csv": "gt_csv",
            "unique_mss.txt": "unique_mss",
            "unique_wscale.txt": "unique_wscale",
            "sack_count.txt": "sack_count",
            "ts_count.txt": "ts_count",
            "most_common_mss.txt": "common_mss",
            "most_common_dst_port.txt": "common_port"
        }
        
        gt_data = {}
        for filename, key in gt_files.items():
            local_path = os.path.join(temp_dir, filename)
            try:
                copy_from_env(os.path.join(ground_truth_dir, filename), local_path)
                with open(local_path, 'r') as f:
                    gt_data[key] = f.read().strip()
            except Exception as e:
                logger.error(f"Failed to copy ground truth {filename}: {e}")
                gt_data[key] = None

        # 3. Fetch User Output Files
        user_csv_path = os.path.join(temp_dir, "user_report.csv")
        user_summary_path = os.path.join(temp_dir, "user_summary.txt")
        
        user_csv_present = False
        user_summary_present = False

        if task_result.get("csv_exists") == "true":
            try:
                copy_from_env(task_result.get("csv_path"), user_csv_path)
                user_csv_present = True
            except: pass
        
        if task_result.get("summary_exists") == "true":
            try:
                copy_from_env(task_result.get("summary_path"), user_summary_path)
                user_summary_present = True
            except: pass

        # --- SCORING ---
        score = 0
        feedback = []

        # Criteria 1: CSV File Existence & Header (15 pts)
        if user_csv_present:
            try:
                with open(user_csv_path, 'r', newline='') as f:
                    reader = csv.reader(f)
                    header = next(reader, None)
                    if header:
                        required_cols = ['frame_number', 'src_ip', 'dst_ip', 'mss', 'window_scale', 'sack_permitted']
                        # Normalize header for comparison (lower case, strip)
                        header_norm = [h.strip().lower() for h in header]
                        missing = [c for c in required_cols if c not in header_norm]
                        if not missing:
                            score += 15
                            feedback.append("CSV created with correct columns.")
                        else:
                            score += 5
                            feedback.append(f"CSV created but missing columns: {', '.join(missing)}.")
                    else:
                        feedback.append("CSV file is empty.")
            except Exception as e:
                feedback.append(f"Error parsing CSV: {e}")
        else:
            feedback.append("CSV report file not found.")

        # Criteria 2: CSV Row Count (matches SYN count) (20 pts)
        user_syn_count = 0
        if user_csv_present:
            try:
                with open(user_csv_path, 'r') as f:
                    # Subtract 1 for header
                    user_syn_count = sum(1 for line in f) - 1
                
                gt_syn_count = int(gt_data.get("syn_count", 0))
                
                if user_syn_count == gt_syn_count:
                    score += 20
                    feedback.append(f"CSV row count matches SYN packet count ({gt_syn_count}).")
                elif gt_syn_count > 0 and abs(user_syn_count - gt_syn_count) / gt_syn_count < 0.1:
                    score += 10
                    feedback.append(f"CSV row count is close ({user_syn_count} vs {gt_syn_count}).")
                else:
                    feedback.append(f"CSV row count mismatch: {user_syn_count} (expected {gt_syn_count}).")
            except: pass

        # Criteria 3: Spot Check CSV Data (20 pts)
        # We check the first valid data row against ground truth CSV
        # Ground truth CSV format: frame,src,dst,sport,dport,mss,wscale,sack,ts,win
        if user_csv_present and os.path.exists(os.path.join(temp_dir, "ground_truth_syns.csv")):
            try:
                # Load first row of GT
                with open(os.path.join(temp_dir, "ground_truth_syns.csv"), 'r') as gt_f:
                    gt_reader = csv.reader(gt_f)
                    # skip potential header if tshark included it (command didn't include header flag, but verifying first)
                    # The command used -E header=n (default) implies no header
                    gt_rows = list(gt_reader)

                if gt_rows:
                    first_gt = gt_rows[0]
                    target_frame = first_gt[0]
                    
                    # Find this frame in user CSV
                    match_found = False
                    with open(user_csv_path, 'r') as user_f:
                        user_reader = csv.DictReader(user_f)
                        for row in user_reader:
                            # Handle case sensitivity in headers
                            row_lower = {k.lower(): v for k, v in row.items()}
                            if row_lower.get('frame_number') == target_frame:
                                # Check a few fields
                                # MSS
                                u_mss = row_lower.get('mss', '').strip()
                                gt_mss = first_gt[5].strip()
                                
                                # SACK (User: 'yes'/'no', GT: '1' or empty)
                                u_sack = row_lower.get('sack_permitted', '').lower()
                                gt_sack = 'yes' if first_gt[7] == '1' else 'no'
                                
                                if u_mss == gt_mss and (u_sack == gt_sack or (u_sack in ['1','true'] and gt_sack=='yes')):
                                    match_found = True
                                break
                    
                    if match_found:
                        score += 20
                        feedback.append("CSV data verification passed (spot check).")
                    else:
                        feedback.append(f"CSV data mismatch for frame {target_frame}.")
            except Exception as e:
                # Don't penalize too hard if spot check fails due to complex parsing issues
                pass

        # Criteria 4: Summary File - Total Count (10 pts)
        user_summary_text = ""
        if user_summary_present:
            with open(user_summary_path, 'r') as f:
                user_summary_text = f.read().lower()
            
            gt_syn = gt_data.get("syn_count", "0")
            if f"total_syn_packets: {gt_syn}" in user_summary_text:
                score += 10
                feedback.append("Summary: Total count correct.")
            else:
                feedback.append("Summary: Total count incorrect or missing.")

        # Criteria 5: Summary File - Percentages (15 pts)
        if user_summary_present:
            try:
                gt_syn = int(gt_data.get("syn_count", 1))
                gt_sack = int(gt_data.get("sack_count", 0))
                gt_ts = int(gt_data.get("ts_count", 0))
                
                exp_sack_pct = round((gt_sack / gt_syn) * 100) if gt_syn else 0
                exp_ts_pct = round((gt_ts / gt_syn) * 100) if gt_syn else 0
                
                # Simple string check for simplicity
                if f"sack_permitted_percentage: {exp_sack_pct}%" in user_summary_text:
                    score += 7
                elif f"sack_permitted_percentage: {exp_sack_pct}" in user_summary_text: # Allow missing % sign
                    score += 7

                if f"timestamp_present_percentage: {exp_ts_pct}%" in user_summary_text:
                    score += 8
                elif f"timestamp_present_percentage: {exp_ts_pct}" in user_summary_text:
                    score += 8
                    
                feedback.append(f"Summary: Checked percentages (Expected SACK: {exp_sack_pct}%, TS: {exp_ts_pct}%).")
            except: pass

        # Criteria 6: Summary File - Stats (MSS/Port) (10 pts)
        if user_summary_present:
            gt_mss = gt_data.get("common_mss", "")
            gt_port = gt_data.get("common_port", "")
            
            if f"most_common_mss: {gt_mss}" in user_summary_text:
                score += 5
            
            if f"most_common_dst_port: {gt_port}" in user_summary_text:
                score += 5
            
            feedback.append("Summary: Checked common stats.")
        
        # Criteria 7: App Running (10 pts)
        if task_result.get("app_was_running"):
            score += 10
        else:
            feedback.append("Wireshark was not running at end of task.")

        # Final check
        passed = (score >= 60) and user_csv_present and user_summary_present
        
        return {
            "passed": passed,
            "score": score,
            "feedback": " | ".join(feedback)
        }