#!/usr/bin/env python3
"""
Verifier for capinfos_forensic_profiling task.
"""

import json
import tempfile
import os
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_forensic_report(traj, env_info, task_info):
    """
    Verify the forensic report generated by the agent.
    
    The export script runs inside the container and produces a JSON containing:
    - The user's report (parsed)
    - The ground truth (computed dynamically via capinfos/tshark)
    - Metadata about file existence and timestamps
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve the result file
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/final_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve/parse task result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # Extract data sections
    meta = result_data.get("meta", {})
    user_report = result_data.get("user_report", {})
    ground_truth = result_data.get("ground_truth", {})
    
    score = 0
    feedback_parts = []
    
    # ----------------------------------------------------------------
    # Criterion 1: File Validity & Structure (20 pts)
    # ----------------------------------------------------------------
    if not meta.get("report_exists"):
        return {"passed": False, "score": 0, "feedback": "Report file not found"}
        
    score += 5  # Exists
    feedback_parts.append("Report file exists")
    
    if not meta.get("file_created_during_task"):
        feedback_parts.append("Warning: File timestamp indicates it wasn't created during this session")
        # We don't fail immediately but penalize
    else:
        score += 5 # Created during task
    
    if meta.get("report_valid_json"):
        score += 5 # Valid JSON
        
        # Check specific structure
        if "capture_files" in user_report and isinstance(user_report["capture_files"], list) and len(user_report["capture_files"]) == 5:
            score += 5
            feedback_parts.append("Valid JSON structure")
        else:
            feedback_parts.append("JSON structure incorrect (missing capture_files or wrong count)")
    else:
        feedback_parts.append("File is not valid JSON")
        return {"passed": False, "score": score, "feedback": " | ".join(feedback_parts)}

    # ----------------------------------------------------------------
    # Criterion 2: Per-File Data Accuracy (60 pts)
    # ----------------------------------------------------------------
    # Map user files by filename for easy lookup
    user_files_map = {}
    for entry in user_report.get("capture_files", []):
        fname = entry.get("filename", "")
        if fname:
            user_files_map[fname] = entry
            
    gt_files = ground_truth.get("files", {})
    
    files_checked = 0
    correct_packet_counts = 0
    correct_durations = 0
    correct_sizes = 0
    correct_encaps = 0
    
    for fname, gt_info in gt_files.items():
        user_info = user_files_map.get(fname)
        if not user_info:
            continue
            
        files_checked += 1
        
        # Check Packet Count (Int exact)
        # 4 pts per file = 20 total
        try:
            if int(user_info.get("packet_count", -1)) == int(gt_info.get("packet_count", -2)):
                score += 4
                correct_packet_counts += 1
        except: pass
        
        # Check File Size (Int exact)
        # 2 pts per file = 10 total
        try:
            if int(user_info.get("file_size_bytes", -1)) == int(gt_info.get("file_size_bytes", -2)):
                score += 2
                correct_sizes += 1
        except: pass
        
        # Check Duration (Float, 5% tolerance)
        # 3 pts per file = 15 total
        try:
            u_dur = float(user_info.get("capture_duration_seconds", -1))
            g_dur = float(gt_info.get("capture_duration_seconds", -1))
            # Handle 0 duration edge case (unlikely for these files)
            if abs(u_dur - g_dur) <= (0.05 * g_dur) or abs(u_dur - g_dur) < 0.001:
                score += 3
                correct_durations += 1
        except: pass
        
        # Check Avg Packet Size (Float, 5% tolerance)
        # 2 pts per file = 10 total
        try:
            u_avg = float(user_info.get("average_packet_size_bytes", -1))
            g_avg = float(gt_info.get("average_packet_size_bytes", -1))
            if abs(u_avg - g_avg) <= (0.05 * g_avg) or abs(u_avg - g_avg) < 0.1:
                score += 2
        except: pass
        
        # Check Encapsulation (String contains)
        # 1 pt per file = 5 total
        u_encap = str(user_info.get("encapsulation", "")).lower()
        g_encap = str(gt_info.get("encapsulation", "")).lower()
        if g_encap and u_encap and (g_encap in u_encap or u_encap in g_encap):
            score += 1
            correct_encaps += 1

    feedback_parts.append(f"Correct Packet Counts: {correct_packet_counts}/5")
    feedback_parts.append(f"Correct Durations: {correct_durations}/5")

    # ----------------------------------------------------------------
    # Criterion 3: Summary Accuracy (20 pts)
    # ----------------------------------------------------------------
    user_summary = user_report.get("summary", {})
    gt_summary = ground_truth.get("summary", {})
    
    # Total packets (5 pts)
    try:
        if int(user_summary.get("total_packets_all_files", -1)) == int(gt_summary.get("total_packets_all_files", -2)):
            score += 5
    except: pass
    
    # Most packets file (5 pts)
    if user_summary.get("file_with_most_packets") == gt_summary.get("file_with_most_packets"):
        score += 5
        
    # Longest duration file (5 pts)
    if user_summary.get("file_with_longest_duration") == gt_summary.get("file_with_longest_duration"):
        score += 5
        
    # Largest avg size file (5 pts)
    if user_summary.get("file_with_largest_avg_packet_size") == gt_summary.get("file_with_largest_avg_packet_size"):
        score += 5

    # ----------------------------------------------------------------
    # Final Scoring
    # ----------------------------------------------------------------
    passed = score >= 60 and meta.get("report_valid_json")
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }