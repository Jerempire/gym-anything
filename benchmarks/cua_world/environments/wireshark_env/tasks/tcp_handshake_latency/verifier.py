#!/usr/bin/env python3
"""
Verifier for TCP Handshake Latency Task.
Reads the JSON result generated by export_result.sh which contains detailed
comparison metrics against ground truth.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_tcp_handshake_latency(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Environment error: copy function missing"}

    # Retrieve result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task results: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # Parse result sections
    comp = result.get('comparison', {})
    files = result.get('files_created_during_task', {})
    
    score = 0
    feedback = []

    # 1. CSV Existence and Validity (10 pts)
    if comp.get('csv_exists'):
        if files.get('csv') == "true":
            if comp.get('csv_valid'):
                score += 10
                feedback.append("CSV file created and valid.")
            else:
                score += 5
                feedback.append(f"CSV created but invalid format: {comp.get('csv_error')}")
        else:
            feedback.append("CSV file exists but has old timestamp (pre-task).")
    else:
        feedback.append("CSV file not found.")

    # 2. Connection Count (15 pts)
    if comp.get('connection_count_match'):
        score += 15
        feedback.append("Correct number of connections identified.")
    elif comp.get('csv_valid'):
        feedback.append(f"Incorrect connection count (User: {comp.get('user_count')}, Truth: {comp.get('ground_truth_count')}).")

    # 3. RTT Value Accuracy (25 pts)
    # Full points if > 90% of rows match
    matches = comp.get('matches_count', 0)
    total = comp.get('matches_total', 1) # avoid div/0
    match_rate = matches / total if total > 0 else 0
    
    if match_rate >= 0.9:
        score += 25
        feedback.append(f"RTT values accurate ({int(match_rate*100)}% match).")
    elif match_rate >= 0.5:
        score += 10
        feedback.append(f"RTT values partially accurate ({int(match_rate*100)}% match).")
    else:
        feedback.append(f"RTT values inaccurate ({int(match_rate*100)}% match).")

    # 4. Summary File Existence (5 pts)
    if comp.get('summary_exists') and files.get('summary') == "true":
        score += 5
        feedback.append("Summary file created.")
    else:
        feedback.append("Summary file missing or old.")

    # 5. Summary Statistics (10 pts each: Min, Max, Mean)
    stats_acc = comp.get('stats_accuracy', {})
    
    if stats_acc.get('min_ok'):
        score += 10
        feedback.append("Minimum RTT correct.")
    else:
        feedback.append("Minimum RTT incorrect.")
        
    if stats_acc.get('max_ok'):
        score += 10
        feedback.append("Maximum RTT correct.")
    else:
        feedback.append("Maximum RTT incorrect.")
        
    if stats_acc.get('mean_ok'):
        score += 10
        feedback.append("Mean RTT correct.")
    else:
        feedback.append("Mean RTT incorrect.")

    # 6. Slowest Connection Identification (15 pts)
    if comp.get('slowest_connection_correct'):
        score += 15
        feedback.append("Slowest connection correctly identified.")
    else:
        feedback.append("Slowest connection details missing or incorrect.")

    # Final tally
    passed = (score >= 60) and comp.get('csv_valid')
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }