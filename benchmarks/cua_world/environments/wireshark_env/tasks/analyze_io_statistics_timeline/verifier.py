#!/usr/bin/env python3
"""
Verifier for analyze_io_statistics_timeline task.
"""

import json
import base64
import tempfile
import os
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_analyze_io_statistics_timeline(traj, env_info, task_info):
    """
    Verify the I/O statistics report generated by the agent.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy result file from container
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 1. Check file existence and freshness (anti-gaming)
    if not result.get('report_exists'):
        return {"passed": False, "score": 0, "feedback": "Report file not found."}
    
    if not result.get('report_fresh'):
        return {"passed": False, "score": 0, "feedback": "Report file was not modified during the task."}
    
    score += 5 # File exists and is fresh

    # 2. Parse User Report
    report_content_b64 = result.get('report_content_b64', '')
    if not report_content_b64:
        return {"passed": False, "score": score, "feedback": "Report file is empty."}
        
    try:
        report_text = base64.b64decode(report_content_b64).decode('utf-8')
    except:
        return {"passed": False, "score": score, "feedback": "Report file is not valid UTF-8 text."}

    user_values = {}
    required_keys = [
        "total_packets", "capture_duration_secs", "busiest_interval_start", 
        "busiest_interval_packets", "avg_packets_per_sec", "total_bytes"
    ]
    
    try:
        for line in report_text.splitlines():
            line = line.strip()
            if not line or ':' not in line:
                continue
            key, val = line.split(':', 1)
            key = key.strip()
            val = val.strip()
            if key in required_keys:
                user_values[key] = float(val)
    except ValueError:
        pass # Handle parse errors gracefully

    # Check parseability
    keys_found = sum(1 for k in required_keys if k in user_values)
    if keys_found == 6:
        score += 5
        feedback_parts.append("Report format correct")
    elif keys_found > 0:
        score += 2
        feedback_parts.append(f"Report partial format ({keys_found}/6 keys)")
    else:
        feedback_parts.append("Report format incorrect")

    # 3. Compare with Ground Truth
    gt = result.get('ground_truth', {})
    
    # Helper to check tolerances
    def check(key, gt_key, tolerance, points, is_percent=False):
        if key not in user_values:
            return 0, f"Missing {key}"
        
        user_val = user_values[key]
        gt_val = gt.get(gt_key, 0)
        
        diff = abs(user_val - gt_val)
        passed = False
        
        if is_percent:
            # Avoid division by zero
            if gt_val == 0:
                passed = (diff == 0)
            else:
                passed = (diff / gt_val) <= (tolerance / 100.0)
        else:
            passed = diff <= tolerance
            
        if passed:
            return points, f"{key} correct"
        else:
            return 0, f"{key} incorrect (got {user_val}, expected {gt_val})"

    # Apply scoring rules
    # Total packets (15 pts, +/- 1)
    pts, msg = check("total_packets", "total_packets", 1, 15)
    score += pts
    feedback_parts.append(msg)

    # Capture duration (15 pts, +/- 0.5)
    pts, msg = check("capture_duration_secs", "duration", 0.5, 15)
    score += pts
    feedback_parts.append(msg)

    # Busiest interval start (20 pts, +/- 1.0)
    pts, msg = check("busiest_interval_start", "busiest_start", 1.0, 20)
    score += pts
    feedback_parts.append(msg)

    # Busiest interval packets (15 pts, +/- 2)
    pts, msg = check("busiest_interval_packets", "busiest_packets", 2, 15)
    score += pts
    feedback_parts.append(msg)

    # Avg PPS (10 pts, +/- 5%)
    pts, msg = check("avg_packets_per_sec", "avg_pps", 5, 10, is_percent=True)
    score += pts
    feedback_parts.append(msg)

    # Total Bytes (15 pts, +/- 0.5%)
    pts, msg = check("total_bytes", "total_bytes", 0.5, 15, is_percent=True)
    score += pts
    feedback_parts.append(msg)

    # Final decision
    # Pass threshold: 60 points + mandatory fields correct
    mandatory_passed = (
        abs(user_values.get("total_packets", -999) - gt.get("total_packets", 0)) <= 1 and
        abs(user_values.get("capture_duration_secs", -999) - gt.get("duration", 0)) <= 0.5
    )
    
    passed = (score >= 60) and mandatory_passed
    
    if not mandatory_passed and score >= 60:
        feedback_parts.append("Failed: Critical metrics (packets/duration) incorrect despite high score")

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }