#!/usr/bin/env python3
"""
Verifier for NHTSA Fleet Recall Check task.
"""

import json
import os
import re
import tempfile
import logging
from datetime import datetime

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_nhtsa_fleet_recall_check(traj, env_info, task_info):
    """
    Verifies the NHTSA fleet recall task.
    
    Scoring Criteria (100 pts total):
    1. Firefox Usage (15 pts): Visited nhtsa.gov enough times.
    2. Bookmarks (20 pts): 'Fleet Safety Recalls' folder exists (10) + contains >=5 NHTSA links (10).
    3. File Metadata (10 pts): Report file exists and is created during task.
    4. JSON Validity (5 pts): Report parses as valid JSON.
    5. Data Completeness (20 pts): Contains all 5 required vehicles with correct keys.
    6. Data Quality (30 pts): 
       - NHTSA Campaign IDs match format (10)
       - Descriptions are non-empty (10)
       - Logic check: total_recalls matches array length (10)
    """
    
    # 1. Setup - Get copy function
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Verification failed: copy_from_env not available"}

    # 2. Retrieve files from container
    # We need the export result (system stats) and the agent's report
    stats_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    report_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    
    stats_data = {}
    report_data = {}
    
    try:
        # Copy task_result.json (generated by export_result.sh)
        try:
            copy_from_env("/tmp/task_result.json", stats_file.name)
            with open(stats_file.name, 'r') as f:
                stats_data = json.load(f)
        except Exception as e:
            logger.error(f"Failed to copy/load stats file: {e}")
            # Continue, assuming 0 for stats-based scores

        # Copy agent's output report
        try:
            copy_from_env("/home/ga/Documents/fleet_recall_report.json", report_file.name)
            with open(report_file.name, 'r') as f:
                report_data = json.load(f)
                report_valid_json = True
        except Exception as e:
            logger.error(f"Failed to copy/load report file: {e}")
            report_valid_json = False
            
    finally:
        if os.path.exists(stats_file.name): os.unlink(stats_file.name)
        if os.path.exists(report_file.name): os.unlink(report_file.name)

    # 3. Evaluate Criteria
    score = 0
    feedback = []
    
    # Criterion 1: Firefox History (15 pts)
    # Expecting at least 5 visits (one per car)
    visits = stats_data.get('nhtsa_visits', 0)
    if visits >= 5:
        score += 15
        feedback.append("Firefox History: Verified extensive research on NHTSA.gov (15/15)")
    elif visits > 0:
        score += 5
        feedback.append(f"Firefox History: Some NHTSA visits detected, but fewer than expected ({visits}) (5/15)")
    else:
        feedback.append("Firefox History: No evidence of visiting NHTSA.gov (0/15)")
        
    # Criterion 2: Bookmarks (20 pts)
    folder_exists = stats_data.get('bookmark_folder_exists', False)
    bookmark_count = stats_data.get('nhtsa_bookmarks_count', 0)
    
    if folder_exists:
        score += 10
        feedback.append("Bookmarks: 'Fleet Safety Recalls' folder found (10/10)")
        if bookmark_count >= 5:
            score += 10
            feedback.append(f"Bookmarks: Found {bookmark_count} NHTSA bookmarks in folder (10/10)")
        elif bookmark_count > 0:
            score += 5
            feedback.append(f"Bookmarks: Found {bookmark_count} bookmarks (expected 5+) (5/10)")
        else:
            feedback.append("Bookmarks: Folder exists but is empty of NHTSA links (0/10)")
    else:
        feedback.append("Bookmarks: 'Fleet Safety Recalls' folder not found (0/20)")

    # Criterion 3: File Metadata (10 pts)
    file_exists = stats_data.get('report_file_exists', False)
    file_fresh = stats_data.get('report_file_fresh', False)
    
    if file_exists and file_fresh:
        score += 10
        feedback.append("File: Report created during task (10/10)")
    elif file_exists:
        score += 0 # Zero points if pre-existing (anti-gaming)
        feedback.append("File: Report exists but has old timestamp (0/10)")
    else:
        feedback.append("File: Report not found at expected path (0/10)")
        
    # STOP if file is missing or invalid JSON
    if not report_valid_json:
        feedback.append("Content: File is missing or invalid JSON. Stopping verification.")
        return {"passed": False, "score": score, "feedback": "\n".join(feedback)}
        
    score += 5 # Points for valid JSON
    
    # Criterion 4: Data Completeness (20 pts)
    required_vehicles = [
        "2019_honda_crv", "2020_toyota_camry", "2021_chevrolet_equinox", 
        "2018_ford_f150", "2022_hyundai_tucson"
    ]
    
    vehicles_data = report_data.get("vehicles", {})
    found_vehicles = 0
    for v_key in required_vehicles:
        if v_key in vehicles_data:
            found_vehicles += 1
            
    # 4 pts per vehicle
    completeness_score = found_vehicles * 4
    score += completeness_score
    feedback.append(f"Content: Found {found_vehicles}/5 required vehicles ({completeness_score}/20)")

    # Criterion 5: Data Quality (30 pts)
    # Checks: Campaign format, Description length, Count consistency
    quality_score = 0
    
    campaign_format_valid = True
    descriptions_valid = True
    counts_consistent = True
    
    nhtsa_campaign_pattern = re.compile(r'^\d{2}[A-Z]\d+$') # e.g., 20V123000
    
    for v_key in required_vehicles:
        if v_key not in vehicles_data: continue
        
        entry = vehicles_data[v_key]
        reported_total = entry.get("total_recalls", 0)
        recalls_list = entry.get("recalls", [])
        
        # Check count consistency
        if reported_total != len(recalls_list):
            counts_consistent = False
            
        # Check details
        for recall in recalls_list:
            cid = recall.get("nhtsa_campaign_number", "")
            desc = recall.get("description", "")
            
            if not nhtsa_campaign_pattern.match(cid):
                # Allow empty if no recalls found, but if recall exists it must match
                campaign_format_valid = False
            
            if len(desc) < 5:
                descriptions_valid = False

    if campaign_format_valid:
        quality_score += 10
    else:
        feedback.append("Quality: Some NHTSA campaign numbers invalid format (e.g. 20V123000)")
        
    if descriptions_valid:
        quality_score += 10
    else:
        feedback.append("Quality: Some descriptions missing or too short")
        
    if counts_consistent:
        quality_score += 10
    else:
        feedback.append("Quality: Mismatch between total_recalls count and array length")
        
    if found_vehicles == 0:
        quality_score = 0 # No points if no vehicles found
        
    score += quality_score
    feedback.append(f"Content: Data Quality score ({quality_score}/30)")

    # Final result
    passed = (score >= 60) and (found_vehicles >= 3)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": "\n".join(feedback)
    }