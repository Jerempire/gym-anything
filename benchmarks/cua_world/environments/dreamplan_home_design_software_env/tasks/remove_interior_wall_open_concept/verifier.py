#!/usr/bin/env python3
"""
Verifier for remove_interior_wall_open_concept task.

Verification Strategy:
1. File Verification (30 pts):
   - Check if 'OpenConcept' project file exists
   - Verify it was created/modified during the task window
2. VLM Trajectory Verification (70 pts):
   - Workflow: Did the agent select a wall and delete it?
   - Outcome: Is the specific interior wall gone?
   - Safety: Are exterior walls still intact? (No collateral damage)
"""

import json
import os
import tempfile
import logging
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_open_concept_wall_removal(traj, env_info, task_info):
    """
    Verify the agent removed the specific interior wall to create an open concept.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # ================================================================
    # 1. File System Verification
    # ================================================================
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Note: Depending on environment driver, this might need C:\tmp or just /tmp/
        # Using the path generated by export_result.ps1
        copy_from_env("C:\\tmp\\task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            file_result = json.load(f)
    except Exception as e:
        logger.error(f"Failed to load task result: {e}")
        file_result = {}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # Check file existence (15 pts)
    if file_result.get("output_exists", False):
        score += 15
        feedback_parts.append("Project file 'OpenConcept' saved successfully.")
        
        # Check timestamp (15 pts)
        if file_result.get("file_created_during_task", False):
            score += 15
            feedback_parts.append("File saved during task session.")
        else:
            feedback_parts.append("Warning: File timestamp indicates it wasn't saved during this session.")
    else:
        feedback_parts.append("Project file 'OpenConcept' not found.")

    # ================================================================
    # 2. VLM Trajectory Verification
    # ================================================================
    frames = sample_trajectory_frames(traj, n=5)
    final_frame = get_final_screenshot(traj)
    
    if not frames or not final_frame:
        return {
            "passed": False, 
            "score": score, 
            "feedback": " | ".join(feedback_parts) + " | No visual evidence available."
        }

    # Prompt for workflow analysis
    prompt = """
    You are verifying a home design task in DreamPlan software.
    The goal was to REMOVE an interior wall between the Kitchen and Dining/Living room to create an open concept.
    
    Analyze these screenshots (chronological order) and the final result:
    
    1. WALL_SELECTION: Do you see the agent selecting a wall (highlighted or handles visible)?
    2. DELETION_ACTION: Do you see the wall disappearing?
    3. FINAL_LAYOUT: In the final image (likely a 2D blueprint or 3D view), is the wall between the kitchen and adjacent room GONE?
    4. COLLATERAL_DAMAGE: Are the exterior walls still intact? (The house should not be missing outer walls).
    5. OPEN_SPACE: Do the two rooms now appear as one continuous space?
    
    Return JSON:
    {
        "wall_selected": boolean,
        "wall_deleted": boolean,
        "final_layout_is_open_concept": boolean,
        "exterior_walls_intact": boolean,
        "confidence": "high/medium/low",
        "reasoning": "string"
    }
    """
    
    vlm_response = query_vlm(images=frames + [final_frame], prompt=prompt)
    
    if vlm_response.get("success"):
        parsed = vlm_response.get("parsed", {})
        
        # Scoring logic for visuals
        if parsed.get("wall_selected", False) or parsed.get("wall_deleted", False):
            score += 20
            feedback_parts.append("Visual evidence of wall modification found.")
        
        if parsed.get("final_layout_is_open_concept", False):
            score += 30
            feedback_parts.append("Final layout confirms open concept (wall removed).")
        else:
            feedback_parts.append("Visuals do not clearly show the wall was removed.")
            
        if parsed.get("exterior_walls_intact", True):
            score += 20
            feedback_parts.append("Exterior walls preserved.")
        else:
            score -= 10
            feedback_parts.append("Penalty: Possible damage to exterior walls detected.")
            
    else:
        feedback_parts.append("VLM verification failed to process images.")

    # Calculate final result
    # Pass threshold: 60 points AND open concept achieved visually
    visual_success = vlm_response.get("parsed", {}).get("final_layout_is_open_concept", False)
    passed = score >= 60 and visual_success

    return {
        "passed": passed,
        "score": min(100, score),
        "feedback": " | ".join(feedback_parts),
        "details": vlm_response.get("parsed", {})
    }