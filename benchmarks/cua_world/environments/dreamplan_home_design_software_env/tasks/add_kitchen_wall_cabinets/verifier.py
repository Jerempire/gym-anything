#!/usr/bin/env python3
"""
Verifier for add_kitchen_wall_cabinets task.

Uses a Hybrid Verification Strategy:
1. Programmatic: Checks if a project file was saved and modified (Anti-gaming).
2. VLM (Visual): Analyzes trajectory to confirm cabinets were actually placed in the kitchen.

This approach prevents "empty save" gaming while ensuring complex visual criteria 
(alignment, specific room) are met by VLM.
"""

import json
import os
import tempfile
import logging
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_add_kitchen_wall_cabinets(traj, env_info, task_info):
    """
    Verify that the agent added wall cabinets to the kitchen.
    """
    # 1. Setup and access to container files
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 2. Retrieve Programmatic Evidence (File Checks)
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Note: Container path is Windows style, but copy_from_env handles the path mapping
        # We access the file generated by export_result.ps1
        copy_from_env("C:\\tmp\\task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        logger.error(f"Failed to read result file: {e}")
        result_data = {}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # 3. Analyze Programmatic Evidence
    score = 0
    feedback_parts = []
    
    file_exists = result_data.get('project_file_exists', False)
    file_fresh = result_data.get('file_created_during_task', False)
    file_size = result_data.get('file_size_bytes', 0)
    app_running = result_data.get('app_was_running', False)

    # Criterion: Project Saved (20 pts)
    if file_exists and file_fresh:
        if file_size > 1000:  # Arbitrary small threshold for a non-empty project
            score += 20
            feedback_parts.append("Project file saved successfully.")
        else:
            score += 10
            feedback_parts.append("Project file saved but seems very small.")
    elif file_exists:
        feedback_parts.append("Project file exists but was not modified during task.")
    else:
        feedback_parts.append("No project file saved.")

    # Criterion: App Running (10 pts)
    if app_running:
        score += 10
    
    # 4. VLM Verification (Visual Trajectory Analysis) - The Core Check (70 pts)
    # We look at frames to see if the agent navigated to cabinets and placed them.
    
    frames = sample_trajectory_frames(traj, n=5)
    final_frame = get_final_screenshot(traj)
    
    if not frames:
        return {"passed": False, "score": score, "feedback": "No trajectory frames available for verification."}

    vlm_prompt = """
    You are verifying a user task in Home Design Software. 
    The goal is: "Add at least 3 upper/wall cabinets to the kitchen."

    Review the sequence of images.
    1. Did the user open a furniture/object library?
    2. Did the user select 'Cabinets' (specifically Wall/Upper cabinets, not base cabinets)?
    3. In the final view, are there at least 3 upper cabinets visible on a wall in a kitchen setting?
    
    Distinguish between:
    - Base cabinets (on the floor)
    - Wall cabinets (mounted higher up on the wall)
    
    Respond in JSON:
    {
        "library_accessed": true,
        "cabinets_visible_count": 3,
        "are_wall_cabinets": true,
        "in_kitchen": true,
        "reasoning": "..."
    }
    """
    
    # Include final frame for the "final state" check
    all_images = frames + [final_frame] if final_frame else frames
    
    try:
        vlm_response = query_vlm(images=all_images, prompt=vlm_prompt)
        vlm_data = vlm_response.get('parsed', {})
        
        # Scoring VLM
        if vlm_data.get('library_accessed'):
            score += 10
            feedback_parts.append("Verified library access.")
            
        cabinets_count = vlm_data.get('cabinets_visible_count', 0)
        are_wall = vlm_data.get('are_wall_cabinets', False)
        in_kitchen = vlm_data.get('in_kitchen', False)
        
        if cabinets_count >= 3:
            score += 30
            feedback_parts.append(f"Visible {cabinets_count} cabinets.")
        elif cabinets_count > 0:
            score += 15
            feedback_parts.append(f"Only saw {cabinets_count} cabinets (target: 3).")
        else:
            feedback_parts.append("No cabinets visible.")
            
        if are_wall:
            score += 15
            feedback_parts.append("Cabinets identified as Wall/Upper type.")
        else:
            feedback_parts.append("Cabinets might be base cabinets (wrong type).")
            
        if in_kitchen:
            score += 15
            feedback_parts.append("Placement confirmed in kitchen area.")
            
    except Exception as e:
        logger.error(f"VLM verification failed: {e}")
        feedback_parts.append("Visual verification failed due to system error.")

    # Final Pass/Fail
    passed = score >= 60 and file_fresh and (vlm_data.get('cabinets_visible_count', 0) >= 2)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }