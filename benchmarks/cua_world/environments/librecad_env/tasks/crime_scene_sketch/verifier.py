#!/usr/bin/env python3
"""
Verifier for crime_scene_sketch task.
Reads the JSON result generated by the container's export script (which runs ezdxf internally).
"""

import json
import logging
import os
import tempfile
import math

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def dist(p1, p2):
    """Euclidean distance between two 2D points."""
    return math.sqrt((p1[0] - p2[0])**2 + (p1[1] - p2[1])**2)

def verify_crime_scene_sketch(traj, env_info, task_info):
    """
    Verify the crime scene sketch task.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Retrieve Result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve result: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # 2. Extract Data
    file_exists = result.get("file_exists", False)
    valid_dxf = result.get("valid_dxf", False)
    created_during_task = result.get("created_during_task", False)
    layers = result.get("layers_found", [])
    evidence_items = result.get("evidence_items", [])
    measurement_lines = result.get("measurement_lines", [])
    text_labels = result.get("text_labels", [])
    floorplan_preserved = result.get("floorplan_preserved", False)

    score = 0
    feedback_parts = []
    
    # 3. Scoring Logic
    
    # Criterion: File Validity (10 pts)
    if file_exists and valid_dxf and created_during_task:
        score += 10
        feedback_parts.append("Valid DXF created")
    elif file_exists:
        feedback_parts.append("File exists but issue with validity/timestamp")
    else:
        return {"passed": False, "score": 0, "feedback": "No output file found"}

    # Criterion: Layers (20 pts)
    # Need EVIDENCE and MEASUREMENTS
    has_evidence_layer = "EVIDENCE" in layers
    has_measure_layer = "MEASUREMENTS" in layers
    
    if has_evidence_layer: score += 10
    if has_measure_layer: score += 10
    if has_evidence_layer and has_measure_layer:
        feedback_parts.append("Layers correct")
    else:
        feedback_parts.append(f"Missing layers (Found: {layers})")

    # Criterion: Evidence Geometry (30 pts)
    # Circle at (4500, 2500) r=50
    # Line (5000, 3000) -> (5600, 3200)
    
    found_circle = False
    found_crowbar = False
    
    for item in evidence_items:
        if item["type"] == "CIRCLE":
            c = item["center"]
            if dist(c, [4500, 2500]) < 10 and abs(item["radius"] - 50) < 5:
                found_circle = True
        
        if item["type"] == "LINE":
            s = item["start"]
            e = item["end"]
            # Check matches expected coordinates (allowing for swapped start/end)
            match1 = dist(s, [5000, 3000]) < 10 and dist(e, [5600, 3200]) < 10
            match2 = dist(s, [5600, 3200]) < 10 and dist(e, [5000, 3000]) < 10
            if match1 or match2:
                found_crowbar = True

    if found_circle:
        score += 15
        feedback_parts.append("Evidence E1 (Circle) correct")
    else:
        feedback_parts.append("Evidence E1 incorrect/missing")

    if found_crowbar:
        score += 15
        feedback_parts.append("Evidence E2 (Crowbar) correct")
    else:
        feedback_parts.append("Evidence E2 incorrect/missing")

    # Criterion: Triangulation Lines (20 pts)
    # (4500, 2500) -> (0,0) AND (4500, 2500) -> (6000,0)
    
    found_ref_a = False
    found_ref_b = False
    
    target_origin = [4500, 2500]
    ref_a = [0, 0]
    ref_b = [6000, 0]
    
    for line in measurement_lines:
        s = line["start"]
        e = line["end"]
        
        # Check connection to Origin
        if (dist(s, target_origin) < 10 and dist(e, ref_a) < 10) or \
           (dist(e, target_origin) < 10 and dist(s, ref_a) < 10):
            found_ref_a = True
            
        # Check connection to Point B
        if (dist(s, target_origin) < 10 and dist(e, ref_b) < 10) or \
           (dist(e, target_origin) < 10 and dist(s, ref_b) < 10):
            found_ref_b = True

    if found_ref_a and found_ref_b:
        score += 20
        feedback_parts.append("Triangulation lines correct")
    elif found_ref_a or found_ref_b:
        score += 10
        feedback_parts.append("Partial triangulation lines")
    else:
        feedback_parts.append("Triangulation lines missing")

    # Criterion: Text Labels (10 pts)
    found_e1_text = any("E1" in t for t in text_labels)
    found_e2_text = any("E2" in t for t in text_labels)
    
    if found_e1_text and found_e2_text:
        score += 10
        feedback_parts.append("Labels correct")
    else:
        feedback_parts.append(f"Labels missing (Found: {text_labels})")

    # Criterion: Context Preservation (10 pts)
    if floorplan_preserved:
        score += 10
    else:
        feedback_parts.append("Warning: Floorplan geometry seems deleted")

    passed = score >= 70 and found_circle and (found_ref_a or found_ref_b)

    return {
        "passed": passed,
        "score": score,
        "feedback": "; ".join(feedback_parts)
    }