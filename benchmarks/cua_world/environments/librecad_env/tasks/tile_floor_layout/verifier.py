#!/usr/bin/env python3
"""
Verifier for tile_floor_layout@1.

Verification Strategy:
1. Programmatic Check (Primary):
   - Reads the JSON report generated by export_result.sh (which ran ezdxf inside the container).
   - Validates existence of 5 specific layers.
   - Validates geometry bounding boxes for WALLS (3000x2400) and BORDER (2700x2100).
   - Validates grid density (line count) and dimensions/text presence.

2. VLM Check (Secondary):
   - Uses VLM to look at the final screenshot/trajectory.
   - confirm visual coherence if programmatic checks are ambiguous.
   - (Note: Programmatic checks here are strict enough to determine pass/fail, VLM adds robustness).

Pass Threshold: 60/100 points
"""

import json
import tempfile
import os
import logging
from gym_anything.vlm import query_vlm, get_final_screenshot

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_tile_floor_layout(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    dxf = result.get("dxf_analysis", {})
    score = 0
    feedback = []
    
    # ---------------------------------------------------------
    # Criterion 1: File Existence & Validity (10 pts)
    # ---------------------------------------------------------
    if result.get("output_exists") and result.get("file_created_during_task") and dxf.get("valid_dxf"):
        score += 10
        feedback.append("Valid DXF file created")
    else:
        feedback.append("DXF file missing, invalid, or pre-existing")
        return {"passed": False, "score": 0, "feedback": " | ".join(feedback)}

    # ---------------------------------------------------------
    # Criterion 2: Layers (20 pts)
    # ---------------------------------------------------------
    found_layers = set(dxf.get("layers_found", []))
    required_layers = {"WALLS", "BORDER", "TILES", "DIMENSIONS", "TEXT"}
    missing_layers = required_layers - found_layers
    
    if not missing_layers:
        score += 20
        feedback.append("All layers present")
    else:
        # Partial credit: 4 pts per layer
        present_count = len(required_layers) - len(missing_layers)
        points = present_count * 4
        score += points
        feedback.append(f"Missing layers: {missing_layers} ({points}/20 pts)")

    # ---------------------------------------------------------
    # Criterion 3: WALLS Geometry (20 pts)
    # ---------------------------------------------------------
    walls_bbox = dxf.get("walls_bbox")
    if walls_bbox:
        width = walls_bbox[2] - walls_bbox[0]
        height = walls_bbox[3] - walls_bbox[1]
        # Tolerance +/- 50mm
        if abs(width - 3000) <= 50 and abs(height - 2400) <= 50:
            score += 20
            feedback.append("WALLS geometry correct")
        else:
            score += 5
            feedback.append(f"WALLS geometry incorrect size: {width:.0f}x{height:.0f}")
    else:
        feedback.append("WALLS layer empty")

    # ---------------------------------------------------------
    # Criterion 4: BORDER Geometry (15 pts)
    # ---------------------------------------------------------
    border_bbox = dxf.get("border_bbox")
    if border_bbox:
        width = border_bbox[2] - border_bbox[0]
        height = border_bbox[3] - border_bbox[1]
        # Expected: 2700x2100 (3000-300, 2400-300)
        if abs(width - 2700) <= 50 and abs(height - 2100) <= 50:
            score += 15
            feedback.append("BORDER geometry correct")
        else:
            score += 5
            feedback.append(f"BORDER geometry incorrect size: {width:.0f}x{height:.0f}")
    else:
        feedback.append("BORDER layer empty")

    # ---------------------------------------------------------
    # Criterion 5: TILES Grid (15 pts)
    # ---------------------------------------------------------
    line_count = dxf.get("tiles_line_count", 0)
    if line_count >= 12:
        score += 15
        feedback.append(f"TILES grid populated ({line_count} lines)")
    elif line_count > 0:
        score += 5
        feedback.append("TILES grid sparse")
    else:
        feedback.append("TILES layer empty")

    # ---------------------------------------------------------
    # Criterion 6: Annotations (10 pts)
    # ---------------------------------------------------------
    dim_count = dxf.get("dim_count", 0)
    texts = dxf.get("text_content", [])
    has_title = any("TILE" in t.upper() for t in texts)
    
    if dim_count >= 2 and has_title:
        score += 10
        feedback.append("Dimensions and Title present")
    elif dim_count >= 2 or has_title:
        score += 5
        feedback.append("Partial annotations")
    else:
        feedback.append("Missing annotations")

    # ---------------------------------------------------------
    # Criterion 7: VLM Confirmation (10 pts)
    # ---------------------------------------------------------
    # Use VLM to confirm it looks like a technical drawing
    final_screenshot = get_final_screenshot(traj)
    if final_screenshot:
        vlm_resp = query_vlm(
            prompt="Does this screenshot show a technical CAD drawing with a rectangular floor plan and grid lines?",
            image=final_screenshot
        )
        if vlm_resp.get("parsed", {}).get("answer", False) or "yes" in vlm_resp.get("response", "").lower():
            score += 10
            feedback.append("Visual verification passed")
        else:
            feedback.append("Visual verification failed")
    else:
        # Fallback if no screenshot (programmatic checks strict enough)
        if score >= 50:
            score += 10 
            feedback.append("Visual check skipped (no screenshot)")

    passed = score >= 60
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }