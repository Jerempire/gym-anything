#!/usr/bin/env python3
"""
Verifier for evacuation_map_update task.
Combines internal DXF analysis (performed in container) with VLM visual verification.
"""

import json
import tempfile
import os
import logging
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_evacuation_map(traj, env_info, task_info):
    """
    Verify the evacuation map update task.
    
    Scoring:
    - 10 pts: File created and saved
    - 15 pts: Layer SAFETY exists and is Red
    - 25 pts: Evacuation route geometry correct
    - 15 pts: 'You Are Here' marker geometry correct
    - 15 pts: Fire Extinguisher symbol geometry correct
    - 20 pts: Text labels present (or strong visual evidence)
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    # 1. Load basic task result
    task_result = {}
    with tempfile.NamedTemporaryFile(delete=False, suffix='.json') as f:
        temp_name = f.name
    try:
        copy_from_env("/tmp/task_result.json", temp_name)
        with open(temp_name, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        logger.error(f"Failed to load task result: {e}")
        return {"passed": False, "score": 0, "feedback": "Failed to load task result JSON"}
    finally:
        if os.path.exists(temp_name): os.unlink(temp_name)

    # 2. Load DXF Analysis (generated by export_result.sh using ezdxf inside container)
    dxf_analysis = {}
    dxf_analysis_path = "/tmp/dxf_analysis.json"
    
    with tempfile.NamedTemporaryFile(delete=False, suffix='.json') as f:
        analysis_temp = f.name
        
    try:
        copy_from_env(dxf_analysis_path, analysis_temp)
        with open(analysis_temp, 'r') as f:
            dxf_analysis = json.load(f)
    except Exception:
        feedback_parts.append("Could not retrieve DXF analysis (file may not exist)")
    finally:
        if os.path.exists(analysis_temp): os.unlink(analysis_temp)

    # --- SCORING CRITERIA ---

    # Criterion 1: File Saved (10 pts)
    if task_result.get("output_exists") and task_result.get("file_created_during_task"):
        score += 10
        feedback_parts.append("File saved successfully")
    else:
        return {"passed": False, "score": 0, "feedback": "Output file not found or not modified"}

    # Criterion 2: Layer Checks (15 pts)
    if dxf_analysis.get("layer_safety_exists"):
        if dxf_analysis.get("layer_safety_color") == 1:
            score += 15
            feedback_parts.append("Layer SAFETY correct (Red)")
        else:
            score += 10
            feedback_parts.append(f"Layer SAFETY exists but wrong color (expected 1/Red, got {dxf_analysis.get('layer_safety_color')})")
    else:
        feedback_parts.append("Layer SAFETY missing")

    # Criterion 3: Route Geometry (25 pts)
    if dxf_analysis.get("route_valid"):
        score += 25
        feedback_parts.append("Evacuation route geometry correct")
    else:
        feedback_parts.append("Evacuation route geometry invalid or missing")

    # Criterion 4: Marker Geometry (15 pts)
    if dxf_analysis.get("marker_valid"):
        score += 15
        feedback_parts.append("'You Are Here' marker correct")
    else:
        feedback_parts.append("'You Are Here' marker invalid/missing")

    # Criterion 5: FE Symbol Geometry (15 pts)
    if dxf_analysis.get("fe_symbol_valid"):
        score += 15
        feedback_parts.append("Fire Extinguisher symbol correct")
    else:
        feedback_parts.append("FE symbol geometry invalid/missing")

    # Criterion 6: Text Labels (Programmatic) (10 pts)
    found_labels = dxf_analysis.get("text_labels_found", [])
    if len(found_labels) >= 3:
        score += 20
        feedback_parts.append(f"All text labels found: {found_labels}")
    elif len(found_labels) > 0:
        partial = int(20 * (len(found_labels) / 3))
        score += partial
        feedback_parts.append(f"Some labels found: {found_labels}")
    else:
        feedback_parts.append("No text labels found via programmatic check")

    # --- VLM VERIFICATION (Backup/Confirmation) ---
    # If geometry checks failed (e.g. slight coord mismatch), VLM can rescue points
    # Also verify visual elements (Red lines)
    
    frames = sample_trajectory_frames(traj, n=3)
    final_screen = get_final_screenshot(traj)
    
    if final_screen:
        vlm_prompt = """
        Analyze this CAD drawing of a floorplan.
        1. Do you see RED lines drawn over the floorplan?
        2. Is there a path/line indicating an evacuation route?
        3. Do you see text labels like 'YOU ARE HERE', 'EXIT', or 'FE'?
        4. Is there a circle marker and a square symbol?
        
        Respond JSON: {"red_overlay_visible": bool, "route_visible": bool, "text_visible": bool, "symbols_visible": bool}
        """
        
        try:
            vlm_res = query_vlm(images=[final_screen], prompt=vlm_prompt).get('parsed', {})
            
            # Rescue points for Text if programmatic failed but visible
            if len(found_labels) == 0 and vlm_res.get("text_visible"):
                score += 15
                feedback_parts.append("Text labels detected visually (VLM rescue)")
            
            # Rescue points for Route if programmatic failed
            if not dxf_analysis.get("route_valid") and vlm_res.get("route_visible") and vlm_res.get("red_overlay_visible"):
                score += 15
                feedback_parts.append("Route detected visually (VLM rescue)")

        except Exception as e:
            logger.warning(f"VLM check failed: {e}")

    # Final tally
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": min(100, score),
        "feedback": " | ".join(feedback_parts)
    }