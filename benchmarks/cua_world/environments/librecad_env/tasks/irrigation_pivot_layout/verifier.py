#!/usr/bin/env python3
"""
Verifier for Irrigation Pivot Layout task.
Analyzes the JSON report generated by export_result.sh (which ran ezdxf inside the container).
"""

import json
import tempfile
import os
import math
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_irrigation_pivot_layout(traj, env_info, task_info):
    """
    Verifies the DXF drawing based on the analysis performed inside the container.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve result JSON
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve/parse task result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    # Extract data
    meta = result.get('task_meta', {})
    analysis = result.get('dxf_analysis', {})
    data = analysis.get('data', {})

    feedback = []
    score = 0
    max_score = 100

    # --- Criterion 1: File Existence & Validity (10 pts) ---
    if meta.get('file_exists') and analysis.get('valid_dxf'):
        score += 10
        feedback.append("Valid DXF file created")
    else:
        return {"passed": False, "score": 0, "feedback": "DXF file not found or invalid"}

    # --- Criterion 2: Anti-Gaming / Freshness (10 pts) ---
    if meta.get('file_created_during_task'):
        score += 10
        feedback.append("File created during task session")
    else:
        feedback.append("WARNING: File timestamp predates task start")

    # --- Criterion 3: Layers (10 pts) ---
    # Expected: Irrigation_Zones, Irrigation_Arms
    found_layers = [l.lower() for l in analysis.get('layers', [])]
    zones_layer = any("irrigation_zones" in l for l in found_layers)
    arms_layer = any("irrigation_arms" in l for l in found_layers)
    
    if zones_layer: score += 5
    if arms_layer: score += 5
    if zones_layer and arms_layer:
        feedback.append("Required layers found")
    else:
        feedback.append(f"Missing layers (Zones: {zones_layer}, Arms: {arms_layer})")

    # --- Criterion 4: Concentric Circles (30 pts) ---
    # Target: Center (200,200), Radii 50, 100, 150
    circles = data.get('circles', [])
    matched_radii = set()
    correct_layer_circles = 0
    
    for c in circles:
        cx, cy = c.get('center', [0, 0])
        r = c.get('radius', 0)
        layer = c.get('layer', '').lower()
        
        # Check center (tolerance +/- 2)
        if math.hypot(cx - 200, cy - 200) < 2.0:
            # Check radius (tolerance +/- 2)
            if 48 <= r <= 52: matched_radii.add(50)
            elif 98 <= r <= 102: matched_radii.add(100)
            elif 148 <= r <= 152: matched_radii.add(150)
            
            # Check Layer
            if "irrigation_zones" in layer:
                correct_layer_circles += 1

    circles_found = len(matched_radii)
    score += circles_found * 10  # 10 pts per correct circle radius
    feedback.append(f"Circles found: {circles_found}/3")

    # --- Criterion 5: Radial Arms (Lines) (20 pts) ---
    # Target: Start near (200,200), length approx 150, cardinal directions
    lines = data.get('lines', [])
    directions_found = set()
    correct_layer_lines = 0
    
    for l in lines:
        p1 = l.get('start', [0,0])
        p2 = l.get('end', [0,0])
        layer = l.get('layer', '').lower()
        
        # Determine which point is center
        if math.hypot(p1[0]-200, p1[1]-200) < 5:
            center, outer = p1, p2
        elif math.hypot(p2[0]-200, p2[1]-200) < 5:
            center, outer = p2, p1
        else:
            continue # Not a radial line from center
            
        dx = outer[0] - center[0]
        dy = outer[1] - center[1]
        length = math.hypot(dx, dy)
        
        # Check length (approx 150 +/- 10)
        if 140 <= length <= 160:
            # Check direction
            angle = math.degrees(math.atan2(dy, dx)) % 360
            
            if 350 <= angle or angle <= 10: directions_found.add("East")
            elif 80 <= angle <= 100: directions_found.add("North")
            elif 170 <= angle <= 190: directions_found.add("West")
            elif 260 <= angle <= 280: directions_found.add("South")
            
            if "irrigation_arms" in layer:
                correct_layer_lines += 1

    lines_score = min(20, len(directions_found) * 5)
    score += lines_score
    feedback.append(f"Arm directions found: {len(directions_found)}/4")

    # --- Criterion 6: Layer Assignment (10 pts) ---
    # Bonus for putting things on correct layers
    # We tracked counts in loops above
    layer_score = 0
    if correct_layer_circles >= 3: layer_score += 5
    if correct_layer_lines >= 4: layer_score += 5
    score += layer_score
    if layer_score == 10: feedback.append("Entities correctly assigned to layers")

    # --- Criterion 7: Text Labels (10 pts) ---
    texts = data.get('texts', [])
    found_labels = set()
    for t in texts:
        content = t.get('text', '').lower()
        if 'zone a' in content: found_labels.add('A')
        if 'zone b' in content: found_labels.add('B')
        if 'zone c' in content: found_labels.add('C')
    
    text_score = min(10, int(len(found_labels) * 3.4)) # 3->10, 2->6, 1->3
    score += text_score
    feedback.append(f"Labels found: {len(found_labels)}/3")

    # --- Final Result ---
    return {
        "passed": score >= 60,
        "score": score,
        "feedback": " | ".join(feedback)
    }