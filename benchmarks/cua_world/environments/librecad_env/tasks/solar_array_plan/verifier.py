#!/usr/bin/env python3
"""
Verifier for solar_array_plan task.
Analyzes the JSON output generated by export_result.sh which contains
parsed DXF data.
"""

import json
import tempfile
import os
import logging
import math

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_solar_array_plan(traj, env_info, task_info):
    """
    Verify the solar array site plan.
    
    Criteria:
    1. File creation and validity (10 pts)
    2. Layers existence and color correctness (15 pts)
    3. Panel Rows geometry (40 pts - 10 per row)
    4. Combiner Box geometry (10 pts)
    5. Conduit Lines geometry (15 pts)
    6. Site Boundary geometry (10 pts)
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # Basic checks
    if not result.get('output_exists', False):
        return {"passed": False, "score": 0, "feedback": "Output file not found"}
        
    if not result.get('file_created_during_task', False):
        return {"passed": False, "score": 0, "feedback": "File was not modified during the task (anti-gaming)"}

    dxf_data = result.get('dxf_data', {})
    if not dxf_data.get('parsed', False):
        return {"passed": False, "score": 10, "feedback": f"DXF file created but could not be parsed: {dxf_data.get('error')}"}

    score = 10  # Base score for valid file
    feedback = ["Valid DXF file created (+10)"]
    
    # 1. Verify Layers (15 pts)
    # PANELS (Green/3), ELECTRICAL (Red/1), BORDER (White/7)
    layers = dxf_data.get('layers', {})
    layer_score = 0
    
    # Check PANELS
    if 'PANELS' in layers:
        if layers['PANELS']['color'] == 3:
            layer_score += 5
            feedback.append("Layer PANELS correct (+5)")
        else:
            layer_score += 2
            feedback.append(f"Layer PANELS exists but wrong color {layers['PANELS']['color']} (+2)")
    
    # Check ELECTRICAL
    if 'ELECTRICAL' in layers:
        if layers['ELECTRICAL']['color'] == 1:
            layer_score += 5
            feedback.append("Layer ELECTRICAL correct (+5)")
        else:
            layer_score += 2
            feedback.append(f"Layer ELECTRICAL exists but wrong color (+2)")
            
    # Check BORDER
    if 'BORDER' in layers:
        if layers['BORDER']['color'] == 7:
            layer_score += 5
            feedback.append("Layer BORDER correct (+5)")
        else:
            layer_score += 2
            feedback.append(f"Layer BORDER exists but wrong color (+2)")
            
    score += layer_score

    # Helper for geometry matching
    rectangles = dxf_data.get('rectangles', [])
    lines = dxf_data.get('lines', [])
    
    def find_rect(target_x, target_y, target_w, target_h, layer_name, tol=100):
        for r in rectangles:
            if layer_name and r['layer'] != layer_name:
                continue
            b = r['bounds']
            if (abs(b['x'] - target_x) < tol and 
                abs(b['y'] - target_y) < tol and
                abs(b['w'] - target_w) < tol and
                abs(b['h'] - target_h) < tol):
                return True
        return False

    # 2. Verify Panel Rows (40 pts)
    # 4 rows: 10000x2000 at (0,0), (0,4000), (0,8000), (0,12000)
    panels_found = 0
    for i, y in enumerate([0, 4000, 8000, 12000]):
        if find_rect(0, y, 10000, 2000, 'PANELS'):
            panels_found += 1
    
    panel_score = panels_found * 10
    score += panel_score
    if panels_found > 0:
        feedback.append(f"Found {panels_found}/4 Panel Rows (+{panel_score})")
    else:
        feedback.append("No correct Panel Rows found")

    # 3. Verify Combiner Box (10 pts)
    # 600x600 at (11000, 6700)
    if find_rect(11000, 6700, 600, 600, 'ELECTRICAL', tol=200):
        score += 10
        feedback.append("Combiner Box found (+10)")
    else:
        feedback.append("Combiner Box not found or incorrect dimensions")

    # 4. Verify Site Boundary (10 pts)
    # 16000x18000 at (-2000, -2000)
    if find_rect(-2000, -2000, 16000, 18000, 'BORDER', tol=200):
        score += 10
        feedback.append("Site Boundary found (+10)")
    else:
        feedback.append("Site Boundary not found")

    # 5. Verify Conduit Lines (15 pts)
    # End point is approx (11300, 7000). Start points vary.
    # We just check if we have lines on ELECTRICAL layer connecting roughly correct areas.
    # Target hub: (11300, 7000)
    # Sources x: 10000
    
    conduits_found = 0
    hub_point = (11300, 7000)
    
    for line in lines:
        if line['layer'] != 'ELECTRICAL':
            continue
        p1 = line['start']
        p2 = line['end']
        
        # Check if either end is near hub
        dist1 = math.hypot(p1[0]-hub_point[0], p1[1]-hub_point[1])
        dist2 = math.hypot(p2[0]-hub_point[0], p2[1]-hub_point[1])
        
        is_connected_to_hub = (dist1 < 500 or dist2 < 500)
        
        # Check if other end is near x=10000
        other_x = p2[0] if dist1 < 500 else p1[0]
        is_connected_to_panels = abs(other_x - 10000) < 500
        
        if is_connected_to_hub and is_connected_to_panels:
            conduits_found += 1

    # Cap conduits at 4
    conduits_found = min(conduits_found, 4)
    conduit_score = int(conduits_found * (15/4))
    score += conduit_score
    if conduits_found > 0:
        feedback.append(f"Found {conduits_found}/4 Conduit Lines (+{conduit_score})")

    return {
        "passed": score >= 60,
        "score": score,
        "feedback": " | ".join(feedback)
    }