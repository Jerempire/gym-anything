#!/usr/bin/env python3
"""
Verifier for kayak_hull_lofting task.
Reads the JSON result generated by the export script which includes 
geometry analysis performed inside the container (using ezdxf).
"""

import json
import tempfile
import os
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_kayak_hull_lofting(traj, env_info, task_info):
    """
    Verify the kayak hull drawing.
    
    Criteria:
    1. DXF file created during task (10 pts)
    2. Layers correct (MOLD, JIG, TEXT) (10 pts)
    3. Hull Geometry (Bottom/Side/Mirror) (30 pts)
    4. Deck Arc (20 pts)
    5. Jig Lines (15 pts)
    6. Text Label (15 pts)
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy result file
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 1. File existence and creation time (10 pts)
    if result.get('file_exists') and result.get('file_created_during_task'):
        score += 10
        feedback_parts.append("DXF file created")
    elif result.get('file_exists'):
        feedback_parts.append("File exists but not created during task (stale?)")
    else:
        return {"passed": False, "score": 0, "feedback": "No output file found"}

    # Parse analysis
    analysis = result.get('dxf_analysis', {})
    if not analysis.get('valid_dxf'):
        return {"passed": False, "score": score, "feedback": "File created but is not a valid DXF"}

    # 2. Layers (10 pts)
    # Require MOLD, JIG, TEXT
    found_layers = analysis.get('layers_found', [])
    req_layers = ["MOLD", "JIG", "TEXT"]
    missing = [l for l in req_layers if l not in found_layers]
    if not missing:
        score += 10
        feedback_parts.append("Layers correct")
    else:
        score += max(0, 10 - (len(missing) * 3))
        feedback_parts.append(f"Missing layers: {missing}")

    # 3. Hull Geometry (30 pts total)
    geo = analysis.get('geometry', {})
    hull_parts = 0
    if geo.get('right_bottom'): hull_parts += 1
    if geo.get('right_side'): hull_parts += 1
    if geo.get('left_bottom'): hull_parts += 1
    if geo.get('left_side'): hull_parts += 1
    
    geo_score = int((hull_parts / 4) * 30)
    score += geo_score
    if hull_parts == 4:
        feedback_parts.append("Hull outline correct")
    else:
        feedback_parts.append(f"Hull outline incomplete ({hull_parts}/4 segments)")

    # 4. Deck Arc (20 pts)
    if geo.get('deck_arc'):
        score += 20
        feedback_parts.append("Deck arc found")
    else:
        feedback_parts.append("Deck arc missing or incorrect")

    # 5. Jig Lines (15 pts)
    jig_score = 0
    if geo.get('strongback_line'): jig_score += 8
    if geo.get('centerline'): jig_score += 7
    score += jig_score
    if jig_score == 15:
        feedback_parts.append("Jig lines correct")
    
    # 6. Text (15 pts)
    if analysis.get('text_found'):
        score += 15
        feedback_parts.append("Label found")
    else:
        feedback_parts.append("Label missing")

    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }