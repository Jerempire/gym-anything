#!/usr/bin/env python3
"""
Verifier for panel_layout_drawing task.
Parses the JSON analysis generated by the export script (which ran ezdxf inside the container).
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_panel_layout_drawing(traj, env_info, task_info):
    """
    Verify the control panel layout drawing.
    
    Criteria:
    1. File exists and is a valid DXF (5 pts)
    2. Created during task (Anti-gaming)
    3. Required Layers present (12 pts)
    4. Panel Backplate dimensions correct (10 pts)
    5. Entities present on specific layers (Rails, Wireducts, Components) (32 pts)
    6. Text labels correct (12 pts)
    7. Title present (5 pts)
    8. Dimensions present (9 pts)
    9. VLM Visual Check (15 pts)
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Load result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback = []
    
    # Extract data
    file_exists = result.get('file_exists', False)
    created_during = result.get('created_during_task', False)
    dxf_data = result.get('dxf_analysis', {})
    
    # ---------------------------------------------------------
    # Criterion 1: File Existence & Validity (5 pts)
    # ---------------------------------------------------------
    if file_exists and dxf_data.get('valid_dxf', False):
        score += 5
        feedback.append("Valid DXF file found.")
    else:
        return {"passed": False, "score": 0, "feedback": "No valid DXF file found."}

    # ---------------------------------------------------------
    # Criterion 2: Anti-Gaming (Timestamp)
    # ---------------------------------------------------------
    if not created_during:
        feedback.append("WARNING: File timestamp predates task start.")
        # We don't fail immediately but penalize heavily or flag it
        # For this logic, we'll continue but scoring will naturally be lower if empty
    
    layers = set(dxf_data.get('layers', []))
    counts = dxf_data.get('entity_counts', {})
    texts = dxf_data.get('text_content', [])
    text_blob = " ".join(texts).upper()
    
    # ---------------------------------------------------------
    # Criterion 3: Layers (12 pts)
    # ---------------------------------------------------------
    req_layers = ["BACKPLATE", "DINRAIL", "WIREDUCT", "COMPONENTS", "LABELS", "DIMENSIONS"]
    layers_found = 0
    for l in req_layers:
        if l in layers:
            layers_found += 1
    
    layer_score = layers_found * 2
    score += layer_score
    feedback.append(f"Layers found: {layers_found}/{len(req_layers)}")

    # ---------------------------------------------------------
    # Criterion 4: Backplate Dimensions (10 pts)
    # ---------------------------------------------------------
    width = dxf_data.get('bbox_width', 0)
    height = dxf_data.get('bbox_height', 0)
    # Expected 600x800, allow 5% tolerance
    if 570 <= width <= 630 and 760 <= height <= 840:
        score += 10
        feedback.append(f"Backplate dimensions correct (~{int(width)}x{int(height)}).")
    elif width > 0:
        feedback.append(f"Backplate dimensions incorrect: {int(width)}x{int(height)} (Expected 600x800).")
    else:
        feedback.append("Backplate geometry not found.")

    # ---------------------------------------------------------
    # Criterion 5: Entities on Layers (32 pts)
    # ---------------------------------------------------------
    # DINRAIL: 3 rails expected
    if counts.get('DINRAIL', 0) >= 3:
        score += 12
        feedback.append("DIN Rails present.")
    elif counts.get('DINRAIL', 0) > 0:
        score += 6
        feedback.append("Incomplete DIN Rails.")
        
    # WIREDUCT: 4 ducts expected
    if counts.get('WIREDUCT', 0) >= 4:
        score += 8
        feedback.append("Wire ducts present.")
    elif counts.get('WIREDUCT', 0) > 0:
        score += 4
        
    # COMPONENTS: 6 components expected
    if counts.get('COMPONENTS', 0) >= 6:
        score += 12
        feedback.append("Components present.")
    elif counts.get('COMPONENTS', 0) > 0:
        score += 6
        feedback.append("Some components missing.")

    # ---------------------------------------------------------
    # Criterion 6: Component Labels (12 pts)
    # ---------------------------------------------------------
    req_labels = ["QF1", "PLC1", "KM1", "KM2", "KM3", "TB1"]
    labels_found = 0
    for lbl in req_labels:
        if lbl in text_blob:
            labels_found += 1
            
    label_score = labels_found * 2
    score += label_score
    feedback.append(f"Labels found: {labels_found}/{len(req_labels)}")

    # ---------------------------------------------------------
    # Criterion 7: Title (5 pts)
    # ---------------------------------------------------------
    if "MOTOR CONTROL PANEL" in text_blob:
        score += 5
        feedback.append("Title text found.")

    # ---------------------------------------------------------
    # Criterion 8: Dimensions (9 pts)
    # ---------------------------------------------------------
    dim_count = dxf_data.get('dimensions_count', 0)
    if dim_count >= 3:
        score += 9
        feedback.append("Dimensions found.")
    elif dim_count > 0:
        score += 5
        feedback.append("Some dimensions found, but fewer than expected.")
    else:
        feedback.append("No dimensions found.")

    # ---------------------------------------------------------
    # Criterion 9: VLM Visual Check (15 pts)
    # ---------------------------------------------------------
    from gym_anything.vlm import query_vlm, get_final_screenshot
    
    final_screenshot = get_final_screenshot(traj)
    vlm_score = 0
    
    if final_screenshot:
        prompt = """
        Analyze this LibreCAD screenshot. 
        Does it show a technical drawing of a control panel layout?
        Look for:
        1. A rectangular outline (the panel).
        2. Horizontal rails with rectangular blocks (components) on them.
        3. Text labels near the blocks.
        4. Dimension lines (lines with arrows and numbers).
        
        Answer JSON: {"is_panel_drawing": bool, "has_components": bool, "confidence": float}
        """
        try:
            vlm_res = query_vlm(prompt=prompt, image=final_screenshot)
            parsed = vlm_res.get('parsed', {})
            if parsed.get('is_panel_drawing') and parsed.get('has_components'):
                vlm_score = 15
                feedback.append("Visual verification passed.")
            else:
                feedback.append("Visual verification failed (drawing does not look like a panel layout).")
        except Exception as e:
            logger.warning(f"VLM check failed: {e}")
            # Fallback: if programmatic score is high (>60), assume visual is ok to avoid false negative from VLM error
            if score > 60:
                vlm_score = 15
                feedback.append("Visual verification skipped (programmatic pass).")
    
    score += vlm_score

    # ---------------------------------------------------------
    # Final Result
    # ---------------------------------------------------------
    pass_threshold = 60
    # Mandatory checks
    passed = (score >= pass_threshold) and (file_exists) and (labels_found >= 3)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }