#!/usr/bin/env python3
"""Verifier for first_nations_t90_exempt_income task.

Thomas Manybears — First Nations taxpayer living on a reserve.
Mixed income: $18,200 taxable off-reserve T4, $42,500 tax-exempt on-reserve T4 (Box 71).
Key actions: Form T90 to elect CWB on exempt income, CCR Rural Supplement.

Scoring (100 pts total, pass threshold 60):
  Criterion 1: File saved with correct name (15 pts)
  Criterion 2: Timestamp valid (file created during task) (10 pts)
  Criterion 3: Taxpayer name present (10 pts)
  Criterion 4: Taxable T4 income ($18,200) present (10 pts)
  Criterion 5: Exempt T4 income ($42,500) present (10 pts)
  Criterion 6: CRITICAL - Total income != $60,700 (15 pts) - Prevents taxing the exempt income!
  Criterion 7: VLM Verification of Form T90 and CCR Rural checkbox (30 pts)

Score cap: If the agent incorrectly adds the $42,500 to Box 14 (making total income $60,700),
the return is fundamentally flawed and the score is capped at 40 points (FAIL).
"""

import json
import os
import tempfile

def verify_first_nations_t90_exempt_income(traj, env_info, task_info):
    """Verify Thomas Manybears First Nations / T90 return."""
    score = 0
    feedback = []

    # 1. Check for copy_from_env helper
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "No copy_from_env helper available."}

    # 2. Extract JSON result generated by the export script
    try:
        with tempfile.NamedTemporaryFile(delete=False, suffix='.json') as f:
            temp_path = f.name
        copy_from_env("C:/Users/Docker/Desktop/t90_result.json", temp_path)
        with open(temp_path, 'r', encoding='utf-8-sig') as f:
            result = json.load(f)
        os.unlink(temp_path)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Could not read exported result: {e}"}

    # --- Criterion 1: File saved with correct name (15 pts) ---
    file_ok = result.get('file_exists') and result.get('file_size_bytes', 0) > 500
    if file_ok:
        score += 15
        feedback.append("Return file 'thomas_manybears.24t' successfully saved.")
    else:
        feedback.append("FAIL: Return file not found or too small.")

    # --- Criterion 2: Timestamp valid (10 pts) ---
    if result.get('file_is_new'):
        score += 10
        feedback.append("File timestamp valid (created during task).")
    else:
        feedback.append("FAIL: File timestamp invalid or pre-existing.")

    # --- Criterion 3: Taxpayer name present (10 pts) ---
    name_ok = result.get('contains_manybears') and result.get('contains_thomas')
    if name_ok:
        score += 10
        feedback.append("Taxpayer name (Thomas Manybears) found.")
    elif result.get('contains_manybears') or result.get('contains_thomas'):
        score += 5
        feedback.append("Taxpayer name partially found.")
    else:
        feedback.append("FAIL: Taxpayer name not found in the return.")

    # --- Criterion 4: Taxable T4 income ($18,200) (10 pts) ---
    taxable_ok = result.get('contains_18200', False)
    if taxable_ok:
        score += 10
        feedback.append("Taxable T4 income ($18,200) found.")
    else:
        feedback.append("FAIL: Taxable T4 income ($18,200) not found.")

    # --- Criterion 5: Exempt T4 income ($42,500) (10 pts) ---
    exempt_ok = result.get('contains_42500', False)
    if exempt_ok:
        score += 10
        feedback.append("Exempt Box 71 income ($42,500) found.")
    else:
        feedback.append("FAIL: Exempt Box 71 income ($42,500) not found.")

    # --- Criterion 6: CRITICAL Non-Existence Check for $60,700 (15 pts) ---
    # If the agent erroneously puts the 42,500 in Box 14, total T4 income becomes 60,700
    critical_error = result.get('contains_60700', False)
    if critical_error:
        feedback.append("CRITICAL FAIL: $60,700 found! The agent incorrectly added tax-exempt income to Box 14.")
    else:
        score += 15
        feedback.append("Pass: Tax-exempt income was strictly separated from taxable income.")

    # --- Criterion 7: VLM Verification for Form T90 and CCR Rural (30 pts) ---
    vlm_points = 0
    query_vlm = env_info.get('query_vlm')
    
    if query_vlm:
        # Import frame sampling utilities
        from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot
        frames = sample_trajectory_frames(traj, n=5)
        final_shot = get_final_screenshot(traj)
        images_to_check = frames + [final_shot] if final_shot else frames
        
        prompt = (
            "You are verifying a Canadian personal tax return in StudioTax 2024. "
            "Look across the provided trajectory screenshots and determine: "
            "1. Did the user open or interact with 'Form T90' (Income exempt under the Indian Act) to calculate the Canada Workers Benefit (CWB)? "
            "2. Did the user check the box for the Canada Carbon Rebate (CCR) 'Rural Supplement' (indicating they live outside a CMA)? "
            "Return JSON: {\"form_t90_used\": true/false, \"ccr_rural_checked\": true/false}"
        )
        
        vlm_result = query_vlm(prompt=prompt, images=images_to_check)
        if vlm_result and vlm_result.get("success"):
            parsed = vlm_result.get("parsed", {})
            t90_used = parsed.get("form_t90_used", False)
            ccr_checked = parsed.get("ccr_rural_checked", False)
            
            if t90_used:
                vlm_points += 15
                feedback.append("VLM confirms Form T90 was utilized.")
            else:
                feedback.append("VLM did not detect Form T90 usage.")
                
            if ccr_checked:
                vlm_points += 15
                feedback.append("VLM confirms CCR Rural Supplement was claimed.")
            else:
                feedback.append("VLM did not detect CCR Rural Supplement claim.")
        else:
            feedback.append("VLM query failed or returned no valid data.")
    else:
        feedback.append("VLM function not available. Skipping visual checks.")

    score += vlm_points

    # --- Final Scoring Logic & Caps ---
    if critical_error:
        score = min(score, 40)
        
    passed = score >= 60 and not critical_error

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }