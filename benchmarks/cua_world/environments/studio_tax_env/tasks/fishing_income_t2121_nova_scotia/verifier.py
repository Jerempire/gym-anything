#!/usr/bin/env python3
"""Verifier for fishing_income_t2121_nova_scotia task.

Callum MacNeil — Nova Scotia lobster fisherman.
T2121 Fishing Income ($128,400) + T4 Employment ($14,400).
Checks for appropriate CCA calculations (Class 7 and Class 8).
Utilizes multiple independent signals and trajectory-based VLM verification.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_fishing_income_t2121_nova_scotia(traj, env_info, task_info):
    """Verify Callum MacNeil T2121 Nova Scotia fishing return."""
    score = 0
    feedback = []
    
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: copy_from_env not available"}

    # =========================================================================
    # 1. Load Data from Container
    # =========================================================================
    try:
        with tempfile.NamedTemporaryFile(delete=False, suffix='.json') as f:
            temp_path = f.name
        # Copy the JSON generated by export_result.ps1
        copy_from_env("C:/Users/Docker/Desktop/fishing_result.json", temp_path)
        with open(temp_path, 'r', encoding='utf-8-sig') as f:
            result = json.load(f)
        os.unlink(temp_path)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Could not read result: {e}"}

    # =========================================================================
    # 2. Evaluate Programmatic Criteria (75 points max)
    # =========================================================================
    
    # Criterion A: File Saved Correctly (10 pts)
    file_exists = result.get('file_exists', False)
    file_size = result.get('file_size_bytes', 0)
    if file_exists and file_size > 1000:
        score += 10
        feedback.append("Return file 'callum_macneil.24t' successfully saved")
    else:
        feedback.append("FAIL: Return file not found or appears to be empty/stub")

    # Criterion B: File Created/Modified During Task [Anti-gaming] (10 pts)
    if result.get('file_is_new', False):
        score += 10
        feedback.append("File modification timestamp valid")
    else:
        feedback.append("FAIL: File timestamp invalid (created before task start)")

    # Criterion C: Taxpayer Name (10 pts)
    if result.get('contains_macneil') and result.get('contains_callum'):
        score += 10
        feedback.append("Taxpayer name (Callum MacNeil) found")
    else:
        feedback.append("FAIL: Taxpayer name missing")

    # Criterion D: Gross Fishing Income $128,400 (15 pts) - CRITICAL
    has_gross = result.get('contains_128400', False)
    if has_gross:
        score += 15
        feedback.append("Gross Fishing Income ($128,400) found")
    else:
        feedback.append("FAIL: Gross Fishing Income ($128,400) missing")

    # Criterion E: T4 Employment Income $14,400 (5 pts)
    if result.get('contains_14400', False):
        score += 5
        feedback.append("T4 Employment Income ($14,400) found")
    else:
        feedback.append("MISSING: T4 Employment Income")

    # Criterion F: Fishing Expenses (15 pts)
    wages_ok = result.get('contains_31400', False)
    fuel_ok = result.get('contains_18200', False)
    if wages_ok and fuel_ok:
        score += 15
        feedback.append("Fishing expenses (Wages & Fuel) found")
    elif wages_ok or fuel_ok:
        score += 7
        feedback.append("Partial fishing expenses found")
    else:
        feedback.append("FAIL: Major fishing expenses missing")

    # Criterion G: CCA Calculations (10 pts)
    # Class 7 ($12,750), Class 8 ($2,900), Total CCA ($15,650)
    cca7_ok = result.get('contains_12750', False)
    cca_total_ok = result.get('contains_15650', False)
    if cca7_ok and cca_total_ok:
        score += 10
        feedback.append("CCA properly calculated (Class 7 Vessel)")
    elif cca7_ok or cca_total_ok or result.get('contains_2900', False):
        score += 5
        feedback.append("Partial CCA calculations found")
    else:
        feedback.append("FAIL: CCA calculations missing or incorrect")

    # =========================================================================
    # 3. Evaluate Trajectory VLM Criteria (25 points max)
    # =========================================================================
    vlm_score = 0
    if query_vlm:
        try:
            # Import framework specific VLM utilities
            from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot
            
            # Sample frames from trajectory to verify the process, not just final state
            frames = sample_trajectory_frames(traj, n=4)
            final_frame = get_final_screenshot(traj)
            
            # Combine sampled frames and final frame
            eval_images = frames
            if final_frame:
                eval_images.append(final_frame)
                
            if eval_images:
                prompt = """You are evaluating a tax preparation agent's workflow in StudioTax.
                Look at these screenshots taken during the task.
                
                Please determine:
                1. Did the agent open and utilize 'Form T2121 Statement of Fishing Activities' (look for 'Fishing' or 'T2121' in the forms/trees)?
                2. Is there evidence the agent navigated the StudioTax interface to enter business/fishing expenses or CCA?
                
                Respond in JSON format:
                {
                    "used_t2121_form": true/false,
                    "entered_fishing_expenses": true/false,
                    "reasoning": "Brief explanation"
                }"""
                
                vlm_res = query_vlm(prompt=prompt, images=eval_images)
                
                if vlm_res and vlm_res.get("success"):
                    parsed = vlm_res.get("parsed", {})
                    if parsed.get("used_t2121_form"):
                        vlm_score += 15
                        feedback.append("VLM Verification: T2121 Fishing Form utilized successfully")
                    else:
                        feedback.append("VLM Verification FAIL: T2121 Form not clearly utilized (did they use T2125 by mistake?)")
                        
                    if parsed.get("entered_fishing_expenses"):
                        vlm_score += 10
                        feedback.append("VLM Verification: Evidence of expense/CCA entry found")
                else:
                    feedback.append(f"VLM error: {vlm_res.get('error', 'unknown')}")
            else:
                feedback.append("No trajectory frames available for VLM check.")
        except Exception as e:
            logger.error(f"VLM verification failed: {e}")
            feedback.append("VLM evaluation skipped due to internal error.")

    score += vlm_score

    # =========================================================================
    # 4. Final Caps & Thresholds
    # =========================================================================
    # Score Cap: If the primary fishing income was not entered at all, the return 
    # is fundamentally broken and cannot pass.
    if not has_gross:
        score = min(score, 45)
        feedback.append("CRITICAL: Score capped at 45 because Gross Fishing Income was missing.")
    
    passed = score >= 60

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }