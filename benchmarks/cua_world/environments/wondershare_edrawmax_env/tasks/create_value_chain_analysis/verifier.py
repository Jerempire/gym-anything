#!/usr/bin/env python3
"""
Verifier for create_value_chain_analysis task.
Verifies that the agent created a Value Chain diagram with specific content.
"""

import json
import os
import tempfile
import zipfile
import logging
import shutil

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_create_value_chain_analysis(traj, env_info, task_info):
    """
    Verify the Value Chain Analysis task.
    
    Criteria:
    1. .eddx file exists and is a valid ZIP archive (10 pts)
    2. .png file exists (10 pts)
    3. Files were created during the task window (Anti-gaming) (10 pts)
    4. Content Verification: The .eddx file contains the required text strings (70 pts)
    """
    
    # 1. Setup and retrieve file from environment
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    required_strings = metadata.get('required_strings', [])
    template_indicators = metadata.get('template_indicators', [])
    
    # Load the result JSON generated by export_result.sh
    result_json_path = tempfile.mktemp(suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", result_json_path)
        with open(result_json_path, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {e}"}
    finally:
        if os.path.exists(result_json_path):
            os.remove(result_json_path)

    score = 0
    feedback = []
    
    # 2. Check File Existence and Timestamps
    eddx_exists = task_result.get('eddx_exists', False)
    png_exists = task_result.get('png_exists', False)
    eddx_fresh = task_result.get('eddx_created_during_task', False)
    
    if eddx_exists:
        score += 10
        feedback.append("EDDX file created.")
    else:
        feedback.append("EDDX file missing.")
        
    if png_exists:
        score += 10
        feedback.append("PNG export created.")
    else:
        feedback.append("PNG export missing.")
        
    if eddx_fresh:
        score += 10
        feedback.append("File created during task session.")
    elif eddx_exists:
        feedback.append("Warning: EDDX file timestamp is outside task window.")

    # 3. Content Analysis (Deep inspection of .eddx)
    # The .eddx file is a ZIP containing XML data. We extract and grep for strings.
    content_score = 0
    max_content_score = 70
    
    if eddx_exists:
        temp_eddx = tempfile.mktemp(suffix='.eddx')
        try:
            copy_from_env("/home/ga/Documents/ecotech_value_chain.eddx", temp_eddx)
            
            # EdrawMax files are ZIPs. Text is usually in 'pages/page1.xml' or shared strings.
            # We will read all XML files in the archive to be robust.
            found_strings = set()
            all_text_content = ""
            
            if zipfile.is_zipfile(temp_eddx):
                with zipfile.ZipFile(temp_eddx, 'r') as z:
                    for filename in z.namelist():
                        if filename.endswith('.xml'):
                            try:
                                with z.open(filename) as f:
                                    # Read as binary, decode with errors ignore to handle encoding issues
                                    content = f.read().decode('utf-8', errors='ignore')
                                    all_text_content += content
                            except Exception:
                                continue
                
                # Check for required strings
                matched_count = 0
                missing_strings = []
                
                for s in required_strings:
                    # Simple case-insensitive check
                    if s.lower() in all_text_content.lower():
                        matched_count += 1
                        found_strings.add(s)
                    else:
                        missing_strings.append(s)
                
                # Check for template indicators (structure)
                structure_matched = 0
                for s in template_indicators:
                    if s.lower() in all_text_content.lower():
                        structure_matched += 1
                
                # Scoring logic for content
                # We have required strings (business data) and template strings (structure)
                
                # Calculate percentage of required business data found
                if required_strings:
                    data_points = len(required_strings)
                    data_score = (matched_count / data_points) * 50 # Up to 50 pts for data
                    content_score += data_score
                    feedback.append(f"Data Match: {matched_count}/{data_points} items found.")
                    if missing_strings:
                        feedback.append(f"Missing data: {', '.join(missing_strings[:3])}...")

                # Calculate score for correct template usage
                if template_indicators:
                    struct_points = len(template_indicators)
                    struct_score = (structure_matched / struct_points) * 20 # Up to 20 pts for structure
                    content_score += struct_score
                    if structure_matched < struct_points:
                        feedback.append("Diagram structure labels (e.g. 'Inbound Logistics') missing/incomplete.")
                
            else:
                feedback.append("EDDX file is not a valid ZIP archive.")
                
        except Exception as e:
            feedback.append(f"Error analyzing EDDX content: {e}")
        finally:
            if os.path.exists(temp_eddx):
                os.remove(temp_eddx)
    
    score += int(content_score)
    
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }