#!/usr/bin/env python3
"""
Verifier for create_user_story_map task.

Verification Strategy:
1. File Verification (40 pts): Checks if .eddx and .png exist and were created during the task.
2. Content Verification (30 pts): Inspects the internal XML of the .eddx file for required text labels.
3. VLM Verification (30 pts): Analyzes the exported PNG for visual structure (colors, grid layout).
"""

import json
import os
import tempfile
import zipfile
import logging
from typing import Dict, Any

# Import VLM utilities from the framework
from gym_anything.vlm import query_vlm, get_final_screenshot

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_create_user_story_map(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]) -> Dict[str, Any]:
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load result JSON generated by export_result.sh
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {str(e)}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    score = 0
    feedback_parts = []
    
    # ------------------------------------------------------------------
    # 1. FILE VERIFICATION (40 Points)
    # ------------------------------------------------------------------
    eddx_exists = result.get('eddx_exists', False)
    eddx_fresh = result.get('eddx_created_during_task', False)
    eddx_size = result.get('eddx_size', 0)
    
    png_exists = result.get('png_exists', False)
    png_fresh = result.get('png_created_during_task', False)
    png_size = result.get('png_size', 0)

    if eddx_exists and eddx_fresh and eddx_size > 1000:
        score += 20
        feedback_parts.append("Project file (.eddx) created successfully.")
    elif eddx_exists:
        feedback_parts.append("Project file exists but timestamp check failed (anti-gaming).")
    else:
        feedback_parts.append("Project file (.eddx) missing.")

    if png_exists and png_fresh and png_size > 5000:
        score += 20
        feedback_parts.append("Image export (.png) created successfully.")
    else:
        feedback_parts.append("Image export (.png) missing or invalid.")

    # ------------------------------------------------------------------
    # 2. CONTENT VERIFICATION (Text in XML) (30 Points)
    # ------------------------------------------------------------------
    required_terms = task_info.get('metadata', {}).get('required_terms', [])
    found_terms = []
    
    if eddx_exists:
        temp_eddx = tempfile.NamedTemporaryFile(delete=False, suffix='.eddx')
        try:
            copy_from_env("/home/ga/Documents/skyhigh_story_map.eddx", temp_eddx.name)
            
            # EdrawMax .eddx is a ZIP archive. Text is usually in page/page1.xml or similar.
            text_content = ""
            if zipfile.is_zipfile(temp_eddx.name):
                with zipfile.ZipFile(temp_eddx.name, 'r') as zf:
                    for filename in zf.namelist():
                        if filename.endswith(".xml"):
                            try:
                                text_content += zf.read(filename).decode('utf-8', errors='ignore')
                            except:
                                pass
            
            # Check for terms
            hit_count = 0
            for term in required_terms:
                if term.lower() in text_content.lower():
                    hit_count += 1
                    found_terms.append(term)
            
            # Score based on percentage of terms found
            term_score = min(30, int((hit_count / len(required_terms)) * 30))
            score += term_score
            feedback_parts.append(f"Found {hit_count}/{len(required_terms)} required text labels.")
            
        except Exception as e:
            feedback_parts.append(f"Failed to analyze .eddx content: {str(e)}")
        finally:
            if os.path.exists(temp_eddx.name):
                os.unlink(temp_eddx.name)
    
    # ------------------------------------------------------------------
    # 3. VLM VERIFICATION (Visual Structure) (30 Points)
    # ------------------------------------------------------------------
    # Prefer the exported PNG if available, otherwise use final screenshot
    vlm_image = None
    if png_exists and png_fresh:
        temp_png = tempfile.NamedTemporaryFile(delete=False, suffix='.png')
        try:
            copy_from_env("/home/ga/Documents/skyhigh_story_map.png", temp_png.name)
            vlm_image = temp_png.name
        except:
            pass
    
    if not vlm_image:
        # Fallback to final screenshot if export failed but screen shows work
        vlm_image = get_final_screenshot(traj)

    if vlm_image:
        prompt = """
        You are evaluating a User Story Map diagram.
        
        Look for the following criteria:
        1. **Grid Structure**: Are there distinct columns of cards/shapes?
        2. **Color Coding**: Are there BLUE header cards at the top and YELLOW story cards below?
        3. **Slicing**: Is there a visual separation (gap or line) between an upper group (MVP) and lower group (Release 2)?
        4. **Legibility**: Does the diagram look like a structured chart, not just random shapes?
        
        Respond in JSON:
        {
            "has_blue_headers": true/false,
            "has_yellow_cards": true/false,
            "has_mvp_split": true/false,
            "looks_like_story_map": true/false,
            "confidence": "low/medium/high"
        }
        """
        
        try:
            vlm_result = query_vlm(prompt=prompt, image=vlm_image)
            parsed = vlm_result.get('parsed', {})
            
            vlm_score = 0
            if parsed.get('has_blue_headers'): vlm_score += 10
            if parsed.get('has_yellow_cards'): vlm_score += 10
            if parsed.get('has_mvp_split'): vlm_score += 5
            if parsed.get('looks_like_story_map'): vlm_score += 5
            
            score += vlm_score
            feedback_parts.append(f"Visual analysis score: {vlm_score}/30")
            
        except Exception as e:
            logger.warning(f"VLM check failed: {e}")
            feedback_parts.append("Visual verification skipped due to error.")
            # Grant partial credit if text content was perfect to avoid punishing valid work
            if len(found_terms) >= len(required_terms) - 2:
                 score += 15
                 feedback_parts.append("Granting partial visual credit based on strong text evidence.")
    else:
         feedback_parts.append("No image available for visual verification.")

    # Cleanup temp PNG if we created one
    if vlm_image and vlm_image.endswith('.png') and os.path.exists(vlm_image) and "task_final" not in vlm_image:
        try:
            os.unlink(vlm_image)
        except:
            pass

    # ------------------------------------------------------------------
    # FINAL SCORING
    # ------------------------------------------------------------------
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }