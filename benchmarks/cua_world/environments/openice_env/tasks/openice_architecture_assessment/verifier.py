#!/usr/bin/env python3
"""
Verifier for OpenICE Architecture Assessment task.

Evaluates:
1. Report file quality (existence, size, specific technical content)
2. Functional verification (did the agent actually create devices in the UI?)

Scoring is multi-criterion to prevent gaming.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_openice_architecture_assessment(traj, env_info, task_info):
    """
    Verify the OpenICE Architecture Assessment task.
    
    Requires:
    - copy_from_env capability
    - /tmp/task_result.json generated by export_result.sh
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load result JSON
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result file: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    details = []

    # Extract data sections
    report = result.get("report", {})
    functional = result.get("functional", {})
    app_running = result.get("app_running", False)

    # === GATE CHECK ===
    # If no report AND no devices created, fail immediately (agent did nothing)
    report_exists = report.get("exists", False)
    total_devices = functional.get("total_device_signals", 0)
    app_detected = functional.get("app_detected", False)
    
    if not report_exists and total_devices == 0 and not app_detected:
        return {
            "passed": False,
            "score": 0,
            "feedback": "GATE FAILURE: No report file found and no devices created. Task attempts appear minimal.",
            "details": {"reason": "nothing_done"}
        }

    # === SCORING CRITERIA (100 pts) ===

    # 1. Report File Mechanics (10 pts)
    report_size = report.get("size_bytes", 0)
    created_during = report.get("created_during_task", False)
    
    if report_exists and created_during and report_size >= 500:
        score += 10
        details.append("Report exists, valid size, new timestamp (+10)")
    elif report_exists and created_during:
        score += 5
        details.append("Report exists but small (<500 bytes) (+5)")
        feedback_parts.append("Report file is too short")
    elif report_exists:
        # Exists but timestamp is old (pre-existing file?)
        details.append("Report exists but stale timestamp (+0)")
        feedback_parts.append("Report file appears to be pre-existing")
    else:
        feedback_parts.append("Report file not found")

    # 2. Report Content - Source Modules (15 pts)
    # Did they find 'interop-lab', 'demo-apps', etc?
    mod_refs = report.get("module_references", 0)
    if mod_refs >= 3:
        score += 15
        details.append(f"Report references {mod_refs} source modules (+15)")
    elif mod_refs >= 1:
        score += 5
        details.append(f"Report references {mod_refs} source modules (+5)")
    else:
        feedback_parts.append("Report missing source code module references")

    # 3. Report Content - Device Types (15 pts)
    # Did they list 'Multiparameter', 'Pulse Oximeter', etc?
    dev_refs = report.get("device_type_references", 0)
    if dev_refs >= 4:
        score += 15
        details.append(f"Report lists {dev_refs} device types (+15)")
    elif dev_refs >= 2:
        score += 5
        details.append(f"Report lists {dev_refs} device types (+5)")
    else:
        feedback_parts.append("Report missing device type listings")

    # 4. Report Content - Clinical Apps (10 pts)
    app_refs = report.get("clinical_app_references", 0)
    if app_refs >= 2:
        score += 10
        details.append(f"Report lists {app_refs} clinical apps (+10)")
    elif app_refs >= 1:
        score += 5
        details.append("Report lists 1 clinical app (+5)")
    else:
        feedback_parts.append("Report missing clinical app listings")

    # 5. Report Content - Tech Stack (10 pts)
    tech_refs = report.get("tech_stack_references", 0)
    if tech_refs >= 3:
        score += 10
        details.append(f"Report identifies {tech_refs} tech components (+10)")
    elif tech_refs >= 1:
        score += 5
        details.append("Report identifies some tech components (+5)")
    else:
        feedback_parts.append("Report missing technology stack details")

    # 6. Report Content - Recommendation (5 pts)
    if report.get("has_recommendation", False):
        score += 5
        details.append("Report includes recommendation (+5)")

    # 7. Functional - Device Creation (20 pts)
    # Verified by logs or window titles
    if total_devices >= 2:
        score += 20
        details.append(f"Functional: {total_devices} devices created (+20)")
    elif total_devices == 1:
        score += 10
        details.append("Functional: 1 device created (+10)")
        feedback_parts.append("Created only 1 device (target: 2+)")
    else:
        feedback_parts.append("No simulated devices created")

    # 8. Functional - App Launch (15 pts)
    if app_detected:
        score += 15
        details.append("Functional: Clinical app launched (+15)")
    else:
        feedback_parts.append("No clinical app launch detected")

    # === PASS/FAIL ===
    passed = score >= 60
    
    # Format feedback
    feedback = f"Score: {score}/100. "
    if feedback_parts:
        feedback += "Issues: " + ", ".join(feedback_parts)
    else:
        feedback += "Excellent work."

    return {
        "passed": passed,
        "score": score,
        "feedback": feedback,
        "details": details
    }