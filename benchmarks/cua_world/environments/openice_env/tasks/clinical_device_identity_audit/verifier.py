#!/usr/bin/env python3
"""
Verifier for Clinical Device Identity Audit task.

Verifies that:
1. The CSV inventory file exists and has correct headers.
2. The agent correctly identified 3 distinct device types.
3. The UUIDs in the CSV match the actual UUIDs generated by the system during this session.
   - Matches against 'ground_truth_uuids' extracted from logs.
   - Supports full UUIDs or 8-char prefixes (common in UI).
"""

import json
import os
import tempfile
import csv
import io
import logging

logger = logging.getLogger(__name__)

def verify_clinical_device_identity_audit(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function unavailable"}

    # Load result
    tmp = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", tmp.name)
        with open(tmp.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(tmp.name):
            os.unlink(tmp.name)

    score = 0
    feedback_parts = []
    
    # 1. File Existence & Timing (10 pts)
    if not result.get('file_exists'):
        return {"passed": False, "score": 0, "feedback": "Inventory CSV file not found."}
    
    file_mtime = int(result.get('file_mtime', 0))
    task_start = int(result.get('task_start', 0))
    if file_mtime < task_start:
        return {"passed": False, "score": 0, "feedback": "File exists but was not created during this task session."}

    score += 10
    feedback_parts.append("CSV file created")

    # 2. CSV Content Parsing (10 pts for valid format)
    content = result.get('file_content', '').strip()
    rows = []
    try:
        # Normalize newlines
        content = content.replace('\\n', '\n')
        reader = csv.DictReader(io.StringIO(content))
        
        # Verify headers
        if not reader.fieldnames or 'DeviceType' not in reader.fieldnames or 'UUID' not in reader.fieldnames:
             feedback_parts.append("CSV headers missing or incorrect (expected 'DeviceType,UUID')")
        else:
            score += 10
            feedback_parts.append("Valid CSV format")
            for row in reader:
                if row.get('DeviceType') and row.get('UUID'):
                    rows.append(row)
    except Exception as e:
        return {"passed": False, "score": score, "feedback": f"Failed to parse CSV: {e}"}

    if len(rows) < 3:
        feedback_parts.append(f"Expected 3 device rows, found {len(rows)}")
    
    # 3. Verify UUIDs against Ground Truth (Active Session UUIDs)
    # Ground truth contains all UUIDs seen in logs during this session
    valid_uuids = [u.lower().strip() for u in result.get('ground_truth_uuids', []) if u]
    
    # Required types to check for coverage
    required_types = {
        'multiparameter': False,
        'monitor': False,  # Relaxed matching
        'pulse': False,
        'oximeter': False,
        'infusion': False,
        'pump': False
    }
    
    matches_found = 0
    
    for row in rows:
        device_type = row['DeviceType'].lower()
        user_uuid = row['UUID'].strip().lower()
        
        # Mark type as seen (simple keyword matching)
        for key in required_types:
            if key in device_type:
                required_types[key] = True

        # Verify UUID
        # Check if user_uuid is a valid prefix of any ground truth UUID
        # or exact match
        is_valid = False
        for valid in valid_uuids:
            if valid.startswith(user_uuid) or user_uuid in valid:
                is_valid = True
                break
        
        if is_valid and len(user_uuid) >= 8: # Min length check to prevent "1" matching everything
            matches_found += 1
        elif not is_valid:
            feedback_parts.append(f"Invalid UUID for {row['DeviceType']}: {row['UUID']}")

    # Scoring matches (25 pts each, max 75)
    match_score = min(75, matches_found * 25)
    score += match_score
    feedback_parts.append(f"Verified {matches_found}/3 UUIDs")

    # 4. Device Diversity Check (5 pts)
    # Check if we saw keywords for at least 3 distinct categories in the rows
    types_seen = set()
    if required_types['multiparameter'] or required_types['monitor']: types_seen.add('monitor')
    if required_types['pulse'] or required_types['oximeter']: types_seen.add('oximeter')
    if required_types['infusion'] or required_types['pump']: types_seen.add('pump')
    
    if len(types_seen) >= 3:
        score += 5
        feedback_parts.append("All device types present")
    else:
        feedback_parts.append(f"Missing device types. Found: {types_seen}")

    # Pass/Fail
    # Need at least 2 valid UUID matches (50pts) + File (10pts) + Format (10pts) = 70pts
    # Or 3 valid matches = 75pts + File (10pts) = 85pts
    passed = score >= 60

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts),
        "details": {
            "rows_parsed": len(rows),
            "matches_verified": matches_found,
            "valid_uuids_in_system": len(valid_uuids)
        }
    }