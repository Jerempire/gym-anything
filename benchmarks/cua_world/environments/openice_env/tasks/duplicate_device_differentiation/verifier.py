#!/usr/bin/env python3
"""
Verifier for duplicate_device_differentiation task.
"""

import json
import os
import tempfile
import logging
from typing import Dict, Any

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_duplicate_device_differentiation(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]) -> Dict[str, Any]:
    """
    Verify that the agent identified and mapped two unique device UUIDs.
    
    Scoring Criteria:
    1. Output JSON exists and valid (10 pts)
    2. Two devices created (inferred from logs/windows) (20 pts)
    3. Vital Signs app launched (10 pts)
    4. UUID 1 is valid (matches a real system UUID) (25 pts)
    5. UUID 2 is valid AND distinct from UUID 1 (25 pts)
    6. Evidence screenshot exists (10 pts)
    """
    
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load result from container
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read task result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # --- 1. Output File Existence (10 pts) ---
    output_exists = result.get("output_exists", False)
    user_json = result.get("output_content", {})
    
    # Handle case where output_content might be a string if jq failed in export
    if isinstance(user_json, str):
        try:
            user_json = json.loads(user_json)
        except:
            user_json = {}

    if output_exists and isinstance(user_json, dict) and len(user_json) > 0:
        score += 10
        feedback_parts.append("Output JSON exists and is valid")
    else:
        feedback_parts.append("Output JSON missing or invalid")

    # --- 2. Device Creation (20 pts) ---
    # Check window increase (expecting 2 devices + potentially app window)
    window_inc = result.get("window_increase", 0)
    pulse_ox_log = result.get("pulse_ox_created", False)
    
    if window_inc >= 2 or pulse_ox_log:
        score += 20
        feedback_parts.append(f"Devices created (Windows +{window_inc})")
    else:
        feedback_parts.append("No evidence of new devices created")

    # --- 3. App Launch (10 pts) ---
    if result.get("vital_signs_launched", False):
        score += 10
        feedback_parts.append("Vital Signs app launched")
    else:
        feedback_parts.append("Vital Signs app not launched")

    # --- 4 & 5. UUID Verification (50 pts total) ---
    # Get the pool of actual UUIDs generated by the system
    system_uuids = set(result.get("actual_system_uuids", []))
    
    # Get user provided UUIDs
    # Allow various key names since instructions were "monitor_1_uuid" but agent might vary
    user_vals = []
    for k, v in user_json.items():
        if isinstance(v, str) and len(v) > 10: # Rough check for UUID-like string
            user_vals.append(v.strip())

    valid_matches = set()
    
    for val in user_vals:
        # Check for exact match or substring match (e.g., if user copied "UUID: 1234...")
        # or if user copied just the beginning/end if UI truncated it (though usually full UUID is available)
        for sys_uuid in system_uuids:
            if sys_uuid in val or val in sys_uuid:
                valid_matches.add(sys_uuid)
    
    # Score unique valid matches
    match_count = len(valid_matches)
    
    if match_count >= 1:
        score += 25
        feedback_parts.append("First valid UUID found")
    else:
        feedback_parts.append("No valid UUIDs found matching system logs")
        
    if match_count >= 2:
        score += 25
        feedback_parts.append("Second distinct valid UUID found")
    elif match_count == 1 and len(user_vals) >= 2:
        feedback_parts.append("Only one valid UUID found (duplicate or invalid second ID)")

    # --- 6. Screenshot (10 pts) ---
    if result.get("evidence_screenshot_exists", False):
        score += 10
        feedback_parts.append("Evidence screenshot saved")
    else:
        feedback_parts.append("Evidence screenshot missing")

    # Final Verification
    passed = score >= 65 and match_count >= 1 # Need at least one valid ID to pass
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts),
        "details": {
            "system_uuids": list(system_uuids),
            "user_provided": user_vals,
            "matches": list(valid_matches)
        }
    }