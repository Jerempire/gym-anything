#!/bin/bash
echo "=== Exporting Duplicate Device Differentiation Result ==="

source /workspace/scripts/task_utils.sh

# Take final screenshot (evidence of state)
take_screenshot /tmp/task_final.png

# Timestamps
TASK_START=$(cat /tmp/task_start_timestamp 2>/dev/null || echo "0")
TASK_END=$(date +%s)

# 1. Capture User Output
OUTPUT_FILE="/home/ga/Desktop/device_map.json"
OUTPUT_EXISTS="false"
OUTPUT_CONTENT="{}"
OUTPUT_MTIME="0"

if [ -f "$OUTPUT_FILE" ]; then
    OUTPUT_EXISTS="true"
    OUTPUT_CONTENT=$(cat "$OUTPUT_FILE")
    OUTPUT_MTIME=$(stat -c %Y "$OUTPUT_FILE" 2>/dev/null || echo "0")
fi

# 2. Analyze Logs for ACTUAL Device UUIDs
# We need to find the "Ground Truth" UUIDs generated by the system during this session
LOG_FILE="/home/ga/openice/logs/openice.log"
INITIAL_LOG_SIZE=$(cat /tmp/initial_log_size 2>/dev/null || echo "0")

# Extract new log lines
NEW_LOG=$(tail -c +$((INITIAL_LOG_SIZE + 1)) "$LOG_FILE" 2>/dev/null || echo "")

# Grep for Pulse Oximeter creation and UUIDs
# OpenICE logs typically contain "Created device adapter" or "DeviceIdentity" with UUIDs
# Pattern: standard UUID 8-4-4-4-12 hex digits
# We catch ALL UUIDs appearing in the logs during the session to form a valid pool
ACTUAL_UUIDS=$(echo "$NEW_LOG" | grep -oE '[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}' | sort | uniq)
ACTUAL_UUIDS_JSON=$(echo "$ACTUAL_UUIDS" | jq -R . | jq -s .)

# Check if Pulse Oximeters were specifically requested/created
PULSE_OX_CREATED="false"
if echo "$NEW_LOG" | grep -iE "Pulse.*Oximeter|Nonin|Masimo|Simulator" > /dev/null; then
    PULSE_OX_CREATED="true"
fi

# 3. Check for Clinical App Launch
VITAL_SIGNS_LAUNCHED="false"
if echo "$NEW_LOG" | grep -iE "Vital.*Signs|clinical.*app" > /dev/null; then
    VITAL_SIGNS_LAUNCHED="true"
fi

# 4. Window Counting
INITIAL_WINDOWS=$(cat /tmp/initial_window_count 2>/dev/null || echo "0")
FINAL_WINDOWS=$(DISPLAY=:1 wmctrl -l 2>/dev/null | wc -l)
WINDOW_INCREASE=$((FINAL_WINDOWS - INITIAL_WINDOWS))

# 5. Screenshot Evidence
EVIDENCE_SCREENSHOT_EXISTS="false"
if [ -f "/home/ga/Desktop/id_evidence.png" ]; then
    EVIDENCE_SCREENSHOT_EXISTS="true"
fi

# Create Result JSON
create_result_json << EOF
{
    "task_start": $TASK_START,
    "task_end": $TASK_END,
    "output_exists": $OUTPUT_EXISTS,
    "output_content": $OUTPUT_CONTENT,
    "output_mtime": $OUTPUT_MTIME,
    "actual_system_uuids": $ACTUAL_UUIDS_JSON,
    "pulse_ox_created": $PULSE_OX_CREATED,
    "vital_signs_launched": $VITAL_SIGNS_LAUNCHED,
    "window_increase": $WINDOW_INCREASE,
    "evidence_screenshot_exists": $EVIDENCE_SCREENSHOT_EXISTS,
    "screenshot_path": "/tmp/task_final.png"
}
EOF

echo "=== Export Complete ==="
cat /tmp/task_result.json