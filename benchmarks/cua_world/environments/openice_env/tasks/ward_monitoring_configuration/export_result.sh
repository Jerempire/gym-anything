#!/bin/bash
echo "=== Exporting Ward Monitoring Configuration Result ==="

source /workspace/scripts/task_utils.sh

# Take final screenshot
take_screenshot /tmp/task_final_screenshot.png

# Timestamps
TASK_START=$(cat /tmp/task_start_timestamp 2>/dev/null || echo "0")
TASK_END=$(date +%s)

# 1. Check CSV File
CSV_PATH="/home/ga/Desktop/ward_config.csv"
CSV_EXISTS="false"
CSV_CONTENT=""
CSV_MTIME="0"

if [ -f "$CSV_PATH" ]; then
    CSV_EXISTS="true"
    # Read content, encoding newlines for JSON
    CSV_CONTENT=$(cat "$CSV_PATH" | head -n 20) # Limit size
    CSV_MTIME=$(stat -c %Y "$CSV_PATH" 2>/dev/null || echo "0")
fi

# 2. Extract Device Logs for Verification
# We need to find the REAL UDIs generated by the system to compare against the CSV
LOG_FILE="/home/ga/openice/logs/openice.log"
INITIAL_LOG_SIZE=$(cat /tmp/initial_log_size 2>/dev/null || echo "0")

# Extract new log lines only
NEW_LOG_LINES=""
if [ -f "$LOG_FILE" ]; then
    # Grab new lines, filter for device creation/identity events to keep JSON small
    # OpenICE logs device creation with UUIDs
    NEW_LOG_LINES=$(tail -c +$((INITIAL_LOG_SIZE + 1)) "$LOG_FILE" 2>/dev/null | grep -iE "device|adapter|uuid|monitor|pump|oximeter|created|identity" | head -n 200)
fi

# 3. Window Count Check
INITIAL_WINDOWS=$(cat /tmp/initial_window_count 2>/dev/null || echo "0")
FINAL_WINDOWS=$(DISPLAY=:1 wmctrl -l 2>/dev/null | wc -l)
WINDOW_INCREASE=$((FINAL_WINDOWS - INITIAL_WINDOWS))

# 4. Check OpenICE status
OPENICE_RUNNING="false"
if is_openice_running; then
    OPENICE_RUNNING="true"
fi

# Create Result JSON
# Use python to safely escape JSON strings
python3 -c "
import json
import os

result = {
    'task_start': $TASK_START,
    'task_end': $TASK_END,
    'csv_exists': $CSV_EXISTS,
    'csv_content': '''$CSV_CONTENT''',
    'csv_mtime': $CSV_MTIME,
    'log_snippet': '''$NEW_LOG_LINES''',
    'window_increase': $WINDOW_INCREASE,
    'openice_running': $OPENICE_RUNNING,
    'screenshot_path': '/tmp/task_final_screenshot.png'
}

with open('/tmp/task_result.json', 'w') as f:
    json.dump(result, f)
"

# Handle permissions
chmod 666 /tmp/task_result.json 2>/dev/null || true

echo "=== Export Complete ==="
cat /tmp/task_result.json