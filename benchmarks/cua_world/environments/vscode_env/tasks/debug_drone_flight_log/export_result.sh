#!/bin/bash
source /workspace/scripts/task_utils.sh

echo "=== Exporting Debug Drone Flight Log Result ==="

# Save all active files in VSCode
focus_vscode_window 2>/dev/null || true
sleep 0.5
safe_xdotool ga :1 key --delay 200 ctrl+shift+s 2>/dev/null || true
sleep 1
safe_xdotool ga :1 key --delay 200 ctrl+k ctrl+s 2>/dev/null || true
sleep 2

DISPLAY=:1 scrot /tmp/task_final.png 2>/dev/null || true

# ──────────────────────────────────────────────
# Run Agent Code Against Hidden Holdout Dataset
# ──────────────────────────────────────────────
RUN_DIR="/tmp/drone_verify"
rm -rf "$RUN_DIR"
mkdir -p "$RUN_DIR/data" "$RUN_DIR/output"

# Copy agent's workspace files
cp /home/ga/workspace/drone_auditor/*.py "$RUN_DIR/" 2>/dev/null || true

# Generate the hidden holdout dataset designed to trigger all 5 bugs perfectly
cat > "$RUN_DIR/data/holdout.csv" << 'EOF'
time_us,lat,lon,alt_amsl,alt_rel,vib_x,vib_y,vib_z,voltage
0,37.7749,-122.4194,100.0,0.0,0.0,0.0,0.0,8.0
5000000,37.7749,-122.4139,130.0,40.0,1.0,1.0,1.0,7.5
EOF

chown -R ga:ga "$RUN_DIR"

# Execute agent's code as ga user
su - ga -c "cd $RUN_DIR && python3 main.py > /dev/null 2>&1" || echo "Agent script threw an error"

# ──────────────────────────────────────────────
# Package Output & Source for Verifier
# ──────────────────────────────────────────────
python3 << 'PYEOF'
import json
import os

res = {}
output_path = '/tmp/drone_verify/output/holdout.json'

if os.path.exists(output_path):
    try:
        with open(output_path) as f:
            res = json.load(f)
    except Exception as e:
        res['error'] = f"Failed to parse JSON: {e}"
else:
    res['error'] = 'Holdout output not generated by pipeline'

# Package source code for backup structural verification
src = {}
for fname in ['parser.py', 'geofence.py', 'battery.py']:
    fpath = os.path.join('/tmp/drone_verify', fname)
    if os.path.exists(fpath):
        with open(fpath, 'r', encoding='utf-8') as f:
            src[fname] = f.read()

res['source_code'] = src

with open('/tmp/task_result.json', 'w', encoding='utf-8') as f:
    json.dump(res, f, indent=2)
PYEOF

chmod 666 /tmp/task_result.json
echo "=== Export Complete ==="