#!/usr/bin/env python3
"""
Verifier for fiscal_year_depreciation_setup task.
Evaluates the creation of 3 depreciation schedules, assignment to 12 models, 
and correction of 4 assets.
"""

import json
import math
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def verify_fiscal_year_depreciation_setup(traj, env_info, task_info):
    """
    Evaluates the JSON payload generated by export_result.sh
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get("metadata", {})
    expected_deps = metadata.get("expected_depreciations", {})
    expected_assignments = metadata.get("expected_assignments", {})
    expected_corrections = metadata.get("expected_corrections", {})

    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []

    deps_data = result.get("depreciations", [])
    models_data = result.get("models", [])
    assets_data = result.get("assets", [])

    # Map created depreciations
    dep_name_to_id = {}
    for d in deps_data:
        dep_name_to_id[d["name"].lower()] = {
            "id": d["id"],
            "months": d["months"]
        }

    # ================================================================
    # PART 1: Check Depreciation Schedules (24 pts)
    # ================================================================
    short_life_key = expected_deps.get("short", {}).get("name", "IT Equipment - Short Life").lower()
    std_life_key = expected_deps.get("standard", {}).get("name", "IT Equipment - Standard Life").lower()
    peri_key = expected_deps.get("peripherals", {}).get("name", "Peripherals").lower()

    short_id, std_id, peri_id = None, None, None

    # C1: Short Life
    if short_life_key in dep_name_to_id:
        if dep_name_to_id[short_life_key]["months"] == expected_deps["short"]["months"]:
            score += 8
            short_id = dep_name_to_id[short_life_key]["id"]
            feedback_parts.append("C1: 'IT Equipment - Short Life' created with 36 months (+8)")
        else:
            feedback_parts.append("C1: 'IT Equipment - Short Life' has wrong months (+0)")
    else:
        feedback_parts.append("C1: 'IT Equipment - Short Life' not found (+0)")

    # C2: Standard Life
    if std_life_key in dep_name_to_id:
        if dep_name_to_id[std_life_key]["months"] == expected_deps["standard"]["months"]:
            score += 8
            std_id = dep_name_to_id[std_life_key]["id"]
            feedback_parts.append("C2: 'IT Equipment - Standard Life' created with 60 months (+8)")
        else:
            feedback_parts.append("C2: 'IT Equipment - Standard Life' has wrong months (+0)")
    else:
        feedback_parts.append("C2: 'IT Equipment - Standard Life' not found (+0)")

    # C3: Peripherals
    if peri_key in dep_name_to_id:
        if dep_name_to_id[peri_key]["months"] == expected_deps["peripherals"]["months"]:
            score += 8
            peri_id = dep_name_to_id[peri_key]["id"]
            feedback_parts.append("C3: 'Peripherals' created with 48 months (+8)")
        else:
            feedback_parts.append("C3: 'Peripherals' has wrong months (+0)")
    else:
        feedback_parts.append("C3: 'Peripherals' not found (+0)")

    # ================================================================
    # PART 2: Check Assignments (40 pts)
    # ================================================================
    def check_assignments(expected_substrings, target_dep_id, max_pts, criterion_name):
        nonlocal score
        if not target_dep_id:
            feedback_parts.append(f"{criterion_name}: Schedule missing, assignments skipped (+0)")
            return
        
        matches = 0
        total = len(expected_substrings)
        
        for sub in expected_substrings:
            found_model = next((m for m in models_data if sub.lower() in m["name"].lower()), None)
            if found_model and found_model["depreciation_id"] == target_dep_id:
                matches += 1
                
        if total > 0:
            pts = int((matches / total) * max_pts)
            score += pts
            feedback_parts.append(f"{criterion_name}: {matches}/{total} models correctly assigned (+{pts})")

    # C4: Short life models (16 pts)
    check_assignments(expected_assignments.get("short", []), short_id, 16, "C4")
    
    # C5: Standard life models (14 pts)
    check_assignments(expected_assignments.get("standard", []), std_id, 14, "C5")
    
    # C6: Peripheral models (10 pts)
    check_assignments(expected_assignments.get("peripherals", []), peri_id, 10, "C6")

    # ================================================================
    # PART 3: Check Asset Corrections (36 pts)
    # ================================================================
    asset_dict = {a["tag"]: a for a in assets_data}

    for idx, (tag, exp_data) in enumerate(expected_corrections.items(), start=7):
        pts = 0
        if tag in asset_dict:
            actual = asset_dict[tag]
            
            # Check cost (5 pts)
            if math.isclose(actual["cost"], exp_data["cost"], abs_tol=0.01):
                pts += 5
            
            # Check date (4 pts)
            actual_date = actual["date"].split(" ")[0] if actual["date"] else ""
            if actual_date.startswith(exp_data["date"]):
                pts += 4
                
            score += pts
            feedback_parts.append(f"C{idx}: {tag} correction awarded +{pts}")
        else:
            feedback_parts.append(f"C{idx}: {tag} not found (+0)")

    passed = score >= 60

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }