#!/usr/bin/env python3
"""
Verifier for EGFR Inhibitor Safety Comparison Task.
"""

import json
import tempfile
import os
import logging
import re
from typing import Dict, Any, List

# Import VLM utils provided by the framework
try:
    from gym_anything.vlm import query_vlm, sample_trajectory_frames, get_final_screenshot
except ImportError:
    # Mock for local testing if needed
    def sample_trajectory_frames(traj, n): return []
    def get_final_screenshot(traj): return None
    def query_vlm(**kwargs): return {"success": False, "error": "ImportError"}

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_egfr_comparison(traj, env_info, task_info):
    """
    Verify the EGFR comparison task.
    
    Criteria:
    1. Report file exists and was created during the task. (20 pts)
    2. Report contains names of all 3 drugs (Erlotinib, Afatinib, Osimertinib). (20 pts)
    3. Report contains valid color codes for each. (20 pts)
    4. VLM: Trajectory confirms navigation to all 3 drug pages. (20 pts)
    5. VLM: Trajectory confirms Amiodarone was selected/checked for each. (20 pts)
    """
    
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Retrieve Result JSON from Android Device
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Note: Depending on the env bridge, this path might need adjustment.
        # Standard assumption: copy_from_env handles the container path mapping.
        # For this env, /sdcard/ maps to the tasks mount or artifacts.
        # We try to copy specifically the result file generated by export_result.sh
        copy_from_env("/sdcard/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        logger.error(f"Failed to load result json: {e}")
        return {"passed": False, "score": 0, "feedback": f"Could not retrieve task result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    score = 0
    feedback_parts = []
    
    # --- Criterion 1: File Existence & Anti-Gaming ---
    report_exists = result_data.get("report_exists", False)
    created_during = result_data.get("file_created_during_task", False)
    content = result_data.get("report_content", "")
    
    if report_exists and created_during:
        score += 20
        feedback_parts.append("Report file created successfully.")
    elif report_exists:
        score += 10
        feedback_parts.append("Report file exists but timestamp check failed (anti-gaming).")
    else:
        feedback_parts.append("Report file not found.")
        return {"passed": False, "score": 0, "feedback": "Report file not created."}

    # --- Criterion 2: Content Check (Drug Names) ---
    drugs_found = []
    required_drugs = ["Erlotinib", "Afatinib", "Osimertinib"]
    content_lower = content.lower()
    
    for drug in required_drugs:
        if drug.lower() in content_lower:
            drugs_found.append(drug)
            
    if len(drugs_found) == 3:
        score += 20
        feedback_parts.append("All 3 drugs mentioned in report.")
    else:
        partial = int((len(drugs_found) / 3) * 20)
        score += partial
        feedback_parts.append(f"Found {len(drugs_found)}/3 drugs in report: {drugs_found}.")

    # --- Criterion 3: Content Check (Colors) ---
    # We look for color keywords near drug names or in the text
    valid_colors = ["red", "orange", "yellow", "green", "grey", "gray"]
    colors_found = 0
    
    # Simple heuristic: count distinct color words found in text
    found_colors_list = []
    for color in valid_colors:
        if color in content_lower:
            found_colors_list.append(color)
            
    if len(found_colors_list) >= 1:
        # We assume if they mentioned colors and drugs, they likely mapped them.
        # A stricter check would parse the table, but markdown tables vary in format.
        score += 20
        feedback_parts.append(f"Interaction colors reported: {found_colors_list}")
    else:
        feedback_parts.append("No interaction color codes found in report.")

    # --- Criterion 4 & 5: VLM Verification of Workflow ---
    frames = sample_trajectory_frames(traj, n=8)
    
    vlm_prompt = """
    You are verifying an agent using the 'Liverpool Cancer iChart' app.
    The task is to check interactions for 3 drugs: Erlotinib, Afatinib, and Osimertinib against Amiodarone.
    
    Look at the sequence of screenshots.
    
    1. Did the agent search for 'Erlotinib'?
    2. Did the agent search for 'Afatinib'?
    3. Did the agent search for 'Osimertinib'?
    4. For any of these, is 'Amiodarone' visible selected as a co-medication?
    5. Did the agent view the result/interaction page (showing Red/Orange/Yellow/Green banners)?
    
    Return JSON:
    {
      "erlotinib_checked": boolean,
      "afatinib_checked": boolean,
      "osimertinib_checked": boolean,
      "amiodarone_seen": boolean,
      "results_page_seen": boolean
    }
    """
    
    vlm_result = query_vlm(images=frames, prompt=vlm_prompt)
    
    vlm_score = 0
    if vlm_result.get("success"):
        parsed = vlm_result.get("parsed", {})
        
        # Check if at least 2 drugs were checked (allow 1 miss for partial credit)
        drugs_checked = sum([
            parsed.get("erlotinib_checked", False),
            parsed.get("afatinib_checked", False),
            parsed.get("osimertinib_checked", False)
        ])
        
        if drugs_checked >= 3:
            vlm_score += 20
        elif drugs_checked >= 1:
            vlm_score += 10
            
        if parsed.get("amiodarone_seen", False):
            vlm_score += 10
        if parsed.get("results_page_seen", False):
            vlm_score += 10
            
        feedback_parts.append(f"VLM verified {drugs_checked} drugs checked.")
    else:
        # Fallback if VLM fails: give partial credit if file content was perfect
        if score >= 60:
            vlm_score += 20
            feedback_parts.append("VLM unavailable, trusting file content.")
    
    score += vlm_score

    # Final Pass/Fail
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }