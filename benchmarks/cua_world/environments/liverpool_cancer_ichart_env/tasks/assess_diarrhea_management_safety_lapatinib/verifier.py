#!/usr/bin/env python3
"""
Verifier for assess_diarrhea_management_safety_lapatinib task.
"""

import json
import os
import tempfile
import logging
from typing import Dict, Any, List

# Setup logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_assess_diarrhea_management(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]) -> Dict[str, Any]:
    """
    Verifies that the agent correctly identified interactions for Lapatinib with Loperamide and Paracetamol
    and created the required report file.
    
    Scoring Criteria:
    1. Report file exists (10 pts)
    2. Loperamide result correct (Red/Orange) in text (25 pts)
    3. Paracetamol result correct (Green/Grey) in text (25 pts)
    4. VLM Verification: Agent visited Lapatinib page (10 pts)
    5. VLM Verification: Agent visited Loperamide interaction (15 pts)
    6. VLM Verification: Agent visited Paracetamol interaction (15 pts)
    """
    
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: copy_from_env not available"}

    # Load metadata
    metadata = task_info.get('metadata', {})
    expected_loperamide_colors = ["red", "orange"]
    expected_paracetamol_colors = ["green", "grey", "gray"]

    # Initialize score
    score = 0
    feedback = []
    
    # --- Step 1: Analyze Report File (Programmatic) ---
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Copy result JSON generated by export_result.sh
        copy_from_env("/sdcard/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result_data = json.load(f)
            
        file_exists = result_data.get("file_exists", False)
        content = result_data.get("file_content", "").lower()
        
        if file_exists:
            score += 10
            feedback.append("Report file created.")
            
            # Check Loperamide
            if "loperamide" in content:
                # Check for correct color association near loperamide
                # Simple check: is 'red' or 'orange' in the content?
                # Stronger check: 'loperamide' and ('red' or 'orange') are present
                if any(c in content for c in expected_loperamide_colors):
                    score += 25
                    feedback.append("Loperamide interaction correctly identified as significant (Red/Orange).")
                else:
                    feedback.append("Loperamide mentioned but incorrect or missing color (Expected Red/Orange).")
            else:
                feedback.append("Loperamide missing from report.")

            # Check Paracetamol
            if "paracetamol" in content:
                if any(c in content for c in expected_paracetamol_colors):
                    score += 25
                    feedback.append("Paracetamol interaction correctly identified as safe (Green/Grey).")
                elif "no interaction" in content: # Accept phrase as substitute for green
                     score += 25
                     feedback.append("Paracetamol identified as 'No Interaction' (Equivalent to Green).")
                else:
                    feedback.append("Paracetamol mentioned but incorrect or missing color.")
            else:
                feedback.append("Paracetamol missing from report.")
                
        else:
            feedback.append("Report file '/sdcard/Download/lapatinib_supportive_care_report.txt' was NOT created.")

    except Exception as e:
        logger.error(f"Error reading result file: {e}")
        feedback.append(f"Failed to verify report file: {str(e)}")
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    # --- Step 2: VLM Trajectory Verification ---
    # We need to import helper functions if available, or mock them for the standalone script structure
    # Assuming standard gym-anything structure where verify_task receives 'traj' which has images
    
    try:
        from vlm_utils import sample_trajectory_frames, query_vlm
        
        frames = sample_trajectory_frames(traj, n=8)
        
        prompt = """
        You are verifying an agent's actions in the 'Liverpool Cancer iChart' app.
        The agent's goal was to check interactions for 'Lapatinib' with 'Loperamide' and 'Paracetamol'.
        
        Look at these screenshots and identify:
        1. Was the cancer drug 'Lapatinib' ever selected?
        2. Was the co-medication 'Loperamide' checked? (Look for 'Loperamide' on screen with a colored icon)
        3. Was the co-medication 'Paracetamol' checked? (Look for 'Paracetamol' on screen with a colored icon)
        
        Return JSON:
        {
            "lapatinib_selected": true/false,
            "loperamide_checked": true/false,
            "paracetamol_checked": true/false,
            "reasoning": "brief description of evidence"
        }
        """
        
        vlm_response = query_vlm(images=frames, prompt=prompt)
        
        if vlm_response and vlm_response.get("success"):
            parsed = vlm_response.get("parsed", {})
            
            if parsed.get("lapatinib_selected"):
                score += 10
                feedback.append("VLM confirmed Lapatinib was selected.")
            
            if parsed.get("loperamide_checked"):
                score += 15
                feedback.append("VLM confirmed Loperamide interaction was viewed.")
            
            if parsed.get("paracetamol_checked"):
                score += 15
                feedback.append("VLM confirmed Paracetamol interaction was viewed.")
        else:
            feedback.append("VLM verification failed or inconclusive.")
            # Fallback: if text report is perfect, we assume they must have looked it up
            if score >= 60: 
                score += 20
                feedback.append("Awarding partial VLM points based on correct text report.")

    except ImportError:
        logger.warning("VLM utils not available, skipping visual verification")
        # If VLM unavailable, scale existing score to 100
        # Current max without VLM is 60. 
        # If they got 60/60 on text, scale to 100.
        if score >= 60:
            score = 100
            feedback.append("VLM unavailable - Perfect report score scaled to 100.")
        else:
            # Scale proportionally: score / 0.6
            score = int(score / 0.6)

    # Final result
    passed = score >= 80
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }