#!/usr/bin/env python3
import json
import os
import tempfile
import logging
from gym_anything.vlm import query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_auto_reconnection(traj, env_info, task_info):
    """
    Verifies that the agent simulated a failure and captured the reconnection process.
    
    Scoring:
    - 20 pts: Evidence files exist and were created during task.
    - 10 pts: JVB service is running at the end.
    - 35 pts: 'Reconnecting' screenshot shows error state (VLM).
    - 35 pts: 'Restored' screenshot shows active meeting (VLM).
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    # 1. Retrieve Result JSON
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result JSON: {str(e)}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    # 2. Check File Existence & Timestamps (20 pts)
    rec_exists = result.get("reconnecting_exists", False)
    rec_valid = result.get("reconnecting_created_during_task", False)
    res_exists = result.get("restored_exists", False)
    res_valid = result.get("restored_created_during_task", False)
    
    if rec_exists and rec_valid:
        score += 10
        feedback_parts.append("Reconnecting screenshot created.")
    elif rec_exists:
        feedback_parts.append("Reconnecting screenshot exists but old timestamp.")
    else:
        feedback_parts.append("Reconnecting screenshot missing.")

    if res_exists and res_valid:
        score += 10
        feedback_parts.append("Restored screenshot created.")
    elif res_exists:
        feedback_parts.append("Restored screenshot exists but old timestamp.")
    else:
        feedback_parts.append("Restored screenshot missing.")

    # 3. Check Service Status (10 pts)
    if result.get("jvb_running_at_end", False):
        score += 10
        feedback_parts.append("JVB service running at end.")
    else:
        feedback_parts.append("JVB service NOT running (did you forget to restart it?).")

    # 4. VLM Verification of Images
    # We download the images generated by the agent
    
    # --- Verify Reconnecting Image (35 pts) ---
    if rec_exists:
        rec_img_local = tempfile.NamedTemporaryFile(delete=False, suffix='.png').name
        try:
            copy_from_env(result["reconnecting_path"], rec_img_local)
            
            prompt_rec = (
                "This should be a screenshot of a Jitsi Meet video call experiencing a network/server failure. "
                "Look for: "
                "1. A grey overlay or 'Reconnecting' spinner. "
                "2. A notification saying 'The bridge has disconnected' or 'Connection lost'. "
                "3. A general error state in the call interface. "
                "Does this image show such an error state? Answer JSON with { 'is_error_state': bool, 'reason': str }."
            )
            
            vlm_res = query_vlm(image=rec_img_local, prompt=prompt_rec)
            if vlm_res.get("success"):
                parsed = vlm_res.get("parsed", {})
                if parsed.get("is_error_state", False):
                    score += 35
                    feedback_parts.append("VLM confirmed error/reconnecting state.")
                else:
                    feedback_parts.append(f"VLM did NOT see error state: {parsed.get('reason', 'Unknown')}")
            else:
                feedback_parts.append("VLM failed to analyze reconnecting image.")
        except Exception as e:
            feedback_parts.append(f"Failed to copy reconnecting image: {str(e)}")
        finally:
            if os.path.exists(rec_img_local):
                os.unlink(rec_img_local)

    # --- Verify Restored Image (35 pts) ---
    if res_exists:
        res_img_local = tempfile.NamedTemporaryFile(delete=False, suffix='.png').name
        try:
            copy_from_env(result["restored_path"], res_img_local)
            
            prompt_res = (
                "This should be a screenshot of a healthy, active Jitsi Meet video call. "
                "Look for: "
                "1. Active video or user initials visible without grey error overlays. "
                "2. Standard meeting toolbar (mic, camera, hangup buttons) visible and active. "
                "3. Connection indicators showing valid signal (if visible). "
                "Does this image show a successfully connected meeting? Answer JSON with { 'is_connected': bool, 'reason': str }."
            )
            
            vlm_res = query_vlm(image=res_img_local, prompt=prompt_res)
            if vlm_res.get("success"):
                parsed = vlm_res.get("parsed", {})
                if parsed.get("is_connected", False):
                    score += 35
                    feedback_parts.append("VLM confirmed restored meeting state.")
                else:
                    feedback_parts.append(f"VLM did NOT see connected state: {parsed.get('reason', 'Unknown')}")
            else:
                feedback_parts.append("VLM failed to analyze restored image.")
        except Exception as e:
            feedback_parts.append(f"Failed to copy restored image: {str(e)}")
        finally:
            if os.path.exists(res_img_local):
                os.unlink(res_img_local)

    return {
        "passed": score >= 70,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }