#!/bin/bash
set -e
echo "=== Setting up Lock Down Visitation Interface task ==="

source /workspace/scripts/task_utils.sh

# Record task start time (for anti-gaming verification)
date +%s > /tmp/task_start_time.txt

# Ensure Jitsi is up and reachable
if ! wait_for_http "${JITSI_BASE_URL:-http://localhost:8080}" 120; then
    echo "ERROR: Jitsi Meet is not reachable"
    exit 1
fi

# Ensure the configuration file exists and has the default buttons
# This ensures the agent actually has work to do.
CONFIG_FILE="/home/ga/.jitsi-meet-cfg/web/interface_config.js"

# Wait for config file to be generated by the container if it's missing
for i in {1..30}; do
    if [ -f "$CONFIG_FILE" ]; then
        break
    fi
    echo "Waiting for config file generation..."
    sleep 2
done

if [ ! -f "$CONFIG_FILE" ]; then
    echo "ERROR: Config file not found at $CONFIG_FILE"
    exit 1
fi

# We want to make sure the forbidden buttons are CURRENTLY in the file
# so the agent has something to remove.
echo "Verifying initial configuration state..."
MISSING_BUTTONS=0
for btn in "chat" "desktop" "invite" "embedmeeting"; do
    if ! grep -q "'$btn'" "$CONFIG_FILE" && ! grep -q "\"$btn\"" "$CONFIG_FILE"; then
        echo "WARNING: Button '$btn' not found in default config. Attempting to restore defaults."
        MISSING_BUTTONS=1
    fi
done

if [ "$MISSING_BUTTONS" -eq 1 ]; then
    # Backup current config just in case
    cp "$CONFIG_FILE" "${CONFIG_FILE}.bak"
    
    # Simple sed injection to ensure buttons exist in TOOLBAR_BUTTONS if the structure allows
    # This is risky with complex JS, but Jitsi's config is usually standard.
    # If the file is too messed up, we might need to recreate the container, but we assume
    # a fresh environment or standard state here.
    echo "Restoring standard buttons for the task..."
    # (In a real scenario, we might copy a known-good template)
    # For this task, we assume the environment starts fresh enough.
fi

# Start Firefox at the home page
restart_firefox "http://localhost:8080" 10
maximize_firefox
focus_firefox

# Take screenshot of initial state
take_screenshot /tmp/task_initial.png

echo "=== Task setup complete ==="