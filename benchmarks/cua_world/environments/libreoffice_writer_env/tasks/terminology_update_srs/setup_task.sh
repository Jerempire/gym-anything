#!/bin/bash
# setup_task.sh - Terminology Update SRS

set -e
source /workspace/scripts/task_utils.sh

echo "=== Setting up Terminology Update Task ==="

# 1. Create task directory
sudo -u ga mkdir -p /home/ga/Documents

# 2. Generate the source SRS document
# We use python-docx to create a realistic, multi-page document with structured headings
# and specific "traps" (e.g., Section 2.3 vs Version 2.3).
echo "Generating SRS document..."
python3 << 'PYEOF'
from docx import Document
from docx.shared import Pt, Inches
from docx.enum.text import WD_ALIGN_PARAGRAPH

doc = Document()

# --- Title Page ---
title = doc.add_paragraph("Software Requirements Specification")
title.alignment = WD_ALIGN_PARAGRAPH.CENTER
title.style = "Title"

subtitle = doc.add_paragraph("for")
subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER

product = doc.add_paragraph("DataSync Pro")
product.alignment = WD_ALIGN_PARAGRAPH.CENTER
product.style = "Heading 1"

doc.add_paragraph("\n" * 4)

version = doc.add_paragraph("Version: 2.3")
version.alignment = WD_ALIGN_PARAGRAPH.CENTER

date_line = doc.add_paragraph("June 15, 2024")
date_line.alignment = WD_ALIGN_PARAGRAPH.CENTER

doc.add_paragraph("\n" * 2)

company = doc.add_paragraph("Prepared by Nextera Solutions")
company.alignment = WD_ALIGN_PARAGRAPH.CENTER

doc.add_page_break()

# --- Section 1 ---
doc.add_paragraph("1. Introduction", style="Heading 1")
doc.add_paragraph("1.1 Purpose", style="Heading 2")
doc.add_paragraph(
    "The purpose of this document is to describe the requirements for DataSync Pro, "
    "a comprehensive data synchronization platform developed by Nextera Solutions. "
    "This system allows enterprise clients to synchronize disparate data sources in real-time."
)

doc.add_paragraph("1.2 Scope", style="Heading 2")
doc.add_paragraph(
    "The DataSync software subsystem will interact with external APIs to fetch "
    "incremental updates. It is assumed that the DataSync server is hosted on "
    "Nextera Solutions cloud infrastructure."
)

doc.add_paragraph("1.3 Definitions and Acronyms", style="Heading 2")
doc.add_paragraph(
    "SRS: Software Requirements Specification\n"
    "DS Pro: Abbreviation for DataSync Pro\n"
    "API: Application Programming Interface"
)

# --- Section 2 ---
doc.add_paragraph("2. Overall Description", style="Heading 1")
doc.add_paragraph("2.1 Product Perspective", style="Heading 2")
doc.add_paragraph(
    "DS Pro is a standalone product but can integrate with the legacy DataSync v1.0 modules. "
    "It replaces the old manual import tools."
)

doc.add_paragraph("2.2 User Characteristics", style="Heading 2")
doc.add_paragraph(
    "Users of DS Pro are expected to be technical administrators familiar with SQL and REST APIs."
)

# TRAP: Section 2.3 - "Version 2.3" replacement should NOT affect this heading number
doc.add_paragraph("2.3 Assumptions and Dependencies", style="Heading 2")
doc.add_paragraph(
    "It is assumed that the client has a valid license from Nextera Solutions. "
    "The system requires Python 3.8+."
)

# --- Section 3 ---
doc.add_paragraph("3. Specific Requirements", style="Heading 1")
doc.add_paragraph("3.1 Functional Requirements", style="Heading 2")

doc.add_paragraph("FR-001: Data Ingestion", style="Heading 3")
doc.add_paragraph(
    "The DataSync engine shall ingest JSON payloads up to 50MB. "
    "Upon failure, DataSync Pro shall retry the operation 3 times."
)

doc.add_paragraph("FR-002: Reporting", style="Heading 3")
doc.add_paragraph(
    "Nextera Solutions branding must appear on all generated PDF reports. "
    "The footer shall read 'Generated by DataSync Pro'."
)

# --- Appendix ---
doc.add_page_break()
doc.add_paragraph("Appendix A: Licensing", style="Heading 1")
doc.add_paragraph(
    "Copyright 2024 Nextera Solutions. All rights reserved.\n"
    "Unauthorized duplication of DS Pro binaries is prohibited."
)

doc.save("/home/ga/Documents/DataSync_SRS_v2.3.docx")
PYEOF

# Set permissions
chown ga:ga /home/ga/Documents/DataSync_SRS_v2.3.docx
chmod 666 /home/ga/Documents/DataSync_SRS_v2.3.docx

# 3. Launch LibreOffice Writer
echo "Launching LibreOffice Writer..."
su - ga -c "DISPLAY=:1 libreoffice --writer --norestore /home/ga/Documents/DataSync_SRS_v2.3.docx > /tmp/writer.log 2>&1 &"

# 4. Wait for window and maximize
wait_for_window "DataSync_SRS_v2.3" 30 || wait_for_window "LibreOffice Writer" 30

WID=$(get_writer_window_id)
if [ -n "$WID" ]; then
    echo "Maximizing window $WID..."
    focus_window "$WID"
    DISPLAY=:1 wmctrl -i -r "$WID" -b add,maximized_vert,maximized_horz 2>/dev/null || true
    
    # Dismiss "Tip of the Day" if it appears
    sleep 2
    safe_xdotool ga :1 key Escape
fi

# 5. Record start time and initial state
date +%s > /tmp/task_start_time.txt
# Take initial screenshot
take_screenshot /tmp/task_initial.png

echo "=== Setup Complete ==="