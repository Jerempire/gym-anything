#!/bin/bash
# Setup script for docker_locale_timezone_fix task

set -e
echo "=== Setting up Docker Locale & Timezone Fix Task ==="

source /workspace/scripts/task_utils.sh 2>/dev/null || true

# Helper to wait for Docker
if ! type wait_for_docker &>/dev/null; then
    wait_for_docker() {
        for i in {1..60}; do
            if docker info > /dev/null 2>&1; then return 0; fi
            sleep 2
        done; return 1
    }
fi

wait_for_docker

# 1. Create Project Directory
PROJECT_DIR="/home/ga/projects/global-invoicer"
mkdir -p "$PROJECT_DIR"
cd "$PROJECT_DIR"

# 2. Create Application Code (invoice_gen.py)
# This script relies on the system locale for open() encoding by default in Python 3.
# In a POSIX locale (default Docker), this often defaults to ASCII, causing crash on write.
cat > invoice_gen.py << 'PYEOF'
import csv
import datetime
import os
import sys

# Ensure output dir
os.makedirs('invoices', exist_ok=True)

print(f"Starting invoice generation at {datetime.datetime.now()}")
print(f"System encoding: {sys.getfilesystemencoding()}")

try:
    with open('customers.csv', 'r', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            name = row['name']
            print(f"Processing customer: {name}")
            
            # Construct filename - relies on filesystem encoding
            # In POSIX locale, this might fail or produce garbled filenames
            fname = f"invoices/{name.replace(' ', '_')}.txt"
            
            # Write content - relies on default locale encoding if not specified
            # We intentionally DO NOT specify encoding='utf-8' to force the agent 
            # to fix the system locale.
            with open(fname, 'w') as out:
                out.write(f"INVOICE\n")
                out.write(f"=======\n")
                out.write(f"Customer: {name}\n")
                out.write(f"Date: {datetime.datetime.now()}\n")
                out.write(f"Amount: {row['amount']} EUR\n")
                
    print("All invoices generated successfully.")

except UnicodeEncodeError as e:
    print(f"\nCRITICAL ERROR: Encoding failure. Your environment does not support the required characters.")
    print(f"Details: {e}")
    sys.exit(1)
except Exception as e:
    print(f"Error: {e}")
    sys.exit(1)
PYEOF

# 3. Create Data File (customers.csv) with international characters
cat > customers.csv << 'CSVEOF'
id,name,amount
1,John Smith,100.00
2,René François,120.50
3,Jürgen Müller,90.00
4,田中 太郎,200.00
5,María González,150.00
CSVEOF

# 4. Create Broken Dockerfile
# - Uses ubuntu:20.04 (minimal, no locales generated by default)
# - No TZ configured
cat > Dockerfile << 'DOCKERFILE'
FROM ubuntu:20.04

# Avoid prompts during package installation
ENV DEBIAN_FRONTEND=noninteractive

# Install python3
RUN apt-get update && apt-get install -y python3

WORKDIR /app
COPY . .

# Default command
CMD ["python3", "invoice_gen.py"]
DOCKERFILE

# 5. Build the broken image so the agent can test it immediately
echo "Building initial broken image (acme-invoicer:broken)..."
docker build -t acme-invoicer:broken . > /dev/null 2>&1 || echo "Build warning (expected)"

# Set permissions
chown -R ga:ga "$PROJECT_DIR"

# 6. Record Timestamp for anti-gaming
date +%s > /tmp/task_start_timestamp

# 7. Open Terminal for Agent
mkdir -p /home/ga/Desktop
chown ga:ga /home/ga/Desktop

su - ga -c "DISPLAY=:1 gnome-terminal --maximize -- bash -c 'cd ~/projects/global-invoicer && echo \"Global Invoicer Project\"; echo \"=======================\"; echo \"Try running the current image:\"; echo \"  docker run --rm -v \$(pwd)/invoices:/app/invoices acme-invoicer:broken\"; echo; echo \"You will see it crash or fail with encoding errors.\"; echo \"Fix the Dockerfile to support UTF-8 and Europe/Berlin time.\"; echo; exec bash'" > /tmp/terminal_launch.log 2>&1 &
sleep 2

# Initial Screenshot
if type take_screenshot &>/dev/null; then
    take_screenshot /tmp/task_start_screenshot.png
fi

echo "=== Setup Complete ==="
echo "Project created at $PROJECT_DIR"