#!/bin/bash
# Setup script for docker_cli_tool_wrapper task

set -e
echo "=== Setting up Docker CLI Tool Wrapper Task ==="

source /workspace/scripts/task_utils.sh 2>/dev/null || true

# Wait for Docker
if ! type wait_for_docker &>/dev/null; then
    wait_for_docker() {
        for i in {1..60}; do
            if docker info > /dev/null 2>&1; then return 0; fi
            sleep 2
        done; return 1
    }
fi
wait_for_docker

# 1. Create Network and DB Container
echo "Creating network and database..."
docker network create acme-net 2>/dev/null || true
docker rm -f acme-db 2>/dev/null || true
# Use alpine as a dummy DB that just sleeps
docker run -d --name acme-db --network acme-net alpine:3.18 sleep infinity

# 2. Prepare the 'acme-migrator' image
echo "Building acme-migrator image..."
TEMP_BUILD_DIR=$(mktemp -d)
cat > "$TEMP_BUILD_DIR/migrator.py" << 'PYTHON_EOF'
import os
import sys
import socket
import time

print("--- Acme Migrator v1.0 ---")
print(f"Container User UID: {os.getuid()}")
print(f"Container User GID: {os.getgid()}")
print(f"Arguments received: {sys.argv[1:]}")

# Check CWD
cwd = os.getcwd()
print(f"Working Directory: {cwd}")

# Check DB connectivity
db_host = os.environ.get('DB_HOST')
if db_host:
    print(f"DB_HOST is set to: {db_host}")
    try:
        # Simple check - just resolve it, pinging might require caps
        ip = socket.gethostbyname(db_host)
        print(f"Successfully resolved {db_host} to {ip}")
        # Try a quick connect (won't work on alpine sleep, but resolution proves net connectivity)
        print("Network check passed.")
    except Exception as e:
        print(f"Error resolving {db_host}: {e}")
else:
    print("WARNING: DB_HOST environment variable not set!")

# Generate artifact to test permissions
artifact_path = os.path.join(cwd, "report.log")
try:
    with open(artifact_path, "w") as f:
        f.write(f"Migration Report generated at {time.time()}\n")
        f.write(f"Generated by UID {os.getuid()}\n")
    print(f"Generated artifact: {artifact_path}")
except Exception as e:
    print(f"Error writing artifact: {e}")

PYTHON_EOF

cat > "$TEMP_BUILD_DIR/Dockerfile" << 'DOCKERFILE_EOF'
FROM python:3.11-slim
WORKDIR /app
COPY migrator.py /usr/local/bin/migrator.py
ENTRYPOINT ["python3", "/usr/local/bin/migrator.py"]
DOCKERFILE_EOF

docker build -t acme-migrator:latest "$TEMP_BUILD_DIR"
rm -rf "$TEMP_BUILD_DIR"

# 3. Setup User Environment
echo "Setting up user directories..."
mkdir -p /home/ga/projects/tools
mkdir -p /home/ga/projects/myapp
echo "-- Schema V1 --" > /home/ga/projects/myapp/schema.sql
chown -R ga:ga /home/ga/projects

# 4. Record State
date +%s > /tmp/task_start_timestamp

# 5. Open Terminal
echo "Launching terminal..."
su - ga -c "DISPLAY=:1 gnome-terminal --maximize -- bash -c 'cd ~/projects/tools && echo \"Task: Create a wrapper script named db-migrate for acme-migrator:latest\"; echo \"Requirements:\"; echo \" 1. Mount current dir to /app\"; echo \" 2. Run as current host user (fix permissions)\"; echo \" 3. Connect to acme-net\"; echo \" 4. Pass DB_HOST env var\"; echo \" 5. Pass all arguments\"; echo; echo \"Work in ~/projects/tools/\"; exec bash'" > /tmp/terminal_launch.log 2>&1 &

sleep 2
# Take screenshot
if type take_screenshot &>/dev/null; then
    take_screenshot /tmp/task_start_screenshot.png
fi

echo "=== Setup Complete ==="