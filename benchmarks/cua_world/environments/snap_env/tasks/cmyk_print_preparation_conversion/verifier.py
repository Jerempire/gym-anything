#!/usr/bin/env python3
"""
Verifier for CMYK Cartographic Print Preparation Task

Validates:
1. Product saved in DIMAP format.
2. Required specific bands exist (Cyan_pct, Magenta_pct, Yellow_pct, Key_pct).
3. Evaluates the syntax of the Band Maths virtual expressions for conditional logic (anti-spoofing).
4. Validates GeoTIFF was exported successfully and has a realistic file size.
5. Employs timestamp checks to ensure work was performed during the active task window.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_cmyk_conversion(traj, env_info, task_info):
    # Setup copy_from_env mechanism
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Verifier Error: copy_from_env not available"}

    # Fetch result generated by export_result.sh
    result_path = tempfile.mktemp(suffix='.json')
    try:
        copy_from_env('/tmp/cmyk_conversion_result.json', result_path)
        with open(result_path, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load agent output telemetry: {e}"}
    finally:
        if os.path.exists(result_path):
            os.unlink(result_path)

    score = 0
    feedback_parts = []
    
    # Required targets
    metadata = task_info.get('metadata', {})
    required_bands = [b.lower() for b in metadata.get('required_bands', ['cyan_pct', 'magenta_pct', 'yellow_pct', 'key_pct'])]

    # 1. DIMAP Exists & Anti-Gaming Timestamp
    if result.get('dim_found'):
        if result.get('dim_created_after_start'):
            score += 15
            feedback_parts.append("DIMAP product saved successfully (+15)")
        else:
            score += 5
            feedback_parts.append("DIMAP product found but created BEFORE task started. Possible gaming. (+5)")
    else:
        feedback_parts.append("DIMAP product (cmyk_print_ready.dim) not found (0/15)")

    # 2. Required Bands Named Correctly
    agent_bands = [b.lower() for b in result.get('band_names', [])]
    matched_bands = [b for b in required_bands if b in agent_bands]
    
    if len(matched_bands) == 4:
        score += 20
        feedback_parts.append("All 4 CMYK target bands explicitly named (+20)")
    elif len(matched_bands) > 0:
        score += (len(matched_bands) * 5)
        feedback_parts.append(f"Found {len(matched_bands)}/4 required target bands (+{len(matched_bands)*5})")
    else:
        feedback_parts.append("Missing required CMYK band names (0/20)")

    # 3. Expressions Verified (Checks for Conditional Logic & Native execution)
    expressions = result.get('expressions', {})
    
    # We must see if/then/else OR ? : ternary operators in the expressions to handle K=1.0 division by zero
    # We also check for / (division) to verify formula
    conditional_used = False
    division_used = False
    
    for bname, expr in expressions.items():
        el = expr.lower().replace(' ', '')
        if 'if(' in el or 'if' in el.replace('(', '') or '?' in el:
            conditional_used = True
        if '/' in el:
            division_used = True

    if conditional_used and division_used:
        score += 20
        feedback_parts.append("Expressions verified with division and conditional division-by-zero safeguards (+20)")
    elif conditional_used or division_used:
        score += 10
        feedback_parts.append("Partial mathematical logic detected in Band Maths (+10)")
    else:
        feedback_parts.append("No conditional/division operators detected in band expressions (0/20)")

    # 4. GeoTIFF Export
    if result.get('tif_found'):
        if result.get('tif_created_after_start'):
            score += 15
            feedback_parts.append("GeoTIFF exported successfully (+15)")
        else:
            score += 5
            feedback_parts.append("GeoTIFF found but created BEFORE task started. (+5)")
    else:
        feedback_parts.append("GeoTIFF export (cmyk_print_ready.tif) not found (0/15)")

    # 5. Mathematical Correctness (Checking * 100 or percentages scale within formulas)
    # Check if expressions indicate scaling (100 or 255 interactions)
    math_correctness_score = 0
    scale_100_used = any('100' in expr for expr in expressions.values())
    scale_255_used = any('255' in expr for expr in expressions.values())
    max_used = any('max' in expr.lower() for expr in expressions.values())
    
    if scale_100_used:
        math_correctness_score += 10
    if scale_255_used:
        math_correctness_score += 10
    if max_used:  # for Key channel
        math_correctness_score += 10
        
    score += math_correctness_score
    if math_correctness_score == 30:
        feedback_parts.append("Formulas show correct RGB norm, Max logic, and percentage scaling (+30)")
    else:
        feedback_parts.append(f"Partial mathematical correctness detected based on formula operators (+{math_correctness_score})")

    # Determine Pass/Fail (Threshold: 70 points AND required bands named)
    passed = (score >= 70 and len(matched_bands) == 4 and conditional_used)

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }