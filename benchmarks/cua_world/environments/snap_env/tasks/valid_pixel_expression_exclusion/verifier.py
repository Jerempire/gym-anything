#!/usr/bin/env python3
"""
Verifier for Vegetation Index Valid-Pixel Exclusion task.
"""

import os
import json
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_valid_pixel_exclusion(traj, env_info, task_info):
    """
    Evaluates whether the user computed NDVI and modified the valid-pixel expression.
    Uses programmatic checks of the DIMAP XML file and VLM trajectory analysis.
    """
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Environment copy function not available."}
        
    # Copy JSON result generated by export_result.sh
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve verification data: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 1. DIMAP Product Saved (15 points)
    # Anti-gaming: must be created AFTER the task started
    if result.get('dim_found') and result.get('dim_created_after_start'):
        score += 15
        feedback_parts.append("DIMAP product correctly saved during task (+15)")
    elif result.get('dim_found'):
        score += 5
        feedback_parts.append("DIMAP product found, but timestamp suggests it existed before task (+5)")
    else:
        feedback_parts.append("DIMAP product NOT found at expected path (0/15)")
        
    # 2. NDVI Band Exists (15 points)
    if result.get('ndvi_band_found'):
        score += 15
        feedback_parts.append("NDVI band exists in metadata (+15)")
    else:
        feedback_parts.append("NDVI band NOT found (0/15)")

    # 3. NDVI Math Correct (10 points)
    expr = result.get('ndvi_expression', '').lower().replace(' ', '')
    if 'band_2' in expr and 'band_3' in expr and '/' in expr and ('+' in expr or '-' in expr):
        score += 10
        feedback_parts.append("NDVI mathematical expression is sound (+10)")
    elif expr:
        score += 5
        feedback_parts.append("NDVI expression found but formatting/bands may be unusual (+5)")
    else:
        feedback_parts.append("No NDVI mathematical expression found (0/10)")

    # 4. Valid-Pixel Expression Configured (20 points)
    vpe = result.get('ndvi_valid_pixel_expression', '').strip()
    if vpe and vpe.lower() != 'true':
        score += 20
        feedback_parts.append(f"Valid-Pixel Expression modified from default (+20)")
        
        # 5. Valid-Pixel Logic Sound (15 points)
        # Check for comparative operator and band reference
        if any(op in vpe for op in ['>', '<', '>=', '<=', '==']) and 'band' in vpe.lower():
            score += 15
            feedback_parts.append("Valid-Pixel Expression contains valid comparative logic (+15)")
        else:
            feedback_parts.append("Valid-Pixel Expression missing logical operator or band reference (0/15)")
    else:
        feedback_parts.append("Valid-Pixel Expression was NOT modified or is empty (0/35)")

    # 6. VLM Trajectory Verification (25 points)
    if query_vlm:
        try:
            from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot
            frames = sample_trajectory_frames(traj, n=4)
            final_frame = get_final_screenshot(traj)
            
            prompt = """
            Analyze these chronological screenshots of a user interacting with ESA SNAP Desktop.
            The user was tasked with calculating an NDVI band and modifying the 'Valid-Pixel Expression' in the band's properties dialog.
            
            Focus on observing:
            1. Did the user open the Band Maths tool at any point?
            2. Did the user open the Band Properties dialog? (Usually titled 'Properties', showing a grid of metadata like Name, Description, Valid-Pixel Expression, etc.)
            
            Respond with JSON containing:
            {
                "opened_band_maths": true/false,
                "opened_properties_dialog": true/false,
                "confidence": "high/medium/low"
            }
            """
            
            vlm_res = query_vlm(images=frames + [final_frame], prompt=prompt)
            if vlm_res and vlm_res.get('success'):
                parsed = vlm_res.get('parsed', {})
                if parsed.get('opened_properties_dialog', False):
                    score += 25
                    feedback_parts.append("VLM confirmed interaction with Properties Dialog (+25)")
                elif parsed.get('opened_band_maths', False):
                    score += 10
                    feedback_parts.append("VLM saw Band Maths but no Properties dialog (+10)")
                else:
                    feedback_parts.append("VLM did not observe required UI interaction (0/25)")
            else:
                score += 25
                feedback_parts.append("VLM query failed; assuming VLM points (+25)")
        except Exception as e:
            score += 25
            feedback_parts.append(f"VLM verification skipped/failed ({e}); awarded default points (+25)")
    else:
        score += 25
        feedback_parts.append("VLM not available; awarded default points (+25)")

    # The most critical part of this task is that the valid pixel expression was actually modified
    key_criteria_met = result.get('ndvi_band_found') and (vpe and vpe.lower() != 'true')
    passed = score >= 75 and key_criteria_met

    return {
        "passed": passed,
        "score": score,
        "feedback": "\n".join(feedback_parts)
    }