#!/bin/bash
echo "=== Exporting metadata_reconstruction_band_stacking task results ==="

# Take final screenshot
DISPLAY=:1 scrot /tmp/task_final.png 2>/dev/null || true

# Run Python script to parse the exported DIMAP XML and generate JSON
python3 << 'PYEOF'
import os
import json
import xml.etree.ElementTree as ET

task_start = 0
if os.path.exists('/tmp/task_start_time.txt'):
    try:
        with open('/tmp/task_start_time.txt') as f:
            task_start = int(f.read().strip())
    except:
        pass

result = {
    'product_exists': False,
    'created_after_start': False,
    'band_count': 0,
    'band_names': [],
    'wavelengths': [],
    'has_renamed_bands': False,
    'red_wavelength_set': False,
    'nir_wavelength_set': False
}

exports_dir = '/home/ga/snap_exports'
dim_files = []
if os.path.exists(exports_dir):
    for f in os.listdir(exports_dir):
        if f.endswith('.dim'):
            dim_files.append(os.path.join(exports_dir, f))

# Check for the specifically requested output file first
target_file = '/home/ga/snap_exports/s2_reconstructed_stack.dim'
if target_file in dim_files:
    dim_files.remove(target_file)
    dim_files.insert(0, target_file)

for dim_path in dim_files:
    if result['product_exists']:
        break
        
    result['product_exists'] = True
    if int(os.path.getmtime(dim_path)) > task_start:
        result['created_after_start'] = True
        
    try:
        tree = ET.parse(dim_path)
        root = tree.getroot()
        
        # Look for the Spectral Band Info items inside the XML
        for sbi in root.iter('Spectral_Band_Info'):
            result['band_count'] += 1
            name_el = sbi.find('BAND_NAME')
            if name_el is not None and name_el.text:
                bname = name_el.text.strip()
                result['band_names'].append(bname)
                # Ignore default names generated by RAW reads or collocations
                default_names = ['band_1', 'band_2', 'band_3', 'b04', 'b08', 'sentinel2_b04_red', 'sentinel2_b08_nir', 'band_1_m', 'band_1_s']
                if bname.lower() not in default_names:
                    result['has_renamed_bands'] = True
                    
            wl_el = sbi.find('SPECTRAL_WAVELENGTH')
            if wl_el is not None and wl_el.text:
                try:
                    wl = float(wl_el.text.strip())
                    result['wavelengths'].append(wl)
                    # Use a small tolerance range to account for decimal formatting
                    if 660.0 <= wl <= 670.0:
                        result['red_wavelength_set'] = True
                    if 835.0 <= wl <= 850.0:
                        result['nir_wavelength_set'] = True
                except ValueError:
                    pass
    except Exception as e:
        print(f"Error parsing DIMAP XML: {e}")

# Save results for verifier.py
with open('/tmp/task_result.json', 'w') as f:
    json.dump(result, f, indent=2)

print("Result saved to /tmp/task_result.json")
PYEOF

cat /tmp/task_result.json
echo "=== Export complete ==="