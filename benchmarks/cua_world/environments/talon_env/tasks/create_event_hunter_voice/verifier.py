#!/usr/bin/env python3
"""
Verifier for create_event_hunter_voice task.

This verifier retrieves the executed state JSON from the Windows container, 
validating static AST compliance as well as dynamic behavior proven by the test harness.
"""

import tempfile
import os
import json
import ast
import re
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_create_event_hunter_voice(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available from environment."}

    # Retrieve telemetry
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("C:\\temp\\task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read exported result JSON: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []

    # CRITERION 1: File Structure (10 points)
    files_exist = result.get('py_exists', False) and result.get('talon_exists', False) and result.get('list_exists', False)
    if files_exist:
        score += 10
        feedback_parts.append("All 3 required module files exist.")
    else:
        feedback_parts.append("Missing one or more required module files.")

    # CRITERION 2: Talon List Syntax (10 points)
    list_content = result.get('list_content', "").lower()
    if list_content and ('list: user.wev_log_names' in list_content or 'user.wev_log_names' in list_content):
        if 'system' in list_content and 'application' in list_content and 'security' in list_content:
            score += 10
            feedback_parts.append("Talon log_names.talon-list syntax correctly formatted.")
        else:
            score += 5
            feedback_parts.append("Talon list syntax valid but missing expected log name records.")

    # CRITERION 3: Talon Grammar mapping (20 points)
    talon_content = result.get('talon_content', "")
    if talon_content:
        req_commands = [
            (r'event\s+log', r'user\.wev_set_log'),
            (r'event\s+id', r'user\.wev_set_id'),
            (r'event\s+hours', r'user\.wev_set_hours'),
            (r'event\s+keyword', r'user\.wev_set_keyword'),
            (r'event\s+clear', r'user\.wev_clear'),
            (r'event\s+search', r'user\.wev_run')
        ]
        matched = sum(1 for cmd_regex, action_regex in req_commands 
                      if re.search(cmd_regex, talon_content, re.IGNORECASE) and 
                         re.search(action_regex, talon_content, re.IGNORECASE))
        points = int((matched / len(req_commands)) * 20)
        score += points
        feedback_parts.append(f"Talon grammar mappings found: {matched}/{len(req_commands)}.")

    # CRITERION 4: State Management & AST Validation (20 points)
    sm_score = 0
    py_content = result.get('py_content', "")
    if py_content:
        try:
            tree = ast.parse(py_content)
            for node in ast.walk(tree):
                if isinstance(node, ast.ClassDef):
                    for dec in node.decorator_list:
                        # Find @mod.action_class decorator marking functions as native Talon Actions
                        if (isinstance(dec, ast.Attribute) and dec.attr == 'action_class') or \
                           (isinstance(dec, ast.Name) and dec.id == 'action_class'):
                            sm_score += 10
                            break
        except Exception as e:
            logger.warning(f"Python AST Parse Error: {e}")
            
    if result.get('harness_success'):
        sm_score += 10
    score += sm_score
    feedback_parts.append(f"State management: {sm_score}/20 points.")

    # CRITERION 5: PowerShell Generation (20 points)
    ps_score = 0
    if py_content:
        if 'subprocess' in py_content and ('run' in py_content or 'Popen' in py_content):
            ps_score += 10
        if 'Get-WinEvent' in py_content or 'get-winevent' in py_content.lower():
            ps_score += 10
    score += ps_score
    feedback_parts.append(f"PowerShell logic generated: {ps_score}/20 points.")

    # CRITERION 6: Functional OS Output generated by the Harness (20 points)
    out_score = 0
    if result.get('output_exists') and result.get('output_size', 0) > 0:
        out_score += 10
        content = result.get('output_content', "")
        
        # Determine if returned file reflects standard WinEvent properties
        if re.search(r'(Id|TimeCreated|Message|ProviderName)', content, re.IGNORECASE):
            out_score += 10
    score += out_score
    feedback_parts.append(f"Functional OS Data integration verified: {out_score}/20 points.")

    passed = (score >= 70) and result.get('output_exists') and (result.get('output_size', 0) > 0)

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }