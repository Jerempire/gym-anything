#!/usr/bin/env python3
"""
Verifier for Sankey Supply Chain Viz task.

Criteria:
1. Database & LCIA Methods Import (Derby check)
2. Product System Creation (Derby check)
3. Sankey Diagram Image Export (File check)
4. Summary Report Creation (File content check)
5. Visual Verification of Sankey View (VLM trajectory)

Pass Threshold: 60/100 points, MUST have product system.
"""

import json
import os
import tempfile
import logging

logger = logging.getLogger(__name__)

# ── VLM Prompts ───────────────────────────────────────────────────────────────

SANKEY_TRAJECTORY_PROMPT = """You are analyzing screenshots of an agent using openLCA to generate a Sankey diagram.

Look for these specific stages:
1.  **Product System**: A model graph (boxes and arrows) or product system editor.
2.  **Calculation**: A calculation setup dialog (selecting TRACI/Method).
3.  **Sankey Visualization**: A distinct view with colored flow bands (Sankey diagram) connecting nodes.
    -   This looks like thick colored lines flowing between boxes/bars.
    -   It is DIFFERENT from the standard node graph (thin lines).
4.  **Export/Save**: Dialogs for saving an image or report.

Return JSON:
{
  "product_system_seen": true/false,
  "calculation_dialog_seen": true/false,
  "sankey_diagram_seen": true/false,
  "confidence": "low"/"medium"/"high",
  "observations": "brief description of visual evidence"
}
"""

REPORT_CONTENT_PROMPT = """You are looking at a screenshot of a text report or result summary generated by an agent.
Does it contain:
1.  **GWP / Global Warming** values?
2.  **Process names** (e.g., HDPE, electricity, transport)?
3.  **Percentage** contributions?

Return JSON:
{
  "contains_gwp": true/false,
  "contains_processes": true/false,
  "contains_percentages": true/false
}
"""

def _vlm_query(query_vlm, prompt, image=None, images=None):
    if not query_vlm:
        return None
    try:
        result = query_vlm(prompt=prompt, image=image, images=images)
        if result and result.get("success"):
            return result.get("parsed", {})
    except Exception as e:
        logger.warning(f"VLM error: {e}")
    return None

def verify_sankey_supply_chain_viz(traj, env_info, task_info):
    """Verify the Sankey visualization task."""
    
    # 1. Setup Result Data
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Environment copy failed"}

    temp = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp.name)
        with open(temp.name) as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Could not read result file: {e}"}
    finally:
        if os.path.exists(temp.name):
            os.unlink(temp.name)

    score = 0
    feedback = []
    
    # ── programmatic Checks (70 points total) ──

    # 1. Database & Methods (20 pts)
    # Check if DB has substantial data (processes > 50) and methods imported
    process_count = result.get('process_count', 0)
    methods_count = result.get('impact_methods_count', 0)
    
    if process_count > 50:
        score += 10
        feedback.append(f"USLCI Database active ({process_count} processes)")
    else:
        feedback.append("USLCI Database content not found")

    if methods_count > 0:
        score += 10
        feedback.append(f"LCIA Methods imported ({methods_count} methods)")
    else:
        feedback.append("LCIA Methods not found")

    # 2. Product System (20 pts)
    ps_count = result.get('ps_count', 0)
    if ps_count >= 1:
        score += 20
        feedback.append("Product System created")
    else:
        feedback.append("No Product System found")

    # 3. Sankey Image Export (15 pts)
    if result.get('sankey_exists') and result.get('sankey_created_during'):
        if result.get('sankey_size', 0) > 5000: # >5KB suggests non-empty image
            score += 15
            feedback.append("Sankey image exported")
        else:
            score += 5
            feedback.append("Sankey image file exists but is very small")
    elif result.get('sankey_exists'):
        score += 5
        feedback.append("Sankey image exists (timestamp ambiguous)")
    else:
        feedback.append("Sankey image not found")

    # 4. Report Creation (15 pts)
    if result.get('report_exists') and result.get('report_created_during'):
        # Check keywords
        kws = result.get('report_keywords_found', 0)
        if kws >= 3:
            score += 15
            feedback.append("Summary report created with relevant content")
        elif kws >= 1:
            score += 10
            feedback.append("Summary report created (partial content)")
        else:
            score += 5
            feedback.append("Summary report file exists but lacks keywords")
    else:
        feedback.append("Summary report not found")

    # ── VLM Checks (30 points total) ──
    
    query_vlm = env_info.get('query_vlm')
    if query_vlm:
        # Sample frames for Sankey workflow
        # We need to find the sankey visualization
        from gym_anything.vlm import sample_trajectory_frames
        frames = sample_trajectory_frames(traj, 5)
        
        vlm_res = _vlm_query(query_vlm, SANKEY_TRAJECTORY_PROMPT, images=frames)
        
        if vlm_res:
            if vlm_res.get('sankey_diagram_seen'):
                score += 20
                feedback.append("VLM confirmed Sankey diagram usage")
            elif vlm_res.get('calculation_dialog_seen'):
                score += 10
                feedback.append("VLM confirmed calculation ran (but missed Sankey)")
            
            if vlm_res.get('product_system_seen') and ps_count == 0:
                # Fallback if Derby failed but VLM saw it
                score += 10
                feedback.append("VLM saw product system (Derby check failed)")
                
        # Check report content visually if file check was weak
        if result.get('report_keywords_found', 0) < 3:
            from gym_anything.vlm import get_final_screenshot
            final_img = get_final_screenshot(traj)
            if final_img:
                rep_res = _vlm_query(query_vlm, REPORT_CONTENT_PROMPT, image=final_img)
                if rep_res and rep_res.get('contains_gwp') and rep_res.get('contains_processes'):
                    score += 10
                    feedback.append("VLM verified report content on screen")

    # ── Final Calculation ──
    passed = (score >= 60) and (ps_count >= 1 or "VLM saw product system" in str(feedback))
    
    return {
        "passed": passed,
        "score": min(score, 100),
        "feedback": " | ".join(feedback)
    }