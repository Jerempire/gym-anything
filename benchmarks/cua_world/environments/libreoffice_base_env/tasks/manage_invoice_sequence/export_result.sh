#!/bin/bash
echo "=== Exporting manage_invoice_sequence results ==="

# Source shared utilities
source /workspace/scripts/task_utils.sh

TASK_START=$(cat /tmp/task_start_time.txt 2>/dev/null || echo "0")
ODB_PATH="/home/ga/chinook.odb"

# Take final screenshot before closing app
take_screenshot /tmp/task_final.png

# Check if application was running
APP_RUNNING=$(is_libreoffice_running && echo "true" || echo "false")

# Kill LibreOffice to ensure file lock is released and buffers flushed
kill_libreoffice

# --- Verification Logic ---
# Since this is an embedded HSQLDB, we verify by unzipping the ODB
# and inspecting the 'database/script' file which contains schema and data.

FILE_MODIFIED="false"
INVOICE_412_EXISTS="unknown"
SEQUENCE_IS_1000="false"
INVOICE_LINES_DELETED="unknown"

if [ -f "$ODB_PATH" ]; then
    # Check modification time
    ODB_MTIME=$(stat -c %Y "$ODB_PATH" 2>/dev/null || echo "0")
    if [ "$ODB_MTIME" -gt "$TASK_START" ]; then
        FILE_MODIFIED="true"
    fi

    # Unzip ODB to temp directory for inspection
    WORK_DIR=$(mktemp -d)
    echo "Extracting ODB to $WORK_DIR..."
    unzip -q "$ODB_PATH" -d "$WORK_DIR" 2>/dev/null || echo "Unzip failed"

    SCRIPT_FILE="$WORK_DIR/database/script"

    if [ -f "$SCRIPT_FILE" ]; then
        echo "Inspecting HSQLDB script..."

        # 1. Check if Invoice 412 still exists
        # Look for: INSERT INTO "Invoice" VALUES(412,...
        if grep -q "INSERT INTO \"Invoice\" VALUES(412," "$SCRIPT_FILE"; then
            INVOICE_412_EXISTS="true"
            echo "Found Invoice 412."
        else
            INVOICE_412_EXISTS="false"
            echo "Invoice 412 NOT found."
        fi

        # 2. Check if InvoiceLines for 412 still exist
        # Look for: INSERT INTO "InvoiceLine" VALUES(...,412) -- 412 is the 2nd column (InvoiceId)
        # Regex explanation: VALUES \s* ( \s* [0-9]+ \s* , \s* 412 \s* ,
        if grep -E "INSERT INTO \"InvoiceLine\" VALUES\([0-9]+,412," "$SCRIPT_FILE" > /dev/null; then
            INVOICE_LINES_DELETED="false"
            echo "Found InvoiceLines for 412."
        else
            INVOICE_LINES_DELETED="true"
            echo "InvoiceLines for 412 NOT found."
        fi

        # 3. Check for Sequence Reset
        # We look for the CREATE TABLE definition or an ALTER statement.
        # HSQLDB 1.8 schema usually looks like:
        # CREATE TABLE "Invoice"(...,"InvoiceId" INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1000),...)
        # Or specifically check the explicit sequence restart if saved differently.
        
        # Check specific table definition pattern for InvoiceId starting with 1000
        if grep -F "\"InvoiceId\" INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1000)" "$SCRIPT_FILE" > /dev/null; then
            SEQUENCE_IS_1000="true"
            echo "Sequence START WITH 1000 found in schema definition."
        # Fallback: Check if the last ALTER statement set it
        elif grep -F "ALTER TABLE \"Invoice\" ALTER COLUMN \"InvoiceId\" RESTART WITH 1000" "$SCRIPT_FILE" > /dev/null; then
             SEQUENCE_IS_1000="true"
             echo "RESTART WITH 1000 found in script commands."
        else
            echo "Sequence reset pattern not found."
            # Debug: show what the line looks like
            grep "\"InvoiceId\" INTEGER GENERATED BY DEFAULT AS IDENTITY" "$SCRIPT_FILE" || true
        fi
    else
        echo "ERROR: database/script file not found in ODB archive"
    fi

    # Clean up
    rm -rf "$WORK_DIR"
else
    echo "ERROR: ODB file not found at $ODB_PATH"
fi

# Create JSON result
TEMP_JSON=$(mktemp /tmp/result.XXXXXX.json)
cat > "$TEMP_JSON" << EOF
{
    "file_modified": $FILE_MODIFIED,
    "invoice_412_exists": "$INVOICE_412_EXISTS",
    "invoice_lines_deleted": "$INVOICE_LINES_DELETED",
    "sequence_is_1000": $SEQUENCE_IS_1000,
    "app_was_running": $APP_RUNNING,
    "timestamp": "$(date -Iseconds)"
}
EOF

# Move to final location
rm -f /tmp/task_result.json 2>/dev/null || sudo rm -f /tmp/task_result.json 2>/dev/null || true
cp "$TEMP_JSON" /tmp/task_result.json
chmod 666 /tmp/task_result.json
rm -f "$TEMP_JSON"

echo "Result saved to /tmp/task_result.json"
cat /tmp/task_result.json
echo "=== Export complete ==="