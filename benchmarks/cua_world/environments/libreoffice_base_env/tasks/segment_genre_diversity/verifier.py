#!/usr/bin/env python3
"""
Verifier for segment_genre_diversity task.
Parses the JSON report generated by export_result.sh inside the container.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_segment_genre_diversity(traj, env_info, task_info):
    """
    Verify that the agent correctly segmented customers by genre diversity.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy the JSON result generated by the in-container python script
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/final_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": f"Failed to retrieve validation results: {str(e)}",
            "details": {"error": str(e)}
        }
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    # 1. Check if ODB was modified (Anti-gaming / basic effort)
    if not result.get('odb_exists', False):
        return {"passed": False, "score": 0, "feedback": "Database file missing."}
        
    if not result.get('odb_modified', False):
        feedback_parts.append("Warning: Database file timestamp suggests no save occurred.")
        # We don't fail immediately, but it's suspicious.
    
    # 2. Schema Check (20 pts)
    if result.get('schema_correct', False):
        score += 20
        feedback_parts.append("Schema: 'Segment' column added successfully (+20).")
    else:
        feedback_parts.append("Schema: 'Segment' column NOT found or incorrect type (0).")
        
    # 3. Data Cleanliness (20 pts)
    # All records must have a value (no NULLs, full count match)
    if result.get('data_clean', False):
        score += 20
        feedback_parts.append("Data Integrity: All customers segmented (+20).")
    else:
        feedback_parts.append("Data Integrity: Some customers missing or NULL (0).")
        
    # 4. 'Casual' Classification Precision (25 pts)
    # Did we correctly label the <4 genre customers?
    casual_score = result.get('casual_precision', 0.0)
    points_casual = int(casual_score * 25)
    score += points_casual
    feedback_parts.append(f"Casual Segment Accuracy: {casual_score:.0%} (+{points_casual}).")
    
    # 5. 'Eclectic' Classification Recall (35 pts)
    # Did we correctly label the >=4 genre customers?
    eclectic_score = result.get('eclectic_recall', 0.0)
    points_eclectic = int(eclectic_score * 35)
    score += points_eclectic
    feedback_parts.append(f"Eclectic Segment Accuracy: {eclectic_score:.0%} (+{points_eclectic}).")
    
    # Pass logic
    passed = score >= 80
    
    if result.get('errors'):
        feedback_parts.append(f"Errors encountered: {', '.join(result['errors'])}")

    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback_parts),
        "details": result
    }