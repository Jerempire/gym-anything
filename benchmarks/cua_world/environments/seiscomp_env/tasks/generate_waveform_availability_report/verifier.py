#!/usr/bin/env python3
"""
Verifier for the Data Availability Report Generation task.

Verification Strategy:
1. Ensure CSV report was successfully generated during the task sequence.
2. Read the ground truth generated by the export script.
3. Parse the agent's CSV and validate structure (headers).
4. Cross-reference the agent's extracted timestamps and sample rates against the ground truth.
"""

import os
import json
import csv
import tempfile
import logging
from datetime import datetime

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def parse_iso_date(date_str):
    """Robust parser for ISO-like dates to datetime objects."""
    date_str = date_str.strip().replace('Z', '')
    if 'T' not in date_str and ' ' in date_str:
        date_str = date_str.replace(' ', 'T')
    try:
        return datetime.fromisoformat(date_str)
    except ValueError:
        return None

def verify_waveform_availability_report(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    feedback_parts = []
    score = 0

    # 1. Load task execution metadata
    meta_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", meta_file.name)
        with open(meta_file.name, 'r') as f:
            result_meta = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result metadata: {e}"}
    finally:
        if os.path.exists(meta_file.name):
            os.unlink(meta_file.name)

    output_exists = result_meta.get('output_exists', False)
    file_created = result_meta.get('file_created_during_task', False)

    if not output_exists:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Waveform manifest CSV was not found at ~/reports/waveform_manifest.csv."
        }
    
    score += 10
    feedback_parts.append("Report file exists")

    if file_created:
        score += 10
        feedback_parts.append("File created/modified during task execution")
    else:
        feedback_parts.append("WARNING: File appears to have existed prior to task start")

    # 2. Load Ground Truth
    gt_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/ground_truth.json", gt_file.name)
        with open(gt_file.name, 'r') as f:
            ground_truth = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load ground truth: {e}"}
    finally:
        if os.path.exists(gt_file.name):
            os.unlink(gt_file.name)

    if not ground_truth:
        return {"passed": False, "score": 0, "feedback": "Ground truth is empty. Archive might be missing."}

    # 3. Load and parse the agent's CSV
    agent_csv_file = tempfile.NamedTemporaryFile(delete=False, suffix='.csv')
    try:
        copy_from_env("/home/ga/reports/waveform_manifest.csv", agent_csv_file.name)
        
        agent_data = {}
        headers = []
        with open(agent_csv_file.name, 'r') as f:
            reader = csv.reader(f)
            try:
                headers = next(reader)
            except StopIteration:
                pass
            
            for row in reader:
                if len(row) >= 4:
                    agent_data[row[0].strip()] = {
                        "start": row[1].strip(),
                        "end": row[2].strip(),
                        "rate": row[3].strip()
                    }
    except Exception as e:
        return {"passed": False, "score": score, "feedback": f"Error parsing CSV: {e}"}
    finally:
        if os.path.exists(agent_csv_file.name):
            os.unlink(agent_csv_file.name)

    # 4. Verify Headers
    expected_headers = ["streamid", "starttime", "endtime", "samplerate"]
    actual_headers_clean = [h.strip().lower() for h in headers]
    
    if actual_headers_clean[:4] == expected_headers:
        score += 10
        feedback_parts.append("CSV headers match expectations perfectly")
    elif len(actual_headers_clean) >= 4:
        score += 5
        feedback_parts.append("CSV has sufficient columns but header names differ slightly")
    else:
        feedback_parts.append("CSV headers are missing or malformed")

    # 5. Cross-validate coverage and accuracy
    total_streams = len(ground_truth)
    streams_found = 0
    accurate_times = 0
    accurate_rates = 0

    for stream_id, gt_info in ground_truth.items():
        if stream_id in agent_data:
            streams_found += 1
            ag_info = agent_data[stream_id]

            # Validate Time Accuracy
            gt_start = parse_iso_date(gt_info["start_time"])
            gt_end = parse_iso_date(gt_info["end_time"])
            
            ag_start = parse_iso_date(ag_info["start"])
            ag_end = parse_iso_date(ag_info["end"])

            if gt_start and gt_end and ag_start and ag_end:
                start_diff = abs((gt_start - ag_start).total_seconds())
                end_diff = abs((gt_end - ag_end).total_seconds())
                
                if start_diff <= 1.5 and end_diff <= 1.5:
                    accurate_times += 1

            # Validate Sample Rate
            try:
                ag_rate = float(ag_info["rate"].replace('Hz', '').strip())
                gt_rate = float(gt_info["sample_rate"])
                if abs(ag_rate - gt_rate) < 0.1:
                    accurate_rates += 1
            except ValueError:
                pass

    # Score scaling
    coverage_ratio = streams_found / total_streams if total_streams > 0 else 0
    score += int(30 * coverage_ratio)
    
    if streams_found > 0:
        time_accuracy_ratio = accurate_times / streams_found
        rate_accuracy_ratio = accurate_rates / streams_found
        
        score += int(30 * time_accuracy_ratio)
        score += int(10 * rate_accuracy_ratio)

        feedback_parts.append(f"Coverage: {streams_found}/{total_streams} streams")
        feedback_parts.append(f"Time Accuracy: {accurate_times}/{streams_found} correct")
        feedback_parts.append(f"Rate Accuracy: {accurate_rates}/{streams_found} correct")
    else:
        feedback_parts.append("No valid streams found in CSV matching ground truth IDs")

    passed = score >= 75 and coverage_ratio >= 0.8 and file_created

    return {
        "passed": bool(passed),
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }