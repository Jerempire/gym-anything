#!/usr/bin/env python3
"""
Verifier for audit_sds_archive_report task.

Evaluates the data availability report generated by the agent against the actual
contents of the SDS archive (computed dynamically during setup).
"""

import os
import json
import tempfile
import re
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_audit_sds_archive(traj, env_info, task_info):
    """
    Verify the agent's data availability report.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    feedback_parts = []
    score = 0
    max_score = 100

    # 1. Read exported result metadata
    result_tmp = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", result_tmp.name)
        with open(result_tmp.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result metadata: {e}"}
    finally:
        if os.path.exists(result_tmp.name):
            os.unlink(result_tmp.name)

    # Criterion 1: Report file exists and has substantive content
    report_exists = result.get("report_exists", False)
    report_created = result.get("report_created_during_task", False)
    report_size = result.get("report_size_bytes", 0)

    if not report_exists:
        return {"passed": False, "score": 0, "feedback": "Report file not found at /home/ga/data_availability_report.txt"}
    if not report_created:
        return {"passed": False, "score": 0, "feedback": "Report file was not created or modified during the task (anti-gaming)"}
    if report_size < 200:
        return {"passed": False, "score": 0, "feedback": f"Report file is too small ({report_size} bytes) to contain the required data"}

    score += 10
    feedback_parts.append("Report file exists and was generated during task")

    # 2. Read ground truth
    gt_tmp = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/archive_ground_truth.json", gt_tmp.name)
        with open(gt_tmp.name, 'r') as f:
            gt = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read ground truth: {e}"}
    finally:
        if os.path.exists(gt_tmp.name):
            os.unlink(gt_tmp.name)

    # 3. Read the generated report
    report_tmp = tempfile.NamedTemporaryFile(delete=False, suffix='.txt')
    try:
        copy_from_env("/tmp/data_availability_report.txt", report_tmp.name)
        with open(report_tmp.name, 'r', encoding='utf-8') as f:
            report_text = f.read()
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read report content: {e}"}
    finally:
        if os.path.exists(report_tmp.name):
            os.unlink(report_tmp.name)

    report_lower = report_text.lower()
    report_lines = report_text.split('\n')

    # Criterion 2: Format compliance
    has_details_header = "station details" in report_lower
    has_summary_header = "summary" in report_lower
    
    if has_details_header and has_summary_header:
        score += 5
        feedback_parts.append("Format headers found")
    else:
        feedback_parts.append("Missing required section headers")

    # Metrics trackers
    stations_found = set()
    channels_found = 0
    correct_files = 0
    correct_sizes = 0
    correct_dates = 0

    # Parse details
    for detail in gt.get("details", []):
        sta = detail["sta"]
        cha = detail["cha"]
        files = detail["files"]
        size = detail["size"]
        date_range = detail["date_range"]

        # Find line(s) mentioning this station and channel
        matching_lines = [line for line in report_lines if sta in line and cha in line]
        
        if matching_lines:
            stations_found.add(sta)
            channels_found += 1
            
            # Check numbers in matching lines
            for line in matching_lines:
                # Find all numbers in the line
                numbers = [float(n) for n in re.findall(r'\b\d+\b', line)]
                
                # Files check (exact)
                if files in numbers:
                    correct_files += 1
                    
                # Size check (allow 5% tolerance in case of kb/mb rounding, though bytes requested)
                size_found = False
                for n in numbers:
                    if size * 0.95 <= n <= size * 1.05:
                        size_found = True
                        break
                if size_found:
                    correct_sizes += 1
                    
                # Date range check
                if date_range in line:
                    correct_dates += 1
                
                # If we matched the basics, break to avoid double counting on duplicate lines
                if size_found or files in numbers:
                    break

    # Score Station Coverage (20 points)
    gt_stations = gt.get("stations", [])
    if len(gt_stations) > 0:
        station_ratio = len(stations_found) / len(gt_stations)
        station_pts = int(20 * station_ratio)
        score += station_pts
        feedback_parts.append(f"Found {len(stations_found)}/{len(gt_stations)} stations ({station_pts} pts)")
    
    # Check if ALL stations were listed (Required for passing)
    all_stations_listed = len(stations_found) == len(gt_stations)

    # Score Channel Coverage (15 points)
    total_details = len(gt.get("details", []))
    if total_details > 0:
        channel_ratio = channels_found / total_details
        channel_pts = int(15 * channel_ratio)
        score += channel_pts
        feedback_parts.append(f"Found {channels_found}/{total_details} channels ({channel_pts} pts)")

        # Score Files Accuracy (15 points)
        files_ratio = correct_files / total_details
        files_pts = int(15 * files_ratio)
        score += files_pts
        
        # Score Size Accuracy (15 points)
        size_ratio = correct_sizes / total_details
        size_pts = int(15 * size_ratio)
        score += size_pts
        
        # Score Date Range (10 points)
        dates_ratio = correct_dates / total_details
        dates_pts = int(10 * dates_ratio)
        score += dates_pts

    # Score Summary Totals (10 points)
    summary_pts = 0
    
    gt_total_net = len(gt.get("networks", []))
    gt_total_sta = len(gt_stations)
    gt_total_cha = len(gt.get("channels", []))
    gt_total_files = gt.get("total_files", 0)
    gt_total_size = gt.get("total_size", 0)

    # Extract numbers that follow "Total..."
    def extract_total(keyword):
        match = re.search(f"{keyword}[^\\d]*(\\d+)", report_lower)
        if match:
            return int(match.group(1))
        return None

    t_net = extract_total("networks")
    t_sta = extract_total("stations")
    t_cha = extract_total("channels")
    t_files = extract_total("files")
    t_size = extract_total("size")

    if t_net == gt_total_net: summary_pts += 2
    if t_sta == gt_total_sta: summary_pts += 2
    if t_cha == gt_total_cha: summary_pts += 2
    if t_files == gt_total_files: summary_pts += 2
    
    if t_size is not None and gt_total_size * 0.95 <= t_size <= gt_total_size * 1.05:
        summary_pts += 2

    score += summary_pts
    feedback_parts.append(f"Summary totals score: {summary_pts}/10")

    passed = score >= 60 and all_stations_listed

    if not all_stations_listed:
        feedback_parts.append("FAILED: Not all archive stations were listed in the report")

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts),
        "details": {
            "stations_found": len(stations_found),
            "expected_stations": len(gt_stations),
            "channels_found": channels_found,
            "correct_file_counts": correct_files,
            "correct_sizes": correct_sizes,
            "summary_points": summary_pts
        }
    }