#!/usr/bin/env python3
"""
Verifier for the Stepped Terrace Solar task in SketchUp Make 2017 (Skelion).
Uses multiple signals:
1. File verification (existence, size, modification timestamp)
2. Trajectory VLM visual verification of building geometry, panel placement, and panel tilt.
"""

import json
import tempfile
import os
import logging
from typing import Dict, Any
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_stepped_terrace_solar(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]) -> Dict[str, Any]:
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')

    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Execution error: copy_from_env function not available"}
    if not query_vlm:
        return {"passed": False, "score": 0, "feedback": "Execution error: query_vlm function not available"}

    metadata = task_info.get('metadata', {})
    min_size = metadata.get('min_file_size_bytes', 60000)

    score = 0
    feedback_parts = []
    
    # ---------------------------------------------------------
    # 1. Read exported results via copy_from_env
    # ---------------------------------------------------------
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Use Windows-style path matching the container's output location
        copy_from_env("C:\\Users\\Docker\\Documents\\task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result JSON from container: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    file_exists = result.get('output_exists', False)
    created_during_task = result.get('file_created_during_task', False)
    file_size = result.get('output_size_bytes', 0)

    # File Checks
    if not file_exists:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Target file stepped_terrace.skp was not found in C:\\Users\\Docker\\Documents\\"
        }

    if created_during_task:
        score += 20
        feedback_parts.append("✅ File saved during task.")
    else:
        feedback_parts.append("❌ File exists but appears unmodified during the task (possible gaming).")
        return {"passed": False, "score": 0, "feedback": " | ".join(feedback_parts)}

    if file_size >= min_size:
        score += 10
        feedback_parts.append(f"✅ File size is adequate ({file_size} bytes).")
    else:
        feedback_parts.append(f"⚠️ File size ({file_size} bytes) is very small, missing Skelion components?")

    # ---------------------------------------------------------
    # 2. VLM Trajectory Verification
    # ---------------------------------------------------------
    # Sample 4 frames across the trajectory to capture building process + final state
    frames = sample_trajectory_frames(traj, n=4)
    final_img = get_final_screenshot(traj)
    if final_img:
        frames.append(final_img)

    if not frames:
        feedback_parts.append("❌ No trajectory screenshots available for visual verification.")
        return {"passed": False, "score": score, "feedback": " | ".join(feedback_parts)}

    prompt = """You are an expert CAD/SketchUp reviewer evaluating a student's solar installation model.
    Review the provided sequence of screenshots from the modeling session.
    
    Task Requirements:
    1. A 3-story stepped building ("wedding cake" or terrace style). The building must have 3 distinct horizontal roof levels stacked on each other, stepping back to leave exposed terraces.
    2. Solar panels (blue/black grid shapes generated by Skelion) must be placed on MULTIPLE roof levels/terraces.
    3. The solar panels must be visibly TILTED (e.g., ~20 degrees), casting small shadows or showing a triangular profile, rather than lying completely flat/flush against the roof surface.
    
    Based on the evidence in the screenshots, answer the following in JSON format:
    {
        "stepped_geometry_present": true/false,
        "panels_on_multiple_levels": true/false,
        "panels_are_tilted": true/false,
        "reasoning": "Brief explanation of what you observed in the screenshots."
    }
    """

    vlm_resp = query_vlm(images=frames, prompt=prompt)
    
    if not vlm_resp or not vlm_resp.get("success"):
        feedback_parts.append("⚠️ VLM verification failed to process.")
        return {"passed": False, "score": score, "feedback": " | ".join(feedback_parts)}

    parsed = vlm_resp.get("parsed", {})
    
    geo_present = parsed.get("stepped_geometry_present", False)
    multi_level = parsed.get("panels_on_multiple_levels", False)
    tilted = parsed.get("panels_are_tilted", False)
    reasoning = parsed.get("reasoning", "No reasoning provided.")

    if geo_present:
        score += 20
        feedback_parts.append("✅ 3-story stepped geometry confirmed.")
    else:
        feedback_parts.append("❌ Stepped 3-story geometry not observed.")

    if multi_level:
        score += 25
        feedback_parts.append("✅ Panels installed on multiple terraces.")
    else:
        feedback_parts.append("❌ Panels missing from multiple levels.")

    if tilted:
        score += 25
        feedback_parts.append("✅ Tilted solar panels confirmed.")
    else:
        feedback_parts.append("❌ Panels do not appear properly tilted.")

    feedback_parts.append(f"[VLM Reasoning: {reasoning}]")

    # Evaluate Pass/Fail
    # To pass, they must have the file correctly saved, geometry built, and panels on multiple levels
    key_criteria_met = file_exists and created_during_task and geo_present and multi_level
    passed = (score >= 75) and key_criteria_met

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }