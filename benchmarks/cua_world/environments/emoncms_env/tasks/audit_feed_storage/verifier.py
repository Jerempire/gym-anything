#!/usr/bin/env python3
"""
Verifier for audit_feed_storage task.
Checks if the CSV report generated by the agent matches the actual system state.
"""

import json
import csv
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_audit_feed_storage(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    required_columns = metadata.get('required_columns', [])
    engine_mapping = metadata.get('engine_mapping', {})

    # Retrieve result JSON and the Agent's CSV file
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    temp_csv = tempfile.NamedTemporaryFile(delete=False, suffix='.csv')
    
    try:
        # Get metadata JSON
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result_data = json.load(f)
            
        output_exists = result_data.get("output_exists", False)
        ground_truth_feeds = result_data.get("ground_truth_feeds", [])
        
        # If output exists, get the CSV file content
        agent_rows = []
        if output_exists:
            try:
                copy_from_env("/tmp/agent_audit.csv", temp_csv.name)
                with open(temp_csv.name, 'r', encoding='utf-8') as f:
                    reader = csv.DictReader(f)
                    # Normalize field names (strip spaces)
                    if reader.fieldnames:
                        reader.fieldnames = [x.strip() for x in reader.fieldnames]
                    
                    # Verify headers match exactly
                    missing_headers = [h for h in required_columns if h not in (reader.fieldnames or [])]
                    
                    for row in reader:
                        # Convert numeric fields for comparison
                        clean_row = {}
                        for k, v in row.items():
                            if k in ['id', 'engine', 'interval', 'npoints', 'start_time', 'size']:
                                try:
                                    clean_row[k] = int(float(v))
                                except:
                                    clean_row[k] = v
                            else:
                                clean_row[k] = v
                        agent_rows.append(clean_row)
            except Exception as e:
                return {"passed": False, "score": 0, "feedback": f"Failed to parse CSV: {str(e)}"}
        
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)
        if os.path.exists(temp_csv.name):
            os.unlink(temp_csv.name)

    # === Scoring ===
    score = 0
    feedback = []
    
    # 1. File Existence (10 pts)
    if not output_exists:
        return {"passed": False, "score": 0, "feedback": "Output CSV file not found."}
    score += 10
    feedback.append("File exists.")
    
    # 2. File created during task (5 pts)
    if result_data.get("file_created_during_task", False):
        score += 5
    else:
        feedback.append("Warning: File timestamp indicates it wasn't created during this task session.")

    # 3. CSV Headers (15 pts)
    if not missing_headers:
        score += 15
        feedback.append("CSV headers are correct.")
    else:
        feedback.append(f"Missing CSV headers: {', '.join(missing_headers)}")
        # If headers are missing, we likely can't verify rows properly, but we'll try
    
    # 4. Row Count (15 pts)
    if len(agent_rows) == len(ground_truth_feeds):
        score += 15
        feedback.append(f"Row count matches ({len(agent_rows)}).")
    else:
        feedback.append(f"Row count mismatch: Expected {len(ground_truth_feeds)}, got {len(agent_rows)}.")
        
    # 5. ID Completeness (10 pts)
    gt_ids = set(f['id'] for f in ground_truth_feeds)
    agent_ids = set(row.get('id') for row in agent_rows if row.get('id') is not None)
    
    if gt_ids == agent_ids:
        score += 10
    else:
        missing_ids = gt_ids - agent_ids
        extra_ids = agent_ids - gt_ids
        if missing_ids:
            feedback.append(f"Missing Feed IDs: {list(missing_ids)[:3]}...")
        if extra_ids:
            feedback.append(f"Extra Feed IDs: {list(extra_ids)[:3]}...")

    # 6. Content Accuracy (30 pts)
    # Check mappings and metadata for overlapping IDs
    accuracy_score = 0
    row_count = len(ground_truth_feeds)
    if row_count > 0:
        points_per_row = 30 / row_count
        
        for gt_row in ground_truth_feeds:
            # Find matching agent row
            agent_match = next((r for r in agent_rows if r.get('id') == gt_row['id']), None)
            
            if agent_match:
                row_correct = True
                
                # Check Engine Mapping
                expected_engine_name = engine_mapping.get(str(gt_row['engine']), "Unknown")
                agent_engine_name = agent_match.get('engine_name', '')
                if agent_engine_name != expected_engine_name:
                    row_correct = False
                
                # Check metrics (allow minor tolerance for live system changes)
                # npoints and size might change slightly if data is incoming
                for field in ['interval', 'start_time']:
                    if agent_match.get(field) != gt_row[field]:
                        row_correct = False
                        
                # Tolerance check for size/npoints (5%)
                for field in ['npoints', 'size']:
                    gt_val = gt_row[field]
                    ag_val = agent_match.get(field, 0)
                    if isinstance(ag_val, int) and gt_val > 0:
                        diff = abs(gt_val - ag_val)
                        if (diff / gt_val) > 0.05:
                            row_correct = False
                    elif ag_val != gt_val:
                        row_correct = False

                if row_correct:
                    accuracy_score += points_per_row
        
        score += min(30, int(accuracy_score))
        if int(accuracy_score) >= 25:
            feedback.append("Data content is accurate.")
        else:
            feedback.append("Some data fields (metadata or engine mapping) are incorrect.")

    # 7. Sorting (5 pts)
    # Check if agent_ids list is sorted
    agent_ids_list = [row.get('id') for row in agent_rows if row.get('id') is not None]
    if agent_ids_list == sorted(agent_ids_list) and len(agent_ids_list) > 0:
        score += 5
        feedback.append("Rows are sorted by ID.")
    else:
        feedback.append("Rows are not sorted by ID.")
        
    # 8. Start Time Plausibility (10 pts)
    # Check if start_times look like valid recent epochs (e.g., > 2020)
    # 1577836800 is 2020-01-01
    valid_times = 0
    total_checked = 0
    for row in agent_rows:
        st = row.get('start_time')
        if isinstance(st, int) and st > 1577836800:
            valid_times += 1
        total_checked += 1
    
    if total_checked > 0 and valid_times == total_checked:
        score += 10
        feedback.append("Timestamps appear plausible.")
    elif total_checked > 0:
        feedback.append(f"Invalid timestamps detected ({total_checked - valid_times} rows).")

    return {
        "passed": score >= 65 and not missing_headers,
        "score": score,
        "feedback": " ".join(feedback)
    }