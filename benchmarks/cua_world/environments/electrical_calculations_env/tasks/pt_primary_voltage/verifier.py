#!/usr/bin/env python3
"""
Verifier for pt_primary_voltage task.

Verifies:
1. Agent navigated to PT calculator and entered correct values.
2. Agent calculated the correct primary voltage (~129600 V).
3. Agent saved the result to /sdcard/pt_result.txt (Primary method).
4. Fallback: VLM verification of the final screen and trajectory.
"""

import json
import tempfile
import os
import logging
import re
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_pt_primary_voltage(traj, env_info, task_info):
    """
    Verify PT Primary Voltage calculation task.
    """
    copy_from_env = env_info.get('copy_from_env')
    
    # Metadata
    metadata = task_info.get('metadata', {})
    expected_val = metadata.get('expected_value', 129600)
    tolerance = metadata.get('tolerance', 100)
    
    score = 0
    feedback_parts = []
    
    # =======================================================
    # 1. Attempt File-Based Verification (Preferred)
    # =======================================================
    file_verification_success = False
    result_data = {}
    
    if copy_from_env:
        temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
        try:
            # Try to copy the result JSON generated by export_result.sh
            # Note: Path must be accessible by the container runtime
            copy_from_env("/sdcard/task_result.json", temp_file.name)
            
            with open(temp_file.name, 'r') as f:
                result_data = json.load(f)
                
            # Check file existence and creation
            if result_data.get('output_exists'):
                score += 10
                feedback_parts.append("Result file exists.")
                
                if result_data.get('created_during_task'):
                    score += 10
                    feedback_parts.append("File created during task.")
                else:
                    feedback_parts.append("File timestamp too old.")
                
                # Check Content
                content = str(result_data.get('output_content', '')).strip()
                # Clean non-numeric characters (except dot)
                numeric_str = re.sub(r'[^\d.]', '', content)
                
                try:
                    val = float(numeric_str)
                    diff = abs(val - expected_val)
                    if diff <= tolerance:
                        score += 30
                        feedback_parts.append(f"Value correct ({val}).")
                        file_verification_success = True
                    else:
                        feedback_parts.append(f"Value incorrect. Got {val}, expected ~{expected_val}.")
                except ValueError:
                    feedback_parts.append(f"Could not parse numeric value from file: '{content}'.")
            else:
                feedback_parts.append("Result file not found.")
                
        except Exception as e:
            logger.warning(f"File copy failed (expected if env doesn't expose /sdcard directly): {e}")
            feedback_parts.append("Could not retrieve result file.")
        finally:
            if os.path.exists(temp_file.name):
                os.unlink(temp_file.name)
    else:
        feedback_parts.append("Copy function unavailable.")

    # =======================================================
    # 2. VLM Verification (Trajectory & Final State)
    # =======================================================
    # We use VLM to verify if the file check failed OR to supplement the score
    # (Checking if the user actually used the app correctly)
    
    frames = sample_trajectory_frames(traj, n=4)
    final_screen = get_final_screenshot(traj)
    
    if not final_screen:
        return {"passed": False, "score": 0, "feedback": "No screenshots available"}
        
    all_images = frames + [final_screen]
    
    prompt = f"""
    You are verifying an Android task for an electrical engineering app.
    
    Goal: Calculate PT Primary Voltage.
    Inputs visible should be:
    - Primary Nominal: 132000 (or 132k)
    - Secondary Nominal: 110
    - Measured Secondary: 108
    
    Expected Result: ~129600
    
    Analyze the screenshots:
    1. Did the user navigate to a 'PT' or 'Potential Transformer' calculator?
    2. Are the input values (132000, 110, 108) visible?
    3. Is the result (129600) visible on the screen?
    
    Return JSON:
    {{
      "calculator_opened": boolean,
      "inputs_correct": boolean,
      "result_visible": boolean,
      "result_value": number_or_null
    }}
    """
    
    vlm_response = query_vlm(images=all_images, prompt=prompt)
    
    if vlm_response.get('success'):
        analysis = vlm_response.get('parsed', {})
        
        if analysis.get('calculator_opened'):
            score += 15
            feedback_parts.append("Correct calculator opened.")
        
        if analysis.get('inputs_correct'):
            score += 15
            feedback_parts.append("Input values verified visualy.")
            
        if analysis.get('result_visible'):
            # If we already verified via file, don't double count too much, but give credit for UI state
            if not file_verification_success:
                score += 30
                feedback_parts.append("Result visible on screen.")
            else:
                score += 10 # Bonus for seeing it
                
        # Check visually extracted value if file failed
        vis_val = analysis.get('result_value')
        if not file_verification_success and vis_val is not None:
             if abs(vis_val - expected_val) <= tolerance:
                 score += 10 # Partial credit for seeing it even if file save failed
                 feedback_parts.append("Visual result matches expectation.")

    # Final Check
    # Pass if score >= 70 AND (File is correct OR Visual result is clear)
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": min(100, score),
        "feedback": " | ".join(feedback_parts)
    }