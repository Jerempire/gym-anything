# ── Gather all results via Python ─────────────────────────────────────────────
python3 << 'PYEOF'
import json, os, sqlite3, glob

result = {
    "task": "web_browser_artifact_triage",
    "case_db_found": False,
    "case_name_matches": False,
    "data_source_added": False,
    "recent_activity_run": False,
    "db_search_count": 0,
    "db_download_count": 0,
    "searches_file_exists": False,
    "searches_mtime": 0,
    "searches_content": "",
    "downloads_file_exists": False,
    "downloads_mtime": 0,
    "downloads_content": "",
    "summary_file_exists": False,
    "summary_content": "",
    "start_time": 0,
    "error": ""
}

try:
    with open("/tmp/web_browser_start_time") as f:
        result["start_time"] = int(f.read().strip())
except Exception:
    pass

# Locate autopsy.db
db_paths = glob.glob("/home/ga/Cases/DarkWeb_Investigation_2024*/autopsy.db")
if not db_paths:
    result["error"] = "autopsy.db not found for case DarkWeb_Investigation_2024"
    with open("/tmp/web_browser_result.json", "w") as f:
        json.dump(result, f, indent=2)
    import sys; sys.exit(0)

db_path = db_paths[0]
print(f"Found DB: {db_path}")
result["case_db_found"] = True
result["case_name_matches"] = True

try:
    conn = sqlite3.connect(f"file:{db_path}?mode=ro", uri=True)
    conn.row_factory = sqlite3.Row
    cur = conn.cursor()

    # Check logical data source was added
    try:
        cur.execute("SELECT COUNT(*) FROM data_source_info")
        result["data_source_added"] = cur.fetchone()[0] > 0
    except Exception:
        pass

    # Check for Web Searches and Web Downloads artifacts generated by Autopsy
    try:
        cur.execute("""
            SELECT bat.type_name, COUNT(ba.artifact_id) as cnt
            FROM blackboard_artifact_types bat
            LEFT JOIN blackboard_artifacts ba ON bat.artifact_type_id = ba.artifact_type_id
            WHERE bat.type_name IN ('TSK_WEB_SEARCH', 'TSK_WEB_DOWNLOAD')
            GROUP BY bat.type_name
        """)
        for row in cur.fetchall():
            if row["type_name"] == "TSK_WEB_SEARCH":
                result["db_search_count"] = row["cnt"]
            elif row["type_name"] == "TSK_WEB_DOWNLOAD":
                result["db_download_count"] = row["cnt"]
        
        if result["db_search_count"] > 0 or result["db_download_count"] > 0:
            result["recent_activity_run"] = True
    except Exception as e:
        result["error"] += f" | DB Artifact query error: {e}"

    conn.close()
except Exception as e:
    result["error"] += f" | DB open error: {e}"

# Check Searches report file
searches_path = "/home/ga/Reports/web_searches.txt"
if os.path.exists(searches_path):
    result["searches_file_exists"] = True
    result["searches_mtime"] = int(os.path.getmtime(searches_path))
    with open(searches_path, "r", errors="replace") as f:
        result["searches_content"] = f.read(16384)

# Check Downloads report file
downloads_path = "/home/ga/Reports/web_downloads.txt"
if os.path.exists(downloads_path):
    result["downloads_file_exists"] = True
    result["downloads_mtime"] = int(os.path.getmtime(downloads_path))
    with open(downloads_path, "r", errors="replace") as f:
        result["downloads_content"] = f.read(16384)

# Check Summary file
summary_path = "/home/ga/Reports/browser_summary.txt"
if os.path.exists(summary_path):
    result["summary_file_exists"] = True
    with open(summary_path, "r", errors="replace") as f:
        result["summary_content"] = f.read(2048)

print(json.dumps(result, indent=2))
with open("/tmp/web_browser_result.json", "w") as f:
    json.dump(result, f, indent=2)
print("Result written to /tmp/web_browser_result.json")
PYEOF

echo "=== Export complete ==="