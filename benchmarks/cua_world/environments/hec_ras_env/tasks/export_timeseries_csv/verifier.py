#!/usr/bin/env python3
"""
Verifier for export_timeseries_csv task.
Scores based on JSON report generated by export_result.sh running inside the container.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_export_timeseries_csv(traj, env_info, task_info):
    """
    Verify that HEC-RAS time series were correctly exported to CSV.
    
    Criteria:
    1. Files exist and were created during task (Anti-gaming).
    2. CSV structure is valid (Headers, Numeric data).
    3. Data values are physically reasonable (Range checks).
    4. Data matches HDF5 ground truth (Summary statistics check).
    """
    
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}
        
    # Copy result JSON from container
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve verification results: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)
            
    # Calculate Score
    score = 0
    feedback = []
    
    # 1. Existence & Timestamp (20 pts)
    if result.get("flow_exists") and result.get("wse_exists"):
        score += 10
        feedback.append("Both CSV files found.")
        if result.get("flow_created_during_task") and result.get("wse_created_during_task"):
            score += 10
            feedback.append("Files created during task window.")
        else:
            feedback.append("WARNING: Files have old timestamps (pre-existing?).")
    else:
        feedback.append("Missing one or both output CSV files.")
        
    # 2. Structure (20 pts)
    if result.get("flow_columns_valid") and result.get("wse_columns_valid"):
        score += 20
        feedback.append("CSV columns (Time + Stations) appear valid.")
    elif result.get("flow_columns_valid") or result.get("wse_columns_valid"):
        score += 10
        feedback.append("One file has valid columns, one missing/invalid.")
    else:
        feedback.append("CSV column structure invalid (missing Time or Station columns).")

    # 3. Data Reasonableness (20 pts)
    if result.get("flow_data_reasonable") and result.get("wse_data_reasonable"):
        score += 20
        feedback.append("Data values are within reasonable physical ranges.")
    else:
        feedback.append("Data values contain errors or are out of physical range.")
        
    # 4. Consistency (monotonicity + row counts) (10 pts)
    if result.get("time_monotonic") and result.get("row_count_match"):
        score += 10
        feedback.append("Time series is monotonic and files match in length.")
    
    # 5. HDF5 Match (30 pts)
    match_score = result.get("hdf_match_score", 0.0)
    # match_score is 0.0, 0.5, or 1.0 based on max flow and mean wse checks
    points_from_match = int(match_score * 30)
    score += points_from_match
    if match_score == 1.0:
        feedback.append("Data matches HDF5 source statistics perfectly.")
    elif match_score > 0:
        feedback.append("Data matches HDF5 source statistics partially.")
    else:
        feedback.append("Data does NOT match HDF5 source statistics.")

    # Report Errors
    if result.get("errors"):
        feedback.append(f"Verification Errors: {'; '.join(result['errors'])}")

    passed = score >= 60
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }