#!/usr/bin/env python3
"""
Verifier for generate_model_config_report task.

Checks if the agent correctly parsed the HEC-RAS input files (.prj, .p04)
and extracted the specific randomized values injected during setup.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_generate_model_config_report(traj, env_info, task_info):
    """
    Verify the generated model configuration JSON report.
    """
    # 1. Setup access to container files
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Copy the result JSON generated by export_result.sh
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read task result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # 2. Extract Data
    output_exists = result.get('output_exists', False)
    created_during_task = result.get('file_created_during_task', False)
    agent_output = result.get('agent_output', {})
    ground_truth = result.get('ground_truth', {})

    if not output_exists:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Output file model_config.json was not found."
        }

    if not isinstance(agent_output, dict):
        return {
            "passed": False,
            "score": 10,
            "feedback": "Output file exists but contains invalid JSON (not a dictionary)."
        }

    # 3. Scoring Logic
    score = 0
    feedback_parts = []
    
    # Base points for creating the file
    if output_exists:
        score += 10
        feedback_parts.append("File created")

    # Check Project Title (from .prj)
    # Allow some fuzzy matching (stripping quotes/whitespace)
    agent_proj = str(agent_output.get('project_title', '')).strip().strip('"').strip("'")
    gt_proj = str(ground_truth.get('project_title', '')).strip()
    
    if agent_proj == gt_proj and agent_proj != "":
        score += 15
        feedback_parts.append("Project Title Correct")
    else:
        feedback_parts.append(f"Project Title mismatch (Expected: '{gt_proj}', Got: '{agent_proj}')")

    # Check Plan Title (from .p04)
    agent_plan = str(agent_output.get('plan_title', '')).strip().strip('"').strip("'")
    gt_plan = str(ground_truth.get('plan_title', '')).strip()
    
    if agent_plan == gt_plan and agent_plan != "":
        score += 15
        feedback_parts.append("Plan Title Correct")
    else:
        feedback_parts.append(f"Plan Title mismatch (Expected: '{gt_plan}', Got: '{agent_plan}')")

    # Check Start Date
    agent_start = str(agent_output.get('simulation_start_date', '')).strip()
    gt_start = str(ground_truth.get('simulation_start_date', '')).strip()
    
    if agent_start == gt_start and agent_start != "":
        score += 12.5
        feedback_parts.append("Start Date Correct")
    else:
        feedback_parts.append(f"Start Date mismatch (Expected: '{gt_start}', Got: '{agent_start}')")

    # Check End Date
    agent_end = str(agent_output.get('simulation_end_date', '')).strip()
    gt_end = str(ground_truth.get('simulation_end_date', '')).strip()
    
    if agent_end == gt_end and agent_end != "":
        score += 12.5
        feedback_parts.append("End Date Correct")
    else:
        feedback_parts.append(f"End Date mismatch (Expected: '{gt_end}', Got: '{agent_end}')")

    # Check Computation Interval
    agent_interval = str(agent_output.get('computation_interval', '')).strip()
    gt_interval = str(ground_truth.get('computation_interval', '')).strip()
    
    if agent_interval == gt_interval and agent_interval != "":
        score += 15
        feedback_parts.append("Interval Correct")
    else:
        feedback_parts.append(f"Interval mismatch (Expected: '{gt_interval}', Got: '{agent_interval}')")

    # Check Geometry File Ext
    agent_geom = str(agent_output.get('geometry_file_extension', '')).strip()
    gt_geom = str(ground_truth.get('geometry_file_extension', '')).strip()
    
    if agent_geom == gt_geom and agent_geom != "":
        score += 10
        feedback_parts.append("Geom Ext Correct")
    else:
        feedback_parts.append(f"Geom Ext mismatch (Expected: '{gt_geom}', Got: '{agent_geom}')")

    # Anti-gaming check: File creation time
    if created_during_task:
        score += 10
        feedback_parts.append("Freshly created")
    else:
        feedback_parts.append("File timestamp pre-dates task start")

    # Final Pass Determination
    # Must get the dynamic values correct (Title, Date, Interval) to pass
    key_fields_correct = (
        agent_plan == gt_plan and 
        agent_start == gt_start and 
        agent_end == gt_end and 
        agent_interval == gt_interval
    )
    
    passed = (score >= 70) and key_fields_correct

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }