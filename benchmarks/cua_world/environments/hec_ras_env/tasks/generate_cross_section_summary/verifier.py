#!/usr/bin/env python3
"""
Verifier for generate_cross_section_summary task.
Compares agent's CSV output against ground truth extracted from HDF5 file.
"""

import json
import os
import tempfile
import pandas as pd
import numpy as np
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_cross_section_summary(traj, env_info, task_info):
    """
    Verify the CSV summary table generated by the agent.
    
    Criteria:
    1. File existence and valid CSV format (5 pts)
    2. Created during task (Anti-gaming) (5 pts)
    3. Correct columns present (10 pts)
    4. Data accuracy (River Station, WSE, Flow, Velocity, Depth) (75 pts)
    5. Sorting (5 pts)
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Temp files for copying
    result_json_path = tempfile.mktemp(suffix='.json')
    gt_json_path = tempfile.mktemp(suffix='.json')
    csv_path = tempfile.mktemp(suffix='.csv')

    try:
        # 1. Load Metadata / Task Result
        copy_from_env("/tmp/task_result.json", result_json_path)
        with open(result_json_path, 'r') as f:
            task_result = json.load(f)

        if not task_result.get("output_exists"):
            return {
                "passed": False, 
                "score": 0, 
                "feedback": "Output CSV file was not found."
            }
            
        if not task_result.get("file_created_during_task"):
             return {
                "passed": False, 
                "score": 0, 
                "feedback": "Output CSV file was not created/modified during the task session."
            }

        # 2. Load Ground Truth
        if not task_result.get("ground_truth_available"):
            # This implies the setup/export script failed to generate GT
            return {"passed": False, "score": 0, "feedback": "System Error: Ground truth could not be generated from HDF file."}
        
        copy_from_env("/tmp/ground_truth.json", gt_json_path)
        with open(gt_json_path, 'r') as f:
            gt_data = json.load(f)
            
        if not gt_data.get("valid"):
            return {"passed": False, "score": 0, "feedback": f"System Error: Ground truth extraction failed: {gt_data.get('error')}"}
        
        gt_df = pd.DataFrame(gt_data["cross_sections"])
        
        # 3. Load Agent CSV
        copy_from_env("/tmp/agent_output.csv", csv_path)
        try:
            agent_df = pd.read_csv(csv_path)
        except Exception as e:
            return {"passed": False, "score": 5, "feedback": f"CSV file exists but could not be parsed: {e}"}

        # --- SCORING ---
        score = 10 # Base score for file existing and parseable
        feedback = []

        # Check Columns
        required_cols = [
            "River_Station", "Min_Ch_El_ft", "Peak_WSE_ft", 
            "Max_Depth_ft", "Peak_Flow_cfs", "Peak_Vel_fps"
        ]
        
        missing_cols = [c for c in required_cols if c not in agent_df.columns]
        if missing_cols:
            feedback.append(f"Missing columns: {missing_cols}")
        else:
            score += 10
            feedback.append("All required columns present.")

        # Check Row Count
        if len(agent_df) != len(gt_df):
            feedback.append(f"Row count mismatch: Expected {len(gt_df)}, got {len(agent_df)}")
        else:
            score += 10
            feedback.append("Row count correct.")

        # Check Data Accuracy
        # We need to align by River_Station to compare values
        # River Station might be string or float in CSV
        
        # Normalize River Station for merging
        agent_df['River_Station'] = agent_df['River_Station'].astype(str).str.strip()
        gt_df['River_Station'] = gt_df['River_Station'].astype(str).str.strip()
        
        merged = pd.merge(agent_df, gt_df, on='River_Station', suffixes=('_agent', '_gt'), how='inner')
        
        matched_rows = len(merged)
        if matched_rows < len(gt_df) * 0.9: # Allow small mismatch if formatting differs wildly
            feedback.append(f"Could only match {matched_rows} of {len(gt_df)} river stations.")
        else:
            score += 10
            feedback.append("River stations matched successfully.")

        if matched_rows > 0:
            # Tolerances
            # Elevation/Depth: +/- 0.5 ft
            # Flow: +/- 5%
            # Velocity: +/- 10%
            
            # Min Ch El
            diff_el = np.abs(merged['Min_Ch_El_ft_agent'] - merged['Min_Ch_El_ft_gt'])
            valid_el = (diff_el <= 0.5).mean()
            score += 15 * valid_el
            
            # Peak WSE
            diff_wse = np.abs(merged['Peak_WSE_ft_agent'] - merged['Peak_WSE_ft_gt'])
            valid_wse = (diff_wse <= 0.5).mean()
            score += 15 * valid_wse
            
            # Peak Flow
            # Avoid division by zero
            gt_flow = merged['Peak_Flow_cfs_gt'].replace(0, 0.001) 
            diff_flow_pct = np.abs((merged['Peak_Flow_cfs_agent'] - merged['Peak_Flow_cfs_gt']) / gt_flow)
            valid_flow = (diff_flow_pct <= 0.05).mean()
            score += 15 * valid_flow
            
            # Peak Vel
            gt_vel = merged['Peak_Vel_fps_gt'].replace(0, 0.001)
            diff_vel_pct = np.abs((merged['Peak_Vel_fps_agent'] - merged['Peak_Vel_fps_gt']) / gt_vel)
            valid_vel = (diff_vel_pct <= 0.10).mean()
            score += 10 * valid_vel
            
            # Computed Depth Check (Internal Consistency)
            # Depth = WSE - MinEl
            computed_depth = merged['Peak_WSE_ft_agent'] - merged['Min_Ch_El_ft_agent']
            diff_depth_consistency = np.abs(merged['Max_Depth_ft_agent'] - computed_depth)
            valid_consistency = (diff_depth_consistency <= 0.1).mean()
            score += 10 * valid_consistency
            
            feedback.append(f"Data Accuracy: El={valid_el:.1%}, WSE={valid_wse:.1%}, Flow={valid_flow:.1%}, Vel={valid_vel:.1%}")

        # Sorting Check
        # Should be descending by station (upstream to downstream is typical, or usually numeric descending)
        # We assume River Station is numeric-ish
        try:
            # Convert to float for sorting check
            stations_float = pd.to_numeric(agent_df['River_Station'], errors='coerce')
            if stations_float.is_monotonic_decreasing:
                score += 5
                feedback.append("Sorted descending correctly.")
            elif stations_float.is_monotonic_increasing:
                feedback.append("Sorted ascending (descending expected).")
        except:
            pass

        final_score = int(score)
        passed = final_score >= 60 and matched_rows > 0

        return {
            "passed": passed,
            "score": final_score,
            "feedback": " ".join(feedback)
        }

    except Exception as e:
        logger.error(f"Verification error: {e}")
        return {"passed": False, "score": 0, "feedback": f"Verification failed with error: {str(e)}"}

    finally:
        # Cleanup
        for f in [result_json_path, gt_json_path, csv_path]:
            if os.path.exists(f):
                os.unlink(f)