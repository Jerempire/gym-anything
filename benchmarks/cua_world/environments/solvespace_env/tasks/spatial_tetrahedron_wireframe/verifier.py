#!/usr/bin/env python3
"""
Verifier for spatial_tetrahedron_wireframe task.

This verifier uses robust programmatic checks by parsing the SolveSpace `.slvs` text file:
1. Validates that the file was created during the task session.
2. Extracts all evaluated 3D point coordinates generated by the SolveSpace geometric solver.
3. Mathematically verifies that at least one point is anchored to (0,0,0).
4. Iterates through combinations of points to find exactly 4 points where all 6 pairwise 
   distances equal exactly 50.0 mm (proving a perfect, constrained regular tetrahedron exists).
5. Samples trajectory frames with VLM to ensure the agent actively used the software GUI.
"""

import json
import tempfile
import os
import logging
import math
import itertools
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def extract_points_from_slvs(slvs_text):
    """
    Parses a SolveSpace text file (.slvs) line by line to extract the evaluated 
    actPoint coordinates computed by the constraint solver.
    """
    points = []
    current_x, current_y, current_z = None, None, None
    
    for line in slvs_text.split('\n'):
        line = line.strip()
        if line.startswith('Entity.actPoint.x='):
            current_x = float(line.split('=')[1])
        elif line.startswith('Entity.actPoint.y='):
            current_y = float(line.split('=')[1])
        elif line.startswith('Entity.actPoint.z='):
            current_z = float(line.split('=')[1])
        elif line.startswith('AddEntity'):
            # When an entity block ends, append the point if it had coordinate data
            if current_x is not None or current_y is not None:
                points.append((
                    current_x if current_x is not None else 0.0,
                    current_y if current_y is not None else 0.0,
                    current_z if current_z is not None else 0.0
                ))
            # Reset for the next entity
            current_x, current_y, current_z = None, None, None
            
    return points


def verify_spatial_tetrahedron_wireframe(traj, env_info, task_info):
    # Retrieve required tools
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: copy_from_env not available."}

    metadata = task_info.get('metadata', {})
    expected_output_path = metadata.get('expected_output_file', '/home/ga/Documents/SolveSpace/tetrahedron.slvs')
    expected_edge = metadata.get('expected_edge_length', 50.0)

    feedback = []
    score = 0
    max_score = 100

    # 1. READ RESULT JSON
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    result = {}
    try:
        copy_from_env("/tmp/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result JSON: {e}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    # 2. FILE VERIFICATION
    output_exists = result.get("output_exists", False)
    file_created = result.get("file_created_during_task", False)

    if not output_exists:
        return {"passed": False, "score": 0, "feedback": "FAIL: Output file 'tetrahedron.slvs' was not saved."}
        
    if not file_created:
        feedback.append("WARNING: File timestamps suggest it was not created during the task window.")
    else:
        score += 10
        feedback.append("File created successfully.")

    # 3. GEOMETRY VERIFICATION (SLVS PARSING)
    temp_slvs = tempfile.NamedTemporaryFile(delete=False, suffix='.slvs')
    slvs_text = ""
    try:
        copy_from_env(expected_output_path, temp_slvs.name)
        with open(temp_slvs.name, 'r', encoding='utf-8', errors='ignore') as f:
            slvs_text = f.read()
    except Exception as e:
        feedback.append(f"Failed to read SLVS file contents: {e}")
        return {"passed": False, "score": score, "feedback": " | ".join(feedback)}
    finally:
        if os.path.exists(temp_slvs.name):
            os.unlink(temp_slvs.name)

    points = extract_points_from_slvs(slvs_text)
    
    # Filter duplicate points (within 0.001mm tolerance)
    unique_points = []
    for p in points:
        if not any(math.dist(p, up) < 0.001 for up in unique_points):
            unique_points.append(p)

    feedback.append(f"Found {len(unique_points)} unique evaluated points.")

    # Check for origin anchor
    has_origin = any(math.dist(p, (0, 0, 0)) < 0.1 for p in unique_points)
    if has_origin:
        score += 20
        feedback.append("Model is correctly anchored to the origin (0,0,0).")
    else:
        feedback.append("Model is NOT anchored to the origin.")

    # Search for tetrahedron mathematically
    tetrahedron_found = False
    if len(unique_points) >= 4:
        score += 10
        feedback.append("File has at least 4 unique spatial points.")
        
        # Test all possible subsets of 4 points
        for subset in itertools.combinations(unique_points, 4):
            # Calculate all 6 pairwise distances in the subset
            dists = [math.dist(p1, p2) for p1, p2 in itertools.combinations(subset, 2)]
            
            # Check if all 6 edges match the expected length (with 0.5mm tolerance)
            if all(abs(d - expected_edge) < 0.5 for d in dists):
                tetrahedron_found = True
                break
                
    if tetrahedron_found:
        score += 40
        feedback.append(f"SUCCESS: Mathematical verification confirms a perfect regular tetrahedron with {expected_edge}mm edges!")
    else:
        feedback.append("FAIL: Did not find 4 points forming a regular tetrahedron with the correct edge lengths.")

    # 4. VLM VERIFICATION (Trajectory check for UI usage)
    if query_vlm:
        frames = sample_trajectory_frames(traj, n=3)
        final = get_final_screenshot(traj)
        images = frames + [final] if final else frames
        
        if images:
            prompt = (
                "Look at these screenshots of a user interacting with SolveSpace CAD software. "
                "Did the user actively sketch a 3D wireframe shape (such as a tetrahedron or pyramid) in the workspace? "
                "Respond strictly with JSON format: {\"wireframe_sketched\": true/false}"
            )
            try:
                vlm_res = query_vlm(images=images, prompt=prompt)
                parsed = vlm_res.get("parsed", {})
                if parsed.get("wireframe_sketched", False):
                    score += 20
                    feedback.append("VLM confirms 3D sketching activity in the UI.")
                else:
                    feedback.append("VLM could not visually confirm 3D sketching activity.")
            except Exception as e:
                feedback.append(f"VLM check failed: {e}")
    else:
        # If no VLM is available, auto-grant these points if the mathematical constraints solved perfectly
        if tetrahedron_found:
            score += 20
            feedback.append("No VLM available; granting UI points based on mathematical perfection.")

    # Determine final pass status
    key_criteria_met = file_created and output_exists and tetrahedron_found
    passed = (score >= 80) and key_criteria_met

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback),
        "details": {
            "unique_points_count": len(unique_points),
            "origin_anchored": has_origin,
            "tetrahedron_found": tetrahedron_found
        }
    }