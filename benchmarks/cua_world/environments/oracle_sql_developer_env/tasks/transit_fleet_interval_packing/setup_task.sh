#!/bin/bash
echo "=== Setting up Transit Fleet Interval Packing task ==="

source /workspace/scripts/task_utils.sh

# Record task start time for anti-gaming checks
date +%s > /home/ga/.task_start_time
mkdir -p /home/ga/Documents/exports
chown -R ga:ga /home/ga/Documents/exports

# Verify Oracle is running
CONTAINER_STATUS=$(sudo docker inspect --format='{{.State.Status}}' oracle-xe 2>/dev/null)
if [ "$CONTAINER_STATUS" != "running" ]; then
    echo "ERROR: Oracle container not running"
    exit 1
fi

# Clean up previous runs
echo "Cleaning up schema..."
oracle_query "BEGIN
  EXECUTE IMMEDIATE 'DROP USER transit_ops CASCADE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
EXIT;" "system" 2>/dev/null || true
sleep 2

# Create schema
echo "Creating TRANSIT_OPS schema..."
oracle_query "CREATE USER transit_ops IDENTIFIED BY Transit2024
  DEFAULT TABLESPACE users
  TEMPORARY TABLESPACE temp
  QUOTA UNLIMITED ON users;
GRANT CONNECT, RESOURCE, CREATE VIEW, CREATE PROCEDURE TO transit_ops;
EXIT;" "system"

# Create tables and inject data
echo "Creating tables and injecting data..."
sudo docker exec -i oracle-xe sqlplus -s transit_ops/Transit2024@//localhost:1521/XEPDB1 << 'EOSQL'
SET ECHO OFF FEEDBACK ON

