    locomotive_id NUMBER PRIMARY KEY,
    model VARCHAR2(50),
    in_service_date DATE,
    fleet_type VARCHAR2(50),
    base_yard VARCHAR2(50)
);

CREATE TABLE mechanics (
    mechanic_id NUMBER PRIMARY KEY,
    first_name VARCHAR2(50),
    last_name VARCHAR2(50),
    certification_level VARCHAR2(20),
    shift VARCHAR2(20)
);

CREATE TABLE maintenance_logs (
    log_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    locomotive_id NUMBER REFERENCES locomotives(locomotive_id),
    mechanic_id NUMBER REFERENCES mechanics(mechanic_id),
    start_time DATE,
    end_time DATE,
    work_order_type VARCHAR2(50),
    notes VARCHAR2(200)
);

