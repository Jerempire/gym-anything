#!/bin/bash
echo "=== Setting up Clinical Trial Safety Analysis Task ==="

source /workspace/scripts/task_utils.sh

# Record task start time
date +%s > /home/ga/.task_start_time

# Verify Oracle is running
CONTAINER_STATUS=$(sudo docker inspect --format='{{.State.Status}}' oracle-xe 2>/dev/null)
if [ "$CONTAINER_STATUS" != "running" ]; then
    echo "ERROR: Oracle container not running"
    exit 1
fi
echo "Oracle container is running"

# Clean up any previous runs
echo "Cleaning up schema..."
oracle_query "BEGIN
  EXECUTE IMMEDIATE 'DROP USER trial_dm CASCADE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
EXIT;" "system" 2>/dev/null || true
sleep 2

# Create schema and grant privileges
echo "Creating TRIAL_DM schema..."
oracle_query "CREATE USER trial_dm IDENTIFIED BY Trial2024
  DEFAULT TABLESPACE users
  TEMPORARY TABLESPACE temp
  QUOTA UNLIMITED ON users;
GRANT CONNECT TO trial_dm;
GRANT RESOURCE TO trial_dm;
GRANT CREATE VIEW TO trial_dm;
GRANT CREATE MATERIALIZED VIEW TO trial_dm;
GRANT CREATE PROCEDURE TO trial_dm;
GRANT CREATE SESSION TO trial_dm;
GRANT CREATE SEQUENCE TO trial_dm;
EXIT;" "system"

# Create tables
echo "Creating Clinical Trial Data Model tables..."
sudo docker exec -i oracle-xe sqlplus -s trial_dm/Trial2024@//localhost:1521/XEPDB1 << 'EOSQL'
SET ECHO OFF FEEDBACK OFF

CREATE TABLE study (
    study_id          NUMBER PRIMARY KEY,
    protocol_number   VARCHAR2(50) NOT NULL,
    phase             VARCHAR2(20),
    therapeutic_area  VARCHAR2(100),
    sponsor           VARCHAR2(100)
);

CREATE TABLE sites (
    site_id           NUMBER PRIMARY KEY,
    site_number       VARCHAR2(20) NOT NULL,
    investigator_name VARCHAR2(100),
    country           VARCHAR2(50)
);

CREATE TABLE treatment_arms (
    arm_id            NUMBER PRIMARY KEY,
    arm_name          VARCHAR2(100) NOT NULL,
    drug_name         VARCHAR2(100),
    is_placebo        NUMBER(1) DEFAULT 0
);

CREATE TABLE subjects (
    subject_id        NUMBER PRIMARY KEY,
    subject_number    VARCHAR2(20) UNIQUE NOT NULL,
    site_id           NUMBER REFERENCES sites(site_id),
    arm_id            NUMBER REFERENCES treatment_arms(arm_id),
    randomization_date DATE,
    age               NUMBER,
    sex               VARCHAR2(1)
);

CREATE TABLE visits (
    visit_id          NUMBER PRIMARY KEY,
    subject_id        NUMBER REFERENCES subjects(subject_id),
    visit_number      NUMBER NOT NULL,
    planned_date      DATE NOT NULL,
    actual_date       DATE,
    visit_status      VARCHAR2(20)
);

CREATE TABLE treatment_exposure (
    exposure_id       NUMBER PRIMARY KEY,
    subject_id        NUMBER REFERENCES subjects(subject_id),
    first_dose_date   DATE,
    last_dose_date    DATE
);

CREATE TABLE adverse_events (
    ae_id             NUMBER PRIMARY KEY,
    subject_id        NUMBER REFERENCES subjects(subject_id),
    ae_term           VARCHAR2(200),
    meddra_pt         VARCHAR2(200),
    meddra_soc        VARCHAR2(200),
    onset_date        DATE,
    severity          VARCHAR2(20)
);

CREATE TABLE lab_results (
    lab_id            NUMBER PRIMARY KEY,
    subject_id        NUMBER REFERENCES subjects(subject_id),
    visit_number      NUMBER,
    test_code         VARCHAR2(20),
    test_name         VARCHAR2(100),
    result_value      NUMBER(10,2),
    normal_low        NUMBER(10,2),
    normal_high       NUMBER(10,2)
);

CREATE TABLE protocol_deviations (
    deviation_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subject_id        NUMBER,
    visit_id          NUMBER,
    deviation_type    VARCHAR2(100),
    description       VARCHAR2(500)
);
EXIT;
EOSQL

# Generate highly realistic clinical dataset using PL/SQL (~8000+ rows)
echo "Generating clinical dataset..."
sudo docker exec -i oracle-xe sqlplus -s trial_dm/Trial2024@//localhost:1521/XEPDB1 << 'EOSQL'
SET SERVEROUTPUT ON
DECLARE
    v_sub_id NUMBER;
    v_site_id NUMBER;
    v_arm_id NUMBER;
    v_rand_date DATE;
    v_plan_date DATE;
    v_actual_date DATE;
    v_ae_count NUMBER;
    v_pt VARCHAR2(100);
    v_soc VARCHAR2(100);
    v_lab_val NUMBER;
    v_base_val NUMBER;
BEGIN
    -- Reference Data
    INSERT INTO study VALUES (1, 'RVX4010-301', 'Phase III', 'Cardiovascular', 'PharmaCo');
    
    INSERT INTO sites VALUES (101, 'S-101', 'Dr. Smith', 'USA');
    INSERT INTO sites VALUES (102, 'S-102', 'Dr. Jones', 'USA');
    INSERT INTO sites VALUES (103, 'S-103', 'Dr. Garcia', 'Spain');
    
    INSERT INTO treatment_arms VALUES (1, 'RVX-4010 20MG', 'Rivaroxaban-analog', 0);
    INSERT INTO treatment_arms VALUES (2, 'PLACEBO', 'Placebo', 1);

    -- Generate 200 Subjects
    FOR i IN 1..200 LOOP
        v_site_id := CASE MOD(i, 3) WHEN 0 THEN 101 WHEN 1 THEN 102 ELSE 103 END;
        v_arm_id := CASE WHEN MOD(i, 2) = 0 THEN 1 ELSE 2 END; -- 1:1 Randomization
        v_rand_date := DATE '2023-01-01' + DBMS_RANDOM.VALUE(0, 180);
        
        INSERT INTO subjects VALUES (i, 'SUBJ-' || LPAD(i, 4, '0'), v_site_id, v_arm_id, v_rand_date, ROUND(DBMS_RANDOM.VALUE(45, 80)), CASE WHEN DBMS_RANDOM.VALUE > 0.5 THEN 'M' ELSE 'F' END);
        
        INSERT INTO treatment_exposure VALUES (i, i, v_rand_date + 1, v_rand_date + 180);
        
        -- Generate 6 Visits per subject
        FOR v IN 1..6 LOOP
            v_plan_date := v_rand_date + (v-1)*30;
            v_actual_date := v_plan_date + ROUND(DBMS_RANDOM.VALUE(-3, 3));
            
            -- Plant deviations
            IF i = 10 AND v = 3 THEN v_actual_date := NULL; -- Missing
            ELSIF i = 25 AND v = 2 THEN v_actual_date := v_plan_date + 15; -- Out of window
            ELSIF i = 42 AND v = 4 THEN v_actual_date := v_plan_date - 40; -- Sequence violation (v4 before v3)
            ELSIF i = 80 AND v > 4 THEN v_actual_date := NULL; -- Dropout
            ELSIF i = 150 AND v = 5 THEN v_actual_date := v_plan_date - 12; -- Out of window early
            END IF;
            
            INSERT INTO visits VALUES ((i-1)*6 + v, i, v, v_plan_date, v_actual_date, CASE WHEN v_actual_date IS NULL THEN 'MISSED' ELSE 'COMPLETED' END);
            
            -- Generate Lab Results
            IF v_actual_date IS NOT NULL THEN
                -- ALT
                v_base_val := ROUND(DBMS_RANDOM.VALUE(15, 35));
                v_lab_val := v_base_val + ROUND(DBMS_RANDOM.VALUE(-5, 5));
                -- Plant shift to HIGH for some subjects on drug
                IF v_arm_id = 1 AND MOD(i, 15) = 0 AND v > 2 THEN v_lab_val := ROUND(DBMS_RANDOM.VALUE(45, 80)); END IF;
                INSERT INTO lab_results VALUES (seq_labs.NEXTVAL, i, v, 'ALT', 'Alanine Aminotransferase', v_lab_val, 10, 40);
                
                -- Hemoglobin
                v_base_val := ROUND(DBMS_RANDOM.VALUE(13.0, 16.0), 1);
                v_lab_val := v_base_val + ROUND(DBMS_RANDOM.VALUE(-1.0, 1.0), 1);
                -- Plant shift to LOW for drug arm bleeding cases
                IF v_arm_id = 1 AND MOD(i, 10) = 0 AND v > 3 THEN v_lab_val := ROUND(DBMS_RANDOM.VALUE(9.0, 11.5), 1); END IF;
                INSERT INTO lab_results VALUES (seq_labs.NEXTVAL, i, v, 'HGB', 'Hemoglobin', v_lab_val, 12.0, 17.5);
            END IF;
        END LOOP;
        
        -- Generate Adverse Events
        v_ae_count := ROUND(DBMS_RANDOM.VALUE(0, 3));
        FOR a IN 1..v_ae_count LOOP
            -- Plant safety signal: Drug arm has more bleeding
            IF v_arm_id = 1 AND DBMS_RANDOM.VALUE < 0.3 THEN
                v_pt := 'Gastrointestinal hemorrhage';
                v_soc := 'Gastrointestinal disorders';
            ELSIF v_arm_id = 1 AND DBMS_RANDOM.VALUE < 0.2 THEN
                v_pt := 'Epistaxis';
                v_soc := 'Respiratory, thoracic and mediastinal disorders';
            ELSE
                IF DBMS_RANDOM.VALUE < 0.5 THEN
                    v_pt := 'Headache';
                    v_soc := 'Nervous system disorders';
                ELSE
                    v_pt := 'Nausea';
                    v_soc := 'Gastrointestinal disorders';
                END IF;
            END IF;
            
            INSERT INTO adverse_events VALUES (seq_aes.NEXTVAL, i, v_pt || ' reported', v_pt, v_soc, v_rand_date + ROUND(DBMS_RANDOM.VALUE(5, 100)), 'MILD');
        END LOOP;
    END LOOP;
    COMMIT;
END;
/
EXIT;
EOSQL
echo "Dataset generated successfully."

# Create sequences for data generation
sudo docker exec -i oracle-xe sqlplus -s trial_dm/Trial2024@//localhost:1521/XEPDB1 << 'EOSQL'
CREATE SEQUENCE seq_labs START WITH 10000;
CREATE SEQUENCE seq_aes START WITH 5000;
EXIT;
EOSQL

# Pre-configure SQL Developer connection for the agent
echo "Configuring SQL Developer connection..."
ensure_hr_connection "Trial Database" "trial_dm" "Trial2024"

# Create a blank CSV file to ensure directory exists and permissions are right
touch /home/ga/safety_summary_report.csv
chown ga:ga /home/ga/safety_summary_report.csv

# Launch SQL Developer
echo "Launching SQL Developer..."
su - ga -c "DISPLAY=:1 JAVA_HOME=/usr/lib/jvm/java-17-openjdk-amd64 JAVA_TOOL_OPTIONS='--add-opens=java.base/java.net=ALL-UNNAMED --add-opens=java.base/java.lang=ALL-UNNAMED --add-opens=java.base/sun.net.www.protocol.jar=ALL-UNNAMED --add-opens=java.base/sun.net.www=ALL-UNNAMED --add-opens=java.desktop/sun.awt=ALL-UNNAMED --add-opens=java.desktop/sun.awt.X11=ALL-UNNAMED -Dsun.java2d.xrender=false -Dsun.java2d.opengl=false' /opt/sqldeveloper/sqldeveloper.sh > /tmp/sqldeveloper.log 2>&1 &"

# Wait and maximize
sleep 25
WID=$(DISPLAY=:1 wmctrl -l | grep -iE "sql developer|oracle sql" | head -1 | awk '{print $1}')
if [ -n "$WID" ]; then
    DISPLAY=:1 wmctrl -ia "$WID" 2>/dev/null || true
    DISPLAY=:1 wmctrl -r :ACTIVE: -b add,maximized_vert,maximized_horz 2>/dev/null || true
    sleep 2
fi

open_hr_connection_in_sqldeveloper

# Take initial screenshot
take_screenshot /tmp/task_initial_state.png

echo "=== Task Setup Complete ==="