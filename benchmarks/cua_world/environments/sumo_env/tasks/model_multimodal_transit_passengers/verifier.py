#!/usr/bin/env python3
"""
Verifier for model_multimodal_transit_passengers task.

Verification Strategy:
1. Valid Pedestrian XML creation: Checks if `pedestrians.rou.xml` exists and is valid. (15 pts)
2. Pedestrian Logic: Checks if at least 10 <person> elements exist with <walk> and <ride>. (30 pts)
3. Config Integration: Checks if `run.sumocfg` was correctly modified to include pedestrians.rou.xml and personinfo.xml. (20 pts)
4. Successful Execution Output: Checks if `personinfo.xml` was actually generated by SUMO and contains valid logs. (20 pts)
5. Anti-Gaming: Checks file timestamps to prevent dummy empty files from scoring. (15 pts)
"""

import json
import os
import tempfile
import xml.etree.ElementTree as ET
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_model_multimodal_transit_passengers(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    min_persons = metadata.get('min_persons', 10)

    score = 0
    feedback_parts = []
    
    # Files to copy
    files_to_copy = {
        "result": "/tmp/task_result.json",
        "ped": "/tmp/pedestrians.rou.xml",
        "cfg": "/tmp/run.sumocfg",
        "out": "/tmp/personinfo.xml"
    }
    
    local_files = {}
    
    for key, container_path in files_to_copy.items():
        temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.xml' if 'xml' in container_path else '.json')
        try:
            copy_from_env(container_path, temp_file.name)
            local_files[key] = temp_file.name
        except Exception as e:
            logger.warning(f"Failed to copy {container_path}: {e}")
            local_files[key] = None

    try:
        # Load JSON result
        if not local_files["result"] or not os.path.exists(local_files["result"]) or os.path.getsize(local_files["result"]) == 0:
            return {"passed": False, "score": 0, "feedback": "Task result JSON missing or empty."}
            
        with open(local_files["result"], 'r') as f:
            result = json.load(f)
            
        # ==========================================
        # Anti-Gaming Check (15 pts)
        # ==========================================
        if result.get("files_modified_during_task"):
            score += 15
            feedback_parts.append("Files modified during task")
        else:
            feedback_parts.append("WARNING: Files not created/modified during task session!")
            return {"passed": False, "score": 0, "feedback": " | ".join(feedback_parts)}
            
        # ==========================================
        # Check 1: Pedestrian Demand File (15 pts) + Check 2: Ped Logic (30 pts)
        # ==========================================
        valid_persons = 0
        has_ride_and_walk = False
        
        if local_files["ped"] and os.path.exists(local_files["ped"]) and os.path.getsize(local_files["ped"]) > 0:
            try:
                tree = ET.parse(local_files["ped"])
                root = tree.getroot()
                
                # Check <routes> root
                score += 15
                feedback_parts.append("Valid XML demand file found")
                
                # Check persons
                persons = root.findall('.//person')
                for p in persons:
                    walks = p.findall('walk')
                    rides = p.findall('ride')
                    
                    if len(walks) >= 1 and len(rides) >= 1:
                        valid_persons += 1
                        has_ride_and_walk = True
                        
                        # Verify ride has lines attribute
                        for r in rides:
                            if 'lines' not in r.attrib:
                                valid_persons -= 1
                                break
                                
                if valid_persons >= min_persons:
                    score += 30
                    feedback_parts.append(f"Found {valid_persons} valid multimodal commuters")
                elif valid_persons > 0:
                    score += int(30 * (valid_persons / min_persons))
                    feedback_parts.append(f"Found {valid_persons}/{min_persons} valid multimodal commuters")
                else:
                    feedback_parts.append("No valid multimodal persons (missing walk/ride/lines)")
            except ET.ParseError:
                feedback_parts.append("Pedestrian file contains invalid XML")
        else:
            feedback_parts.append("Pedestrian file missing or empty")

        # ==========================================
        # Check 3: Config Integration (20 pts)
        # ==========================================
        cfg_integrated = False
        if local_files["cfg"] and os.path.exists(local_files["cfg"]) and os.path.getsize(local_files["cfg"]) > 0:
            try:
                tree = ET.parse(local_files["cfg"])
                root = tree.getroot()
                
                # Check route-files
                route_files = root.find('.//route-files')
                has_ped_route = False
                if route_files is not None and route_files.get('value'):
                    if 'pedestrians.rou.xml' in route_files.get('value'):
                        has_ped_route = True
                
                # Check output config
                person_info = root.find('.//personinfo-output')
                has_person_out = False
                if person_info is not None and person_info.get('value'):
                    if 'personinfo.xml' in person_info.get('value'):
                        has_person_out = True
                        
                if has_ped_route and has_person_out:
                    score += 20
                    cfg_integrated = True
                    feedback_parts.append("Configuration correctly updated")
                else:
                    feedback_parts.append(f"Config issues: route={has_ped_route}, out={has_person_out}")
            except ET.ParseError:
                feedback_parts.append("Config file contains invalid XML")
        else:
            feedback_parts.append("Configuration file not found or empty")

        # ==========================================
        # Check 4: Successful Output Generated (20 pts)
        # ==========================================
        output_successful = False
        if result.get("out_file_exists") and local_files["out"] and os.path.exists(local_files["out"]) and os.path.getsize(local_files["out"]) > 0:
            try:
                tree = ET.parse(local_files["out"])
                root = tree.getroot()
                
                person_infos = root.findall('.//personinfo')
                if len(person_infos) > 0:
                    score += 20
                    output_successful = True
                    feedback_parts.append(f"Simulation generated {len(person_infos)} personinfo records")
                else:
                    feedback_parts.append("personinfo.xml was generated but contains no records")
            except ET.ParseError:
                feedback_parts.append("personinfo.xml generated but invalid XML")
        else:
            feedback_parts.append("personinfo.xml output not generated (simulation likely failed or config was wrong)")

        # Calculate pass/fail
        key_criteria = (valid_persons >= min_persons) and output_successful and cfg_integrated
        passed = (score >= 70) and key_criteria
        
        return {
            "passed": passed,
            "score": score,
            "feedback": " | ".join(feedback_parts)
        }
        
    finally:
        # Cleanup temporary files
        for path in local_files.values():
            if path and os.path.exists(path):
                os.unlink(path)