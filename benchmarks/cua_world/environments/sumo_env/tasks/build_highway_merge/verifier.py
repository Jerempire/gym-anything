#!/usr/bin/env python3
"""
Verifier for build_highway_merge task.

Evaluates the XML files generated by the agent to ensure they conform to SUMO schema, 
contain correct geometries and simulation parameters, and successfully produced valid
tripinfo and detector outputs.
"""

import json
import os
import tempfile
import tarfile
import xml.etree.ElementTree as ET
import logging
import shutil

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_build_highway_merge(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback = []
    
    # Get task_result.json
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    result_data = {}
    try:
        copy_from_env("/tmp/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        feedback.append(f"Failed to read task result metadata: {e}")
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    if not result_data.get('work_dir_exists', False):
        return {"passed": False, "score": 0, "feedback": "Work directory missing. Agent did not create ~/SUMO_Output/highway_merge"}

    # Extract archive of files
    temp_tar = tempfile.NamedTemporaryFile(delete=False, suffix='.tar.gz')
    extract_dir = tempfile.mkdtemp()
    
    try:
        copy_from_env("/tmp/highway_merge_files.tar.gz", temp_tar.name)
        with tarfile.open(temp_tar.name, "r:gz") as tar:
            tar.extractall(path=extract_dir)
    except Exception as e:
        feedback.append(f"Failed to read/extract output files: {e}")
        return {"passed": False, "score": 0, "feedback": " | ".join(feedback)}
    finally:
        if os.path.exists(temp_tar.name):
            os.unlink(temp_tar.name)

    # Verification helper for robust XML loading
    def get_xml_root(filename):
        path = os.path.join(extract_dir, filename)
        if not os.path.exists(path):
            return None, f"File {filename} not found"
        try:
            tree = ET.parse(path)
            return tree.getroot(), "Success"
        except ET.ParseError as e:
            return None, f"XML Parse error in {filename}: {e}"

    # Criterion 1: Node file (5 pts)
    root_nod, msg = get_xml_root("highway_merge.nod.xml")
    if root_nod is not None:
        nodes = root_nod.findall("node")
        if len(nodes) >= 4:
            score += 5
            feedback.append("Node file valid (>=4 nodes)")
        else:
            feedback.append(f"Node file has insufficient nodes: {len(nodes)}")
    else:
        feedback.append(msg)

    # Criterion 2: Edge file (10 pts)
    root_edg, msg = get_xml_root("highway_merge.edg.xml")
    if root_edg is not None:
        edges = root_edg.findall("edge")
        if len(edges) >= 3:
            lanes = [e.get('numLanes', '1') for e in edges]
            if any(l == '3' for l in lanes) and any(l == '1' for l in lanes):
                score += 10
                feedback.append("Edge file valid with correct lane counts")
            else:
                score += 5
                feedback.append("Edge file valid, but lane counts don't match 3-lane mainline and 1-lane ramp specs")
        else:
            feedback.append(f"Edge file has insufficient edges: {len(edges)}")
    else:
        feedback.append(msg)

    # Criterion 3: Net file (15 pts)
    root_net, msg = get_xml_root("highway_merge.net.xml")
    if root_net is not None and root_net.tag == 'net':
        if len(root_net.findall("edge")) >= 3:
            score += 15
            feedback.append("Network compiled successfully via netconvert")
        else:
            feedback.append("Network file missing compiled edges")
    else:
        feedback.append(msg)

    # Criterion 4: Route file (15 pts)
    root_rou, msg = get_xml_root("highway_merge.rou.xml")
    if root_rou is not None:
        flows = root_rou.findall("flow")
        routes = root_rou.findall("route")
        vehicles = root_rou.findall("vehicle")
        if len(flows) > 0 or len(vehicles) > 0 or len(routes) > 0:
            score += 15
            feedback.append("Route file contains demand/flow definitions")
        else:
            feedback.append("Route file missing flows/vehicles/routes")
    else:
        feedback.append(msg)

    # Criterion 5: Detector file (10 pts)
    root_det, msg = get_xml_root("highway_merge.det.xml")
    if root_det is not None:
        e1s = root_det.findall("e1Detector") + root_det.findall("inductionLoop")
        if len(e1s) > 0:
            if any(det.get("freq") == "300" for det in e1s):
                score += 10
                feedback.append("Detector configured correctly (freq=300)")
            else:
                score += 5
                feedback.append("Detector configured but frequency != 300")
        else:
            feedback.append("No E1 induction loop found in detector file")
    else:
        feedback.append(msg)

    # Criterion 6: Config file (5 pts)
    root_cfg, msg = get_xml_root("highway_merge.sumocfg")
    if root_cfg is not None and root_cfg.tag == 'configuration':
        score += 5
        feedback.append("Config file valid")
    else:
        feedback.append(msg)

    # Criterion 7: Tripinfo / Simulation Output (20 pts)
    root_trip, msg = get_xml_root("tripinfo.xml")
    trips = 0
    if root_trip is not None:
        trips = len(root_trip.findall("tripinfo"))
        # Using 100 as an anti-gaming floor metric: actual demand is 1600 veh/hr
        # so over 1800s simulation it should process ~800, giving generous leeway for congestion
        if trips >= 100:
            score += 20
            feedback.append(f"Simulation generated {trips} completed trips")
        elif trips > 0:
            score += 10
            feedback.append(f"Simulation generated {trips} trips (expected >100 for 1600 veh/hr demand)")
        else:
            feedback.append("Simulation completed but 0 trips finished")
    else:
        feedback.append(msg)

    # Criterion 8: Detector output (15 pts)
    root_det_out, msg = get_xml_root("detector_output.xml")
    if root_det_out is not None:
        intervals = root_det_out.findall("interval")
        if len(intervals) > 0:
            # Check if any vehicles were actually detected
            veh_contrib = sum(int(i.get("nVehContrib", i.get("entered", 0))) for i in intervals)
            if veh_contrib > 0:
                score += 15
                feedback.append(f"Detector recorded {veh_contrib} vehicles")
            else:
                score += 5
                feedback.append("Detector output generated but 0 vehicles detected")
        else:
            feedback.append("Detector output empty")
    else:
        feedback.append(msg)

    # Criterion 9: Speed and lane geometry sanity check (5 pts)
    if root_net is not None:
        lanes = root_net.findall(".//lane")
        speeds = [float(l.get("speed", 0)) for l in lanes]
        if any(s >= 30.0 for s in speeds) and any(s >= 20.0 for s in speeds):
            score += 5
            feedback.append("Network speeds conform to requirements")

    # Optional VLM Trajectory Verification
    vlm_score = 0
    query_vlm = env_info.get('query_vlm')
    if query_vlm:
        try:
            from gym_anything.vlm import sample_trajectory_frames
            frames = sample_trajectory_frames(traj, n=3)
            if frames:
                prompt = "Does the agent appear to be using a terminal or text editor (like nano, vim, gedit) to write XML code or run commands? Reply with exactly 'yes' or 'no'."
                vlm_result = query_vlm(images=frames, prompt=prompt)
                if vlm_result and vlm_result.get("response", "").strip().lower().startswith("yes"):
                    feedback.append("VLM confirms terminal/editor usage (+0 pts bonus logic)")
                else:
                    feedback.append("VLM did not detect terminal/editor usage")
        except Exception as e:
            logger.warning(f"VLM trajectory verification failed: {e}")

    # Clean up extraction dir
    shutil.rmtree(extract_dir, ignore_errors=True)

    # To pass, the user must essentially succeed at the fundamental goals:
    # compiling the network, and completing a meaningful simulation run.
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }