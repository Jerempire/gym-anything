#!/usr/bin/env python3
"""
Verifier for model_turn_prohibition task.

Evaluates:
1. Ground truth calculation of the busiest intersection in the original network.
2. Checks that left turns are successfully removed in the modified network.
3. Checks that other connections at that intersection remain intact.
4. Parses simulation outputs for successfully completed trips.
5. Verifies the accuracy and formatting of the generated text report.
"""

import os
import json
import tempfile
import xml.etree.ElementTree as ET

def analyze_network(net_file_path, target_junc=None):
    """
    Parses a SUMO .net.xml file to compute junction connection statistics.
    Returns a dictionary of metrics for the busiest junction (or a specified target).
    """
    try:
        tree = ET.parse(net_file_path)
        root = tree.getroot()
    except Exception as e:
        return {"error": f"Failed to parse XML: {e}"}

    # Map normal (non-internal) edges to their destination junctions
    edge_to_junc = {}
    for edge in root.findall('edge'):
        if edge.attrib.get('function', '') != 'internal':
            eid = edge.attrib.get('id')
            to_j = edge.attrib.get('to')
            if eid and to_j:
                edge_to_junc[eid] = to_j

    # Count incoming edges per junction
    junc_inc_counts = {}
    for to_junc in edge_to_junc.values():
        junc_inc_counts[to_junc] = junc_inc_counts.get(to_junc, 0) + 1

    if not junc_inc_counts:
        return {"error": "No incoming edges found in network."}

    # Identify target junction
    if target_junc is None:
        max_edges = max(junc_inc_counts.values())
        busiest_juncs = [j for j, count in junc_inc_counts.items() if count == max_edges]
        # Lexicographical sort to break ties deterministically
        busiest_junc = sorted(busiest_juncs)[0]
        incoming_edges = max_edges
    else:
        busiest_junc = target_junc
        incoming_edges = junc_inc_counts.get(busiest_junc, 0)

    # Count connections going into this junction
    left_turns = 0
    other_turns = 0
    total_conns = 0

    for conn in root.findall('connection'):
        from_edge = conn.attrib.get('from')
        # If the edge originates outside but goes to our junction
        if from_edge in edge_to_junc and edge_to_junc[from_edge] == busiest_junc:
            total_conns += 1
            direction = conn.attrib.get('dir', '')
            if direction in ['l', 'L']:
                left_turns += 1
            else:
                other_turns += 1

    return {
        "busiest_junction": busiest_junc,
        "incoming_edges": incoming_edges,
        "left_turns": left_turns,
        "other_turns": other_turns,
        "total_connections": total_conns
    }


def parse_report(report_path):
    """Parses the text report generated by the agent."""
    report_data = {}
    try:
        with open(report_path, 'r') as f:
            for line in f:
                if ':' in line:
                    parts = line.split(':', 1)
                    key = parts[0].strip().lower()
                    val = parts[1].strip()
                    report_data[key] = val
        return report_data
    except Exception as e:
        return {"error": str(e)}


def check_tripinfo(tripinfo_path):
    """Verifies that the tripinfo XML contains completed trips."""
    try:
        tree = ET.parse(tripinfo_path)
        root = tree.getroot()
        trips = root.findall('tripinfo')
        return len(trips)
    except Exception:
        return 0


def verify_model_turn_prohibition(traj, env_info, task_info):
    """Main verifier logic."""
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Environment error: copy_from_env missing"}

    score = 0
    feedback = []
    
    # Use temporary directory to hold copied files
    with tempfile.TemporaryDirectory() as tmpdir:
        def fetch_file(remote_path, local_name):
            local_path = os.path.join(tmpdir, local_name)
            try:
                copy_from_env(remote_path, local_path)
                if os.path.exists(local_path):
                    return local_path
            except Exception:
                pass
            return None

        # Fetch metadata JSON
        result_json_path = fetch_file("/tmp/task_result.json", "result.json")
        if not result_json_path:
            return {"passed": False, "score": 0, "feedback": "Failed to read result export JSON."}
            
        with open(result_json_path, 'r') as f:
            export_data = json.load(f)

        timestamps_valid = export_data.get('timestamps_valid', False)
        if timestamps_valid:
            score += 5
            feedback.append("Timestamps valid (+5)")
        else:
            feedback.append("Timestamps invalid (Possible gaming)")

        # Fetch and analyze original network
        orig_net_path = fetch_file("/tmp/original.net.xml", "original.net.xml")
        if not orig_net_path:
            return {"passed": False, "score": score, "feedback": "Failed to load original network for verification."}
        
        orig_stats = analyze_network(orig_net_path)
        if "error" in orig_stats:
            return {"passed": False, "score": score, "feedback": f"Original network error: {orig_stats['error']}"}
            
        target_junc = orig_stats['busiest_junction']
        orig_left = orig_stats['left_turns']
        orig_other = orig_stats['other_turns']
        orig_total = orig_stats['total_connections']

        # 1. Modified network exists & valid XML (10 pts)
        mod_net_path = fetch_file("/tmp/acosta_restricted.net.xml", "modified.net.xml")
        if not mod_net_path:
            feedback.append("Modified network file missing")
            return {"passed": False, "score": score, "feedback": " | ".join(feedback)}
            
        mod_stats = analyze_network(mod_net_path, target_junc=target_junc)
        if "error" in mod_stats:
            feedback.append(f"Modified network XML error: {mod_stats['error']}")
        else:
            score += 10
            feedback.append("Modified network valid (+10)")

            # 2. Left turns removed correctly (20 pts)
            mod_left = mod_stats['left_turns']
            if mod_left == 0 and orig_left > 0:
                score += 20
                feedback.append(f"Left turns completely removed at {target_junc} (+20)")
                left_turns_pass = True
            else:
                feedback.append(f"Left turns not removed. Expected 0, found {mod_left}")
                left_turns_pass = False

            # 3. Other connections preserved (10 pts)
            mod_other = mod_stats['other_turns']
            if mod_other > 0 and mod_other >= orig_other * 0.9:  # Allow 10% drift if netconvert pruned dead ends
                score += 10
                feedback.append("Other connections preserved (+10)")
            else:
                feedback.append(f"Other connections damaged. Orig: {orig_other}, Mod: {mod_other}")

        # 4. Simulation tripinfo exists (10 pts) and has completed trips (10 pts)
        tripinfo_path = fetch_file("/tmp/tripinfo_restricted.xml", "tripinfo.xml")
        if tripinfo_path:
            trips_completed = check_tripinfo(tripinfo_path)
            if trips_completed > 0:
                score += 10  # Exists
                feedback.append("Tripinfo exists (+10)")
                if trips_completed >= 10:
                    score += 10  # Completed trips
                    feedback.append(f"Simulation completed {trips_completed} trips (+10)")
                else:
                    feedback.append(f"Simulation had very few trips: {trips_completed}")
            else:
                feedback.append("Tripinfo XML invalid or empty")
        else:
            feedback.append("Tripinfo file missing")

        # 5. Report file exists and accuracy
        report_path = fetch_file("/tmp/restriction_report.txt", "report.txt")
        report_pass = False
        junction_identified = False
        
        if report_path:
            report_data = parse_report(report_path)
            if "error" not in report_data and len(report_data) >= 6:
                score += 10
                feedback.append("Report format valid (+10)")

                # Correct junction identified (15 pts)
                rep_junc = report_data.get('junction_id', '').strip()
                if rep_junc == target_junc:
                    score += 15
                    junction_identified = True
                    feedback.append(f"Correct junction identified: {target_junc} (+15)")
                else:
                    feedback.append(f"Wrong junction ID in report. Expected {target_junc}, got {rep_junc}")

                # Report values accurate (10 pts)
                try:
                    rep_inc = int(report_data.get('incoming_edges', '0'))
                    rep_left = int(report_data.get('left_turns_removed', '0'))
                    rep_before = int(report_data.get('connections_before', '0'))
                    rep_after = int(report_data.get('connections_after', '0'))
                    
                    if (rep_inc == orig_stats['incoming_edges'] and 
                        rep_left == orig_left and 
                        rep_before == orig_total and 
                        (rep_after == orig_total - orig_left or rep_after == mod_stats.get('total_connections', 0))):
                        score += 10
                        feedback.append("Report values accurate (+10)")
                    else:
                        feedback.append("Report values contain inaccuracies")
                except ValueError:
                    feedback.append("Report values are not integers")
            else:
                feedback.append("Report file malformed or missing fields")
        else:
            feedback.append("Report file missing")

    # Pass condition: at least 60 points, and core objectives met
    passed = (score >= 60) and left_turns_pass and junction_identified

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }