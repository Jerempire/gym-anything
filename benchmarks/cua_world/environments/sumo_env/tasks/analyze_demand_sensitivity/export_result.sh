#!/bin/bash
echo "=== Exporting analyze_demand_sensitivity result ==="

source /workspace/scripts/task_utils.sh

# Take final screenshot
take_screenshot /tmp/task_end.png

export TASK_START=$(cat /tmp/task_start_time.txt 2>/dev/null || echo "0")
export TASK_END=$(date +%s)

# Use Python to safely parse the generated XML files and the CSV report,
# assembling everything into a single JSON for the verifier to consume via copy_from_env.
python3 << 'EOF'
import os
import json
import xml.etree.ElementTree as ET
import csv

output_dir = "/home/ga/SUMO_Output"
scales = ["0.8", "0.9", "1.0", "1.1", "1.2"]

result = {
    "task_start": int(os.environ.get("TASK_START", 0)),
    "task_end": int(os.environ.get("TASK_END", 0)),
    "xml_data": {},
    "csv_exists": False,
    "csv_headers": [],
    "csv_data": {}
}

# 1. Parse the generated XML files
for scale in scales:
    xml_path = os.path.join(output_dir, f"stats_{scale}.xml")
    if os.path.exists(xml_path):
        try:
            tree = ET.parse(xml_path)
            root = tree.getroot()
            vts = root.find("vehicleTripStatistics")
            if vts is not None:
                result["xml_data"][scale] = {
                    "timeLoss": float(vts.get("timeLoss", 0)),
                    "count": int(vts.get("count", 0))
                }
            else:
                result["xml_data"][scale] = {"error": "No vehicleTripStatistics element found"}
        except Exception as e:
            result["xml_data"][scale] = {"error": f"XML parsing error: {str(e)}"}
    else:
        result["xml_data"][scale] = {"error": "File not found"}

# 2. Parse the CSV generated by the agent
csv_path = os.path.join(output_dir, "demand_sensitivity.csv")
if os.path.exists(csv_path):
    result["csv_exists"] = True
    try:
        with open(csv_path, 'r', encoding='utf-8') as f:
            reader = csv.reader(f)
            # Try to read headers
            try:
                headers = next(reader)
                result["csv_headers"] = [h.strip() for h in headers]
            except StopIteration:
                pass
            
            # Read data rows
            for row in reader:
                if len(row) >= 2:
                    raw_scale = row[0].strip()
                    try:
                        # Normalize scale format (e.g., '0.80' -> '0.8')
                        scale_float = float(raw_scale)
                        normalized_scale = f"{scale_float:.1f}"
                        val = float(row[1].strip())
                        result["csv_data"][normalized_scale] = val
                    except ValueError:
                        pass
    except Exception as e:
        result["csv_error"] = str(e)

# Write to a JSON file to be exported
with open("/tmp/task_result.json", "w") as f:
    json.dump(result, f)
EOF

# Ensure permissions allow copy_from_env to read it
chmod 666 /tmp/task_result.json 2>/dev/null || true

echo "Result saved to /tmp/task_result.json"
echo "=== Export complete ==="