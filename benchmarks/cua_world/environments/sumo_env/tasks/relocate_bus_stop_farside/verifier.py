#!/usr/bin/env python3
"""
Verifier for relocate_bus_stop_farside task.

Robust multi-stage verification:
1. XML Parsing: Confirms agent actually modified the bus stop lane assignment.
2. Route Graph Validation: Parses routes to mathematically prove the new lane is the valid immediate downstream edge.
3. Simulation Output Analysis: Computes true ground-truth averages from the simulation outputs.
4. Data Analysis Validation: Verifies the agent's reported metrics match the true computed results.
"""

import json
import os
import tempfile
import xml.etree.ElementTree as ET
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def get_first_bus_stop(xml_path):
    """Extract the first bus stop ID and Lane from the additionals file."""
    try:
        tree = ET.parse(xml_path)
        root = tree.getroot()
        for bs in root.findall('busStop'):
            return bs.attrib.get('id'), bs.attrib.get('lane')
    except Exception as e:
        logger.error(f"Error parsing {xml_path}: {e}")
    return None, None

def get_route_edges(xml_path):
    """Extract sequences of edges forming valid bus routes."""
    routes = []
    try:
        tree = ET.parse(xml_path)
        root = tree.getroot()
        for route in root.findall('route'):
            edges = route.attrib.get('edges', '').split()
            if edges:
                routes.append(edges)
        for vehicle in root.findall('vehicle'):
            for route in vehicle.findall('route'):
                edges = route.attrib.get('edges', '').split()
                if edges:
                    routes.append(edges)
    except Exception as e:
        logger.error(f"Error parsing routes in {xml_path}: {e}")
    return routes

def calculate_avg_duration(xml_path):
    """Programmatically compute the average duration for all bus trips."""
    try:
        tree = ET.parse(xml_path)
        root = tree.getroot()
        durations = []
        for trip in root.findall('tripinfo'):
            vid = trip.attrib.get('id', '').lower()
            vtype = trip.attrib.get('vType', '').lower()
            if 'bus' in vid or 'bus' in vtype:
                durations.append(float(trip.attrib.get('duration', 0)))
        if durations:
            return sum(durations) / len(durations)
    except Exception as e:
        logger.error(f"Error parsing durations in {xml_path}: {e}")
    return None

def parse_report(filepath):
    """Parse the text report generated by the agent."""
    report = {}
    try:
        with open(filepath, 'r') as f:
            for line in f:
                if ':' in line:
                    k, v = line.split(':', 1)
                    report[k.strip().lower()] = v.strip()
    except Exception as e:
        logger.error(f"Error parsing report {filepath}: {e}")
    return report

def verify_relocate_bus_stop_farside(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    files_to_copy = {
        "result.json": "/tmp/task_result.json",
        "agent_stops.xml": "/tmp/agent_bus_stops.add.xml",
        "orig_stops.xml": "/tmp/orig_bus_stops.add.xml",
        "orig_routes.xml": "/tmp/orig_busses.rou.xml",
        "baseline.xml": "/tmp/baseline_tripinfos.xml",
        "modified.xml": "/tmp/modified_tripinfos.xml",
        "report.txt": "/tmp/agent_report.txt"
    }
    
    local_files = {}
    temp_dir = tempfile.mkdtemp()
    
    try:
        for key, remote_path in files_to_copy.items():
            local_path = os.path.join(temp_dir, key)
            try:
                copy_from_env(remote_path, local_path)
                if os.path.exists(local_path) and os.path.getsize(local_path) > 0:
                    local_files[key] = local_path
            except Exception:
                pass

        if "result.json" not in local_files:
            return {"passed": False, "score": 0, "feedback": "Task result metadata not found"}

        with open(local_files["result.json"], 'r') as f:
            result_meta = json.load(f)

        # 1. Baseline Simulation Check (15 pts)
        baseline_avg = None
        if "baseline.xml" in local_files:
            baseline_avg = calculate_avg_duration(local_files["baseline.xml"])
            if baseline_avg is not None:
                score += 15
                feedback_parts.append(f"Baseline simulated (Avg: {baseline_avg:.2f}s)")
            else:
                feedback_parts.append("Baseline output invalid or no bus trips found")
        else:
            feedback_parts.append("Baseline output missing")

        # 2. Modified Simulation Check (15 pts) - Anti-Gaming required
        modified_avg = None
        modified_during_task = result_meta.get("modified_during_task", False)
        if "modified.xml" in local_files and modified_during_task:
            modified_avg = calculate_avg_duration(local_files["modified.xml"])
            if modified_avg is not None:
                score += 15
                feedback_parts.append(f"Modified simulated (Avg: {modified_avg:.2f}s)")
            else:
                feedback_parts.append("Modified output invalid")
        elif "modified.xml" in local_files:
            feedback_parts.append("Modified output created before task started (Invalid)")
        else:
            feedback_parts.append("Modified output missing")

        # 3. XML Configuration Edit Check (30 pts)
        orig_id, orig_lane = get_first_bus_stop(local_files.get("orig_stops.xml", ""))
        agent_id, agent_lane = get_first_bus_stop(local_files.get("agent_stops.xml", ""))
        
        orig_edge = orig_lane.rsplit('_', 1)[0] if orig_lane else None
        agent_edge = agent_lane.rsplit('_', 1)[0] if agent_lane else None

        if agent_lane and agent_lane != orig_lane:
            score += 30
            feedback_parts.append("Bus stop XML successfully modified")
        elif agent_lane == orig_lane:
            feedback_parts.append("Bus stop XML not modified")
        else:
            feedback_parts.append("Bus stop XML invalid or missing")

        # 4. Correct Stop Placement (20 pts)
        routes = get_route_edges(local_files.get("orig_routes.xml", ""))
        valid_next_edges = set()
        if orig_edge:
            for edges in routes:
                if orig_edge in edges:
                    idx = edges.index(orig_edge)
                    if idx + 1 < len(edges):
                        valid_next_edges.add(edges[idx + 1])
        
        correct_placement = False
        if valid_next_edges and agent_edge in valid_next_edges:
            score += 20
            correct_placement = True
            feedback_parts.append(f"Correctly relocated to downstream edge ({agent_edge})")
        elif agent_edge:
            feedback_parts.append(f"Incorrect relocation (placed on {agent_edge}, expected one of {valid_next_edges})")

        # 5. Accurate Reporting (20 pts)
        accurate_report = False
        if "report.txt" in local_files:
            report_data = parse_report(local_files["report.txt"])
            try:
                rep_base = float(report_data.get("baseline_avg_duration", -1))
                rep_mod = float(report_data.get("modified_avg_duration", -1))
            except ValueError:
                rep_base, rep_mod = -1, -1
            
            if baseline_avg is not None and modified_avg is not None:
                # Tolerance allows for varying precision
                if abs(rep_base - baseline_avg) < 0.5 and abs(rep_mod - modified_avg) < 0.5:
                    score += 20
                    accurate_report = True
                    feedback_parts.append("Report averages accurately match simulation metrics")
                else:
                    feedback_parts.append(f"Report inaccurate (Expected {baseline_avg:.1f}, {modified_avg:.1f}; Got {rep_base}, {rep_mod})")
            else:
                feedback_parts.append("Cannot verify report accuracy without valid simulation outputs")
        else:
            feedback_parts.append("Report file missing")

        # Final Evaluation
        key_criteria_met = correct_placement and accurate_report
        passed = score >= 80 and key_criteria_met

    except Exception as e:
        logger.error(f"Error during verification: {e}")
        return {"passed": False, "score": score, "feedback": f"Verification error: {e}"}
    finally:
        for f in local_files.values():
            try:
                os.unlink(f)
            except:
                pass
        try:
            os.rmdir(temp_dir)
        except:
            pass

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }