#!/usr/bin/env python3
"""
Verifier for analyze_trip_performance task.

Evaluates the agent's ability to run a SUMO simulation and accurately parse 
the resulting XML output. Computes the ground truth metrics directly from 
the generated XML to ensure precision, rather than using hardcoded values 
(since simulations can have slight variances based on seeds or SUMO versions).
"""

import os
import json
import tempfile
import logging
import xml.etree.ElementTree as ET

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def parse_agent_report(report_path):
    """Parses the key=value format report generated by the agent."""
    metrics = {}
    try:
        with open(report_path, 'r') as f:
            for line in f:
                line = line.strip()
                if not line or '=' not in line:
                    continue
                key, val = line.split('=', 1)
                key = key.strip()
                val = val.strip()
                metrics[key] = val
    except Exception as e:
        logger.error(f"Failed to parse agent report: {e}")
    return metrics

def compute_ground_truth(xml_path):
    """Parses tripinfos.xml to compute exact ground truth metrics."""
    metrics = {
        'total_trips': 0,
        'avg_duration': 0.0,
        'avg_waitingTime': 0.0,
        'avg_timeLoss': 0.0,
        'max_duration_vehicle': None,
        'max_duration_value': -1.0
    }
    
    try:
        tree = ET.parse(xml_path)
        root = tree.getroot()
        
        durations = []
        waiting_times = []
        time_losses = []
        
        for tripinfo in root.findall('tripinfo'):
            trip_id = tripinfo.get('id')
            duration = float(tripinfo.get('duration', 0))
            waitingTime = float(tripinfo.get('waitingTime', 0))
            timeLoss = float(tripinfo.get('timeLoss', 0))
            
            durations.append(duration)
            waiting_times.append(waitingTime)
            time_losses.append(timeLoss)
            
            if duration > metrics['max_duration_value']:
                metrics['max_duration_value'] = duration
                metrics['max_duration_vehicle'] = trip_id
                
        metrics['total_trips'] = len(durations)
        if metrics['total_trips'] > 0:
            metrics['avg_duration'] = sum(durations) / metrics['total_trips']
            metrics['avg_waitingTime'] = sum(waiting_times) / metrics['total_trips']
            metrics['avg_timeLoss'] = sum(time_losses) / metrics['total_trips']
            
    except Exception as e:
        logger.error(f"Failed to parse ground truth XML: {e}")
        return None
        
    return metrics

def is_close(val1, val2, tolerance_pct=0.01):
    """Checks if two float values are close within a relative tolerance."""
    if val1 == val2:
        return True
    if val1 == 0 or val2 == 0:
        return abs(val1 - val2) <= 0.05  # small absolute tolerance if one is 0
    return abs(val1 - val2) / max(abs(val1), abs(val2)) <= tolerance_pct

def verify_analyze_trip_performance(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    # 1. Retrieve the task result JSON
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    # Validate basic file creation and timestamps
    tripinfo_exists = result.get('tripinfo_exists', False)
    report_exists = result.get('report_exists', False)
    task_start = result.get('task_start_time', 0)
    tripinfo_mtime = result.get('tripinfo_mtime', 0)
    
    if not tripinfo_exists:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "tripinfos.xml was not created. The simulation was not run or did not complete."
        }
        
    if tripinfo_mtime < task_start:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "tripinfos.xml is older than the task start time (Anti-gaming check failed)."
        }

    # Retrieve the tripinfos XML and report txt
    temp_xml = tempfile.NamedTemporaryFile(delete=False, suffix='.xml')
    temp_report = tempfile.NamedTemporaryFile(delete=False, suffix='.txt')
    
    gt_metrics = None
    agent_metrics = {}
    
    try:
        # Get XML and compute Ground Truth
        copy_from_env("/tmp/agent_tripinfos.xml", temp_xml.name)
        gt_metrics = compute_ground_truth(temp_xml.name)
        
        # Determine simulation completeness (15 points)
        if gt_metrics and gt_metrics['total_trips'] > 0:
            score += 15
            feedback_parts.append(f"Simulation completed ({gt_metrics['total_trips']} trips)")
        else:
            return {
                "passed": False, 
                "score": 0, 
                "feedback": "tripinfos.xml is empty or invalid. Simulation did not run properly."
            }

        # Check Report
        if report_exists:
            score += 10
            feedback_parts.append("Report file exists")
            
            copy_from_env("/tmp/agent_trip_report.txt", temp_report.name)
            agent_metrics = parse_agent_report(temp_report.name)
            
            # Check report format (10 points)
            required_keys = ['total_trips', 'avg_duration', 'avg_waitingTime', 'avg_timeLoss', 'max_duration_vehicle', 'max_duration_value']
            if all(k in agent_metrics for k in required_keys):
                score += 10
                feedback_parts.append("Report format correct")
            else:
                missing = [k for k in required_keys if k not in agent_metrics]
                feedback_parts.append(f"Missing keys in report: {missing}")

            # Verify metrics
            # total_trips (15 points)
            agent_total = int(agent_metrics.get('total_trips', -1))
            if agent_total == gt_metrics['total_trips']:
                score += 15
                feedback_parts.append("total_trips correct")
            else:
                feedback_parts.append(f"total_trips mismatch: expected {gt_metrics['total_trips']}, got {agent_total}")
                
            # avg_duration (15 points)
            try:
                agent_avg_dur = float(agent_metrics.get('avg_duration', -1))
                if is_close(agent_avg_dur, gt_metrics['avg_duration']):
                    score += 15
                    feedback_parts.append("avg_duration correct")
                else:
                    feedback_parts.append(f"avg_duration mismatch (expected ~{gt_metrics['avg_duration']:.2f})")
            except ValueError:
                feedback_parts.append("Invalid avg_duration format")

            # avg_waitingTime (10 points)
            try:
                agent_wait = float(agent_metrics.get('avg_waitingTime', -1))
                if is_close(agent_wait, gt_metrics['avg_waitingTime']):
                    score += 10
                    feedback_parts.append("avg_waitingTime correct")
                else:
                    feedback_parts.append(f"avg_waitingTime mismatch")
            except ValueError:
                pass

            # avg_timeLoss (10 points)
            try:
                agent_loss = float(agent_metrics.get('avg_timeLoss', -1))
                if is_close(agent_loss, gt_metrics['avg_timeLoss']):
                    score += 10
                    feedback_parts.append("avg_timeLoss correct")
                else:
                    feedback_parts.append(f"avg_timeLoss mismatch")
            except ValueError:
                pass

            # max_duration_vehicle (10 points)
            if agent_metrics.get('max_duration_vehicle') == gt_metrics['max_duration_vehicle']:
                score += 10
                feedback_parts.append("max_duration_vehicle correct")
            else:
                feedback_parts.append(f"max_duration_vehicle mismatch")

            # max_duration_value (5 points)
            try:
                agent_max_val = float(agent_metrics.get('max_duration_value', -1))
                if is_close(agent_max_val, gt_metrics['max_duration_value']):
                    score += 5
                    feedback_parts.append("max_duration_value correct")
                else:
                    feedback_parts.append(f"max_duration_value mismatch")
            except ValueError:
                pass
                
        else:
            feedback_parts.append("Report file not found")
            
    finally:
        if os.path.exists(temp_xml.name):
            os.unlink(temp_xml.name)
        if os.path.exists(temp_report.name):
            os.unlink(temp_report.name)

    passed = score >= 60 and report_exists and tripinfo_exists
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }