#!/bin/bash
# Task: equipment_acquisition_full_cycle
# Export: Query entities, purchases, fixed assets, outgoing payments, and
#         journal entries created after task start. Write JSON to /tmp/task_result.json.

echo "=== Exporting equipment_acquisition_full_cycle result ==="

source /workspace/scripts/task_utils.sh

if ! type ekylibre_db_query &>/dev/null; then
    ekylibre_db_query() {
        docker exec ekylibre-db psql -U ekylibre -d ekylibre_production -t -A \
            -c "SET search_path TO demo,lexicon,public; $1" 2>/dev/null || echo ""
    }
fi

# Capture final screenshot
take_screenshot /tmp/task_final.png

TASK_START=$(cat /tmp/task_start_time.txt 2>/dev/null || echo "0")
TASK_END=$(date +%s)

# =============================================================================
# 1. SUPPLIER ENTITY
# =============================================================================

NEW_ENTITIES=$(ekylibre_db_query "
SELECT COUNT(*) FROM entities
WHERE EXTRACT(EPOCH FROM created_at)::bigint > $TASK_START;" \
    | tr -d '[:space:]')
NEW_ENTITIES=${NEW_ENTITIES:-0}

# Look for the specific Claas entity
ENTITY_DATA=$(ekylibre_db_query "
SELECT e.id, e.full_name, e.siret_number
FROM entities e
WHERE e.full_name ILIKE '%claas%'
  AND EXTRACT(EPOCH FROM e.created_at)::bigint > $TASK_START
ORDER BY e.created_at DESC LIMIT 1;" | head -1)

ENTITY_FOUND="false"
ENTITY_ID=""
ENTITY_NAME=""
ENTITY_SIRET=""
if [ -n "$ENTITY_DATA" ]; then
    ENTITY_FOUND="true"
    ENTITY_ID=$(echo "$ENTITY_DATA" | cut -d'|' -f1)
    ENTITY_NAME=$(echo "$ENTITY_DATA" | cut -d'|' -f2)
    ENTITY_SIRET=$(echo "$ENTITY_DATA" | cut -d'|' -f3)
fi

# =============================================================================
# 2. PURCHASE INVOICE
# =============================================================================

INITIAL_PURCHASES=$(cat /tmp/initial_purchase_count.txt 2>/dev/null || echo "0")
FINAL_PURCHASES=$(ekylibre_db_query "
SELECT COUNT(*) FROM purchases WHERE type = 'PurchaseInvoice';" \
    | tr -d '[:space:]')
FINAL_PURCHASES=${FINAL_PURCHASES:-0}

# Look for invoice by reference or by recency
INVOICE_DATA=$(ekylibre_db_query "
SELECT p.id, p.reference_number, p.pretax_amount, p.amount,
       p.invoiced_at, p.state,
       EXTRACT(EPOCH FROM p.created_at)::bigint as created_ts
FROM purchases p
WHERE p.type = 'PurchaseInvoice'
  AND EXTRACT(EPOCH FROM p.created_at)::bigint > $TASK_START
ORDER BY p.created_at DESC LIMIT 1;" | head -1)

INVOICE_FOUND="false"
INVOICE_ID=""
INVOICE_REF=""
INVOICE_PRETAX=""
INVOICE_AMOUNT=""
INVOICE_DATE=""
INVOICE_STATE=""
if [ -n "$INVOICE_DATA" ]; then
    INVOICE_FOUND="true"
    INVOICE_ID=$(echo "$INVOICE_DATA" | cut -d'|' -f1)
    INVOICE_REF=$(echo "$INVOICE_DATA" | cut -d'|' -f2)
    INVOICE_PRETAX=$(echo "$INVOICE_DATA" | cut -d'|' -f3)
    INVOICE_AMOUNT=$(echo "$INVOICE_DATA" | cut -d'|' -f4)
    INVOICE_DATE=$(echo "$INVOICE_DATA" | cut -d'|' -f5)
    INVOICE_STATE=$(echo "$INVOICE_DATA" | cut -d'|' -f6)
fi

# =============================================================================
# 3. JOURNAL ENTRIES (generated by invoice validation)
# =============================================================================

INITIAL_JE=$(cat /tmp/initial_je_count.txt 2>/dev/null || echo "0")
FINAL_JE=$(ekylibre_db_query "
SELECT COUNT(*) FROM journal_entries
WHERE EXTRACT(EPOCH FROM created_at)::bigint > $TASK_START;" \
    | tr -d '[:space:]')
FINAL_JE=${FINAL_JE:-0}

# =============================================================================
# 4. FIXED ASSET
# =============================================================================

INITIAL_ASSETS=$(cat /tmp/initial_fixed_asset_count.txt 2>/dev/null || echo "0")
FINAL_ASSETS=$(ekylibre_db_query "
SELECT COUNT(*) FROM fixed_assets;" \
    | tr -d '[:space:]')
FINAL_ASSETS=${FINAL_ASSETS:-0}

ASSET_DATA=$(ekylibre_db_query "
SELECT fa.id, fa.name, fa.depreciable_amount,
       fa.depreciation_method, fa.started_on, fa.stopped_on,
       fa.depreciation_percentage, fa.currency
FROM fixed_assets fa
WHERE (fa.name ILIKE '%lexion%' OR fa.name ILIKE '%claas%')
  AND EXTRACT(EPOCH FROM fa.created_at)::bigint > $TASK_START
ORDER BY fa.created_at DESC LIMIT 1;" | head -1)

ASSET_FOUND="false"
ASSET_NAME=""
ASSET_AMOUNT=""
ASSET_METHOD=""
ASSET_START=""
ASSET_STOP=""
if [ -n "$ASSET_DATA" ]; then
    ASSET_FOUND="true"
    ASSET_NAME=$(echo "$ASSET_DATA" | cut -d'|' -f2)
    ASSET_AMOUNT=$(echo "$ASSET_DATA" | cut -d'|' -f3)
    ASSET_METHOD=$(echo "$ASSET_DATA" | cut -d'|' -f4)
    ASSET_START=$(echo "$ASSET_DATA" | cut -d'|' -f5)
    ASSET_STOP=$(echo "$ASSET_DATA" | cut -d'|' -f6)
fi

# =============================================================================
# 5. OUTGOING PAYMENT
# =============================================================================

INITIAL_PAYMENTS=$(cat /tmp/initial_payment_count.txt 2>/dev/null || echo "0")
FINAL_PAYMENTS=$(ekylibre_db_query "
SELECT COUNT(*) FROM outgoing_payments;" \
    | tr -d '[:space:]')
FINAL_PAYMENTS=${FINAL_PAYMENTS:-0}

PAYMENT_DATA=$(ekylibre_db_query "
SELECT op.id, op.amount, op.to_bank_at,
       e.full_name as payee_name,
       EXTRACT(EPOCH FROM op.created_at)::bigint as created_ts
FROM outgoing_payments op
LEFT JOIN entities e ON op.payee_id = e.id
WHERE EXTRACT(EPOCH FROM op.created_at)::bigint > $TASK_START
ORDER BY op.created_at DESC LIMIT 1;" | head -1)

PAYMENT_FOUND="false"
PAYMENT_AMOUNT=""
PAYMENT_DATE=""
PAYMENT_PAYEE=""
if [ -n "$PAYMENT_DATA" ]; then
    PAYMENT_FOUND="true"
    PAYMENT_AMOUNT=$(echo "$PAYMENT_DATA" | cut -d'|' -f2)
    PAYMENT_DATE=$(echo "$PAYMENT_DATA" | cut -d'|' -f3)
    PAYMENT_PAYEE=$(echo "$PAYMENT_DATA" | cut -d'|' -f4)
fi

# =============================================================================
# WRITE JSON
# =============================================================================

TEMP_JSON=$(mktemp /tmp/result.XXXXXX.json)
cat > "$TEMP_JSON" << EOF
{
    "task": "equipment_acquisition_full_cycle",
    "task_start": $TASK_START,
    "task_end": $TASK_END,
    "entity": {
        "found": $ENTITY_FOUND,
        "new_count": $NEW_ENTITIES,
        "name": "$(echo "$ENTITY_NAME" | tr '"' "'")",
        "siret": "$ENTITY_SIRET"
    },
    "invoice": {
        "found": $INVOICE_FOUND,
        "initial_count": $INITIAL_PURCHASES,
        "final_count": $FINAL_PURCHASES,
        "reference": "$INVOICE_REF",
        "pretax_amount": "${INVOICE_PRETAX:-0}",
        "total_amount": "${INVOICE_AMOUNT:-0}",
        "date": "$INVOICE_DATE",
        "state": "$INVOICE_STATE"
    },
    "journal_entries": {
        "initial_count": $INITIAL_JE,
        "final_count": $FINAL_JE
    },
    "fixed_asset": {
        "found": $ASSET_FOUND,
        "initial_count": $INITIAL_ASSETS,
        "final_count": $FINAL_ASSETS,
        "name": "$(echo "$ASSET_NAME" | tr '"' "'")",
        "depreciable_amount": "${ASSET_AMOUNT:-0}",
        "depreciation_method": "$ASSET_METHOD",
        "started_on": "$ASSET_START",
        "stopped_on": "$ASSET_STOP"
    },
    "payment": {
        "found": $PAYMENT_FOUND,
        "initial_count": $INITIAL_PAYMENTS,
        "final_count": $FINAL_PAYMENTS,
        "amount": "${PAYMENT_AMOUNT:-0}",
        "date": "$PAYMENT_DATE",
        "payee": "$(echo "$PAYMENT_PAYEE" | tr '"' "'")"
    },
    "screenshot_path": "/tmp/task_final.png"
}
EOF

# Move to final location with permission handling
rm -f /tmp/task_result.json 2>/dev/null || sudo rm -f /tmp/task_result.json 2>/dev/null || true
cp "$TEMP_JSON" /tmp/task_result.json 2>/dev/null || sudo cp "$TEMP_JSON" /tmp/task_result.json
chmod 666 /tmp/task_result.json 2>/dev/null || sudo chmod 666 /tmp/task_result.json 2>/dev/null || true
rm -f "$TEMP_JSON"

echo "=== Export Complete ==="
echo "Entity found:   $ENTITY_FOUND (name=$ENTITY_NAME, siret=$ENTITY_SIRET)"
echo "Invoice found:  $INVOICE_FOUND (ref=$INVOICE_REF, state=$INVOICE_STATE, pretax=$INVOICE_PRETAX)"
echo "Journal entries: initial=$INITIAL_JE, final=$FINAL_JE"
echo "Asset found:    $ASSET_FOUND (name=$ASSET_NAME, method=$ASSET_METHOD, amount=$ASSET_AMOUNT)"
echo "Payment found:  $PAYMENT_FOUND (amount=$PAYMENT_AMOUNT, payee=$PAYMENT_PAYEE)"
cat /tmp/task_result.json
