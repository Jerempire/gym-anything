#!/usr/bin/env python3
"""
Verifier for generate_downsampled_matlab task.

Criteria:
1. .mat file exists and was created during the task.
2. File is a valid MATLAB binary readable by scipy.io.
3. Contains specific tracks (ECG, PLETH).
4. Data is DOWNSAMPLED to 100 Hz (Critical check).
   - Original data is 500 Hz.
   - 5 minutes at 500 Hz = 150,000 samples.
   - 5 minutes at 100 Hz = 30,000 samples.
   - We verify the array length is approx 30,000.
"""

import json
import os
import tempfile
import logging
import numpy as np

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_matlab_export(traj, env_info, task_info):
    # 1. Setup and imports
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: Copy function not available"}

    # Import scipy.io (available in basic python env)
    try:
        import scipy.io
    except ImportError:
        return {"passed": False, "score": 0, "feedback": "System error: scipy not available for verification"}

    metadata = task_info.get('metadata', {})
    expected_samples = metadata.get('expected_samples', 30000)
    tolerance = metadata.get('sample_tolerance', 1000)  # Allow small buffer variations
    
    score = 0
    feedback = []
    
    # 2. Get JSON Result from container (generated by export_result.ps1)
    result_json_path = tempfile.mktemp(suffix=".json")
    try:
        copy_from_env("C:\\tmp\\task_result.json", result_json_path)
        with open(result_json_path, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task results: {str(e)}"}
    finally:
        if os.path.exists(result_json_path):
            os.remove(result_json_path)

    # 3. Verify File Existence & Anti-Gaming
    if not task_result.get('output_exists', False):
        return {"passed": False, "score": 0, "feedback": "Output .mat file was not found."}
    
    score += 10
    feedback.append("File exists.")

    if not task_result.get('file_created_during_task', False):
        return {"passed": False, "score": 10, "feedback": "File exists but has an old timestamp (pre-dating task start)."}
    
    score += 10
    feedback.append("File created during task.")

    # 4. Copy the actual .mat file for content analysis
    mat_local_path = tempfile.mktemp(suffix=".mat")
    try:
        copy_from_env(task_result['output_path'], mat_local_path)
        
        # Load .mat file
        try:
            mat_data = scipy.io.loadmat(mat_local_path)
            score += 20
            feedback.append("File is a valid MATLAB format.")
        except Exception as e:
            return {"passed": False, "score": score, "feedback": f"File is not a valid .mat file: {str(e)}"}

        # 5. Check Content (Tracks)
        # Vital Recorder exports tracks as variables. Names might be sanitized (e.g., 'SNUADC/ECG_II' -> 'SNUADC_ECG_II')
        keys = [k for k in mat_data.keys() if not k.startswith('__')]
        
        has_ecg = any('ECG' in k.upper() for k in keys)
        has_pleth = any('PLETH' in k.upper() for k in keys)
        
        if has_ecg and has_pleth:
            score += 20
            feedback.append("Required tracks (ECG, PLETH) found.")
        else:
            feedback.append(f"Missing required tracks. Found: {keys}")

        # 6. Check Downsampling (The Core Test)
        # Find the main data arrays
        valid_arrays = [v for k, v in mat_data.items() if not k.startswith('__') and isinstance(v, (np.ndarray, list))]
        
        if not valid_arrays:
            return {"passed": False, "score": score, "feedback": "No data arrays found in file."}
        
        # Check length of the first substantial array
        data_len = 0
        for arr in valid_arrays:
            if hasattr(arr, 'shape') and max(arr.shape) > 1000:
                data_len = max(arr.shape)
                break
        
        # Analysis
        # 100Hz * 300s = 30,000 samples
        # 500Hz * 300s = 150,000 samples
        
        if abs(data_len - expected_samples) <= tolerance:
            score += 40
            feedback.append(f"Correct sampling rate verified (~{data_len} samples for 100Hz/5min).")
        elif data_len > 100000:
            feedback.append(f"Failed downsampling check. Found ~{data_len} samples (likely original 500Hz).")
        else:
            feedback.append(f"Incorrect data length: {data_len} samples. Expected ~{expected_samples}.")

    except Exception as e:
        feedback.append(f"Error analyzing file content: {str(e)}")
    finally:
        if os.path.exists(mat_local_path):
            os.remove(mat_local_path)

    # 7. Final Scoring
    passed = score >= 80  # Requires existence(20) + format(20) + tracks(20) + downsampling(20/40)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }