#!/usr/bin/env python3
"""
Verifier for archive_case_pdf_report task.
Verifies that the user created a PDF summary of the vital signs case
with the correct tracks and full duration.
"""

import json
import os
import tempfile
import logging
import re
import sys

# Attempt to import pdfminer for text extraction
try:
    from pdfminer.high_level import extract_text
    PDFMINER_AVAILABLE = True
except ImportError:
    PDFMINER_AVAILABLE = False

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_archive_case_pdf_report(traj, env_info, task_info):
    """
    Verify the agent created a PDF report with specific vital signs.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    expected_path_win = metadata.get('expected_output_path', r"C:\Users\Docker\Documents\case3_summary.pdf")
    
    # Map Windows path to something copy_from_env understands
    # The environment usually maps C:\Users\Docker to /home/ga/ or similar, 
    # OR copy_from_env handles the path conversion for the specific env.
    # For vital_recorder_windows_env, we typically use the Windows path directly if the tool supports it,
    # or the result JSON tells us where it is relative to the share.
    # However, copy_from_env usually copies FROM the container path.
    # Let's assume copy_from_env takes the Windows path for Windows envs.
    
    # Paths in result JSON are generated by export_result.ps1
    
    score = 0
    feedback_parts = []
    
    # 1. Get Result JSON
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # The export script saves to C:\Users\Docker\AppData\Local\Temp\task_result.json
        copy_from_env(r"C:\Users\Docker\AppData\Local\Temp\task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task result: {str(e)}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    output_exists = result_data.get('output_exists', False)
    file_created = result_data.get('file_created_during_task', False)
    file_size = result_data.get('output_size_bytes', 0)

    # CRITERION 1: File Existence & Creation (30 pts)
    if output_exists:
        if file_created:
            score += 30
            feedback_parts.append("PDF output created successfully.")
        else:
            score += 10
            feedback_parts.append("PDF output exists but timestamp indicates it wasn't created during this run.")
    else:
        return {"passed": False, "score": 0, "feedback": "No PDF output file found."}

    # CRITERION 2: File Validity & Size (10 pts)
    if file_size > 1000: # Arbitrary small limit for empty file
        score += 10
    else:
        feedback_parts.append("PDF file seems too small/empty.")

    # CRITERION 3: Content Verification (60 pts)
    # We need to analyze the PDF content.
    temp_pdf = tempfile.NamedTemporaryFile(delete=False, suffix='.pdf')
    try:
        copy_from_env(expected_path_win, temp_pdf.name)
        
        pdf_text = ""
        if PDFMINER_AVAILABLE:
            try:
                pdf_text = extract_text(temp_pdf.name)
            except Exception as e:
                logger.warning(f"PDF extraction failed: {e}")
                # Fallback: simple binary check for strings if extraction fails
                with open(temp_pdf.name, 'rb') as f:
                    pdf_content = f.read()
                    # Convert binary to string loosely for keyword search
                    pdf_text = str(pdf_content)
        else:
            # Fallback for when pdfminer is not installed
            with open(temp_pdf.name, 'rb') as f:
                pdf_content = f.read()
                pdf_text = str(pdf_content)

        # Normalize text
        pdf_text_upper = pdf_text.upper()

        # Check Required Tracks (Positive Constraints)
        required_tracks = ["HR", "SPO2", "MBP", "CO2"] # Basic keywords
        # Note: VitalDB often labels as "Solar8000/HR", "ART_MBP", etc.
        # We check for generic terms or specific substrings
        
        found_required = 0
        missing_required = []
        for term in required_tracks:
            # Allow some flexibility, e.g. "ART_MBP" covers "MBP"
            if term in pdf_text_upper or (term == "MBP" and "ART_MBP" in pdf_text_upper) or (term == "CO2" and "ETCO2" in pdf_text_upper):
                found_required += 1
            else:
                missing_required.append(term)
        
        # 30 points for required tracks
        if found_required == len(required_tracks):
            score += 30
            feedback_parts.append("All required tracks found in PDF.")
        elif found_required > 0:
            partial_pts = int(30 * (found_required / len(required_tracks)))
            score += partial_pts
            feedback_parts.append(f"Missing some required tracks: {', '.join(missing_required)}.")
        else:
            feedback_parts.append("No required vital sign labels found in PDF.")

        # Check Forbidden Tracks (Negative Constraints)
        forbidden_tracks = ["ECG", "PLETH", "AWP", "TV"] 
        # "ECG" usually appears as "ECG_II", "ECG_V5". "PLETH" is pulse ox waveform.
        
        found_forbidden = []
        for term in forbidden_tracks:
            if term in pdf_text_upper:
                found_forbidden.append(term)
        
        # 30 points for cleanliness
        if not found_forbidden:
            score += 30
            feedback_parts.append("PDF view is clean (no extraneous tracks).")
        else:
            feedback_parts.append(f"PDF contains excluded tracks: {', '.join(found_forbidden)}.")

    except Exception as e:
        feedback_parts.append(f"Error analyzing PDF content: {str(e)}")
    finally:
        if os.path.exists(temp_pdf.name):
            os.unlink(temp_pdf.name)

    passed = (score >= 80)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback_parts)
    }