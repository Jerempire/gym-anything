#!/bin/bash
echo "=== Setting up build_comparative_study_dashboard task ==="

source /workspace/scripts/task_utils.sh

# Record task start time
date +%s > /tmp/task_start_time.txt

# Create the data tiddlers directly in the tiddlers directory
echo "Creating EnergySource tiddlers..."

cat > "$TIDDLER_DIR/Solar Power.tid" << 'EOF'
title: Solar Power
tags: EnergySource
created: 20231012120000000

Solar power is the conversion of energy from sunlight into electricity. This can be done directly using photovoltaics (PV), or indirectly using concentrated solar power (CSP). It is a rapidly growing renewable energy source with decreasing costs, though it requires energy storage solutions for nighttime and cloudy conditions.
EOF

cat > "$TIDDLER_DIR/Wind Power.tid" << 'EOF'
title: Wind Power
tags: EnergySource
created: 20231012120500000

Wind power involves using wind turbines to generate electricity. It is a popular, sustainable, and renewable energy source that has a much smaller impact on the environment compared to burning fossil fuels. Offshore wind farms are becoming increasingly common due to stronger and more consistent winds at sea.
EOF

cat > "$TIDDLER_DIR/Geothermal Energy.tid" << 'EOF'
title: Geothermal Energy
tags: EnergySource
created: 20231012121000000

Geothermal energy is thermal energy generated and stored in the Earth. It is a reliable, baseload renewable energy source because it operates continuously, unaffected by weather or time of day. However, it is geographically limited to areas near tectonic plate boundaries.
EOF

cat > "$TIDDLER_DIR/Hydroelectric Power.tid" << 'EOF'
title: Hydroelectric Power
tags: EnergySource
created: 20231012121500000

Hydroelectric power is generated by harnessing the energy of flowing water, typically through a dam. It is currently the largest source of renewable electricity globally. While highly efficient, large-scale hydroelectric projects can have significant environmental and social impacts on river ecosystems.
EOF

# Fix permissions
chown ga:ga "$TIDDLER_DIR"/*.tid

# Record initial tiddler count
INITIAL_COUNT=$(count_user_tiddlers)
echo "$INITIAL_COUNT" > /tmp/initial_tiddler_count
echo "Initial tiddler count: $INITIAL_COUNT"

# Verify TiddlyWiki is running
echo "Waiting for TiddlyWiki..."
for i in {1..15}; do
    if curl -s http://localhost:8080/ > /dev/null 2>&1; then
        echo "TiddlyWiki server is running"
        break
    fi
    sleep 1
done

# Ensure Firefox is focused and ready
echo "Focusing Firefox..."
DISPLAY=:1 xdotool search --name "TiddlyWiki\|firefox\|Mozilla" windowactivate 2>/dev/null || true
sleep 1

# Take initial screenshot
take_screenshot /tmp/task_initial_state.png

echo "=== Task setup complete ==="