#!/usr/bin/env python3
"""
Verifier for clinical_trial_cohort task.

Logic:
1. Reads the /tmp/task_result.json generated by export_result.sh inside the container.
2. Applies the scoring rubric defined in the task description.
"""

import json
import logging
import os
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_clinical_trial_cohort(traj, env_info, task_info):
    """
    Verify the clinical trial cohort extraction task.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve analysis result from container
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve verification results: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback = []
    
    # 1. CSV Existence and Format (20 pts)
    if result.get('csv_exists'):
        score += 10
        feedback.append("CSV file exists.")
        if result.get('csv_valid_format'):
            score += 10
            feedback.append("CSV format is valid.")
        else:
            feedback.append("CSV missing required columns.")
    else:
        feedback.append("CSV file NOT found.")

    # 2. Data Accuracy (45 pts)
    if result.get('row_count_match'):
        score += 15
        feedback.append("Patient count matches database.")
    else:
        feedback.append("Patient count mismatch.")

    age_acc = result.get('age_accuracy', 0.0)
    if age_acc >= 0.8:
        score += 15
        feedback.append(f"Age calculations correct ({int(age_acc*100)}%).")
    else:
        feedback.append(f"Age calculations inaccurate ({int(age_acc*100)}%).")

    elig_acc = result.get('eligibility_accuracy', 0.0)
    if elig_acc >= 0.9:
        score += 15
        feedback.append(f"Eligibility classification correct ({int(elig_acc*100)}%).")
    else:
        feedback.append(f"Eligibility classification inaccurate ({int(elig_acc*100)}%).")

    # 3. Logic Consistency (5 pts)
    if result.get('exclusion_reason_ok'):
        score += 5
        feedback.append("Exclusion reasons logical.")
    
    # 4. Summary Report (30 pts)
    if result.get('summary_exists'):
        score += 5
        feedback.append("Summary report exists.")
        
        if result.get('summary_content_ok'):
            score += 5
            feedback.append("Summary contains protocol ID and counts.")
        
        if result.get('cim10_codes_present'):
            score += 10
            feedback.append("Summary lists diabetes CIM10 codes.")
        else:
            feedback.append("Summary missing diabetes codes.")
            
        if result.get('db_table_list_present'):
            score += 5
            feedback.append("Summary lists database tables.")
    else:
        feedback.append("Summary report NOT found.")

    # Anti-gaming check (timestamps)
    # This is implicitly checked in export_result.sh and reported in errors
    if any("pre-dates task start" in err for err in result.get('errors', [])):
        score = 0
        feedback.append("FAIL: Output files pre-date task start (anti-gaming).")
    else:
        score += 5 # Bonus for valid timestamps

    # Final tally
    passed = (score >= 60) and result.get('csv_exists') and (elig_acc > 0.5)

    return {
        "passed": passed,
        "score": min(100, score),
        "feedback": " | ".join(feedback),
        "details": result
    }