#!/usr/bin/env python3
"""
Verifier for Fire Safety Inspection Report task.
Reads the JSON result generated by export_result.sh inside the environment.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_fire_safety_report(traj, env_info, task_info):
    """
    Verify the fire safety report creation task.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Load result from environment
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # Scoring Variables
    score = 0
    feedback_parts = []
    
    # 1. GATE: File Existence & Size (5 pts)
    # Must be > 5KB to contain minimal required content
    if not result.get("file_exists", False):
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Report file FSI_Report_Glenwood_2024.odt not found."
        }
    
    if result.get("file_size", 0) < 5000:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Report file is too small/empty (<5KB)."
        }
    
    score += 5
    feedback_parts.append("File exists")

    # 2. Structure: Table of Contents (15 pts)
    if result.get("has_toc", False):
        score += 15
        feedback_parts.append("TOC present")
    else:
        feedback_parts.append("TOC missing")

    # 3. Structure: Heading 1 (15 pts)
    h1 = result.get("heading1_count", 0)
    if h1 >= 6:
        score += 15
        feedback_parts.append(f"H1 count OK ({h1})")
    elif h1 >= 3:
        score += 7
        feedback_parts.append(f"H1 count partial ({h1}/6)")
    else:
        feedback_parts.append(f"H1 styles missing/low ({h1})")

    # 4. Structure: Heading 2 (10 pts)
    h2 = result.get("heading2_count", 0)
    if h2 >= 8:
        score += 10
        feedback_parts.append(f"H2 count OK ({h2})")
    elif h2 >= 4:
        score += 5
        feedback_parts.append(f"H2 count partial ({h2}/8)")
    else:
        feedback_parts.append(f"H2 styles missing/low ({h2})")

    # 5. Content: Tables (20 pts)
    tables = result.get("table_count", 0)
    if tables >= 6:
        score += 20
        feedback_parts.append(f"Tables OK ({tables})")
    elif tables >= 3:
        score += 10
        feedback_parts.append(f"Tables partial ({tables}/6)")
    else:
        feedback_parts.append(f"Tables missing/low ({tables})")

    # 6. Structure: Footer/Page Numbers (10 pts)
    if result.get("has_page_numbers", False):
        score += 10
        feedback_parts.append("Page numbers OK")
    else:
        feedback_parts.append("Page numbers missing")

    # 7. Content: Body Text / Paragraphs (5 pts)
    paras = result.get("paragraph_count", 0)
    if paras >= 40:
        score += 5
        feedback_parts.append("Content length OK")
    else:
        feedback_parts.append(f"Content sparse ({paras} paras)")

    # 8. Content: Building Names (10 pts)
    content_check = result.get("text_content_check", {})
    b_names = content_check.get("building_names_found", 0)
    if b_names >= 5:
        score += 10
        feedback_parts.append("All buildings found")
    elif b_names >= 3:
        score += 5
        feedback_parts.append(f"Some buildings found ({b_names}/5)")
    else:
        feedback_parts.append(f"Buildings missing ({b_names}/5)")

    # 9. Content: NFPA/Terms (5 pts)
    if content_check.get("nfpa_terms_found", False):
        score += 5
        feedback_parts.append("NFPA terms found")
    else:
        feedback_parts.append("NFPA terms missing")

    # 10. Content: Scores (5 pts)
    s_found = content_check.get("scores_found", 0)
    if s_found >= 3:
        score += 5
        feedback_parts.append("Scores found")
    else:
        feedback_parts.append("Scores missing")

    # Final Calculation
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": ", ".join(feedback_parts)
    }