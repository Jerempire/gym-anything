#!/usr/bin/env python3
"""
Verifier for configure_local_admin_alert task.

Verification Strategy:
1. Validates that a screenshot was captured.
2. Uses VLM (Vision-Language Model) to inspect the screenshot and trajectory.
3. Checks specifically for the 'Local Admin Monitor' profile, 'Critical' severity, 
   and the correct 'Member Added to Local Group' category with 'Administrators' filter.
"""

import json
import os
import sys
import tempfile
import logging
from typing import Dict, Any

# Setup logging
logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# Import VLM utilities if available in the framework
try:
    from gym_anything.vlm import query_vlm, get_final_screenshot, sample_trajectory_frames
except ImportError:
    # Fallback/Mock for development if not in actual env
    def query_vlm(prompt, image=None, images=None):
        return {"success": False, "error": "VLM module not found"}
    def get_final_screenshot(traj):
        return None
    def sample_trajectory_frames(traj, n=5):
        return []

def verify_configure_local_admin_alert(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]):
    """
    Verify the configuration of the local admin alert.
    """
    # 1. Setup and read result file from container
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: copy_from_env not available"}

    temp_result_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    result_data = {}
    
    try:
        # Copy the JSON result file generated by export_result.ps1
        # Path must match what's in export_result.ps1
        copy_from_env("C:\\workspace\\task_result.json", temp_result_file.name)
        with open(temp_result.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        logger.error(f"Failed to read task result: {e}")
        return {"passed": False, "score": 0, "feedback": "Could not read task result file. Did the agent run the export script?"}
    finally:
        if os.path.exists(temp_result_file.name):
            os.unlink(temp_result.name)

    # 2. Extract basic metrics
    screenshot_exists = result_data.get('screenshot_exists', False)
    screenshot_path = result_data.get('screenshot_path', "")
    
    if not screenshot_exists:
        return {"passed": False, "score": 0, "feedback": "No final screenshot found. Task incomplete."}

    # 3. VLM Verification
    # We use both trajectory frames (process) and the final screenshot (outcome)
    traj_frames = sample_trajectory_frames(traj, n=4)
    final_img = get_final_screenshot(traj)
    
    # Prompt for VLM
    prompt = """
    You are a cybersecurity auditor verifying a task in ManageEngine ADAudit Plus.
    
    Task Goal: Create a NEW Alert Profile with these exact settings:
    1. Name: "Local Admin Monitor"
    2. Severity: "Critical"
    3. Category: "Member Server Audit" -> "Member Added to Local Group" (NOT Domain group)
    4. Filter: "Group Name" contains "Administrators"
    
    Review the provided screenshots.
    
    Check for:
    - Is the "Alert Profiles" page visible?
    - Do you see a profile named "Local Admin Monitor"?
    - Is its severity marked as Critical (often red icon)?
    - Do you see the category "Member Added to Local Group"?
    - Do you see a filter configuration for "Administrators"?
    - Is the status "Enabled" (green check)?
    
    Provide a JSON response:
    {
        "profile_name_correct": boolean,
        "severity_critical": boolean,
        "category_correct": boolean,
        "filter_correct": boolean,
        "status_enabled": boolean,
        "confidence": "low/medium/high",
        "explanation": "string"
    }
    """
    
    # Combine frames for context
    images_to_check = traj_frames + [final_img] if final_img else traj_frames
    
    vlm_response = query_vlm(prompt=prompt, images=images_to_check)
    
    if not vlm_response.get('success'):
        return {"passed": False, "score": 0, "feedback": "Verification failed: VLM error"}
        
    audit = vlm_response.get('parsed', {})
    
    # 4. Scoring Logic
    score = 0
    feedback = []
    
    # Criteria 1: Profile Name (30 pts)
    if audit.get('profile_name_correct'):
        score += 30
        feedback.append("Profile 'Local Admin Monitor' found.")
    else:
        feedback.append("Profile name incorrect or not found.")

    # Criteria 2: Category (30 pts)
    if audit.get('category_correct'):
        score += 30
        feedback.append("Correct category 'Member Added to Local Group' selected.")
    else:
        feedback.append("Category incorrect (must be Member Server Audit > Added to Local Group).")

    # Criteria 3: Filter (20 pts)
    if audit.get('filter_correct'):
        score += 20
        feedback.append("Filter for 'Administrators' group found.")
    else:
        feedback.append("Filter configuration missing or incorrect.")
        
    # Criteria 4: Severity (10 pts)
    if audit.get('severity_critical'):
        score += 10
        feedback.append("Severity set to Critical.")
    else:
        feedback.append("Severity not set to Critical.")

    # Criteria 5: Status (10 pts)
    if audit.get('status_enabled'):
        score += 10
        feedback.append("Profile is enabled.")
    else:
        feedback.append("Profile is not enabled.")

    passed = (score >= 90) # Strict passing threshold because security alerts must be precise
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback),
        "details": audit
    }