#!/usr/bin/env python3
"""
Verifier for customize_issue_priorities task.

Verification Strategy:
1. Load the JSON export from the container (generated by rails runner).
2. Validate:
   - "Immediate" is gone.
   - "Critical" exists.
   - "Blocker" exists.
   - "Low" is inactive.
   - "Normal" is default.
   - Check sort order of active items.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_customize_issue_priorities(traj, env_info, task_info):
    """
    Verify the Redmine Issue Priorities configuration.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve result file
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result file: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    priorities = result.get('priorities', [])
    if not priorities:
        return {"passed": False, "score": 0, "feedback": "No priority data found in export"}

    # Convert list to dict for easier lookup by name
    # We verify names case-insensitively just in case, though instructions were specific
    p_map = {p['name']: p for p in priorities}
    
    score = 0
    feedback = []
    
    # 1. Rename "Immediate" to "Critical" (20 pts)
    # Success: "Critical" exists, "Immediate" does not
    has_critical = 'Critical' in p_map
    has_immediate = 'Immediate' in p_map
    
    if has_critical and not has_immediate:
        score += 20
        feedback.append("Renamed Immediate to Critical")
    elif has_critical:
        score += 10
        feedback.append("Created Critical but Immediate still exists (did not rename?)")
    else:
        feedback.append("Failed to create 'Critical' priority")

    # 2. Create "Blocker" (20 pts)
    if 'Blocker' in p_map:
        if p_map['Blocker']['active']:
            score += 20
            feedback.append("Created active Blocker priority")
        else:
            score += 10
            feedback.append("Created Blocker but it is inactive")
    else:
        feedback.append("Blocker priority not found")

    # 3. Deactivate "Low" (15 pts) + Preserve Low (10 pts)
    # Total 25 pts for handling Low correctly
    if 'Low' in p_map:
        score += 10 # Preserved
        if not p_map['Low']['active']:
            score += 15
            feedback.append("Correctly deactivated Low")
        else:
            feedback.append("Low is still active")
    else:
        feedback.append("Low priority was deleted (should have been deactivated)")

    # 4. "Normal" is default (15 pts)
    if 'Normal' in p_map and p_map['Normal']['is_default']:
        score += 15
        feedback.append("Normal is default")
    else:
        feedback.append("Normal is not set as default")

    # 5. Check Sort Order (20 pts)
    # Expected order of ACTIVE items: Normal < High < Urgent < Critical < Blocker
    # We extract active items and sort them by position
    active_items = sorted([p for p in priorities if p['active']], key=lambda x: x['position'])
    active_names = [p['name'] for p in active_items]
    
    # Filter to only the ones we care about for the order check
    # This makes it robust if they added extra random ones
    target_chain = ["Normal", "High", "Urgent", "Critical", "Blocker"]
    
    # Check if target_chain appears as a subsequence in active_names
    # We create a mapping of name -> index in active_names
    try:
        indices = [active_names.index(name) for name in target_chain]
        if indices == sorted(indices):
            score += 20
            feedback.append("Sort order is correct")
        else:
            feedback.append(f"Sort order incorrect. Found: {active_names}")
    except ValueError:
        # Missing items
        feedback.append("Cannot verify sort order due to missing priorities")

    return {
        "passed": score >= 70,
        "score": score,
        "feedback": "; ".join(feedback)
    }