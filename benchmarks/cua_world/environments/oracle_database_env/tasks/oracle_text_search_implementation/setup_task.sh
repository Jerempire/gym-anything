#!/bin/bash
set -e
echo "=== Setting up Oracle Text Search Task ==="

# Source shared utilities
source /workspace/scripts/task_utils.sh

# Record task start time
date +%s > /tmp/task_start_time.txt

# Ensure Oracle is running
if ! pgrep -f "oracle" > /dev/null && ! docker ps | grep -q "oracle-xe"; then
    echo "Starting Oracle..."
    # Rely on environment post_start hooks, but wait for it
    sleep 5
fi

# Wait for Oracle to be ready
echo "Waiting for Oracle database..."
wait_for_oracle_ready() {
    for i in {1..60}; do
        if sudo docker exec oracle-xe sqlplus -s system/OraclePassword123@localhost:1521/XEPDB1 <<< "SELECT 1 FROM dual;" | grep -q "1"; then
            return 0
        fi
        sleep 2
    done
    return 1
}
wait_for_oracle_ready

# Grant Oracle Text permissions to HR (Required for CTX_DDL)
echo "Granting CTX permissions to HR..."
sudo docker exec -i oracle-xe sqlplus -s system/OraclePassword123@localhost:1521/XEPDB1 << 'SQLEOF'
GRANT CTXAPP TO hr;
GRANT EXECUTE ON CTXSYS.CTX_DDL TO hr;
GRANT EXECUTE ON CTXSYS.CTX_DOC TO hr;
GRANT EXECUTE ON CTXSYS.CTX_OUTPUT TO hr;
ALTER USER hr QUOTA UNLIMITED ON users;
EXIT;
SQLEOF

# Generate Data Loading Script (Python to ensure diverse text)
cat > /tmp/gen_data.py << 'PYEOF'
import random

titles = [
    "The Great Gatsby", "Moby Dick", "Pride and Prejudice", "1984", "Brave New World",
    "To Kill a Mockingbird", "The Catcher in the Rye", "The Hobbit", "Fahrenheit 451", "Jane Eyre"
]

authors = [
    "F. Scott Fitzgerald", "Herman Melville", "Jane Austen", "George Orwell", "Aldous Huxley",
    "Harper Lee", "J.D. Salinger", "J.R.R. Tolkien", "Ray Bradbury", "Charlotte Bronte"
]

# Words for stemming tests
stems = [
    ("run", "running", "ran"),
    ("swim", "swimming", "swam"),
    ("fly", "flying", "flew"),
    ("mouse", "mice", "mouse"),
    ("leaf", "leaves", "leaf")
]

# Stopwords to test
stopwords = ["edition", "volume", "series", "copyright", "publisher"]

print("DROP TABLE library_catalog CASCADE CONSTRAINTS;")
print("""
CREATE TABLE library_catalog (
    book_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR2(255),
    synopsis CLOB,
    author VARCHAR2(100),
    published_date DATE
);
""")

print("INSERT INTO library_catalog (title, author, synopsis, published_date) VALUES ('Reference Manual', 'System', 'This is the first edition of the volume on series copyright by the publisher.', SYSDATE);")

for i in range(100):
    t = random.choice(titles)
    a = random.choice(authors)
    
    # Construct a synopsis
    stem_group = random.choice(stems)
    word = random.choice(stem_group)
    stop = random.choice(stopwords)
    
    synopsis = f"This is a synopsis for {t}. The character decided to {word} away quickly. This text also mentions {stop} number {i}."
    
    print(f"INSERT INTO library_catalog (title, author, synopsis, published_date) VALUES ('{t} {i}', '{a}', '{synopsis}', SYSDATE);")

print("COMMIT;")
PYEOF

# Execute data generation
echo "Generating data SQL..."
python3 /tmp/gen_data.py > /tmp/load_data.sql

# Load Data
echo "Loading Library Data..."
sudo docker exec -i oracle-xe sqlplus -s hr/hr123@localhost:1521/XEPDB1 < "/tmp/load_data.sql" > /dev/null

# Create Legacy Script Artifact
cat > /home/ga/Desktop/legacy_search.sql << 'EOF'
-- CURRENT SLOW SEARCH IMPLEMENTATION
-- This uses standard wildcards which are slow and don't handle word stems
-- (e.g., searching for 'run' misses 'running')

VAR search_term VARCHAR2(100);
EXEC :search_term := '%whale%';

SELECT title, author 
FROM library_catalog 
WHERE LOWER(synopsis) LIKE LOWER(:search_term);
EOF

# Ensure DBeaver is installed (for agent convenience)
if ! which dbeaver-ce > /dev/null 2>&1; then
    echo "Installing DBeaver..."
    sudo snap install dbeaver-ce --classic 2>/dev/null || true
fi

# Take initial screenshot
echo "Capturing initial state..."
sleep 2
DISPLAY=:1 scrot /tmp/task_initial.png 2>/dev/null || true

echo "=== Task setup complete ==="