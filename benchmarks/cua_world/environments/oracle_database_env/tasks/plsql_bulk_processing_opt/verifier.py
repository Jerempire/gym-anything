#!/usr/bin/env python3
"""
Verifier for PL/SQL Bulk Processing Optimization task.

Scoring Breakdown (100 pts):
- Procedure Validity (10 pts): Procedure exists and is VALID.
- Static Code Analysis (50 pts):
  - Uses BULK COLLECT (20 pts)
  - Uses FORALL (20 pts)
  - Uses LIMIT clause (10 pts)
- Functional Correctness (40 pts):
  - Processes exactly 50,000 records (20 pts)
  - Calculates correct balances (20 pts)

Uses copy_from_env to read the JSON result generated by export_result.sh
"""

import json
import logging
import os
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_bulk_processing(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy unavailable"}

    # Get result file
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    score = 0
    feedback_parts = []
    
    # 1. Procedure Status (10 pts)
    status = result.get("procedure_status", "MISSING")
    if status == "VALID":
        score += 10
        feedback_parts.append("Procedure is VALID")
    else:
        feedback_parts.append(f"Procedure status is {status}")
        return {"passed": False, "score": 0, "feedback": "Procedure not valid", "details": result}

    # 2. Static Analysis (50 pts)
    if result.get("bulk_collect_found"):
        score += 20
        feedback_parts.append("Uses BULK COLLECT (+20)")
    else:
        feedback_parts.append("Missing BULK COLLECT")

    if result.get("forall_found"):
        score += 20
        feedback_parts.append("Uses FORALL (+20)")
    else:
        feedback_parts.append("Missing FORALL")

    if result.get("limit_found"):
        score += 10
        feedback_parts.append("Uses LIMIT (+10)")
    else:
        feedback_parts.append("Missing LIMIT clause (memory risk)")

    # 3. Functional Test (40 pts)
    log_count = result.get("log_count", 0)
    if log_count == 50000:
        score += 20
        feedback_parts.append("Processed 50,000 records (+20)")
    else:
        feedback_parts.append(f"Processed {log_count}/50,000 records")

    if result.get("functional_test_passed"):
        score += 20
        feedback_parts.append("Balances match expected total (+20)")
    elif log_count == 50000:
        feedback_parts.append("Balances incorrect despite correct record count")

    # Bonus info
    exec_time = result.get("execution_time_sec", 0)
    if exec_time > 0:
        feedback_parts.append(f"Execution time: {exec_time:.2f}s")

    return {
        "passed": score >= 70,
        "score": score,
        "feedback": " | ".join(feedback_parts),
        "details": result
    }