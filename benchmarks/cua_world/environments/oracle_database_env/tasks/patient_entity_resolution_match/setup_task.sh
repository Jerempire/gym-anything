#!/bin/bash
# Setup script for Patient Entity Resolution task
# Generates synthetic patient data with specific "hard" matching cases (typos, formatting)

set -e

echo "=== Setting up Patient Entity Resolution Task ==="

source /workspace/scripts/task_utils.sh

# --- Pre-flight Checks ---
echo "[1/4] Checking Oracle container..."
if ! sudo docker ps | grep -q "$ORACLE_CONTAINER"; then
    echo "ERROR: Oracle container not running!"
    exit 1
fi

# --- Clean up previous state ---
echo "[2/4] Cleaning up previous artifacts..."
oracle_query "
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE patient_linkage CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE clinic_intake CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE master_patient_index CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
" "hr" > /dev/null 2>&1 || true

rm -f /home/ga/Desktop/linkage_report.csv
date +%s > /tmp/task_start_timestamp

# --- Generate Data ---
echo "[3/4] Generating Patient Data (Master Index + Clinic Intake)..."

# We use a PL/SQL block to generate data.
# 1. Create Tables
# 2. Populate Master Index (1000 rows)
# 3. Populate Intake (50 rows) with specific test cases
oracle_query "
CREATE TABLE master_patient_index (
    mpi_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name  VARCHAR2(100),
    last_name   VARCHAR2(100),
    dob         DATE,
    gender      CHAR(1)
);

CREATE TABLE clinic_intake (
    intake_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name  VARCHAR2(100),
    last_name   VARCHAR2(100),
    dob         DATE,
    gender      CHAR(1)
);

DECLARE
    TYPE name_array IS TABLE OF VARCHAR2(50);
    first_names name_array := name_array('James', 'John', 'Robert', 'Michael', 'William', 'David', 'Richard', 'Joseph', 'Thomas', 'Charles', 'Mary', 'Patricia', 'Jennifer', 'Linda', 'Elizabeth', 'Barbara', 'Susan', 'Jessica', 'Sarah', 'Karen');
    last_names  name_array := name_array('Smith', 'Johnson', 'Williams', 'Brown', 'Jones', 'Miller', 'Davis', 'Garcia', 'Rodriguez', 'Wilson', 'Martinez', 'Anderson', 'Taylor', 'Thomas', 'Hernandez', 'Moore', 'Martin', 'Jackson', 'Thompson', 'White');
    
    v_fname VARCHAR2(100);
    v_lname VARCHAR2(100);
    v_dob   DATE;
    v_gender CHAR(1);
    
    -- Specific test variables
    v_mpi_id NUMBER;
BEGIN
    -- 1. Generate 1000 random Master records
    FOR i IN 1..1000 LOOP
        v_fname := first_names(TRUNC(DBMS_RANDOM.VALUE(1, first_names.COUNT + 1)));
        v_lname := last_names(TRUNC(DBMS_RANDOM.VALUE(1, last_names.COUNT + 1)));
        -- Random DOB between 1950 and 2000
        v_dob := TO_DATE('01-01-1950', 'DD-MM-YYYY') + TRUNC(DBMS_RANDOM.VALUE(0, 18000));
        
        IF MOD(i, 2) = 0 THEN v_gender := 'M'; ELSE v_gender := 'F'; END IF;
        
        INSERT INTO master_patient_index (first_name, last_name, dob, gender)
        VALUES (v_fname, v_lname, v_dob, v_gender);
    END LOOP;
    
    -- 2. Insert Specific 'Hard' Matching Cases (PLANTED MATCHES)
    
    -- Case A: Exact Match (Intake ID will be 1)
    INSERT INTO master_patient_index (first_name, last_name, dob, gender) 
    VALUES ('Jonathan', 'Doe', DATE '1980-05-15', 'M') RETURNING mpi_id INTO v_mpi_id;
    
    INSERT INTO clinic_intake (intake_id, first_name, last_name, dob, gender)
    VALUES (1, 'Jonathan', 'Doe', DATE '1980-05-15', 'M');
    
    -- Case B: Typo in First Name (Similarity > 90) (Intake ID 2)
    -- Jonathan vs Jonathon
    INSERT INTO master_patient_index (first_name, last_name, dob, gender) 
    VALUES ('Christopher', 'Nolan', DATE '1975-08-20', 'M') RETURNING mpi_id INTO v_mpi_id;
    
    INSERT INTO clinic_intake (intake_id, first_name, last_name, dob, gender)
    VALUES (2, 'Christoper', 'Nolan', DATE '1975-08-20', 'M'); -- Missing 'h'
    
    -- Case C: Formatting / Apostrophe (Intake ID 3)
    INSERT INTO master_patient_index (first_name, last_name, dob, gender) 
    VALUES ('Sarah', 'O''Connor', DATE '1990-12-10', 'F') RETURNING mpi_id INTO v_mpi_id;
    
    INSERT INTO clinic_intake (intake_id, first_name, last_name, dob, gender)
    VALUES (3, 'Sarah', 'Oconnor', DATE '1990-12-10', 'F');
    
    -- Case D: Abbreviation/Variation (Intake ID 4)
    INSERT INTO master_patient_index (first_name, last_name, dob, gender) 
    VALUES ('William', 'Gates', DATE '1955-10-28', 'M') RETURNING mpi_id INTO v_mpi_id;
    
    INSERT INTO clinic_intake (intake_id, first_name, last_name, dob, gender)
    VALUES (4, 'Bill', 'Gates', DATE '1955-10-28', 'M'); -- 'Bill' vs 'William' usually fails Jaro-Winkler 90 threshold, this is a TRAP (Should NOT match)
    
    -- Case E: Different Person (Same Name, Diff DOB) (Intake ID 5)
    INSERT INTO master_patient_index (first_name, last_name, dob, gender) 
    VALUES ('Michael', 'Jordan', DATE '1963-02-17', 'M');
    
    INSERT INTO clinic_intake (intake_id, first_name, last_name, dob, gender)
    VALUES (5, 'Michael', 'Jordan', DATE '1980-01-01', 'M'); -- Diff DOB, should NOT match
    
    -- 3. Fill the rest of Intake with random noise (non-matches)
    FOR i IN 6..50 LOOP
        v_fname := first_names(TRUNC(DBMS_RANDOM.VALUE(1, first_names.COUNT + 1)));
        v_lname := last_names(TRUNC(DBMS_RANDOM.VALUE(1, last_names.COUNT + 1)));
        v_dob := SYSDATE - 10000; -- Unlikely to collide exactly
        
        INSERT INTO clinic_intake (intake_id, first_name, last_name, dob, gender)
        VALUES (i, v_fname, v_lname, v_dob, 'F');
    END LOOP;
    
    COMMIT;
END;
/
" "hr" > /dev/null 2>&1

# --- Save Ground Truth Keys for Verifier ---
# We need to know the MPI_IDs generated for our planted cases.
# We query the DB to find them.

# Case A: Jonathan Doe
ID_A=$(oracle_query_raw "SELECT mpi_id FROM master_patient_index WHERE first_name='Jonathan' AND last_name='Doe'" "hr" | tr -d ' ')
# Case B: Christopher Nolan
ID_B=$(oracle_query_raw "SELECT mpi_id FROM master_patient_index WHERE first_name='Christopher' AND last_name='Nolan'" "hr" | tr -d ' ')
# Case C: Sarah O'Connor
ID_C=$(oracle_query_raw "SELECT mpi_id FROM master_patient_index WHERE first_name='Sarah' AND last_name='O''Connor'" "hr" | tr -d ' ')

# Save these IDs to a hidden file for the export script to use
echo "{\"case_a\": $ID_A, \"case_b\": $ID_B, \"case_c\": $ID_C}" > /tmp/planted_match_ids.json
chmod 600 /tmp/planted_match_ids.json

echo "[4/4] Data generation complete. Planted IDs: A=$ID_A, B=$ID_B, C=$ID_C"

# --- Launch DBeaver (Agent Convenience) ---
echo "Launching DBeaver..."
if ! pgrep -f dbeaver > /dev/null; then
    su - ga -c "DISPLAY=:1 /usr/local/bin/dbeaver &" > /dev/null 2>&1 || true
    sleep 5
fi

take_screenshot /tmp/task_initial.png