#!/bin/bash
# Setup script for SQL Macros Fiscal Reporting task
# Creates the SALES_LEDGER table and populates it with realistic transaction data

set -e

echo "=== Setting up SQL Macros Fiscal Reporting Task ==="

source /workspace/scripts/task_utils.sh

# --- Pre-flight: Verify Oracle is running ---
echo "[1/4] Checking Oracle container..."
if ! sudo docker ps | grep -q "$ORACLE_CONTAINER"; then
    echo "ERROR: Oracle container not running!"
    exit 1
fi

# --- Pre-flight: Verify HR schema connectivity ---
echo "[2/4] Verifying HR schema connectivity..."
CONN_OK=0
for attempt in 1 2 3; do
    CONN_TEST=$(oracle_query_raw "SELECT 1 FROM DUAL;" "hr" 2>/dev/null | tr -d ' ')
    if [ "$CONN_TEST" == "1" ]; then
        echo "  HR schema ready."
        CONN_OK=1
        break
    fi
    echo "  Attempt $attempt failed, waiting 5s..."
    sleep 5
done
if [ "$CONN_OK" -eq 0 ]; then
    echo "ERROR: Cannot connect to HR schema."
    exit 1
fi

# --- Clean up and Create Data ---
echo "[3/4] Creating SALES_LEDGER table and data..."

# Drop existing objects if any
oracle_query "
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE sales_ledger CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP FUNCTION get_fiscal_year';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP FUNCTION get_high_impact_sales';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/" "hr" > /dev/null 2>&1

# Create Table
oracle_query "
CREATE TABLE sales_ledger (
    txn_id      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    txn_date    DATE NOT NULL,
    amount      NUMBER(10, 2) NOT NULL,
    region      VARCHAR2(20) NOT NULL,
    customer_id NUMBER(6)
);
" "hr" > /dev/null

# Populate with PL/SQL (1500 rows, random dates 2023-2026)
# Logic:
# - Region: EAST, WEST, NORTH, SOUTH
# - Amount: 50 - 5000
# - Date: Random offset from Jan 1 2023
oracle_query "
DECLARE
    v_regions  SYS.ODCIVARCHAR2LIST := SYS.ODCIVARCHAR2LIST('EAST', 'WEST', 'NORTH', 'SOUTH');
    v_date     DATE;
    v_amount   NUMBER;
    v_region   VARCHAR2(20);
BEGIN
    FOR i IN 1..1500 LOOP
        v_region := v_regions(TRUNC(DBMS_RANDOM.VALUE(1, 5)));
        v_amount := ROUND(DBMS_RANDOM.VALUE(50, 5000), 2);
        -- Random date between Jan 1 2023 and Dec 31 2026 (~1460 days)
        v_date := TO_DATE('2023-01-01', 'YYYY-MM-DD') + TRUNC(DBMS_RANDOM.VALUE(0, 1460));
        
        INSERT INTO sales_ledger (txn_date, amount, region, customer_id)
        VALUES (v_date, v_amount, v_region, TRUNC(DBMS_RANDOM.VALUE(100, 999)));
    END LOOP;
    COMMIT;
END;
/" "hr" > /dev/null

COUNT=$(oracle_query_raw "SELECT COUNT(*) FROM sales_ledger;" "hr" | tr -d ' ')
echo "  Created SALES_LEDGER with $COUNT rows."

# Remove any existing report file
rm -f /home/ga/Desktop/fiscal_impact_report.csv

# Record start time
date +%s > /tmp/task_start_time

# --- Final UI Setup ---
echo "[4/4] Ensuring DBeaver is available..."
if ! pgrep -f dbeaver > /dev/null; then
    # Do not auto-launch to avoid clutter, just ensure it's installed
    if ! which dbeaver-ce > /dev/null 2>&1; then
        echo "WARNING: DBeaver not found."
    fi
fi

# Take initial screenshot
take_screenshot /tmp/task_initial.png

echo "=== Setup Complete ==="