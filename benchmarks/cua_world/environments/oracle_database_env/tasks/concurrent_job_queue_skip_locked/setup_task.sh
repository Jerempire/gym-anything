#!/bin/bash
# Setup script for Concurrent Job Queue task
# Creates the PAYMENT_JOBS table and populates it with test data.

set -e

echo "=== Setting up Concurrent Job Queue Task ==="

source /workspace/scripts/task_utils.sh

# --- Verify Oracle is running ---
echo "Checking Oracle container..."
if ! sudo docker ps | grep -q "$ORACLE_CONTAINER"; then
    echo "ERROR: Oracle container not running!"
    exit 1
fi

# --- Wait for DB connectivity ---
echo "Waiting for database connection..."
for i in {1..30}; do
    if oracle_query_raw "SELECT 1 FROM dual;" "hr" >/dev/null 2>&1; then
        echo "Database connected."
        break
    fi
    sleep 2
done

# --- Clean up previous state ---
echo "Cleaning up schema..."
oracle_query "
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE payment_jobs PURGE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP PROCEDURE claim_next_job';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
" "hr" > /dev/null 2>&1 || true

# --- Create Table and Data ---
echo "Creating PAYMENT_JOBS table and data..."
oracle_query "
CREATE TABLE payment_jobs (
    job_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    priority NUMBER NOT NULL,
    status VARCHAR2(20) DEFAULT 'PENDING' CHECK (status IN ('PENDING', 'PROCESSING', 'COMPLETED')),
    worker_id VARCHAR2(50),
    payload VARCHAR2(4000),
    created_at DATE DEFAULT SYSDATE
);

-- Index for performance (hint for the agent, though not strictly required for functional correctness)
CREATE INDEX idx_jobs_priority ON payment_jobs(status, priority DESC, job_id ASC);

-- Insert High Priority (10) - Rows 1-5
INSERT INTO payment_jobs (priority, payload) VALUES (10, '{\"type\": \"instant_transfer\", \"amt\": 5000}');
INSERT INTO payment_jobs (priority, payload) VALUES (10, '{\"type\": \"instant_transfer\", \"amt\": 1200}');
INSERT INTO payment_jobs (priority, payload) VALUES (10, '{\"type\": \"merchant_settle\", \"amt\": 9000}');
INSERT INTO payment_jobs (priority, payload) VALUES (10, '{\"type\": \"vip_refund\", \"amt\": 450}');
INSERT INTO payment_jobs (priority, payload) VALUES (10, '{\"type\": \"crypto_buy\", \"amt\": 30000}');

-- Insert Medium Priority (5) - Rows 6-15
INSERT INTO payment_jobs (priority, payload) SELECT 5, '{\"type\": \"standard_transfer\", \"id\": ' || level || '}' FROM dual CONNECT BY level <= 10;

-- Insert Low Priority (1) - Rows 16-20
INSERT INTO payment_jobs (priority, payload) SELECT 1, '{\"type\": \"audit_log\", \"id\": ' || level || '}' FROM dual CONNECT BY level <= 5;

COMMIT;
" "hr"

# --- Create a naive (blocking) procedure as a trap/reference ---
# This is NOT the solution, but shows what currently exists if they look
oracle_query "
CREATE OR REPLACE PROCEDURE claim_job_naive(p_worker_id IN VARCHAR2, o_job_id OUT NUMBER) IS
BEGIN
    -- This causes blocking contention!
    SELECT job_id INTO o_job_id
    FROM payment_jobs
    WHERE status = 'PENDING'
    ORDER BY priority DESC, job_id ASC
    FETCH FIRST 1 ROWS ONLY
    FOR UPDATE; 
    
    UPDATE payment_jobs SET status = 'PROCESSING', worker_id = p_worker_id WHERE job_id = o_job_id;
    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        o_job_id := NULL;
END;
/
" "hr"

echo "Data setup complete. 20 jobs created."

# Record start time
date +%s > /tmp/task_start_time.txt
# Record initial max ID to ensure no trickery
get_table_count "payment_jobs" "hr" > /tmp/initial_job_count.txt

# --- Launch DBeaver ---
if ! pgrep -f dbeaver > /dev/null; then
    echo "Starting DBeaver..."
    su - ga -c "DISPLAY=:1 /usr/local/bin/sqldeveloper &" 2>/dev/null || \
    su - ga -c "DISPLAY=:1 dbeaver &" 2>/dev/null &
fi

# Take initial screenshot
sleep 5
take_screenshot /tmp/task_initial.png

echo "=== Setup Complete ==="