#!/bin/bash
# Setup script for RFID JSON Analytics task
# Generates realistic RFID event data in JSON format and loads it into Oracle

set -e

echo "=== Setting up RFID JSON Analytics Task ==="

source /workspace/scripts/task_utils.sh

# --- 1. Verify Oracle is running ---
echo "[1/4] Checking Oracle container..."
if ! sudo docker ps | grep -q "$ORACLE_CONTAINER"; then
    echo "ERROR: Oracle container not running!"
    exit 1
fi

# --- 2. Verify HR schema connectivity ---
echo "[2/4] Verifying HR schema..."
for attempt in {1..5}; do
    if oracle_query_raw "SELECT 1 FROM DUAL;" "hr" > /dev/null 2>&1; then
        echo "  HR schema connected."
        break
    fi
    echo "  Waiting for database..."
    sleep 5
done

# --- 3. Clean up and Initialize Schema ---
echo "[3/4] Initializing RFID_EVENTS table..."
oracle_query "
BEGIN
  BEGIN EXECUTE IMMEDIATE 'DROP VIEW rfid_events_flat'; EXCEPTION WHEN OTHERS THEN NULL; END;
  BEGIN EXECUTE IMMEDIATE 'DROP VIEW zone_traffic_summary'; EXCEPTION WHEN OTHERS THEN NULL; END;
  BEGIN EXECUTE IMMEDIATE 'DROP FUNCTION get_tag_history'; EXCEPTION WHEN OTHERS THEN NULL; END;
  BEGIN EXECUTE IMMEDIATE 'DROP TABLE rfid_events PURGE'; EXCEPTION WHEN OTHERS THEN NULL; END;
  
  EXECUTE IMMEDIATE '
    CREATE TABLE rfid_events (
      event_id    NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
      event_data  CLOB CHECK (event_data IS JSON),
      created_at  TIMESTAMP DEFAULT SYSTIMESTAMP
    )';
END;
/" "hr" > /dev/null

# --- 4. Generate and Load Data ---
echo "[4/4] Generating 500+ RFID JSON events..."

# Python script to generate SQL insert statements
cat > /tmp/generate_rfid_data.py << 'PYEOF'
import json
import random
import datetime

FACILITIES = ["WAREHOUSE-A", "WAREHOUSE-B"]
ZONES = ["RECEIVING", "STAGING", "STORAGE-A", "STORAGE-B", "PACKING", "SHIPPING"]
EVENT_TYPES = ["ARRIVAL", "MOVEMENT", "INVENTORY_SCAN", "DEPARTURE"]
CATEGORIES = ["MECHANICAL_PARTS", "ELECTRONICS", "PACKAGING", "CHEMICALS", "TEXTILES"]

# Generate catalog of items
items = []
for i in range(50):
    cat = random.choice(CATEGORIES)
    items.append({
        "sku": f"{cat[:3]}-{random.randint(1000,9999)}-{random.choice(['A','B','C'])}",
        "description": f"Item {i} {cat.lower()}",
        "category": cat,
        "unit_weight_kg": round(random.uniform(0.1, 50.0), 2),
        "unit_value_usd": round(random.uniform(5.0, 500.0), 2)
    })

# Generate tags
tags = []
for i in range(100):
    tags.append(f"3034257BF{random.randint(100000000, 999999999):X}")

sql_file = open("/tmp/insert_rfid_data.sql", "w")
sql_file.write("SET DEFINE OFF;\n")

start_time = datetime.datetime(2024, 11, 15, 8, 0, 0)

for i in range(550):
    tag = random.choice(tags)
    item = random.choice(items)
    facility = random.choice(FACILITIES)
    zone = random.choice(ZONES)
    etype = random.choice(EVENT_TYPES)
    
    # Time progression
    start_time += datetime.timedelta(seconds=random.randint(10, 600))
    read_time = start_time.strftime("%Y-%m-%dT%H:%M:%SZ")
    
    # Logic for event types and zones
    if zone == "RECEIVING": etype = "ARRIVAL" if random.random() > 0.3 else "MOVEMENT"
    if zone == "SHIPPING": etype = "DEPARTURE" if random.random() > 0.3 else "MOVEMENT"
    
    event = {
        "tag_id": tag,
        "epc_uri": f"urn:epc:tag:sgtin-96:1.0614141.{random.randint(100000,999999)}.{random.randint(1000,9999)}",
        "reader": {
            "id": f"RDR-{facility[-1]}-{zone[:3]}{random.randint(1,4):02d}",
            "facility": facility,
            "zone": zone,
            "antenna_port": random.randint(1, 4)
        },
        "read_time": read_time,
        "signal": {
            "rssi_dbm": random.randint(-85, -30),
            "phase_angle": round(random.uniform(0, 6.28), 3),
            "frequency_mhz": random.choice([902.5, 915.0, 928.0])
        },
        "item": item,
        "event_type": etype,
        "batch_id": f"BATCH-2024-{random.randint(100,200)}"
    }
    
    json_str = json.dumps(event)
    # Escape single quotes for SQL
    json_str = json_str.replace("'", "''")
    
    sql_file.write(f"INSERT INTO rfid_events (event_data) VALUES ('{json_str}');\n")

sql_file.write("COMMIT;\n")
sql_file.close()
PYEOF

python3 /tmp/generate_rfid_data.py

# Execute the generated SQL
oracle_query "@ /tmp/insert_rfid_data.sql" "hr" > /dev/null

# Clean up
rm -f /tmp/generate_rfid_data.py /tmp/insert_rfid_data.sql

# Record start state
date +%s > /tmp/task_start_time.txt
rm -f /home/ga/Desktop/warehouse_report.txt

# Ensure DBeaver is available (agent might use it)
if ! pgrep -f dbeaver > /dev/null; then
    su - ga -c "DISPLAY=:1 /usr/local/bin/dbeaver &" > /dev/null 2>&1 || true
fi

# Initial screenshot
take_screenshot /tmp/task_initial.png

echo "=== RFID Data Loaded. Task Ready. ==="