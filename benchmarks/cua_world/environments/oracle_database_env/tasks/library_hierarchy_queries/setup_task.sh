#!/bin/bash
# Setup for library_hierarchy_queries task
# Creates hierarchical tables and populates them with Dewey Decimal Classification data

set -e

echo "=== Setting up Library Hierarchy Queries Task ==="

source /workspace/scripts/task_utils.sh

# --- Pre-flight Check ---
echo "[1/4] Checking Oracle status..."
if ! sudo docker ps | grep -q "$ORACLE_CONTAINER"; then
    echo "ERROR: Oracle container not running"
    exit 1
fi

# --- Cleanup ---
echo "[2/4] Cleaning up prior artifacts..."
oracle_query "
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE library_items CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE library_categories CASCADE CONSTRAINTS';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/" "hr" > /dev/null 2>&1 || true

rm -f /home/ga/Desktop/hierarchy_queries.sql
rm -f /home/ga/Desktop/hierarchy_output.txt

# --- Schema Creation ---
echo "[3/4] Creating library schema..."
oracle_query "
CREATE TABLE library_categories (
    category_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_code VARCHAR2(10) NOT NULL UNIQUE,
    category_name VARCHAR2(100) NOT NULL,
    parent_id NUMBER,
    CONSTRAINT fk_cat_parent FOREIGN KEY (parent_id) REFERENCES library_categories(category_id)
);

CREATE TABLE library_items (
    item_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR2(200) NOT NULL,
    author VARCHAR2(100),
    publication_year NUMBER(4),
    category_id NUMBER,
    CONSTRAINT fk_item_cat FOREIGN KEY (category_id) REFERENCES library_categories(category_id)
);
" "hr" > /dev/null 2>&1

# --- Data Population ---
echo "[4/4] Populating hierarchical data..."
# Using Python for cleaner large-batch insertion logic
python3 << 'PYEOF'
import oracledb
import random

try:
    conn = oracledb.connect(user="hr", password="hr123", dsn="localhost:1521/XEPDB1")
    cursor = conn.cursor()

    # 1. Insert Categories
    # We need to track IDs to link parents
    cats = {} # code -> id

    # Level 1: Main Classes
    main_classes = [
        ('000', 'Computer Science, Information & General Works'),
        ('100', 'Philosophy & Psychology'),
        ('200', 'Religion'),
        ('300', 'Social Sciences'),
        ('400', 'Language'),
        ('500', 'Science'),
        ('600', 'Technology'),
        ('700', 'Arts & Recreation'),
        ('800', 'Literature'),
        ('900', 'History & Geography')
    ]

    for code, name in main_classes:
        cursor.execute("INSERT INTO library_categories (category_code, category_name, parent_id) VALUES (:1, :2, NULL) RETURNING category_id INTO :3", [code, name, cursor.var(int)])
        cats[code] = cursor.var(int).getvalue()[0]

    # Level 2: Divisions (Sample)
    divisions = [
        ('000', '010', 'Bibliography'),
        ('000', '020', 'Library & information sciences'),
        ('500', '510', 'Mathematics'),
        ('500', '520', 'Astronomy'),
        ('500', '530', 'Physics'),
        ('600', '610', 'Medicine & health'),
        ('800', '810', 'American literature'),
        ('900', '930', 'History of ancient world'),
        ('900', '940', 'History of Europe')
    ]

    for p_code, code, name in divisions:
        if p_code in cats:
            cursor.execute("INSERT INTO library_categories (category_code, category_name, parent_id) VALUES (:1, :2, :3) RETURNING category_id INTO :4", [code, name, cats[p_code], cursor.var(int)])
            cats[code] = cursor.var(int).getvalue()[0]

    # Level 3: Sections (Sample)
    sections = [
        ('000', '004', 'Data processing & computer science'), # Leaf, will leave empty of items
        ('000', '005', 'Computer programming, programs & data'), # Leaf, will fill
        ('510', '512', 'Algebra'),
        ('510', '515', 'Analysis'),
        ('520', '523', 'Specific celestial bodies'),
        ('940', '941', 'British Isles')
    ]

    for p_code, code, name in sections:
        # parent code might be level 1 (e.g., 000->004 direct in some DDC simplified views, but strictly 000 is usually parent of 000s)
        # For this hierarchy logic, let's assume 004 is child of 000 if 000 is the closest defined parent
        # But wait, 000 is both a Main Class and a specific range.
        # Let's link 004/005 to 000 for simplicity in this task.
        parent_id = cats.get(p_code)
        if parent_id:
            cursor.execute("INSERT INTO library_categories (category_code, category_name, parent_id) VALUES (:1, :2, :3) RETURNING category_id INTO :4", [code, name, parent_id, cursor.var(int)])
            cats[code] = cursor.var(int).getvalue()[0]

    conn.commit()

    # 2. Insert Items
    # We want:
    # - 005 (Programming): Many items
    # - 004 (Data processing): ZERO items (for Q4)
    # - 512 (Algebra): Some items
    # - 500 (Science - root): Some items directly assigned? DDC usually assigns to leaves, but let's allow mixed.

    items_data = [
        ('005', 'The Art of Computer Programming', 'Knuth'),
        ('005', 'Clean Code', 'Martin'),
        ('005', 'Refactoring', 'Fowler'),
        ('005', 'Design Patterns', 'Gang of Four'),
        ('512', 'Abstract Algebra', 'Dummit'),
        ('512', 'Linear Algebra Done Right', 'Axler'),
        ('515', 'Calculus', 'Spivak'),
        ('523', 'The Planet Mars', 'Vaucouleurs'),
        ('941', 'A History of Britain', 'Schama'),
        ('940', 'Europe: A History', 'Davies'),
        ('500', 'The Science Book', 'DK'), # Assigned to root 500
    ]

    for code, title, author in items_data:
        cat_id = cats.get(code)
        if cat_id:
            year = random.randint(1950, 2023)
            cursor.execute("INSERT INTO library_items (title, author, publication_year, category_id) VALUES (:1, :2, :3, :4)", [title, author, year, cat_id])

    conn.commit()
    cursor.close()
    conn.close()
    print("Database populated successfully.")

except Exception as e:
    print(f"Error populating data: {e}")
    exit(1)
PYEOF

# --- Record Timestamp ---
date +%s > /tmp/task_start_time.txt
chmod 600 /tmp/task_start_time.txt

# --- Initial Screenshot ---
take_screenshot /tmp/task_initial.png

echo "=== Setup Complete ==="