#!/usr/bin/env python3
"""
Verifier for Warehouse Transfer API task.
"""

import json
import logging
import os
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_warehouse_transfer(traj, env_info, task_info):
    """
    Verifies the PL/SQL Warehouse Transfer task.
    
    Scoring Criteria:
    1. Constraint Exists (15 pts)
    2. Package Valid (10 pts)
    3. Valid Transfer Logic (20 pts)
    4. New Row Creation Logic (15 pts)
    5. Insufficient Funds Handling (15 pts)
    6. Audit Logging (15 pts)
    7. Transaction Safety/Rollback (10 pts)
    
    Pass Threshold: 65 points
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Retrieve result JSON generated by export_result.sh inside the container
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load verification results: {str(e)}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    score = 0
    feedback_parts = []
    
    # 1. Constraint (15 pts)
    if result_data.get("constraint_exists"):
        score += 15
        feedback_parts.append("Constraint CHK_INV_QTY_NONNEG active")
    else:
        feedback_parts.append("Constraint CHK_INV_QTY_NONNEG missing/disabled")

    # 2. Package Validity (10 pts)
    if result_data.get("package_valid"):
        score += 10
        feedback_parts.append("Package INV_MANAGER is valid")
    else:
        feedback_parts.append("Package INV_MANAGER invalid or missing")
        # If package is invalid, most functional tests will fail
        return {
            "passed": False, 
            "score": score, 
            "feedback": " | ".join(feedback_parts) + ". Package compilation failed, functional tests skipped.",
            "details": result_data.get("error_log", [])
        }

    # 3. Valid Transfer (20 pts)
    if result_data.get("test_valid_transfer"):
        score += 20
        feedback_parts.append("Basic transfer logic correct")
    else:
        feedback_parts.append("Basic transfer logic failed")

    # 4. New Row Creation (15 pts)
    if result_data.get("test_new_row_creation"):
        score += 15
        feedback_parts.append("Handles new destination row correctly")
    else:
        feedback_parts.append("Failed to create new inventory row at destination")

    # 5. Insufficient Funds (15 pts)
    if result_data.get("test_insufficient_funds"):
        score += 15
        feedback_parts.append("Correctly blocks insufficient stock")
    else:
        feedback_parts.append("Failed to block insufficient stock transfer")

    # 6. Audit Logging (15 pts)
    if result_data.get("test_audit_logging"):
        score += 15
        feedback_parts.append("Audit logging verified")
    else:
        feedback_parts.append("Transfer not logged to TRANSFER_LOG")

    # 7. Transaction Safety (10 pts)
    if result_data.get("test_transaction_safety"):
        score += 10
        feedback_parts.append("Transaction safety (rollback) verified")
    else:
        feedback_parts.append("Transaction safety check failed (possible partial commit)")

    passed = score >= 65
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts),
        "details": result_data.get("error_log", [])
    }