#!/bin/bash
# Setup script for IoT Pipelined ETL Task
# Creates schema objects and generates realistic raw sensor data

set -e

echo "=== Setting up IoT ETL Task ==="

source /workspace/scripts/task_utils.sh

# --- 1. Verify Database Connection ---
echo "Verifying database connection..."
if ! wait_for_oracle 60; then
    echo "ERROR: Oracle database not reachable."
    exit 1
fi

# --- 2. Clean up previous attempts ---
echo "Cleaning up schema..."
oracle_query "
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE raw_sensor_logs PURGE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TABLE iot_parse_errors PURGE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP PACKAGE pkg_iot_etl';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TYPE t_iot_metric_tab FORCE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/
BEGIN
    EXECUTE IMMEDIATE 'DROP TYPE t_iot_metric FORCE';
EXCEPTION WHEN OTHERS THEN NULL;
END;
/" "hr" > /dev/null 2>&1

# --- 3. Create Tables ---
echo "Creating tables..."
oracle_query "
CREATE TABLE raw_sensor_logs (
    log_id      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    raw_payload VARCHAR2(4000)
);

CREATE TABLE iot_parse_errors (
    log_id    NUMBER,
    error_msg VARCHAR2(4000),
    log_date  DATE DEFAULT SYSDATE
);
" "hr"

# --- 4. Generate Data (Python) ---
echo "Generating sensor data..."
python3 << 'PYEOF'
import random
import datetime

# Configuration
TOTAL_ROWS = 500
ERROR_RATE = 0.04 # 4% errors (~20 rows)

valid_template = "{id}|{time}|{temp}|{batt}|{status}"

# Generate SQL insert script
with open("/tmp/insert_data.sql", "w") as f:
    f.write("SET DEFINE OFF;\n")
    
    for i in range(1, TOTAL_ROWS + 1):
        # Base values
        sensor_id = f"WH-{random.choice(['A','B','C'])}-{random.randint(1,100):03d}"
        now = datetime.datetime(2024, 3, 8, 10, 0, 0) + datetime.timedelta(seconds=i*30)
        iso_time = now.isoformat()
        temp_f = round(random.uniform(30.0, 90.0), 1)
        battery = random.randint(5, 100)
        status = "OK" if battery > 15 else "LOW_BATT"
        
        # Decide if this row should be malformed
        is_error = random.random() < ERROR_RATE
        payload = ""
        
        if is_error:
            error_type = random.choice(['bad_temp', 'bad_date', 'missing_field'])
            if error_type == 'bad_temp':
                payload = f"{sensor_id}|{iso_time}|ERR_READ|{battery}|{status}"
            elif error_type == 'bad_date':
                payload = f"{sensor_id}|2024-99-99T99:99|{temp_f}|{battery}|{status}"
            elif error_type == 'missing_field':
                payload = f"{sensor_id}|{iso_time}|{temp_f}|{status}" # Missing battery
        else:
            payload = valid_template.format(
                id=sensor_id, time=iso_time, temp=temp_f, batt=battery, status=status
            )
            
        # SQL Escape
        payload = payload.replace("'", "''")
        f.write(f"INSERT INTO raw_sensor_logs (raw_payload) VALUES ('{payload}');\n")
        
        if i % 100 == 0:
            f.write("COMMIT;\n")

    f.write("COMMIT;\n")
PYEOF

# --- 5. execute Inserts ---
echo "Loading data into Oracle..."
# sqlplus is faster for bulk inserts than repeated python calls
sudo docker exec -i $ORACLE_CONTAINER sqlplus -s hr/hr123@localhost:1521/XEPDB1 < /tmp/insert_data.sql > /dev/null

# --- 6. Verification ---
COUNT=$(get_table_count "raw_sensor_logs" "hr")
echo "Setup complete. Loaded $COUNT rows into RAW_SENSOR_LOGS."

# Timestamp for verification
date +%s > /tmp/task_start_time.txt
rm -f /tmp/insert_data.sql