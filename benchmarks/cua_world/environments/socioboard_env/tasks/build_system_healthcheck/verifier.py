#!/usr/bin/env python3
"""
Verifier for build_system_healthcheck task.

Evaluates the bash script generated by the agent. To prevent hardcoded
JSON responses, this verifier depends on output dynamically generated
in the export_result.sh script across 3 mutated states:
1. Normal/Healthy
2. Apache down (frontend unavailable)
3. MariaDB down (database disconnected)
"""

import os
import json
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_build_system_healthcheck(traj, env_info, task_info):
    """
    Score the health check script output.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read exported results: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    
    task_start = result.get('task_start', 0)
    script_exists = result.get('script_exists', False)
    is_executable = result.get('is_executable', False)
    mtime = result.get('mtime', 0)

    # 1. File Setup (10 points)
    if not script_exists:
        return {"passed": False, "score": 0, "feedback": "Script /usr/local/bin/socioboard-health.sh does not exist."}
    
    if mtime < task_start:
        return {"passed": False, "score": 0, "feedback": "Script was not created or modified during the task."}
    
    if is_executable:
        score += 10
        feedback_parts.append("Script exists and is executable (+10)")
    else:
        score += 5
        feedback_parts.append("Script exists but lacks executable permissions (+5)")

    out_healthy = result.get('out_healthy', '')
    out_no_apache = result.get('out_no_apache', '')
    out_no_db = result.get('out_no_db', '')

    # 2. Valid JSON Output (20 points)
    healthy_json = None
    try:
        healthy_json = json.loads(out_healthy)
        score += 20
        feedback_parts.append("Output is valid JSON (+20)")
    except Exception as e:
        feedback_parts.append(f"Output is not valid JSON: {out_healthy[:50]}... (-20)")
        return {"passed": False, "score": score, "feedback": " | ".join(feedback_parts)}

    # Helper function to safely extract integer status code
    def parse_frontend_code(val):
        try:
            return int(val)
        except (ValueError, TypeError):
            return -1

    # 3. Healthy State Accuracy (20 points)
    try:
        h_apache = healthy_json.get('apache') == 'online'
        h_mariadb = healthy_json.get('mariadb') == 'online'
        h_mongodb = healthy_json.get('mongodb') == 'online'
        h_db = healthy_json.get('database') == 'connected'
        h_frontend = parse_frontend_code(healthy_json.get('frontend')) == 200
        h_overall = healthy_json.get('overall') == 'healthy'

        if all([h_apache, h_mariadb, h_mongodb, h_db, h_frontend, h_overall]):
            score += 20
            feedback_parts.append("Healthy state evaluated correctly (+20)")
        else:
            feedback_parts.append("Healthy state evaluation incorrect (-20)")
            logger.info(f"Healthy state mismatch. Data: {healthy_json}")
    except Exception as e:
        feedback_parts.append(f"Healthy state evaluation crashed: {e}")

    # 4. Dynamic State: Apache down (25 points)
    try:
        na_json = json.loads(out_no_apache)
        na_apache = na_json.get('apache') == 'offline'
        na_frontend = parse_frontend_code(na_json.get('frontend')) != 200
        na_overall = na_json.get('overall') == 'unhealthy'

        if na_apache and na_frontend and na_overall:
            score += 25
            feedback_parts.append("Dynamic Apache down state correct (+25)")
        else:
            feedback_parts.append("Dynamic Apache down state incorrect (-25)")
            logger.info(f"Apache down mismatch. Data: {na_json}")
    except Exception:
        feedback_parts.append("Dynamic Apache down state failed (Invalid JSON)")

    # 5. Dynamic State: MariaDB down (25 points)
    try:
        nd_json = json.loads(out_no_db)
        nd_mariadb = nd_json.get('mariadb') == 'offline'
        nd_db = nd_json.get('database') == 'disconnected'
        nd_overall = nd_json.get('overall') == 'unhealthy'

        if nd_mariadb and nd_db and nd_overall:
            score += 25
            feedback_parts.append("Dynamic MariaDB down state correct (+25)")
        else:
            feedback_parts.append("Dynamic MariaDB down state incorrect (-25)")
            logger.info(f"MariaDB down mismatch. Data: {nd_json}")
    except Exception:
        feedback_parts.append("Dynamic MariaDB down state failed (Invalid JSON)")

    # Pass threshold is 75 points.
    passed = score >= 75

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }