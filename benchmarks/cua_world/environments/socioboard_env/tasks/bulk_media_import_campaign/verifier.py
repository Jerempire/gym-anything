#!/usr/bin/env python3
"""
Verifier for bulk_media_import_campaign task.

Uses MULTIPLE INDEPENDENT SIGNALS to verify task success:
1. Local File Extraction & Saving: Checks if directory exists and contains files.
2. Image Processing Integrity: Validates that files were successfully resized to exactly 1080px.
3. Anti-gaming (Timestamps): Ensures files were processed AFTER task start.
4. Remote Upload (MongoDB): Verifies database media count increased by the expected number.
"""

import os
import json
import tempfile
import logging
from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def verify_bulk_media_import(traj, env_info, task_info):
    """
    Verify the batch resizing and uploading task.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    metadata = task_info.get('metadata', {})
    expected_width = metadata.get('expected_width', 1080)
    expected_count = metadata.get('expected_count', 10)

    # 1. Read JSON result generated by export_result.sh
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read exported result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    score = 0
    feedback_parts = []
    
    task_start = result.get('task_start', 0)
    dir_exists = result.get('dir_exists', False)
    processed_files = result.get('processed_files', [])
    
    initial_media = result.get('initial_media_count', 0)
    final_media = result.get('final_media_count', 0)
    uploaded_count = max(0, final_media - initial_media)

    # --- Criterion 1: Processed Directory Exists (10 points) ---
    if dir_exists:
        score += 10
        feedback_parts.append("Directory 'Processed_Campaign' created.")
    else:
        feedback_parts.append("Directory 'Processed_Campaign' NOT found.")
        return {"passed": False, "score": score, "feedback": " | ".join(feedback_parts)}

    # --- Criterion 2: Extracted & Processed Count (20 points) ---
    valid_files = []
    for f in processed_files:
        if f.get('mtime', 0) > task_start:
            valid_files.append(f)
            
    if len(valid_files) >= expected_count:
        score += 20
        feedback_parts.append(f"Successfully processed {len(valid_files)} files.")
    elif len(valid_files) > 0:
        score += int((len(valid_files) / expected_count) * 20)
        feedback_parts.append(f"Only processed {len(valid_files)}/{expected_count} files.")
    else:
        feedback_parts.append("No new files found in the directory.")
        
    # --- Criterion 3: Image Resizing to EXACTLY 1080px (30 points) ---
    correctly_resized = 0
    for f in valid_files:
        if f.get('width', 0) == expected_width:
            correctly_resized += 1
            
    if correctly_resized == expected_count:
        score += 30
        feedback_parts.append(f"All images correctly resized to {expected_width}px.")
    elif correctly_resized > 0:
        score += int((correctly_resized / expected_count) * 30)
        feedback_parts.append(f"{correctly_resized}/{expected_count} images correctly resized to {expected_width}px.")
    else:
        feedback_parts.append(f"No images were correctly resized to {expected_width}px.")

    # --- Criterion 4: Remote Upload to Socioboard (40 points) ---
    if uploaded_count >= expected_count:
        score += 40
        feedback_parts.append(f"Successfully uploaded {uploaded_count} files to Socioboard.")
    elif uploaded_count > 0:
        score += 20 + int((uploaded_count / expected_count) * 15)  # Base 20 pts for figuring out upload
        feedback_parts.append(f"Partially uploaded {uploaded_count}/{expected_count} files to Socioboard.")
    else:
        feedback_parts.append("No files were uploaded to Socioboard (MongoDB count unchanged).")

    # --- Optional VLM Validation for robustness if score is borderline ---
    if 40 <= score < 100 and uploaded_count == 0:
        # Check if they automated via UI but Mongo didn't register (fallback check)
        frames = sample_trajectory_frames(traj, n=4)
        final = get_final_screenshot(traj)
        
        prompt = (
            "You are observing a user interacting with a social media management app (Socioboard). "
            "Did the user successfully interact with the File Upload / Media Library interface to "
            "select and upload image files? Look for file browser dialogs, progress bars, or media library grids."
            "Answer 'yes' if there is clear evidence of media files being uploaded."
        )
        try:
            vlm_response = query_vlm(images=frames + [final], prompt=prompt)
            if vlm_response and "yes" in vlm_response.get("answer", "").lower():
                score += 15
                feedback_parts.append("VLM confirms upload interaction (Partial credit).")
        except Exception as e:
            logger.warning(f"VLM check failed: {e}")

    # Determine Pass/Fail (Requires resizing at least some images AND uploading them)
    passed = score >= 80

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }