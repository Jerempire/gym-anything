#!/usr/bin/env python3
"""
Verifier for asat_debris_dispersion@1

Agent must simulate the Kosmos 1408 ASAT test by creating a GMAT script with 21 
spacecraft (1 parent, 20 fragments), applying velocity kicks from -100 m/s to +100 m/s, 
propagating for 90 days with J2-only gravity, and reporting the RAAN spread.

Scoring (total 100 pts, pass >= 60):
  - script_generated (10): .script file exists and was created during the task.
  - maneuvers_configured (20): Script contains >= 20 ImpulsiveBurns and Maneuver logic.
  - physics_correct (20): ForceModel uses J2 only (Degree 2, Order 0) and ElapsedDays = 90.
  - report_created (15): debris_raan_report.txt generated by GMAT with populated data.
  - json_created (10): dispersion_summary.json exists with correct keys.
  - spread_accurate (25): Calculated RAAN spread matches the physical expectation (14.0 - 19.0 deg).

Pass condition: score >= 60 AND spread_accurate AND maneuvers_configured.
"""

import json
import os
import re
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def verify_asat_debris_dispersion(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}

    metadata = task_info.get('metadata', {})
    expected_spread_min = metadata.get('expected_spread_min_deg', 14.0)
    expected_spread_max = metadata.get('expected_spread_max_deg', 19.0)

    scores = {
        "script_generated": 10,
        "maneuvers_configured": 20,
        "physics_correct": 20,
        "report_created": 15,
        "json_created": 10,
        "spread_accurate": 25,
    }

    total_score = 0
    feedback = []
    maneuvers_ok = False
    spread_ok = False

    # Load task result
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    # 1. Check script file
    script_file = task_result.get('script_file', {})
    script_path = task_result.get('script_path', '/home/ga/GMAT_output/simulate_debris.script')
    if isinstance(script_file, dict) and script_file.get('created_during_task'):
        total_score += scores["script_generated"]
        feedback.append("Script generated during task window.")
    else:
        feedback.append("Script not found or not generated during task.")

    # 2. Analyze Script Content (Maneuvers and Physics)
    if isinstance(script_file, dict) and script_file.get('exists'):
        temp_script = tempfile.NamedTemporaryFile(delete=False, suffix='.script')
        try:
            copy_from_env(script_path, temp_script.name)
            with open(temp_script.name, 'r', encoding='utf-8', errors='ignore') as f:
                script_content = f.read()

            # Check Spacecraft count
            sc_count = len(re.findall(r'Create\s+Spacecraft', script_content))
            
            # Check Maneuvers
            burn_count = len(re.findall(r'Create\s+ImpulsiveBurn', script_content))
            if burn_count >= 20 and sc_count >= 21:
                total_score += scores["maneuvers_configured"]
                maneuvers_ok = True
                feedback.append(f"Found {sc_count} Spacecraft and {burn_count} ImpulsiveBurns.")
            else:
                total_score += scores["maneuvers_configured"] // 2
                feedback.append(f"Insufficient Spacecraft ({sc_count}) or ImpulsiveBurns ({burn_count}).")

            # Check Physics (J2, 90 days)
            has_j2_degree = bool(re.search(r'Degree\s*=\s*2\b', script_content))
            has_j2_order = bool(re.search(r'Order\s*=\s*0\b', script_content))
            has_90_days = bool(re.search(r'ElapsedDays\s*=\s*90\b', script_content))
            no_drag = not bool(re.search(r'AtmosphereModel\s*=\s*\w+', script_content))

            physics_pts = 0
            if has_j2_degree and has_j2_order:
                physics_pts += 10
            if has_90_days:
                physics_pts += 5
            if no_drag:
                physics_pts += 5
            
            total_score += physics_pts
            if physics_pts == 20:
                feedback.append("Physics configured correctly (J2 only, 90 days).")
            else:
                feedback.append(f"Physics configuration partially correct (Score: {physics_pts}/20).")

        except Exception as e:
            feedback.append(f"Error parsing script: {e}")
        finally:
            if os.path.exists(temp_script.name):
                os.unlink(temp_script.name)

    # 3. Check Report File
    report_file = task_result.get('report_file', {})
    if isinstance(report_file, dict) and report_file.get('exists') and report_file.get('size', 0) > 100:
        total_score += scores["report_created"]
        feedback.append("GMAT RAAN report successfully generated.")
    else:
        feedback.append("GMAT RAAN report missing or empty.")

    # 4. Check JSON and Spread Accuracy
    json_file = task_result.get('json_file', {})
    json_path = task_result.get('json_path', '/home/ga/GMAT_output/dispersion_summary.json')
    if isinstance(json_file, dict) and json_file.get('exists'):
        temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
        try:
            copy_from_env(json_path, temp_json.name)
            with open(temp_json.name, 'r') as f:
                agent_json = json.load(f)
            
            # Check keys
            keys_present = all(k in agent_json for k in ["max_raan_deg", "min_raan_deg", "raan_spread_deg"])
            if keys_present:
                total_score += scores["json_created"]
                feedback.append("Summary JSON created with correct keys.")

                # Check physics accuracy
                spread_val = float(agent_json.get("raan_spread_deg", 0))
                if expected_spread_min <= spread_val <= expected_spread_max:
                    total_score += scores["spread_accurate"]
                    spread_ok = True
                    feedback.append(f"RAAN spread physically accurate: {spread_val:.2f} deg.")
                else:
                    feedback.append(f"RAAN spread inaccurate: {spread_val:.2f} deg (expected {expected_spread_min}-{expected_spread_max} deg).")
            else:
                feedback.append("Summary JSON missing required keys.")

        except Exception as e:
            feedback.append(f"Failed to read/parse summary JSON: {e}")
        finally:
            if os.path.exists(temp_json.name):
                os.unlink(temp_json.name)
    else:
        feedback.append("Summary JSON not found.")

    # Final Pass Condition Evaluation
    passed = (total_score >= 60) and maneuvers_ok and spread_ok

    if passed:
        feedback.insert(0, "SUCCESS: Debris dispersion successfully modeled.")
    else:
        feedback.insert(0, "FAILED: Task requirements not met.")

    return {
        "passed": passed,
        "score": total_score,
        "feedback": " | ".join(feedback),
        "details": {
            "maneuvers_ok": maneuvers_ok,
            "spread_ok": spread_ok
        }
    }