#!/usr/bin/env python3
import json
import os
import re
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_tundra_orbit_longitude_targeting(traj, env_info, task_info):
    """
    Verify the agent successfully designed a Tundra orbit and targeted Apogee Longitude
    using a Differential Corrector.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}

    metadata = task_info.get('metadata', {})
    script_path = metadata.get('script_path', '/home/ga/GMAT_output/borealis_tundra.script')
    report_path = metadata.get('report_path', '/home/ga/GMAT_output/apogee_target_report.txt')

    scores = {
        "script_created": 10,
        "correct_baseline": 15,
        "dc_logic": 15,
        "report_generated": 10,
        "longitude_achieved": 25,
        "latitude_validated": 15,
        "altitude_validated": 10
    }

    total_score = 0
    feedback = []
    longitude_ok = False
    report_ok = False

    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    # 1. Verify Script Creation
    script_file = task_result.get('script_file', {})
    if isinstance(script_file, dict) and script_file.get('created_during_task'):
        total_score += scores["script_created"]
        feedback.append("Script created during task window.")
    else:
        feedback.append("Script not created during task window.")

    # 2 & 3. Analyze script for baseline physical elements and targeting logic
    if isinstance(script_file, dict) and script_file.get('exists'):
        temp_script = tempfile.NamedTemporaryFile(delete=False, suffix='.script')
        try:
            copy_from_env(script_path, temp_script.name)
            with open(temp_script.name, 'r', encoding='utf-8', errors='ignore') as f:
                script_content = f.read()

            # Baseline elements Check
            has_sma = bool(re.search(r'SMA\s*=\s*42164', script_content))
            has_ecc = bool(re.search(r'ECC\s*=\s*0\.28', script_content))
            has_inc = bool(re.search(r'INC\s*=\s*63\.4', script_content))
            has_aop = bool(re.search(r'AOP\s*=\s*270', script_content))

            elems = [has_sma, has_ecc, has_inc, has_aop]
            if all(elems):
                total_score += scores["correct_baseline"]
                feedback.append("Baseline orbital elements properly configured.")
            elif any(elems):
                total_score += scores["correct_baseline"] // 2
                feedback.append("Partial baseline orbital elements configured.")
            else:
                feedback.append("Baseline orbital elements not found.")

            # Differential Corrector Logic Check
            has_dc = bool(re.search(r'Create\s+DifferentialCorrector', script_content))
            has_target = "Target " in script_content
            has_vary = "Vary " in script_content
            has_achieve = "Achieve " in script_content

            if has_dc and has_target and has_vary and has_achieve:
                total_score += scores["dc_logic"]
                feedback.append("Differential Corrector logic (Target/Vary/Achieve) is present.")
            else:
                feedback.append("Incomplete Differential Corrector logic.")

        except Exception as e:
            feedback.append(f"Error reading script: {e}")
        finally:
            if os.path.exists(temp_script.name):
                os.unlink(temp_script.name)

    # 4, 5, 6, 7. Verify Report Data Robustly
    # (Checking the `report_file_rerun` verifies the script actually outputted this data, thwarting fakes)
    report_file_rerun = task_result.get('report_file_rerun', {})
    if isinstance(report_file_rerun, dict) and report_file_rerun.get('exists'):
        total_score += scores["report_generated"]
        report_ok = True
        feedback.append("Report file validly generated by script execution.")

        temp_report = tempfile.NamedTemporaryFile(delete=False, suffix='.txt')
        try:
            copy_from_env(report_path, temp_report.name)
            with open(temp_report.name, 'r', encoding='utf-8', errors='ignore') as f:
                report_content = f.read()

            lines = report_content.strip().split('\n')
            data_lines = [line for line in lines if not line.strip().startswith('%') and line.strip()]

            lat_ok = False
            alt_ok = False

            if data_lines:
                # We check all numeric columns across data rows robustly regardless of column order
                for line in data_lines:
                    tokens = line.split()
                    nums = []
                    for t in tokens:
                        try:
                            nums.append(float(t))
                        except ValueError:
                            pass

                    # Expected Longitude ~ -105.0°
                    if any(-105.5 <= n <= -104.5 for n in nums):
                        longitude_ok = True
                    # Expected Latitude ~ 63.4° (Due to critical inclination and AOP=270)
                    if any(62.9 <= n <= 63.9 for n in nums):
                        lat_ok = True
                    # Expected Altitude at apogee ~ 47600 km
                    if any(46000 <= n <= 49000 for n in nums):
                        alt_ok = True

                if longitude_ok:
                    total_score += scores["longitude_achieved"]
                    feedback.append("Longitude target achieved (~ -105.0°).")
                else:
                    feedback.append("Longitude target not achieved (or not exported in report).")

                if lat_ok:
                    total_score += scores["latitude_validated"]
                    feedback.append("Latitude validation passed (~ 63.4°).")
                else:
                    feedback.append("Latitude validation failed.")

                if alt_ok:
                    total_score += scores["altitude_validated"]
                    feedback.append("Altitude validation passed (~ 47600 km).")
                else:
                    feedback.append("Altitude validation failed.")
            else:
                feedback.append("Report file is empty or missing data rows.")

        except Exception as e:
            feedback.append(f"Error reading report: {e}")
        finally:
            if os.path.exists(temp_report.name):
                os.unlink(temp_report.name)
    else:
        feedback.append("Report file not generated (or failed during script rerun).")

    # VLM Trajectory check to confirm actual tool usage over spoofing
    try:
        from gym_anything.vlm import get_final_screenshot, query_vlm, sample_trajectory_frames
        frames = sample_trajectory_frames(traj, n=3)
        final = get_final_screenshot(traj)
        if final:
            vlm_res = query_vlm(
                images=frames + [final],
                prompt="Check if NASA GMAT was used to set up a spacecraft targeting sequence. Is the GMAT UI visible with a script or resource tree showing a DifferentialCorrector and Target sequence? Return a JSON with `gmat_used`: true/false."
            )
            if vlm_res and not vlm_res.get('parsed', {}).get('gmat_used', True):
                feedback.append("[VLM Warning] GMAT usage not detected visually.")
    except Exception as e:
        logger.warning(f"VLM verification skipped or failed: {e}")

    # Final Pass assessment
    passed = (total_score >= 70) and longitude_ok and report_ok
    
    return {
        "passed": passed,
        "score": total_score,
        "feedback": " | ".join(feedback)
    }