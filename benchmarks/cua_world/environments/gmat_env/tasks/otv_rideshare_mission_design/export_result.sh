#!/bin/bash
set -euo pipefail

echo "=== Exporting otv_rideshare_mission_design results ==="

source /workspace/scripts/task_utils.sh || { echo "Failed to source task_utils.sh"; exit 1; }

TASK_START=$(cat /tmp/task_start_time.txt 2>/dev/null || echo "0")
TASK_END=$(date +%s)

SCRIPT_PATH="/home/ga/Documents/missions/otv_rideshare.script"
REPORT_PATH="/home/ga/GMAT_output/otv_mission_report.txt"

# Capture final screenshot
take_screenshot /tmp/task_final.png

check_file() {
    local fpath="$1"
    if [ -f "$fpath" ]; then
        local mtime=$(stat -c %Y "$fpath")
        local size=$(stat -c %s "$fpath")
        local created_during=$([ "$mtime" -ge "$TASK_START" ] && echo "true" || echo "false")
        echo "{\"exists\": true, \"size\": $size, \"created_during_task\": $created_during}"
    else
        echo "{\"exists\": false, \"size\": 0, \"created_during_task\": false}"
    fi
}

SCRIPT_STATS=$(check_file "$SCRIPT_PATH")
REPORT_STATS=$(check_file "$REPORT_PATH")

# Test the GMAT script for physical convergence via headless console
CONSOLE=$(find_gmat_console 2>/dev/null || echo "")
RUN_SUCCESS="false"
if [ -n "$CONSOLE" ] && [ -f "$SCRIPT_PATH" ]; then
    echo "Re-running script via GmatConsole to enforce anti-gaming..."
    # Running via console ensures the report file was actually generated by the script, not faked via bash
    timeout 180 "$CONSOLE" --run "$SCRIPT_PATH" > /tmp/gmat_console_output.txt 2>&1 && RUN_SUCCESS="true" || RUN_SUCCESS="false"
fi

REPORT_STATS_RERUN=$(check_file "$REPORT_PATH")

APP_RUNNING=$(pgrep -f "GMAT" > /dev/null && echo "true" || echo "false")

# Parse the mission report
SMA_A="0"
SMA_B="0"
INC_B="0"
FINAL_FUEL="0"

if [ -f "$REPORT_PATH" ]; then
    SMA_A=$(grep -oP 'Payload_A_SMA_km:\s*\K[0-9]+\.?[0-9]*' "$REPORT_PATH" 2>/dev/null | head -1 || echo "0")
    SMA_B=$(grep -oP 'Payload_B_SMA_km:\s*\K[0-9]+\.?[0-9]*' "$REPORT_PATH" 2>/dev/null | head -1 || echo "0")
    INC_B=$(grep -oP 'Payload_B_INC_deg:\s*\K[0-9]+\.?[0-9]*' "$REPORT_PATH" 2>/dev/null | head -1 || echo "0")
    FINAL_FUEL=$(grep -oP 'Final_Fuel_Mass_kg:\s*\K[0-9]+\.?[0-9]*' "$REPORT_PATH" 2>/dev/null | head -1 || echo "0")
fi

# Deep inspect the GMAT script text for required mechanisms
TANK_CONFIGURED="false"
MASS_DROP_IMPLEMENTED="false"
if [ -f "$SCRIPT_PATH" ]; then
    grep -qi "Create ChemicalTank" "$SCRIPT_PATH" && grep -qi "\.Tanks = {" "$SCRIPT_PATH" && TANK_CONFIGURED="true"
    # Check for GMAT assignment subtracting mass (e.g., GMAT Tug.DryMass = Tug.DryMass - 150, or GMAT Tug.DryMass = 450)
    grep -qi "\.DryMass\s*=" "$SCRIPT_PATH" && MASS_DROP_IMPLEMENTED="true"
fi

# Build JSON report
TEMP_JSON=$(mktemp /tmp/result.XXXXXX.json)
cat > "$TEMP_JSON" << EOF
{
    "task_start": $TASK_START,
    "task_end": $TASK_END,
    "app_was_running": $APP_RUNNING,
    "script_file": $SCRIPT_STATS,
    "report_file": $REPORT_STATS,
    "report_file_rerun": $REPORT_STATS_RERUN,
    "console_run_success": "$RUN_SUCCESS",
    "tank_configured": $TANK_CONFIGURED,
    "mass_drop_implemented": $MASS_DROP_IMPLEMENTED,
    "payload_a_sma_km": "$SMA_A",
    "payload_b_sma_km": "$SMA_B",
    "payload_b_inc_deg": "$INC_B",
    "final_fuel_mass_kg": "$FINAL_FUEL",
    "script_path": "$SCRIPT_PATH",
    "report_path": "$REPORT_PATH"
}
EOF

rm -f /tmp/task_result.json 2>/dev/null || true
cp "$TEMP_JSON" /tmp/task_result.json
chmod 666 /tmp/task_result.json
rm -f "$TEMP_JSON"

echo "Export complete."
cat /tmp/task_result.json
echo "=== Export Done ==="