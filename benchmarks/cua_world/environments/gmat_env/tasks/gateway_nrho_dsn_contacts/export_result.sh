#!/bin/bash
set -euo pipefail

echo "=== Exporting gateway_nrho_dsn_contacts results ==="

source /workspace/scripts/task_utils.sh || { echo "Failed to source task_utils.sh"; exit 1; }

TASK_START=$(cat /tmp/task_start_time.txt 2>/dev/null || echo "0")
TASK_END=$(date +%s)

# Identify the latest script file (agent might have saved as dsn.script or overwritten baseline)
LATEST_SCRIPT=$(ls -t /home/ga/Documents/missions/gateway_nrho*.script 2>/dev/null | head -1 || echo "")
ANALYSIS_PATH="/home/ga/GMAT_output/blackout_analysis.txt"
CONTACT_REPORT="/home/ga/GMAT_output/Gateway_DSN_Contacts.txt"

take_screenshot /tmp/task_final.png

check_file() {
    local fpath="$1"
    if [ -n "$fpath" ] && [ -f "$fpath" ]; then
        local mtime=$(stat -c %Y "$fpath")
        local size=$(stat -c %s "$fpath")
        local created_during=$([ "$mtime" -ge "$TASK_START" ] && echo "true" || echo "false")
        echo "{\"exists\": true, \"size\": $size, \"created_during_task\": $created_during}"
    else
        echo "{\"exists\": false, \"size\": 0, \"created_during_task\": false}"
    fi
}

SCRIPT_STATS=$(check_file "$LATEST_SCRIPT")
ANALYSIS_STATS=$(check_file "$ANALYSIS_PATH")

# Run the script programmatically to generate ground-truth contact report
CONSOLE=$(find_gmat_console 2>/dev/null || echo "")
RUN_SUCCESS="false"
if [ -n "$CONSOLE" ] && [ -n "$LATEST_SCRIPT" ] && [ -f "$LATEST_SCRIPT" ]; then
    echo "Re-running script via GmatConsole to ensure fresh contact report..."
    # Remove existing contact report to guarantee it's freshly generated by the console run
    rm -f "$CONTACT_REPORT"
    timeout 180 "$CONSOLE" --run "$LATEST_SCRIPT" > /tmp/gmat_console_nrho.txt 2>&1 && RUN_SUCCESS="true" || RUN_SUCCESS="false"
fi

REPORT_STATS=$(check_file "$CONTACT_REPORT")

APP_RUNNING=$(pgrep -f "GMAT" > /dev/null && echo "true" || echo "false")

# Extract the agent's reported value from the analysis file
REPORTED_VAL="0"
if [ -f "$ANALYSIS_PATH" ]; then
    REPORTED_VAL=$(grep -oP 'Max blackout:\s*\K[0-9]+\.?[0-9]*' "$ANALYSIS_PATH" 2>/dev/null | head -1 || echo "")
    if [ -z "$REPORTED_VAL" ]; then
        # Fallback to any standalone number in the file
        REPORTED_VAL=$(grep -oP '\b[0-9]+\.?[0-9]*\b' "$ANALYSIS_PATH" | head -1 || echo "0")
    fi
fi

TEMP_JSON=$(mktemp /tmp/result.XXXXXX.json)
cat > "$TEMP_JSON" << EOF
{
    "task_start": $TASK_START,
    "task_end": $TASK_END,
    "app_was_running": $APP_RUNNING,
    "script_file": $SCRIPT_STATS,
    "analysis_file": $ANALYSIS_STATS,
    "report_file": $REPORT_STATS,
    "console_run_success": "$RUN_SUCCESS",
    "reported_blackout_minutes": "$REPORTED_VAL",
    "latest_script_path": "${LATEST_SCRIPT:-none}",
    "analysis_path": "$ANALYSIS_PATH",
    "contact_report_path": "$CONTACT_REPORT"
}
EOF

rm -f /tmp/task_result.json 2>/dev/null || true
cp "$TEMP_JSON" /tmp/task_result.json
chmod 666 /tmp/task_result.json
rm -f "$TEMP_JSON"

echo "Export complete."
cat /tmp/task_result.json
echo "=== Export Done ==="