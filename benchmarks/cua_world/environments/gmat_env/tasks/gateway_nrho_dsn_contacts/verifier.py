#!/usr/bin/env python3
"""
Verifier for gateway_nrho_dsn_contacts@1

Agent must add 3 DSN stations and a ContactLocator to an NRHO script,
with Moon & Earth occultation and 10 deg elevation, then analyze
the resulting report to find the maximum blackout duration.

Scoring (total 100 pts, pass >= 70):
  - script_modified (10): Script modified/saved during task window
  - ground_stations_created (20): 3 distinct GroundStations defined
  - elevation_constraint_set (10): Elevation constraint of 10 deg found
  - contact_locator_configured (20): ContactLocator targeting Gateway w/ Moon + Earth occultation
  - report_generated (10): Contact report successfully generated by script
  - blackout_analyzed (30): Agent's reported max blackout matches programmatic calculation (±2.5 min)
"""

import json
import os
import re
import tempfile
import logging
from datetime import datetime

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def parse_gmat_time(t_str):
    """Parse GMAT UTCGregorian time to epoch seconds. Example: '01 Jan 2025 12:00:00.000'"""
    try:
        dt = datetime.strptime(t_str.strip(), "%d %b %Y %H:%M:%S.%f")
        return dt.timestamp()
    except Exception as e:
        logger.error(f"Time parse error on '{t_str}': {e}")
        return 0.0


def calculate_max_blackout(report_path):
    """Parse GMAT ContactLocator text report to find the max gap (in minutes) where NO station has visibility."""
    if not os.path.exists(report_path):
        return 0.0

    intervals = []
    # Match lines like: 01 Jan 2025 12:00:00.000    01 Jan 2025 18:23:10.000    22990.000
    pattern = re.compile(r'(\d{2}\s+[A-Za-z]{3}\s+\d{4}\s+\d{2}:\d{2}:\d{2}\.\d{3})\s+(\d{2}\s+[A-Za-z]{3}\s+\d{4}\s+\d{2}:\d{2}:\d{2}\.\d{3})')
    
    with open(report_path, 'r', encoding='utf-8', errors='ignore') as f:
        for line in f:
            match = pattern.search(line)
            if match:
                start_ts = parse_gmat_time(match.group(1))
                end_ts = parse_gmat_time(match.group(2))
                if start_ts > 0 and end_ts > 0:
                    intervals.append((start_ts, end_ts))
                    
    if not intervals:
        return 0.0

    # Sort intervals by start time
    intervals.sort(key=lambda x: x[0])
    
    # Merge overlapping intervals
    merged = [intervals[0]]
    for current in intervals[1:]:
        prev = merged[-1]
        if current[0] <= prev[1]:
            # Overlap, extend the end time if necessary
            merged[-1] = (prev[0], max(prev[1], current[1]))
        else:
            # No overlap, new interval
            merged.append(current)

    # Calculate gaps between merged intervals
    max_gap_seconds = 0.0
    for i in range(1, len(merged)):
        gap = merged[i][0] - merged[i-1][1]
        if gap > max_gap_seconds:
            max_gap_seconds = gap

    return max_gap_seconds / 60.0


def verify_gateway_nrho_dsn_contacts(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}

    metadata = task_info.get('metadata', {})
    blackout_tol = metadata.get('blackout_tolerance_minutes', 2.5)

    scores = {
        "script_modified": 10,
        "ground_stations_created": 20,
        "elevation_constraint_set": 10,
        "contact_locator_configured": 20,
        "report_generated": 10,
        "blackout_analyzed": 30,
    }

    total_score = 0
    feedback = []
    locator_ok = False
    analyzed_ok = False

    # Load task result
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    # 1. Check if script was modified/created
    script_file = task_result.get('script_file', {})
    if isinstance(script_file, dict) and script_file.get('created_during_task'):
        total_score += scores["script_modified"]
        feedback.append("Script modified/saved during task window.")
    else:
        feedback.append("Script not modified during task window.")

    # 2. Analyze script content
    script_path = task_result.get('latest_script_path', '')
    if script_path and script_path != 'none':
        temp_script = tempfile.NamedTemporaryFile(delete=False, suffix='.script')
        try:
            copy_from_env(script_path, temp_script.name)
            with open(temp_script.name, 'r', encoding='utf-8', errors='ignore') as f:
                script_content = f.read()

            # Check for GroundStations
            gs_matches = len(re.findall(r'Create\s+GroundStation', script_content))
            if gs_matches >= 3:
                total_score += scores["ground_stations_created"]
                feedback.append(f"Found {gs_matches} GroundStation definitions.")
            elif gs_matches > 0:
                total_score += scores["ground_stations_created"] // 2
                feedback.append(f"Found {gs_matches}/3 GroundStation definitions.")
            else:
                feedback.append("No GroundStations found in script.")

            # Check for 10 degree elevation constraint
            if re.search(r'MinimumElevationAngle\s*=\s*10', script_content):
                total_score += scores["elevation_constraint_set"]
                feedback.append("MinimumElevationAngle set to 10 degrees.")
            else:
                feedback.append("MinimumElevationAngle not properly set to 10 degrees.")

            # Check for ContactLocator configured with Earth and Moon occultations
            has_locator = bool(re.search(r'Create\s+ContactLocator', script_content))
            has_gateway_target = bool(re.search(r'Target\s*=\s*Gateway', script_content))
            has_earth_occ = bool(re.search(r'OccultingBodies\s*=\s*\{[^}]*Earth[^}]*\}', script_content))
            has_moon_occ = bool(re.search(r'OccultingBodies\s*=\s*\{[^}]*Luna[^}]*\}|OccultingBodies\s*=\s*\{[^}]*Moon[^}]*\}', script_content))
            
            if has_locator and has_gateway_target and has_earth_occ and has_moon_occ:
                total_score += scores["contact_locator_configured"]
                locator_ok = True
                feedback.append("ContactLocator properly configured with Moon and Earth occultations.")
            elif has_locator:
                total_score += scores["contact_locator_configured"] // 2
                feedback.append("ContactLocator found, but missing critical Target or OccultingBodies configs.")
            else:
                feedback.append("No ContactLocator found in script.")

        except Exception as e:
            feedback.append(f"Error reading script: {e}")
        finally:
            if os.path.exists(temp_script.name):
                os.unlink(temp_script.name)

    # 3. Check Contact Report Generation & Programmatic Calculation
    report_file = task_result.get('report_file', {})
    actual_max_blackout = 0.0
    
    if isinstance(report_file, dict) and report_file.get('exists'):
        total_score += scores["report_generated"]
        feedback.append("Contact report successfully generated.")
        
        # Pull the report and calculate the actual blackout time programmatically
        report_path = task_result.get('contact_report_path', '/home/ga/GMAT_output/Gateway_DSN_Contacts.txt')
        temp_report = tempfile.NamedTemporaryFile(delete=False, suffix='.txt')
        try:
            copy_from_env(report_path, temp_report.name)
            actual_max_blackout = calculate_max_blackout(temp_report.name)
            feedback.append(f"Programmatic max blackout calculated as: {actual_max_blackout:.2f} minutes.")
        except Exception as e:
            feedback.append(f"Failed to parse contact report: {e}")
            actual_max_blackout = -1.0
        finally:
            if os.path.exists(temp_report.name):
                os.unlink(temp_report.name)
    else:
        feedback.append("Contact report not generated (script may have failed to run or incorrect Filename).")

    # 4. Check Agent's Analysis
    try:
        agent_blackout = float(task_result.get('reported_blackout_minutes', -1))
    except (ValueError, TypeError):
        agent_blackout = -1.0

    if agent_blackout >= 0 and actual_max_blackout > 0:
        if abs(agent_blackout - actual_max_blackout) <= blackout_tol:
            total_score += scores["blackout_analyzed"]
            analyzed_ok = True
            feedback.append(f"Agent's reported blackout ({agent_blackout} min) matches calculated value.")
        else:
            feedback.append(f"Agent's reported blackout ({agent_blackout} min) deviates from calculated value ({actual_max_blackout:.2f} min).")
    elif agent_blackout >= 0:
        feedback.append(f"Agent reported {agent_blackout} min, but reference report generation failed.")
    else:
        feedback.append("Valid blackout time not found in analysis report.")

    passed = (total_score >= 70) and locator_ok and analyzed_ok
    
    return {
        "passed": passed,
        "score": total_score,
        "feedback": " | ".join(feedback),
        "details": {
            "calculated_blackout": actual_max_blackout,
            "agent_blackout": agent_blackout
        }
    }