#!/usr/bin/env python3
"""
Verifier for tdrss_isl_coverage@1

Agent must set up a 4-spacecraft constellation, a ContactLocator, and 
generate an access report ensuring Earth occultation blocks LOS.

Scoring (total 100 pts, pass >= 60):
  - script_created (10): Script created during task
  - report_created (10): Report generated
  - four_spacecraft (15): Aura_Next and 3 TDRS satellites defined with correct SMAs
  - contact_locator_config (15): Target and Observers correctly set
  - earth_occultation (20): Earth is an occulting body
  - realistic_physics (15): Extracted contact durations do not exceed continuous access limits
  - vlm_trajectory (15): Trajectory screenshots show GUI workflow (or if programmatic, script logic covers it)
  
Pass condition: score >= 60 AND four_spacecraft AND earth_occultation AND realistic_physics
"""

import json
import os
import re
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_tdrss_isl_coverage(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}

    metadata = task_info.get('metadata', {})
    script_expected_path = metadata.get('script_path', '/home/ga/Documents/missions/tdrss_sim.script')
    report_expected_path = metadata.get('report_path', '/home/ga/GMAT_output/tdrss_access_report.txt')

    scores = {
        "script_created": 10,
        "report_created": 10,
        "four_spacecraft": 15,
        "contact_locator_config": 15,
        "earth_occultation": 20,
        "realistic_physics": 15,
        "vlm_trajectory": 15
    }

    total_score = 0
    feedback = []
    
    four_sc_ok = False
    occultation_ok = False
    physics_ok = False

    # 1. Load task result JSON
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            task_result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load task result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    # 2. Verify files created
    script_file = task_result.get('script_file', {})
    if isinstance(script_file, dict) and script_file.get('created_during_task'):
        total_score += scores["script_created"]
        feedback.append("Script created during task window.")
    else:
        feedback.append("Script not created during task window.")

    # Prioritize rerun report if available, else original
    report_file_rerun = task_result.get('report_file_rerun', {})
    report_file_orig = task_result.get('report_file', {})
    report_exists = False
    
    if isinstance(report_file_rerun, dict) and report_file_rerun.get('exists'):
        report_exists = True
        total_score += scores["report_created"]
        feedback.append("Report successfully generated via console execution.")
    elif isinstance(report_file_orig, dict) and report_file_orig.get('exists'):
        report_exists = True
        total_score += scores["report_created"]
        feedback.append("Report generated by agent (not updated on rerun).")
    else:
        feedback.append("ContactLocator report not found.")

    # 3. Analyze Script for Configurations
    if isinstance(script_file, dict) and script_file.get('exists'):
        temp_script = tempfile.NamedTemporaryFile(delete=False, suffix='.script')
        try:
            copy_from_env(script_expected_path, temp_script.name)
            with open(temp_script.name, 'r', encoding='utf-8', errors='ignore') as f:
                script_content = f.read()

            # Spacecraft check
            has_aura = bool(re.search(r'Aura_Next\.SMA\s*=\s*7083', script_content, re.IGNORECASE))
            geo_sats = len(re.findall(r'\.SMA\s*=\s*42164', script_content))
            
            if has_aura and geo_sats >= 3:
                total_score += scores["four_spacecraft"]
                four_sc_ok = True
                feedback.append("All 4 spacecraft correctly defined with proper SMAs.")
            else:
                feedback.append(f"Spacecraft definitions incomplete (Aura LEO: {has_aura}, GEOs found: {geo_sats}).")

            # ContactLocator check
            has_target = bool(re.search(r'\.Target\s*=\s*Aura_Next', script_content, re.IGNORECASE))
            has_tdrs_e = bool(re.search(r'TDRS_East', script_content, re.IGNORECASE))
            has_tdrs_w = bool(re.search(r'TDRS_West', script_content, re.IGNORECASE))
            has_tdrs_z = bool(re.search(r'TDRS_Z', script_content, re.IGNORECASE))
            
            if has_target and has_tdrs_e and has_tdrs_w and has_tdrs_z and "ContactLocator" in script_content:
                total_score += scores["contact_locator_config"]
                feedback.append("ContactLocator Target and Observers configured correctly.")
            else:
                feedback.append("ContactLocator Target/Observers incomplete or missing.")

            # Occulting Body Check
            has_occulting = bool(re.search(r'\.OccultingBodies\s*=\s*\{.*Earth.*\}', script_content, re.IGNORECASE))
            if has_occulting:
                total_score += scores["earth_occultation"]
                occultation_ok = True
                feedback.append("Earth correctly configured as an occulting body.")
            else:
                feedback.append("CRITICAL: Earth NOT set as an occulting body in the script.")
                
        except Exception as e:
            feedback.append(f"Error parsing script: {e}")
        finally:
            if os.path.exists(temp_script.name):
                os.unlink(temp_script.name)

    # 4. Analyze Report Physics
    if report_exists:
        temp_rpt = tempfile.NamedTemporaryFile(delete=False, suffix='.txt')
        try:
            copy_from_env(report_expected_path, temp_rpt.name)
            with open(temp_rpt.name, 'r', encoding='utf-8', errors='ignore') as f:
                report_content = f.read()

            durations = []
            for line in report_content.split('\n'):
                line = line.strip()
                parts = line.split()
                # Looking for standard GMAT output line with a duration at the end
                if len(parts) >= 8 and parts[0].isdigit():
                    try:
                        dur_val = float(parts[-1])
                        durations.append(dur_val)
                    except ValueError:
                        pass
            
            if durations:
                max_duration = max(durations)
                # An entire day is 86400s. If Earth blocks LOS, no single contact should be > ~40-60 mins (3600s).
                # We'll generously set the threshold to 4 hours (14400s) to account for grazing orbits,
                # but flag if it's > 20000s which implies zero occultation.
                if max_duration < 20000.0:
                    total_score += scores["realistic_physics"]
                    physics_ok = True
                    feedback.append(f"Realistic physics verified: Max contact duration is {max_duration}s (Earth occultation working).")
                else:
                    feedback.append(f"Physics violation: Found continuous contact duration of {max_duration}s. Earth occultation failed.")
            else:
                feedback.append("Report found but no valid contact durations parsed.")
                
        except Exception as e:
            feedback.append(f"Error parsing report: {e}")
        finally:
            if os.path.exists(temp_rpt.name):
                os.unlink(temp_rpt.name)

    # 5. VLM Trajectory Verification
    vlm_score = 0
    if query_vlm:
        try:
            from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot
            frames = sample_trajectory_frames(traj, n=4)
            final = get_final_screenshot(traj)
            images = frames + [final] if final else frames
            
            if images:
                prompt = """Analyze these trajectory frames from a GMAT software session.
Did the agent actively use the GMAT UI or script editor to:
1. Define spacecraft parameters (Aura_Next, TDRS)?
2. Configure a ContactLocator?
3. Execute the mission?
Respond in JSON format: {"gui_workflow_visible": true/false, "confidence": "high/medium/low"}
"""
                vlm_response = query_vlm(prompt=prompt, images=images)
                
                if vlm_response.get("success"):
                    parsed = vlm_response.get("parsed", {})
                    if parsed.get("gui_workflow_visible", False):
                        vlm_score = scores["vlm_trajectory"]
                        feedback.append("VLM verified meaningful trajectory workflow.")
                    else:
                        feedback.append("VLM did not detect relevant workflow in trajectory.")
                else:
                    feedback.append("VLM query failed during execution.")
        except ImportError:
            # Fallback if VLM libs not available
            vlm_score = scores["vlm_trajectory"]
            feedback.append("VLM library unavailable. Auto-awarding trajectory points.")
        except Exception as e:
            logger.warning(f"VLM verification error: {e}")
            vlm_score = scores["vlm_trajectory"]
            feedback.append("VLM verification errored. Auto-awarding trajectory points.")
    else:
        vlm_score = scores["vlm_trajectory"]
        feedback.append("No VLM provided. Auto-awarding trajectory points.")
        
    total_score += vlm_score

    # Final Decision
    passed = (total_score >= 60) and four_sc_ok and occultation_ok and physics_ok

    if not passed:
        feedback.append("FAILED: Must score >= 60 AND correctly configure 4 spacecraft + Earth occultation + realistic physics.")

    return {
        "passed": passed,
        "score": total_score,
        "feedback": " | ".join(feedback)
    }