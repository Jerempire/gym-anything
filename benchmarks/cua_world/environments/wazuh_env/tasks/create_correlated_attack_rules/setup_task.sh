#!/bin/bash
echo "=== Setting up create_correlated_attack_rules task ==="

source /workspace/scripts/task_utils.sh

# Record task start time (for anti-gaming)
date +%s > /tmp/task_start_time.txt

# Ensure Wazuh manager is running
if ! docker ps | grep -q "${WAZUH_MANAGER_CONTAINER}"; then
    echo "Starting Wazuh manager container..."
    docker-compose -f /home/ga/wazuh/docker-compose.yml up -d wazuh.manager
    wait_for_service "Wazuh Manager" "docker exec ${WAZUH_MANAGER_CONTAINER} /var/ossec/bin/wazuh-control status | grep running" 60
fi

# Reset local_rules.xml to a clean state (empty group)
echo "Resetting local_rules.xml to clean state..."
CLEAN_RULES='<!-- Custom rules generated by internal_options.conf test -->
<group name="local,">
  <!-- Add your custom rules here -->
</group>
'

# Create temp file and copy to container
TEMP_RULES=$(mktemp)
echo "$CLEAN_RULES" > "$TEMP_RULES"
docker cp "$TEMP_RULES" "${WAZUH_MANAGER_CONTAINER}:/var/ossec/etc/rules/local_rules.xml"
rm -f "$TEMP_RULES"

# Fix permissions
docker exec "${WAZUH_MANAGER_CONTAINER}" chown root:wazuh /var/ossec/etc/rules/local_rules.xml
docker exec "${WAZUH_MANAGER_CONTAINER}" chmod 660 /var/ossec/etc/rules/local_rules.xml

# Reload manager to ensure clean state is active
echo "Reloading Wazuh manager..."
restart_wazuh_manager

# Ensure Firefox is open to the Dashboard
echo "Opening Wazuh Dashboard..."
ensure_firefox_wazuh "${WAZUH_URL_RULES}"

# Take initial screenshot
take_screenshot /tmp/task_initial.png

echo "=== Task setup complete ==="