#!/usr/bin/env python3
"""
Verifier for generate_security_posture_report task.
Verifies the JSON report generated by the agent against live Ground Truth data.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_security_posture_report(traj, env_info, task_info):
    """
    Verify the security posture report.
    
    Criteria:
    1. File exists and is valid JSON (10 pts)
    2. File created during task window (Anti-gaming) (5 pts)
    3. Metadata section correct (5 pts)
    4. Manager info matches Ground Truth (15 pts)
    5. Agent summary matches Ground Truth (20 pts)
    6. Rules/Decoders counts match Ground Truth (approx) (25 pts)
    7. Indexer/Indices data matches Ground Truth (20 pts)
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Copy result file
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback = []
    
    # 1. File Existence & Validity
    if not result.get("file_exists", False):
        return {"passed": False, "score": 0, "feedback": "Report file not found"}
    
    if not result.get("file_valid_json", False):
        return {"passed": False, "score": 5, "feedback": "File exists but is not valid JSON"}
    
    score += 10
    feedback.append("Valid JSON file found")

    # 2. Anti-gaming (Time check)
    if result.get("file_created_during_task", False):
        score += 5
        feedback.append("File created during task")
    else:
        feedback.append("WARNING: File timestamp predates task start")

    # Load Data
    report = result.get("agent_report", {})
    gt = result.get("ground_truth", {})

    # 3. Metadata Check
    meta = report.get("report_metadata", {})
    if (meta.get("generated_by") == "wazuh-api" and 
        meta.get("report_type") == "security_posture" and 
        len(meta.get("generated_at", "")) > 10):
        score += 5
        feedback.append("Metadata correct")
    else:
        feedback.append("Metadata missing or incorrect")

    # 4. Manager Info Check
    rep_mgr = report.get("manager_info", {})
    gt_mgr = gt.get("manager", {})
    
    # Version check (handle potential 'v' prefix mismatches)
    rep_ver = rep_mgr.get("version", "").lstrip("v")
    gt_ver = gt_mgr.get("version", "").lstrip("v")
    
    if rep_ver == gt_ver and rep_ver != "":
        score += 15
        feedback.append("Manager version matches")
    else:
        feedback.append(f"Manager version mismatch: expected {gt_ver}, got {rep_ver}")

    # 5. Agent Summary Check
    rep_ag = report.get("agent_summary", {})
    gt_ag = gt.get("agents", {})
    
    # Determine GT Total (Wazuh API structure varies slightly, export script standardizes)
    # The export script puts raw response in 'agents'. 'agents.total' or 'agents.connection.total'
    # We check the 'total' field from the standardized dump if available, or calculate.
    # The setup_task.sh uses /agents/summary/status which returns data.total or data.connection.total
    
    # Depending on export script normalization:
    # GT_AGENTS is raw `data` object from /agents/summary/status
    gt_total = gt_ag.get("total", 0)
    rep_total = rep_ag.get("total", -1)
    
    if abs(int(rep_total) - int(gt_total)) <= 1:
        score += 20
        feedback.append("Agent total count matches")
    else:
        feedback.append(f"Agent count mismatch: expected ~{gt_total}, got {rep_total}")

    # 6. Rules/Decoders Check
    rep_rules = report.get("ruleset_summary", {})
    rep_r_count = int(rep_rules.get("total_rules", 0))
    rep_d_count = int(rep_rules.get("total_decoders", 0))
    
    gt_r_count = int(gt.get("rules_count", 0))
    gt_d_count = int(gt.get("decoders_count", 0))
    
    # Allow small tolerance
    if abs(rep_r_count - gt_r_count) < 20:
        score += 15
        feedback.append("Rule count matches")
    else:
        feedback.append(f"Rule count mismatch: expected ~{gt_r_count}, got {rep_r_count}")
        
    if abs(rep_d_count - gt_d_count) < 20:
        score += 10
        feedback.append("Decoder count matches")
    else:
        feedback.append(f"Decoder count mismatch: expected ~{gt_d_count}, got {rep_d_count}")

    # 7. Indexer Check
    rep_idx = report.get("indexer_health", {})
    gt_idx = gt.get("indexer", {})
    
    if rep_idx.get("status") == gt_idx.get("status"):
        score += 10
        feedback.append("Indexer status matches")
    else:
        feedback.append("Indexer status mismatch")

    rep_indices = report.get("indices", [])
    gt_indices = gt.get("indices", [])
    
    # Check overlap - agent should have found at least some real indices
    if len(rep_indices) > 0 and any(i in gt_indices for i in rep_indices):
        score += 10
        feedback.append("Indices list contains valid entries")
    else:
        feedback.append("Indices list empty or invalid")

    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }