#!/usr/bin/env python3
"""
Verifier for workforce_classification_update task.

This verifier extracts TSV databases generated by export_result.sh using copy_from_env.
It parses them locally to verify:
1. Creation of 3 employment status types (13, 13, 14 pts)
2. Creation of 2 prefix types (5, 5 pts)
3. Assignment of the correct employment status to 4 specific employees (13, 13, 12, 12 pts)

Total points: 100.
Pass threshold: 60 points.
"""

import os
import json
import logging
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def read_tsv(filepath):
    """Parses a TSV file with headers into a list of dictionaries."""
    if not os.path.exists(filepath):
        return []
    with open(filepath, 'r', encoding='utf-8') as f:
        lines = [line.strip('\n') for line in f.readlines()]
    
    if not lines or len(lines) < 2:
        return []
        
    headers = lines[0].split('\t')
    data = []
    for line in lines[1:]:
        values = line.split('\t')
        row = {headers[i]: (values[i] if i < len(values) else '') for i in range(len(headers))}
        data.append(row)
    return data


def verify_workforce_update(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available in environment."}
    
    metadata = task_info.get('metadata', {})
    expected_statuses = metadata.get('expected_statuses', ['Consultant', 'Secondment', 'Fixed-Term Contract'])
    expected_prefixes = metadata.get('expected_prefixes', ['Mx.', 'Prof.'])
    employee_updates = metadata.get('employee_updates', [])
    pass_threshold = metadata.get('pass_threshold', 60)

    # Temporary files for TSV copy
    temp_emp_status = tempfile.NamedTemporaryFile(delete=False, suffix='.tsv')
    temp_users = tempfile.NamedTemporaryFile(delete=False, suffix='.tsv')
    temp_prefixes = tempfile.NamedTemporaryFile(delete=False, suffix='.tsv')
    temp_emp_summary = tempfile.NamedTemporaryFile(delete=False, suffix='.tsv')
    temp_counts = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    
    try:
        copy_from_env("/tmp/emp_status.tsv", temp_emp_status.name)
        copy_from_env("/tmp/users.tsv", temp_users.name)
        copy_from_env("/tmp/prefixes.tsv", temp_prefixes.name)
        copy_from_env("/tmp/emp_summary.tsv", temp_emp_summary.name)
        copy_from_env("/tmp/workforce_counts.json", temp_counts.name)
        
        emp_status_data = read_tsv(temp_emp_status.name)
        users_data = read_tsv(temp_users.name)
        prefixes_data = read_tsv(temp_prefixes.name)
        emp_summary_data = read_tsv(temp_emp_summary.name)
        
        with open(temp_counts.name, 'r') as f:
            counts_data = json.load(f)
            
    except Exception as e:
        logger.error(f"Failed to copy/read verification data: {e}")
        return {"passed": False, "score": 0, "feedback": f"Data read error: {e}"}
    finally:
        for f in [temp_emp_status, temp_users, temp_prefixes, temp_emp_summary, temp_counts]:
            if os.path.exists(f.name):
                os.unlink(f.name)

    score = 0
    feedback_parts = []
    
    # ----------------------------------------------------
    # 1. Verify Employment Status Creation (40 points total)
    # ----------------------------------------------------
    active_statuses = {}
    for row in emp_status_data:
        if str(row.get('isactive', '1')) == '1':
            name = row.get('statusname', '').strip()
            if name:
                active_statuses[name] = row.get('id', '')
                
    status_scores = [13, 13, 14]
    created_status_count = 0
    for i, expected_status in enumerate(expected_statuses):
        pts = status_scores[i]
        if expected_status in active_statuses:
            score += pts
            created_status_count += 1
            feedback_parts.append(f"Status '{expected_status}' exists ({pts}/{pts})")
        else:
            feedback_parts.append(f"Status '{expected_status}' missing (0/{pts})")
            
    # ----------------------------------------------------
    # 2. Verify Prefix Creation (10 points total)
    # ----------------------------------------------------
    active_prefixes = set()
    for row in prefixes_data:
        if str(row.get('isactive', '1')) == '1':
            p_name = row.get('prefixname', row.get('prefix', '')).strip()
            if p_name:
                active_prefixes.add(p_name)
                
    prefix_scores = [5, 5]
    for i, expected_prefix in enumerate(expected_prefixes):
        pts = prefix_scores[i]
        if expected_prefix in active_prefixes:
            score += pts
            feedback_parts.append(f"Prefix '{expected_prefix}' exists ({pts}/{pts})")
        else:
            feedback_parts.append(f"Prefix '{expected_prefix}' missing (0/{pts})")
            
    # ----------------------------------------------------
    # 3. Verify Employee Updates (50 points total)
    # ----------------------------------------------------
    # To look up by status ID
    status_id_to_name = {v: k for k, v in active_statuses.items()}
    
    emp_scores = [13, 13, 12, 12]
    
    for i, update in enumerate(employee_updates):
        empid = update['empid']
        expected_status = update['new_status']
        pts = emp_scores[i]
        
        # Find user
        user_row = next((r for r in users_data if r.get('employeeId', '') == empid), None)
        
        actual_status = None
        if user_row:
            # Check for the status ID column
            status_id = user_row.get('employmentstatus_id', 
                        user_row.get('empstatus_id', 
                        user_row.get('emp_status_id', '')))
            
            if status_id and status_id in status_id_to_name:
                actual_status = status_id_to_name[status_id]
                
            # Fallback: check main_employees_summary
            if not actual_status and emp_summary_data:
                user_id = user_row.get('id', '')
                summary_row = next((r for r in emp_summary_data if r.get('user_id', '') == user_id), None)
                if summary_row:
                    actual_status = summary_row.get('employmentstatus_name', '')
                    
        if not user_row:
            feedback_parts.append(f"Emp {empid} not found (0/{pts})")
        elif actual_status == expected_status:
            score += pts
            feedback_parts.append(f"Emp {empid} status updated to '{expected_status}' ({pts}/{pts})")
        else:
            feedback_parts.append(f"Emp {empid} status '{actual_status}' != '{expected_status}' (0/{pts})")
            
    # ----------------------------------------------------
    # Anti-gaming checks
    # ----------------------------------------------------
    initial_status_count = counts_data.get('initial_status_count', 0)
    current_status_count = counts_data.get('current_status_count', len(emp_status_data))
    
    # If employees were updated but no new statuses were actually created (impossible without cheating DB)
    if created_status_count > 0 and current_status_count <= initial_status_count:
        score = 0
        feedback_parts.append("FAIL: Anti-gaming check triggered. Status count did not increase.")

    passed = score >= pass_threshold
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }