#!/usr/bin/env python3
"""
Verifier for fleet_vehicle_refresh_cycle task.
Reads the task_result.json generated by export_result.sh via copy_from_env.

Scoring breakdown (100 total, 70 passing):
- Category 'Electric Vehicles' created: 10 pts
- Legacy TRK assets retained (compliance check): 15 pts (5 pts each)
- Legacy TRK assets status changed to 'Not Working': 15 pts (5 pts each)
- Legacy TRK allocations revoked: 15 pts (5 pts each)
- New EV assets created: 15 pts (5 pts each)
- New EVs allocated correctly to proper employees: 30 pts (10 pts each)
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_fleet_vehicle_refresh_cycle(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available in environment."}

    # Copy result JSON from container
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read exported results: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    score = 0
    feedback = []
    
    # 1. Category Creation (10 pts)
    if int(result.get('cat_ev_count', '0')) > 0:
        score += 10
        feedback.append("Category 'Electric Vehicles' exists (+10)")
    else:
        feedback.append("Category 'Electric Vehicles' is missing (0/10)")

    # 2. Legacy Asset Compliance: Retention (15 pts), Status Update (15 pts), Allocation Revoked (15 pts)
    legacies = [
        ('TRK-2015-A', 'trk_a_exists', 'trk_a_status', 'trk_a_allocs'),
        ('TRK-2015-B', 'trk_b_exists', 'trk_b_status', 'trk_b_allocs'),
        ('TRK-2015-C', 'trk_c_exists', 'trk_c_status', 'trk_c_allocs')
    ]
    
    for code, k_exists, k_stat, k_allocs in legacies:
        # Retention
        if int(result.get(k_exists, '0')) > 0:
            score += 5
            feedback.append(f"{code} retained in system (+5)")
            
            # Status
            status = result.get(k_stat, '').lower()
            if 'not working' in status:
                score += 5
                feedback.append(f"{code} status updated to 'Not Working' (+5)")
            else:
                feedback.append(f"{code} status incorrect (got '{status}') (0/5)")
                
            # Allocations Revoked
            if int(result.get(k_allocs, '1')) == 0:
                score += 5
                feedback.append(f"{code} allocations successfully revoked (+5)")
            else:
                feedback.append(f"{code} still has active allocations (0/5)")
        else:
            feedback.append(f"{code} DELETED (Compliance Failure) (0/15 for this asset)")

    # 3. New Fleet Provisioning: Asset Creation (15 pts) & Allocation (30 pts)
    new_evs = [
        ('EV-001', 'Sarah Jenkins', 'ev_1_exists', 'ev_1_cat', 'ev_1_alloc'),
        ('EV-002', 'Michael Chang', 'ev_2_exists', 'ev_2_cat', 'ev_2_alloc'),
        ('EV-003', 'David Rodriguez', 'ev_3_exists', 'ev_3_cat', 'ev_3_alloc')
    ]

    for code, employee, k_exists, k_cat, k_alloc in new_evs:
        # Asset Creation
        if int(result.get(k_exists, '0')) > 0:
            cat = result.get(k_cat, '').lower()
            if 'electric' in cat:
                score += 5
                feedback.append(f"{code} created under correct category (+5)")
            else:
                score += 2
                feedback.append(f"{code} created but under wrong category ('{cat}') (+2)")
                
            # Asset Allocation
            alloc = result.get(k_alloc, '').lower()
            # Split employee name to safely check first and last name presence
            parts = employee.lower().split()
            if all(p in alloc for p in parts):
                score += 10
                feedback.append(f"{code} correctly allocated to {employee} (+10)")
            else:
                feedback.append(f"{code} allocation incorrect (expected {employee}, got '{alloc}') (0/10)")
        else:
            feedback.append(f"{code} not found in system (0/15 for this asset)")

    pass_threshold = task_info.get('metadata', {}).get('pass_threshold', 70)
    passed = score >= pass_threshold
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }