#!/usr/bin/env python3
import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_grayscale_penciltest_render(traj, env_info, task_info):
    """
    Verifies the grayscale pencil test render task.
    
    Criteria:
    1. At least 12 PNG/TGA frames created (20 pts)
    2. Images are grayscale (R=G=B within tolerance) (35 pts)
    3. Images have valid dimensions (>=100x100) (15 pts)
    4. Images were created during the task session (20 pts)
    5. Total file size indicates content (>= 100KB) (10 pts)
    """
    
    # 1. Setup and Load Data
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: copy_from_env not available"}

    # Load expected metadata
    metadata = task_info.get('metadata', {})
    min_frame_count = metadata.get('min_frame_count', 12)
    max_dev = metadata.get('max_channel_deviation', 5.0)

    # Copy result JSON from container
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result = json.load(f)
    except FileNotFoundError:
        return {"passed": False, "score": 0, "feedback": "No result file generated by agent."}
    except json.JSONDecodeError:
        return {"passed": False, "score": 0, "feedback": "Result file was corrupt/empty."}
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Error reading results: {str(e)}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # 2. Extract Metrics
    file_count = result.get("file_count", 0)
    fresh_files = result.get("fresh_files", 0)
    avg_dev = result.get("avg_channel_deviation", 255.0)
    total_size = result.get("total_size_bytes", 0)
    dims = result.get("dimensions", [0, 0])
    valid_files = result.get("valid_files", 0)

    score = 0
    feedback = []

    # 3. Score Calculation

    # Criterion 1: Frame count (20 pts)
    if file_count >= min_frame_count:
        score += 20
        feedback.append(f"✓ Generated {file_count} frames (req >= {min_frame_count})")
    elif file_count > 0:
        # Partial credit
        pts = int(20 * (file_count / min_frame_count))
        score += pts
        feedback.append(f"⚠ Generated only {file_count} frames (req {min_frame_count})")
    else:
        feedback.append("✗ No output frames found")

    # Criterion 2: Grayscale Check (35 pts)
    # The script calculates average deviation. 0 is perfect gray.
    if valid_files > 0:
        if avg_dev <= max_dev:
            score += 35
            feedback.append(f"✓ Images are grayscale (Avg dev: {avg_dev:.2f})")
        elif avg_dev <= max_dev * 3:
            # Partial credit for "mostly" gray or subtle color
            score += 15
            feedback.append(f"⚠ Images are not fully grayscale (Avg dev: {avg_dev:.2f} > {max_dev})")
        else:
            feedback.append(f"✗ Images contain significant color (Avg dev: {avg_dev:.2f})")
    else:
        feedback.append("✗ No valid images to analyze for color")

    # Criterion 3: Dimensions (15 pts)
    if dims[0] >= 100 and dims[1] >= 100:
        score += 15
        feedback.append(f"✓ Valid resolution: {dims[0]}x{dims[1]}")
    elif dims[0] > 0:
        feedback.append(f"✗ Resolution too small: {dims[0]}x{dims[1]}")
    else:
        feedback.append("✗ Could not determine resolution")

    # Criterion 4: Freshness (20 pts)
    if fresh_files >= min_frame_count:
        score += 20
        feedback.append("✓ Files created during task session")
    elif fresh_files > 0:
        pts = int(20 * (fresh_files / min_frame_count))
        score += pts
        feedback.append(f"⚠ Only {fresh_files} files were new")
    else:
        feedback.append("✗ No new files created (timestamps pre-date task)")

    # Criterion 5: Size Check (10 pts)
    # 100KB is a very low bar for 12 frames, ensuring they aren't empty 0-byte files
    if total_size >= 100 * 1024:
        score += 10
        feedback.append(f"✓ Total output size good ({total_size/1024:.1f} KB)")
    elif total_size > 0:
        score += 5
        feedback.append("⚠ Output files are very small")
    else:
        feedback.append("✗ Output files are empty")

    # 4. Final Determination
    # Must have substantial frame count AND pass grayscale check to pass
    passed = (score >= 60) and (avg_dev <= max_dev) and (file_count >= 5)

    if not passed and score >= 60:
        feedback.append("FAIL: Score is high but critical grayscale criteria not met.")

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }