#!/usr/bin/env python3
"""
Verifier for link_asset_to_policy task.

Criteria:
1.  Policy and Asset entities must still exist (not deleted).
2.  Database must show an association record linking the Policy ID and Asset ID.
3.  Policy modification timestamp must indicate activity during the task window.
4.  Visual verification (VLM) can confirm the UI state (secondary).
"""

import json
import os
import logging
import tempfile
from typing import Dict, Any

# Adjust import path for vlm_utils if needed, or assume it's in python path
try:
    from gym_anything.vlm import query_vlm, get_final_screenshot
except ImportError:
    # Fallback/Mock for local testing if gym_anything not available
    def query_vlm(**kwargs): return {"success": False, "error": "VLM not loaded"}
    def get_final_screenshot(traj): return None

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_link_asset_to_policy(traj: Dict[str, Any], env_info: Dict[str, Any], task_info: Dict[str, Any]) -> Dict[str, Any]:
    """
    Verify that the agent associated the specific Business Asset with the Security Policy.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Retrieve the JSON result file generated by export_result.sh
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_file.name)
        with open(temp_file.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        return {
            "passed": False,
            "score": 0,
            "feedback": f"Failed to retrieve verification data from environment: {str(e)}"
        }
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    # 2. Extract verification signals
    policy_exists = result_data.get("policy_exists", False)
    asset_exists = result_data.get("asset_exists", False)
    association_found = result_data.get("association_found", False)
    policy_modified = result_data.get("policy_modified_during_task", False)
    screenshot_exists = result_data.get("screenshot_exists", False)

    # 3. Scoring Logic
    score = 0
    feedback_parts = []

    # Prerequisites (20 points)
    if policy_exists and asset_exists:
        score += 20
        feedback_parts.append("Entities preserved.")
    else:
        feedback_parts.append("CRITICAL: Required entities were deleted.")
    
    # Primary Goal: Association (50 points)
    if association_found:
        score += 50
        feedback_parts.append("Association verified in database.")
    else:
        feedback_parts.append("Association NOT found in database.")

    # Anti-gaming: Timestamp Check (15 points)
    if policy_modified:
        score += 15
        feedback_parts.append("Policy modification confirmed.")
    elif association_found:
        # If association found but timestamp not updated, maybe only link table changed? 
        # Usually Eramba updates 'modified' on the parent object too. 
        # We'll give partial credit if association exists but parent not touched.
        score += 5
        feedback_parts.append("Association found, but policy timestamp unchanged (unusual).")
    else:
        feedback_parts.append("No modification detected.")

    # Evidence: Screenshot (15 points)
    if screenshot_exists:
        score += 15
        feedback_parts.append("Final screenshot captured.")
    
    # 4. Optional VLM Check (for robustness or bonus detail)
    # If DB check failed but agent claims success, VLM might give a clue (or confirm failure)
    # Only run VLM if we have a screenshot and we are borderline or just want to confirm UI state
    vlm_confirmed = False
    if screenshot_exists:
        # Try to get screenshot from trajectory or use the path
        # In this setup, we rely on the DB, but let's see if the VLM sees the success message
        pass 
        # (VLM code omitted to keep verifier simple/fast as DB check is definitive for this task)

    # 5. Final Result
    passed = (score >= 85) and association_found
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback_parts)
    }