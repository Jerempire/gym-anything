#!/usr/bin/env python3
"""
Verifier for Franchise Ops Manual Formatting task.

This script parses the JSON output generated by `export_result.sh` within the container,
which safely analyzes the DOCX file formatting properties using python-docx.

Additionally, it uses VLM on trajectory frames to ensure the agent didn't just 
upload a pre-made file and actually performed UI operations.

Criteria:
1. File saved as expected name (10 points)
2. Title formatting (18pt, Bold, Center) (15 points)
3. Section headings formatting (16pt, Bold, Center) (20 points)
4. Sub-headings formatting (14pt, Bold, Left) (15 points)
5. Safety warnings formatting (Bold, Italic, Underline) (20 points)
6. Table creation (>= 1 table exists) (20 points)
"""

import os
import json
import logging
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def build_vlm_prompt():
    return """You are evaluating an AI agent's performance in ONLYOFFICE Document Editor.
Review these frames from the agent's screen recording trajectory.

Task: Format a raw text operations manual (adjust font sizes, alignments, bold/italics, and convert text to table).

1. Did the agent actively use ONLYOFFICE Document Editor?
2. Is there visual evidence of the agent highlighting text and interacting with the top toolbar (e.g., changing font size to 14, 16, or 18, applying Bold, or creating a table)?
3. Did the agent navigate through the document and perform formatting work?

Respond in JSON format:
{
    "used_onlyoffice": true/false,
    "formatting_activity_observed": true/false,
    "confidence": "high/medium/low",
    "reasoning": "Briefly explain what UI interactions are visible"
}"""


def verify_franchise_ops_manual(traj, env_info, task_info):
    # 1. Provide safe handling of copy_from_env
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 2. Extract Container Results
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read exported result: {e}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    # 3. Assess Anti-Gaming Check (File modification constraint)
    if not result.get("modified_during_task", False):
        return {
            "passed": False,
            "score": 0,
            "feedback": "Document was not modified during the task session. Did you save your changes?"
        }

    score = 0
    feedback_parts = []
    
    # --- Criterion 1: File Saved As (10 points) ---
    if result.get("saved_as_final", False):
        score += 10
        feedback_parts.append("✅ File saved correctly as pct_boh_manual_final.docx")
    else:
        feedback_parts.append("❌ File not saved as pct_boh_manual_final.docx (graded draft instead)")

    # --- Criterion 2: Title Formatting (15 points) ---
    title = result.get("title")
    if title:
        # Align: 1 = Center
        if title["bold"] and title["pt_size"] == 18.0 and title["alignment"] == 1:
            score += 15
            feedback_parts.append("✅ Title perfectly formatted (18pt, Bold, Center)")
        else:
            feedback_parts.append(f"⚠️ Title partially formatted (bold={title['bold']}, pt={title['pt_size']}, align={title['alignment']})")
            score += 5  # Partial credit
    else:
        feedback_parts.append("❌ Title not found")

    # --- Criterion 3: Section Headings (20 points) ---
    sections = result.get("sections", {})
    sec_score = 0
    valid_sec_count = 0
    for sec_id in ["1.0", "2.0", "3.0"]:
        sec = sections.get(sec_id)
        if sec:
            if sec["bold"] and sec["pt_size"] == 16.0 and sec["alignment"] == 1:
                valid_sec_count += 1
    
    if valid_sec_count == 3:
        score += 20
        feedback_parts.append("✅ All 3 sections formatted correctly (16pt, Bold, Center)")
    else:
        pts = (valid_sec_count / 3.0) * 20
        score += pts
        feedback_parts.append(f"⚠️ {valid_sec_count}/3 sections properly formatted")

    # --- Criterion 4: Sub-headings (15 points) ---
    subheadings = result.get("subheadings", {})
    valid_sub_count = 0
    for sub_id in ["1.1", "1.2", "2.1", "2.2", "3.1"]:
        sub = subheadings.get(sub_id)
        if sub:
            # Align: 0 or None = Left
            if sub["bold"] and sub["pt_size"] == 14.0 and sub["alignment"] in [0, None]:
                valid_sub_count += 1
                
    if valid_sub_count == 5:
        score += 15
        feedback_parts.append("✅ All 5 sub-headings formatted correctly (14pt, Bold, Left)")
    else:
        pts = (valid_sub_count / 5.0) * 15
        score += pts
        feedback_parts.append(f"⚠️ {valid_sub_count}/5 sub-headings properly formatted")

    # --- Criterion 5: Safety Warnings (20 points) ---
    warnings = result.get("warnings", [])
    valid_warn_count = 0
    for warn in warnings:
        if warn["bold"] and warn["italic"] and warn["underline"]:
            valid_warn_count += 1
            
    if valid_warn_count >= 3:
        score += 20
        feedback_parts.append("✅ All 3 safety warnings formatted correctly (Bold, Italic, Underline)")
    else:
        pts = (min(valid_warn_count, 3) / 3.0) * 20
        score += pts
        feedback_parts.append(f"⚠️ {valid_warn_count}/3 safety warnings properly formatted")

    # --- Criterion 6: Table Creation (20 points) ---
    table_count = result.get("table_count", 0)
    if table_count >= 1:
        score += 20
        feedback_parts.append("✅ Table created successfully")
    else:
        feedback_parts.append("❌ Table not created (raw text not converted)")


    # 4. Integrate VLM Trajectory Verification
    # Ensure work was done via UI interactions
    query_vlm = env_info.get('query_vlm')
    try:
        from gym_anything.vlm import sample_trajectory_frames
        frames = sample_trajectory_frames(traj, n=5)
        
        if frames and query_vlm:
            vlm_res = query_vlm(images=frames, prompt=build_vlm_prompt())
            if vlm_res.get("success"):
                vlm_parsed = vlm_res.get("parsed", {})
                if not vlm_parsed.get("formatting_activity_observed", False):
                    # Anti-gaming: If VLM detects no interaction, penalize heavily
                    logger.warning("VLM detected no formatting activity in ONLYOFFICE.")
                    score *= 0.25
                    feedback_parts.append("🚨 Penalty: VLM found no visual evidence of formatting interaction.")
    except Exception as e:
        logger.warning(f"VLM verification step failed or skipped: {e}")

    passed = score >= 70

    return {
        "passed": passed,
        "score": round(score, 1),
        "feedback": " | ".join(feedback_parts)
    }