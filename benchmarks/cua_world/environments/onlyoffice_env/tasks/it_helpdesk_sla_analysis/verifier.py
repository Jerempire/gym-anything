#!/usr/bin/env python3
"""
Verifier for IT Helpdesk SLA Performance Analysis task.

Evaluates the saved q3_sla_performance.xlsx file to ensure:
1. Date/time math was performed correctly (Resolution_Hours).
2. Conditional SLA breach logic was applied based on Priority.
3. Summary metrics (Compliance %) were calculated.
"""

import sys
import os
import json
import logging
import tempfile
from datetime import datetime

# Try to import openpyxl, install if missing
try:
    from openpyxl import load_workbook
except ImportError:
    import subprocess
    subprocess.check_call([sys.executable, "-m", "pip", "install", "-q", "openpyxl"])
    from openpyxl import load_workbook

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# Ground truth SLA limits
SLA_LIMITS = {
    "P1": 4.0,
    "P2": 8.0,
    "P3": 24.0,
    "P4": 48.0
}


def parse_datetime(dt_val):
    """Safely parse datetime from Excel cell which might be string or datetime object."""
    if isinstance(dt_val, datetime):
        return dt_val
    if isinstance(dt_val, str):
        try:
            return datetime.strptime(dt_val.strip(), "%Y-%m-%d %H:%M:%S")
        except ValueError:
            pass
    return None


def verify_sla_analysis(traj, env_info, task_info):
    """
    Verify the SLA Analysis Excel workbook.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0.0, "feedback": "Copy function not available"}

    # Read the result JSON generated by export_result.sh
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env("/tmp/task_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            export_data = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0.0, "feedback": f"Failed to read export data: {e}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    if not export_data.get("output_file_exists", False):
        return {
            "passed": False,
            "score": 0.0,
            "feedback": "Target file q3_sla_performance.xlsx was not found. Did you save it with the correct name?"
        }

    # Ensure file was actually created during the task
    task_start = export_data.get("task_start_ts", 0)
    file_mtime = export_data.get("output_file_mtime", 0)
    if file_mtime < task_start:
        return {
            "passed": False,
            "score": 0.0,
            "feedback": "Anti-gaming check failed: File timestamp predates task start."
        }

    # Copy the Excel file
    container_path = "/home/ga/Documents/Spreadsheets/q3_sla_performance.xlsx"
    temp_wb = tempfile.NamedTemporaryFile(delete=False, suffix='.xlsx')
    
    try:
        copy_from_env(container_path, temp_wb.name)
        # Load data-only to check computed values
        wb_data = load_workbook(temp_wb.name, data_only=True)
        # Load with formulas to check if they actually used formulas
        wb_formulas = load_workbook(temp_wb.name, data_only=False)
    except Exception as e:
        return {"passed": False, "score": 0.0, "feedback": f"Failed to open Excel workbook: {e}"}
    finally:
        if os.path.exists(temp_wb.name):
            os.unlink(temp_wb.name)

    score = 0.0
    feedback_parts = ["File successfully saved."]
    
    # Check 1: Find data columns and evaluate row-level accuracy
    # We will search the first sheet for headers
    data_sheet = wb_data.active
    header_row_idx = -1
    col_map = {}
    
    for r_idx, row in enumerate(data_sheet.iter_rows(min_row=1, max_row=20, values_only=True), 1):
        row_strs = [str(c).lower().strip() if c else "" for c in row]
        if "incident_id" in row_strs and "created_at" in row_strs:
            header_row_idx = r_idx
            for c_idx, val in enumerate(row_strs):
                if val:
                    col_map[val] = c_idx
            break
            
    if header_row_idx == -1:
        feedback_parts.append("Could not find standard headers (Incident_ID, Created_At).")
        return {"passed": False, "score": 10.0, "feedback": " | ".join(feedback_parts)}
        
    # Look for new columns
    res_hours_col = None
    sla_breach_col = None
    
    for key, c_idx in col_map.items():
        if "hour" in key or "resolution" in key:
            res_hours_col = c_idx
        if "breach" in key or "sla" in key:
            if key != "sla compliance":  # Avoid confusing with summary
                sla_breach_col = c_idx

    # Evaluate Row-Level Math and Logic
    valid_math_count = 0
    valid_logic_count = 0
    checked_rows = 0
    
    # Grab indexes for the source columns
    try:
        idx_created = col_map["created_at"]
        idx_resolved = col_map["resolved_at"]
        idx_priority = col_map["priority"]
    except KeyError:
        feedback_parts.append("Missing source columns (Created_At, Resolved_At, Priority).")
        return {"passed": False, "score": 10.0, "feedback": " | ".join(feedback_parts)}

    if res_hours_col is not None and sla_breach_col is not None:
        feedback_parts.append("Found Resolution_Hours and SLA_Breached columns.")
        
        for r_idx, row in enumerate(data_sheet.iter_rows(min_row=header_row_idx+1, max_row=min(header_row_idx+100, data_sheet.max_row), values_only=True), header_row_idx+1):
            if not row[idx_created] or not row[idx_resolved] or not row[idx_priority]:
                continue
                
            dt_created = parse_datetime(row[idx_created])
            dt_resolved = parse_datetime(row[idx_resolved])
            priority = str(row[idx_priority]).upper()
            
            if not dt_created or not dt_resolved or priority not in SLA_LIMITS:
                continue
                
            expected_hours = (dt_resolved - dt_created).total_seconds() / 3600.0
            expected_breach = expected_hours > SLA_LIMITS[priority]
            
            agent_hours = row[res_hours_col]
            agent_breach_raw = row[sla_breach_col]
            
            # Check math
            try:
                if isinstance(agent_hours, (int, float)):
                    if abs(agent_hours - expected_hours) < 0.2:
                        valid_math_count += 1
                elif isinstance(agent_hours, str):
                    if abs(float(agent_hours) - expected_hours) < 0.2:
                        valid_math_count += 1
            except (ValueError, TypeError):
                pass
                
            # Check logic
            agent_breach_str = str(agent_breach_raw).strip().lower()
            # Interpret agent's boolean/string flag
            agent_is_breach = agent_breach_str in ['true', 'yes', '1', 'breached']
            
            if agent_is_breach == expected_breach:
                valid_logic_count += 1
                
            checked_rows += 1
            if checked_rows >= 50:
                break
                
        # Scoring Row-Level
        if checked_rows > 0:
            math_accuracy = valid_math_count / checked_rows
            logic_accuracy = valid_logic_count / checked_rows
            
            if math_accuracy > 0.9:
                score += 30.0
                feedback_parts.append("Resolution Hours accurately calculated.")
            elif math_accuracy > 0.5:
                score += 15.0
                feedback_parts.append("Resolution Hours partially correct.")
            else:
                feedback_parts.append("Resolution Hours incorrect or missing.")
                
            if logic_accuracy > 0.9:
                score += 30.0
                feedback_parts.append("SLA Breach conditional logic accurate.")
            elif logic_accuracy > 0.5:
                score += 15.0
                feedback_parts.append("SLA Breach conditional logic partially correct.")
            else:
                feedback_parts.append("SLA Breach conditional logic incorrect or missing.")
    else:
        feedback_parts.append("Could not identify Resolution_Hours or SLA_Breached columns.")

    # Evaluate Summary Table
    # Search entire workbook for "Compliance", "%", "P1", "P2"
    all_text = []
    has_compliance_metrics = False
    
    for sheet_name in wb_data.sheetnames:
        sheet = wb_data[sheet_name]
        for row in sheet.iter_rows(values_only=True):
            for cell in row:
                if cell is not None:
                    val = str(cell).lower()
                    all_text.append(val)
                    if isinstance(cell, float) and 0.0 <= cell <= 1.0:
                        # Found a percentage looking number
                        pass

    joined_text = " ".join(all_text)
    
    if "compliance" in joined_text or "total" in joined_text:
        if "p1" in joined_text and "p2" in joined_text:
            score += 20.0
            feedback_parts.append("Summary table with Priorities and Totals found.")
            
            # Check if there are floats indicating percentages (e.g. 0.85, 0.92) or strings like "85%"
            has_percentages = any("%" in t for t in all_text) or any(isinstance(c, float) and 0.0 < c < 1.0 for sn in wb_data.sheetnames for r in wb_data[sn].iter_rows(values_only=True) for c in r)
            if has_percentages:
                score += 20.0
                feedback_parts.append("Compliance percentages found in summary.")
                has_compliance_metrics = True
        else:
            score += 10.0
            feedback_parts.append("Partial summary table found, missing priority breakdowns.")
    else:
        feedback_parts.append("Summary table not detected.")

    # Determine final pass status
    passed = score >= 70.0

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }