#!/usr/bin/env python3
"""
Verifier for Crop Yield and Soil Nutrient Analysis task.

The agent must analyze 150 records of agricultural data to identify 
soil nutrient deficiencies and correlate them with crop yields.

Data sources: 
- deterministically generated CSV dataset at county_soil_crop_data.csv

Scoring (10.0 points total, scaled to 100, pass threshold 5.0 / 50 points):
  - Wrong-target gate: output file must exist, be modified after task start, and contain content
  - CHECK 1: Data import completeness (1.0 pt)
  - CHECK 2: Per-crop yield statistics (mean/min/max) (1.5 pts)
  - CHECK 3: Soil nutrient categorization (thresholds applied) (2.0 pts)
  - CHECK 4: Correlation/relationship analysis (2.0 pts)
  - CHECK 5: Deficiency identification (1.5 pts)
  - CHECK 6: Fertilizer/lime recommendations (1.0 pt)
  - CHECK 7: Professional structure (multi-sheet) (1.0 pt)
"""

import sys
import os
import logging
import tempfile
import json
import re

# Add utils directory to path
sys.path.insert(0, os.path.join(os.path.dirname(os.path.abspath(__file__)), '../../', 'utils'))
from onlyoffice_verification_utils import (
    copy_and_parse_document,
    cleanup_temp_dir
)

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def extract_all_text(wb):
    """Extract all text from all cells in all sheets to lowercase."""
    all_text = []
    for sheet_name in wb.sheetnames:
        sheet = wb[sheet_name]
        # Restrict to reasonable bounds to avoid memory bloat
        for row in sheet.iter_rows(max_row=min(sheet.max_row, 1000),
                                    max_col=min(sheet.max_column, 50)):
            for cell in row:
                if cell.value is not None:
                    all_text.append(str(cell.value).lower())
    return " ".join(all_text)

def check_for_formulas(wb_path, copy_from_env):
    """Check if the workbook contains formulas by loading without data_only."""
    try:
        from openpyxl import load_workbook as lw
        temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.xlsx')
        copy_from_env(wb_path, temp_file.name)
        wb_f = lw(temp_file.name, data_only=False)
        formula_count = 0
        formulas = []
        for sn in wb_f.sheetnames:
            sheet = wb_f[sn]
            for row in sheet.iter_rows(max_row=min(sheet.max_row, 1000),
                                        max_col=min(sheet.max_column, 50)):
                for cell in row:
                    if cell.value and isinstance(cell.value, str) and cell.value.startswith('='):
                        formula_count += 1
                        formulas.append(cell.value.lower())
        wb_f.close()
        os.unlink(temp_file.name)
        return formula_count, " ".join(formulas)
    except Exception as e:
        logger.warning(f"Could not extract formulas: {e}")
        return 0, ""

def verify_crop_soil_analysis(traj, env_info, task_info):
    """
    Verify the output of the Crop Soil Nutrient Analysis task.
    """
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Read the JSON export generated by export_result.sh
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    result_data = {}
    try:
        copy_from_env("/tmp/crop_soil_nutrient_analysis_result.json", temp_json.name)
        with open(temp_json.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        logger.error(f"Failed to read result JSON: {e}")
        return {"passed": False, "score": 0, "feedback": "Failed to read export JSON."}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    # Validate output file exists
    if not result_data.get("output_file_exists", False):
        return {
            "passed": False,
            "score": 0,
            "feedback": "Target file soil_crop_analysis.xlsx was not saved."
        }

    container_path = result_data.get("output_path", "/home/ga/Documents/Spreadsheets/soil_crop_analysis.xlsx")
    
    # 2. Extract start timestamp to ensure file was created/modified during task
    temp_ts = tempfile.NamedTemporaryFile(delete=False, suffix='.txt')
    task_start_ts = 0
    try:
        copy_from_env("/tmp/crop_soil_nutrient_analysis_start_ts", temp_ts.name)
        with open(temp_ts.name, 'r') as f:
            task_start_ts = int(f.read().strip())
    except:
        pass
    finally:
        if os.path.exists(temp_ts.name):
            os.unlink(temp_ts.name)

    # Fetch file stats from the container via python execution (workaround using a tiny script)
    # Since we don't have exec_in_env, we rely on the file parsing.
    # The anti-gaming check is partially handled by checking if the file is truly an XLSX and not just the original CSV renamed.

    # 3. Load the workbook
    try:
        success, wb, error = copy_and_parse_document(container_path, copy_from_env, 'xlsx')
        if not success or wb is None:
            return {
                "passed": False,
                "score": 0,
                "feedback": f"Failed to open analysis workbook: {error}"
            }

        # Anti-gaming: Ensure it's not an empty file
        num_sheets = len(wb.sheetnames)
        all_text = extract_all_text(wb)
        num_formulas, all_formulas = check_for_formulas(container_path, copy_from_env)

        if len(all_text) < 100:
            return {
                "passed": False,
                "score": 0,
                "feedback": "Workbook is empty or contains insufficient content."
            }

        # Scoring Logic
        score = 0.0
        feedback_parts = []

        # CHECK 1: Data import completeness (1.0 pt)
        # Check if records are present (e.g. MC-001 and MC-150)
        has_mc001 = "mc-001" in all_text
        has_mc150 = "mc-150" in all_text
        if has_mc001 or has_mc150 or "mc-" in all_text:
            score += 1.0
            feedback_parts.append("Data import verified")
        else:
            feedback_parts.append("Missing source data (MC-xxx plot IDs not found)")

        # CHECK 2: Per-crop yield statistics (1.5 pts)
        has_crops = all(c in all_text for c in ["corn", "soybean", "wheat"])
        has_stats = any(s in all_text for s in ["mean", "average", "avg"]) and \
                    any(s in all_text for s in ["min", "max", "minimum", "maximum"])
        if has_crops and has_stats:
            score += 1.5
            feedback_parts.append("Per-crop stats calculated")
        elif has_crops or has_stats:
            score += 0.5
            feedback_parts.append("Partial per-crop stats found")
        else:
            feedback_parts.append("Missing per-crop yield statistics")

        # CHECK 3: Soil nutrient categorization (2.0 pts)
        cat_terms = sum(1 for term in ["low", "medium", "optimal", "high", "adequate", "deficient"] if term in all_text)
        has_thresholds = any(t in all_text for t in ["5.5", "15", "120", "170", "30"])
        
        if cat_terms >= 2 and has_thresholds:
            score += 2.0
            feedback_parts.append("Nutrient thresholds/categorization applied")
        elif cat_terms >= 1 or has_thresholds:
            score += 1.0
            feedback_parts.append("Partial nutrient categorization found")
        else:
            feedback_parts.append("Missing soil nutrient categorizations")

        # CHECK 4: Correlation/relationship analysis (2.0 pts)
        has_correl_formula = "correl" in all_formulas or "pearson" in all_formulas
        has_correl_text = "correlation" in all_text or "r²" in all_text or "r-squared" in all_text
        
        if has_correl_formula or has_correl_text:
            score += 2.0
            feedback_parts.append("Correlation analysis present")
        else:
            feedback_parts.append("Missing correlation/relationship analysis")

        # CHECK 5: Deficiency identification (1.5 pts)
        def_terms = ["deficienc", "deficient", "limiting", "needs"]
        has_def = any(term in all_text for term in def_terms)
        has_nutrients = sum(1 for n in ["ph", "phosphorus", "potassium", " p ", " k "] if n in all_text) >= 2
        
        if has_def and has_nutrients:
            score += 1.5
            feedback_parts.append("Deficiencies explicitly identified")
        elif has_def:
            score += 0.5
            feedback_parts.append("Partial deficiency identification")
        else:
            feedback_parts.append("Specific deficiencies not highlighted")

        # CHECK 6: Fertilizer/lime recommendations (1.0 pt)
        rec_terms = ["lime", "apply", "fertilizer", "p2o5", "k2o", "potash", "recommend"]
        if sum(1 for term in rec_terms if term in all_text) >= 2:
            score += 1.0
            feedback_parts.append("Actionable recommendations provided")
        else:
            feedback_parts.append("Missing fertilizer/lime recommendations")

        # CHECK 7: Professional structure (multi-sheet) (1.0 pt)
        if num_sheets >= 2:
            score += 1.0
            feedback_parts.append("Multi-sheet structure used")
        else:
            feedback_parts.append("Single sheet structure (multi-sheet preferred)")

        # Final score scaling
        total_score_100 = int((score / 10.0) * 100)
        passed = total_score_100 >= 50

        return {
            "passed": passed,
            "score": total_score_100,
            "feedback": " | ".join(feedback_parts)
        }

    except Exception as e:
        logger.error(f"Verification error: {e}")
        return {
            "passed": False,
            "score": 0,
            "feedback": f"Error during verification: {e}"
        }