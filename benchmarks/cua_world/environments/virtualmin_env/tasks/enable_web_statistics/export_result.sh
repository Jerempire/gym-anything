#!/bin/bash
echo "=== Exporting enable_web_statistics result ==="

source /workspace/scripts/task_utils.sh

TASK_START=$(cat /tmp/task_start_time.txt 2>/dev/null || echo "0")
TASK_END=$(date +%s)

# 1. Check if Webalizer feature is enabled in Virtualmin
FEATURE_ENABLED="false"
if virtualmin list-domains --domain acmecorp.test --features 2>/dev/null | grep -q "Webalizer reporting"; then
    FEATURE_ENABLED="true"
fi

# 2. Check if the report file exists
REPORT_PATH="/home/acmecorp/public_html/stats/index.html"
REPORT_EXISTS="false"
REPORT_SIZE="0"
REPORT_MTIME="0"
REPORT_CREATED_DURING_TASK="false"
CONTENT_VALID="false"

if [ -f "$REPORT_PATH" ]; then
    REPORT_EXISTS="true"
    REPORT_SIZE=$(stat -c %s "$REPORT_PATH")
    REPORT_MTIME=$(stat -c %Y "$REPORT_PATH")
    
    # Check if created/modified after task start
    if [ "$REPORT_MTIME" -gt "$TASK_START" ]; then
        REPORT_CREATED_DURING_TASK="true"
    fi
    
    # Check content for Webalizer signature or domain name
    if grep -q "Usage Statistics for" "$REPORT_PATH" || grep -q "Generated by Webalizer" "$REPORT_PATH"; then
        CONTENT_VALID="true"
    fi
fi

# 3. Take final screenshot
take_screenshot /tmp/task_final.png

# 4. Generate JSON result
TEMP_JSON=$(mktemp /tmp/result.XXXXXX.json)
cat > "$TEMP_JSON" << EOF
{
    "task_start": $TASK_START,
    "task_end": $TASK_END,
    "feature_enabled": $FEATURE_ENABLED,
    "report_exists": $REPORT_EXISTS,
    "report_size": $REPORT_SIZE,
    "report_created_during_task": $REPORT_CREATED_DURING_TASK,
    "content_valid": $CONTENT_VALID,
    "screenshot_path": "/tmp/task_final.png"
}
EOF

# Save result with permissive permissions
rm -f /tmp/task_result.json 2>/dev/null || true
cp "$TEMP_JSON" /tmp/task_result.json
chmod 666 /tmp/task_result.json
rm -f "$TEMP_JSON"

echo "Result exported to /tmp/task_result.json"
cat /tmp/task_result.json
echo "=== Export complete ==="