#!/usr/bin/env python3
"""
Verifier for assess_inter_radicular_space task.

Task: Measure space between roots at crest and 6mm depth, determine feasibility.
Criteria:
1. Feasibility Report exists and follows format (15 pts)
2. Report created during task (10 pts)
3. Measurement logic is correct (20 pts)
   - Feasible if BOTH crest and depth >= 7.0mm
4. Screenshot exists (10 pts)
5. VLM Verification (45 pts):
   - Shows correct view (roots visible)
   - Measurements visible
"""

import json
import os
import re
import tempfile
import logging
from gym_anything.vlm import query_vlm, get_final_screenshot

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_assess_inter_radicular_space(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Define paths
    task_result_path = "C:\\workspace\\tasks\\assess_inter_radicular_space\\task_result.json"
    linux_result_path = "/tmp/task_result.json" # Intermediate path not needed if we copy directly

    # Copy result JSON from Windows container (path mapping handled by copy_from_env usually, 
    # but here we might need to be specific if the env is Windows. 
    # Assuming standard mapping where copy_from_env handles the container path).
    
    # We need to pull the specific result file generated by powershell
    # The container path is likely C:\workspace\tasks\...
    # Note: copy_from_env usually expects absolute path inside container.
    
    container_json_path = "C:/workspace/tasks/assess_inter_radicular_space/task_result.json"
    
    temp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        copy_from_env(container_json_path, temp_json.name)
        with open(temp_json.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task results: {e}"}
    finally:
        if os.path.exists(temp_json.name):
            os.unlink(temp_json.name)

    score = 0
    feedback = []

    # 1. Check App Running (5 pts)
    if result.get('app_running', False):
        score += 5
    else:
        feedback.append("Blue Sky Plan was not running at end of task.")

    # 2. Check Report Existence & Timing (20 pts)
    report_content = result.get('report_content', "")
    if result.get('report_exists', False):
        if result.get('report_created_during_task', False):
            score += 20
            feedback.append("Report created successfully.")
        else:
            score += 5
            feedback.append("Report exists but was not modified during task.")
    else:
        feedback.append("Feasibility report not found.")

    # 3. Analyze Report Content & Logic (20 pts)
    # Format:
    # Distance at Crest: [X.X] mm
    # Distance at 6mm Depth: [Y.Y] mm
    # 4.0mm Implant Feasible: [YES/NO]
    
    crest_dist = 0.0
    depth_dist = 0.0
    feasibility_stated = ""
    
    content_valid = False
    
    if report_content:
        try:
            crest_match = re.search(r"Crest:\s*([\d\.]+)", report_content)
            depth_match = re.search(r"Depth:\s*([\d\.]+)", report_content)
            feas_match = re.search(r"Feasible:\s*(YES|NO)", report_content, re.IGNORECASE)
            
            if crest_match and depth_match and feas_match:
                crest_dist = float(crest_match.group(1))
                depth_dist = float(depth_match.group(1))
                feasibility_stated = feas_match.group(1).upper()
                
                # Check Logic: Threshold 7.0mm
                expected_feasibility = "YES" if (crest_dist >= 7.0 and depth_dist >= 7.0) else "NO"
                
                if feasibility_stated == expected_feasibility:
                    score += 20
                    feedback.append(f"Logic correct: {crest_dist}mm/{depth_dist}mm -> {feasibility_stated}")
                    content_valid = True
                else:
                    score += 5
                    feedback.append(f"Logic Incorrect: Measurements {crest_dist}/{depth_dist} should imply {expected_feasibility}, but report says {feasibility_stated}")
            else:
                feedback.append("Report format incorrect. Could not parse measurements or conclusion.")
        except Exception as e:
            feedback.append(f"Error parsing report: {e}")

    # 4. Check Screenshot Existence (10 pts)
    target_screenshot_path = result.get('target_screenshot_path', "")
    screenshot_temp = tempfile.NamedTemporaryFile(delete=False, suffix='.png')
    screenshot_retrieved = False
    
    if result.get('screenshot_exists', False):
        score += 10
        try:
            copy_from_env(target_screenshot_path, screenshot_temp.name)
            screenshot_retrieved = True
        except:
            feedback.append("Screenshot file exists but could not be copied.")

    # 5. VLM Verification (45 pts)
    # We prioritize the user-saved screenshot 'root_space_analysis.png'
    # Fallback to final desktop screenshot if user screenshot missing
    
    image_to_analyze = None
    if screenshot_retrieved:
        image_to_analyze = screenshot_temp.name
    else:
        # Fallback to final screenshot from trajectory
        image_to_analyze = get_final_screenshot(traj)

    if image_to_analyze:
        vlm_prompt = """
        You are verifying a dental imaging task in Blue Sky Plan.
        The user was asked to:
        1. Measure the space between two tooth roots at the crest.
        2. Measure the space between the same roots deeper down (mid-root).
        
        Analyze the image:
        1. Does it show a dental scan view (panoramic, cross-section, or tangential)?
        2. Are there two distinct tooth roots visible side-by-side?
        3. Are there measurement lines/annotations visible between the roots?
        4. Do the measurements look like reasonable gap distances (e.g. 3mm to 15mm)?
        
        Output JSON:
        {
            "is_dental_view": true/false,
            "roots_visible": true/false,
            "measurements_present": true/false,
            "values_readable": true/false,
            "confidence": "high/medium/low"
        }
        """
        
        try:
            vlm_result = query_vlm(image=image_to_analyze, prompt=vlm_prompt)
            parsed = vlm_result.get('parsed', {})
            
            vlm_score = 0
            if parsed.get('is_dental_view'): vlm_score += 10
            if parsed.get('roots_visible'): vlm_score += 15
            if parsed.get('measurements_present'): vlm_score += 20
            
            score += vlm_score
            feedback.append(f"VLM Analysis: {parsed}")
        except Exception as e:
            feedback.append(f"VLM verification failed: {e}")
    else:
        feedback.append("No visual evidence available for VLM.")

    # Cleanup
    if os.path.exists(screenshot_temp.name):
        os.unlink(screenshot_temp.name)

    passed = (score >= 70) and content_valid
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }