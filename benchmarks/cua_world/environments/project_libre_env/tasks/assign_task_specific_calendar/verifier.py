#!/usr/bin/env python3
import json
import os
import sys
import tempfile
import xml.etree.ElementTree as ET
from datetime import datetime

def verify_assign_task_specific_calendar(traj, env_info, task_info):
    """
    Verifies the task: assign_task_specific_calendar
    
    Criteria:
    1. File 'weekend_work.xml' exists and is valid XML (10 pts)
    2. A Calendar named "Weekend Override" exists with Saturday set as working day (25 pts)
    3. Task "Post-Deployment Checks" exists (10 pts)
    4. Task is linked to "Weekend Override" calendar (UID match) (25 pts)
    5. Task Start Date is 2025-03-29 (Saturday) (30 pts)
    """
    
    # 1. Setup and Load Result JSON
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System Error: copy_from_env not available"}

    # Load the JSON result generated by export_result.sh
    result_json_path = tempfile.mktemp()
    try:
        copy_from_env("/tmp/task_result.json", result_json_path)
        with open(result_json_path, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Could not load task results: {str(e)}"}
    finally:
        if os.path.exists(result_json_path):
            os.remove(result_json_path)

    # Check basic file existence
    if not result_data.get("output_exists", False):
        return {"passed": False, "score": 0, "feedback": "Output file 'weekend_work.xml' was not found."}
        
    if not result_data.get("file_created_during_task", False):
        return {"passed": False, "score": 0, "feedback": "Output file exists but was not modified during the task session."}

    # 2. Parse the Project XML
    xml_local_path = tempfile.mktemp()
    try:
        copy_from_env(result_data["output_path"], xml_local_path)
        tree = ET.parse(xml_local_path)
        root = tree.getroot()
    except ET.ParseError:
        return {"passed": False, "score": 10, "feedback": "Output file exists but is not valid XML."}
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read output XML: {str(e)}"}
    finally:
        if os.path.exists(xml_local_path):
            os.remove(xml_local_path)

    score = 10 # Base score for valid file
    feedback = ["File saved successfully."]
    
    # Namespaces usually look like {http://schemas.microsoft.com/project}
    # We'll use a helper to find elements regardless of specific namespace prefix
    def find_all_tags(node, tag_name):
        return [elem for elem in node.iter() if elem.tag.endswith(tag_name)]

    def find_text(node, tag_name):
        # Helper to find direct child text ignoring namespace
        for child in node:
            if child.tag.endswith(tag_name):
                return child.text
        return None

    # --- Verify Calendar Creation ---
    calendars = find_all_tags(root, "Calendar")
    weekend_calendar_uid = None
    calendar_correct = False
    
    for cal in calendars:
        cal_name = find_text(cal, "Name")
        if cal_name and "Weekend Override".lower() in cal_name.lower():
            weekend_calendar_uid = find_text(cal, "UID")
            
            # Check WeekDays
            weekdays = find_all_tags(cal, "WeekDay")
            for wd in weekdays:
                day_type = find_text(wd, "DayType") # 1=Sun, 7=Sat
                day_working = find_text(wd, "DayWorking") # 1=Yes, 0=No
                
                # Check for Saturday (7) being Working (1)
                if day_type == "7" and day_working == "1":
                    calendar_correct = True
                    break
            break

    if weekend_calendar_uid:
        if calendar_correct:
            score += 25
            feedback.append("Correct 'Weekend Override' calendar created with Saturday working.")
        else:
            score += 10 # Partial credit for creating calendar, even if settings wrong
            feedback.append("'Weekend Override' calendar found, but Saturday is not set as a working day.")
    else:
        feedback.append("No calendar named 'Weekend Override' found.")

    # --- Verify Task Creation and Calendar Assignment ---
    tasks = find_all_tags(root, "Task")
    target_task = None
    
    for t in tasks:
        t_name = find_text(t, "Name")
        if t_name and "Post-Deployment Checks".lower() in t_name.lower():
            target_task = t
            break
            
    if target_task:
        score += 10
        feedback.append("Task 'Post-Deployment Checks' found.")
        
        # Check Calendar Assignment
        task_cal_uid = find_text(target_task, "CalendarUID")
        if weekend_calendar_uid and task_cal_uid == weekend_calendar_uid:
            score += 25
            feedback.append("Task correctly assigned to 'Weekend Override' calendar.")
        else:
            feedback.append(f"Task has CalendarUID '{task_cal_uid}', expected '{weekend_calendar_uid}'.")

        # Check Schedule Date
        # Format usually: 2025-03-29T08:00:00
        start_date_str = find_text(target_task, "Start")
        if start_date_str and "2025-03-29" in start_date_str:
            score += 30
            feedback.append("Task scheduled correctly on Saturday, March 29, 2025.")
        else:
            feedback.append(f"Task scheduled on '{start_date_str}', expected '2025-03-29'.")
            
    else:
        feedback.append("Task 'Post-Deployment Checks' not found.")

    passed = (score >= 85) # Requires File + Calendar + Task + Assignment + Date (10+25+10+25+30 = 100)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }