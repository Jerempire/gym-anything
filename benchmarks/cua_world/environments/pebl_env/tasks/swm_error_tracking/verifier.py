#!/usr/bin/env python3
"""
Verifier for swm_error_tracking task.

Checks:
1. File Integrity (10 pts): Output file exists, is valid JSON, and was created during the task.
2. Exclude Contaminated (20 pts): 'sub-999' is properly flagged as excluded: true.
3. Individual BSE Accuracy (30 pts): Computed BSE exactly matches ground truth for >= 12 valid participants.
4. Individual WSE Accuracy (20 pts): Computed WSE exactly matches ground truth for >= 12 valid participants.
5. Group Means (20 pts): Group mean BSE and WSE are accurate within +/- 0.1 tolerance.

Anti-gaming logic enforces that the output file is freshly generated.
Pass threshold is 60 points with Individual BSE Accuracy > 0.
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_swm_error_tracking(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    score = 0
    feedback_parts = []
    
    temp_result = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Load the combined verification payload generated by export_result.sh
        copy_from_env("/tmp/task_result.json", temp_result.name)
        with open(temp_result.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to read result: {e}"}
    finally:
        if os.path.exists(temp_result.name):
            os.unlink(temp_result.name)

    agent_report = result.get('agent_report')
    gt = result.get('ground_truth')
    created_during_task = result.get('created_during_task', False)

    # CRITERION 1: File Integrity & Anti-Gaming (10 pts)
    if not agent_report:
        return {"passed": False, "score": 0, "feedback": "Output file not found or invalid JSON."}
    if not created_during_task:
        return {"passed": False, "score": 0, "feedback": "Output file was not modified during the task session."}

    score += 10
    feedback_parts.append("[+10] Valid JSON output created during task.")

    # Parse agent participant list
    participants_list = agent_report.get('participants', [])
    if not isinstance(participants_list, list):
        return {"passed": False, "score": score, "feedback": "Output JSON missing 'participants' list."}

    agent_parts_map = {}
    for entry in participants_list:
        pid = entry.get('id') or entry.get('participant_id')
        if pid:
            agent_parts_map[str(pid)] = entry

    # CRITERION 2: Exclude Contaminated Participant 'sub-999' (20 pts)
    contaminated_id = 'sub-999'
    contaminated_entry = agent_parts_map.get(contaminated_id)
    
    # Accept exclusion via entry flag or complete omission if excluded lists exist
    is_excluded = False
    if contaminated_entry and contaminated_entry.get('excluded') in (True, 'true', 1):
        is_excluded = True
    elif contaminated_id not in agent_parts_map:
        excluded_top = agent_report.get('excluded', [])
        if isinstance(excluded_top, list) and contaminated_id in excluded_top:
            is_excluded = True

    if is_excluded:
        score += 20
        feedback_parts.append("[+20] Contaminated participant correctly excluded.")
    else:
        feedback_parts.append("[0] Contaminated participant (sub-999) was NOT excluded.")

    # CRITERION 3 & 4: Individual BSE & WSE Accuracies (30 pts, 20 pts)
    valid_participants = [p for p in gt['participants'].keys() if p != contaminated_id]
    correct_bse = 0
    correct_wse = 0

    for pid in valid_participants:
        agent_entry = agent_parts_map.get(pid)
        gt_bse = gt['participants'][pid]['bse']
        gt_wse = gt['participants'][pid]['wse']
        
        if agent_entry and not agent_entry.get('excluded'):
            try:
                # Type safe conversion
                ag_bse = float(agent_entry.get('bse', -1))
                ag_wse = float(agent_entry.get('wse', -1))
                
                if abs(ag_bse - gt_bse) < 0.1:
                    correct_bse += 1
                if abs(ag_wse - gt_wse) < 0.1:
                    correct_wse += 1
            except (ValueError, TypeError):
                pass

    if correct_bse >= 12:
        score += 30
        feedback_parts.append(f"[+30] Individual BSE accurate ({correct_bse}/{len(valid_participants)} valid).")
    elif correct_bse >= 5:
        partial = 15
        score += partial
        feedback_parts.append(f"[+{partial}] Individual BSE partially accurate ({correct_bse} valid).")
    else:
        feedback_parts.append(f"[0] Individual BSE largely incorrect ({correct_bse} valid).")

    if correct_wse >= 12:
        score += 20
        feedback_parts.append(f"[+20] Individual WSE accurate ({correct_wse}/{len(valid_participants)} valid).")
    elif correct_wse >= 5:
        partial = 10
        score += partial
        feedback_parts.append(f"[+{partial}] Individual WSE partially accurate ({correct_wse} valid).")
    else:
        feedback_parts.append(f"[0] Individual WSE largely incorrect ({correct_wse} valid).")

    # CRITERION 5: Group Means (20 pts)
    gt_group_bse = gt['group_mean_bse']
    gt_group_wse = gt['group_mean_wse']
    
    ag_group_bse = agent_report.get('group_mean_bse')
    ag_group_wse = agent_report.get('group_mean_wse')
    
    means_correct = False
    try:
        if ag_group_bse is not None and ag_group_wse is not None:
            if abs(float(ag_group_bse) - gt_group_bse) <= 0.1 and \
               abs(float(ag_group_wse) - gt_group_wse) <= 0.1:
                score += 20
                means_correct = True
                feedback_parts.append("[+20] Group means accurate within tolerance.")
            else:
                feedback_parts.append("[0] Group means fell outside tolerance bounds.")
        else:
            feedback_parts.append("[0] Missing group means in report.")
    except (ValueError, TypeError):
        feedback_parts.append("[0] Group means formatted incorrectly.")

    # Determine final pass criteria
    passed = score >= 60 and correct_bse > 0 and is_excluded
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts)
    }