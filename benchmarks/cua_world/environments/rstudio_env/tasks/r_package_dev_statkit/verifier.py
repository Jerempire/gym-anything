#!/usr/bin/env python3
"""
Verifier for r_package_dev_statkit task.
Evaluates programmatic package structure, mathematical accuracy, and utilizes VLM to inspect RStudio workflow.
"""

import json
import os
import tempfile
import logging

logger = logging.getLogger(__name__)

def verify_r_package_dev_statkit(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "copy_from_env unavailable"}

    tmp = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    tmp.close()
    
    try:
        copy_from_env("/tmp/task_result.json", tmp.name)
        with open(tmp.name, 'r') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to load result JSON: {e}"}
    finally:
        if os.path.exists(tmp.name):
            os.unlink(tmp.name)

    score = 0
    feedback = []
    
    # 1. Package Directory structure
    if result.get('pkg_dir_exists'):
        score += 5
        feedback.append("Package directory exists (+5)")
    else:
        feedback.append("Package directory missing (0)")
        
    # 2. Description Check
    if result.get('description_correct'):
        score += 5
        feedback.append("DESCRIPTION file configured correctly (+5)")
    else:
        feedback.append("DESCRIPTION missing or incorrect version (0)")
        
    # 3. Namespace Check
    if result.get('namespace_roxygen'):
        score += 5
        feedback.append("NAMESPACE generated by roxygen2 (+5)")
    else:
        feedback.append("NAMESPACE not generated by roxygen2 (0)")
        
    # 4. Man pages Check
    if result.get('man_std_error') and result.get('man_coef_var'):
        score += 5
        feedback.append("Documentation .Rd files exist (+5)")
    elif result.get('man_std_error') or result.get('man_coef_var'):
        score += 2
        feedback.append("Partial documentation .Rd files exist (+2)")
    else:
        feedback.append("Documentation .Rd files missing (0)")
        
    # 5. Math logic - std_error
    r_tests = result.get('r_tests', {})
    if r_tests.get('std_error_correct'):
        score += 10
        feedback.append("std_error() implemented correctly including na.rm (+10)")
    elif r_tests.get('std_error_partial'):
        score += 5
        feedback.append("std_error() implemented correctly but fails na.rm logic (+5)")
    else:
        feedback.append("std_error() missing or incorrect (0)")
        
    # 6. Math logic - coef_var
    if r_tests.get('coef_var_correct'):
        score += 10
        feedback.append("coef_var() implemented correctly including na.rm (+10)")
    elif r_tests.get('coef_var_partial'):
        score += 5
        feedback.append("coef_var() implemented correctly but fails na.rm logic (+5)")
    else:
        feedback.append("coef_var() missing or incorrect (0)")
        
    # 7. Dataset Check
    if r_tests.get('dataset_exists'):
        score += 10
        feedback.append("Sample dataset included correctly (+10)")
    else:
        feedback.append("Sample dataset missing or invalid (0)")
        
    # 8. Source Tarball
    if result.get('tarball_exists'):
        score += 10
        feedback.append("Source tarball built (+10)")
    else:
        feedback.append("Source tarball not built (0)")
        
    # 9. Installation
    if r_tests.get('pkg_installed'):
        score += 10
        feedback.append("Package installed successfully (+10)")
    else:
        feedback.append("Package not installed in local library (0)")
        
    # 10. Verification Script
    if result.get('verify_script_exists'):
        score += 10
        feedback.append("Verification script created (+10)")
    else:
        feedback.append("Verification script missing (0)")
        
    # VLM Trajectory Verification
    if query_vlm:
        try:
            from gym_anything.vlm import sample_trajectory_frames, get_final_screenshot
            frames = sample_trajectory_frames(traj, n=4)
            final = get_final_screenshot(traj)
            images = frames + [final] if final else frames
            
            prompt = '''You are evaluating an AI agent creating an R package in RStudio.
The agent was asked to create a 'statkit' package with functions std_error and coef_var, add roxygen2 documentation, and build/install it.
Look at the sequence of screenshots and evaluate:
1. wrote_code: Did the agent write actual R code for the functions and documentation?
2. built_package: Is there evidence of the package being built or installed (e.g., Build pane output, devtools::build(), R CMD INSTALL, or tar.gz file visible)?

Respond in JSON format:
{
    "wrote_code": true/false,
    "built_package": true/false
}'''
            if images:
                vlm_res = query_vlm(prompt=prompt, images=images)
                if vlm_res.get('success'):
                    parsed = vlm_res.get('parsed', {})
                    if parsed.get('wrote_code'):
                        score += 10
                        feedback.append("VLM confirmed code written (+10)")
                    if parsed.get('built_package'):
                        score += 10
                        feedback.append("VLM confirmed package build (+10)")
        except Exception as e:
            logger.warning(f"VLM verification error: {e}")
            
    # Success Criteria
    key_criteria_met = result.get('pkg_dir_exists') and (r_tests.get('std_error_partial') or r_tests.get('coef_var_partial'))
    passed = score >= 60 and key_criteria_met
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback)
    }