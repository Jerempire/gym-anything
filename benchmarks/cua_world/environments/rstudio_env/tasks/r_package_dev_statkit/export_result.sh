#!/bin/bash
echo "=== Exporting task result ==="
source /workspace/scripts/task_utils.sh

take_screenshot /tmp/task_end.png
TASK_START=$(cat /tmp/task_start_time 2>/dev/null || echo "0")

PKG_DIR="/home/ga/RProjects/statkit"
TARBALL="/home/ga/RProjects/statkit_0.1.0.tar.gz"
VERIFY_SCRIPT="/home/ga/RProjects/verify_package.R"

# Verification flags
PKG_DIR_EXISTS="false"
TARBALL_EXISTS="false"
VERIFY_SCRIPT_EXISTS="false"
NAMESPACE_ROXYGEN="false"
DESCRIPTION_CORRECT="false"
MAN_STD_ERROR="false"
MAN_COEF_VAR="false"

if [ -d "$PKG_DIR" ]; then
    PKG_DIR_EXISTS="true"
    if grep -qi "Generated by roxygen2" "$PKG_DIR/NAMESPACE" 2>/dev/null; then
        NAMESPACE_ROXYGEN="true"
    fi
    if grep -qi "Version:\s*0\.1\.0" "$PKG_DIR/DESCRIPTION" 2>/dev/null; then
        DESCRIPTION_CORRECT="true"
    fi
    if [ -f "$PKG_DIR/man/std_error.Rd" ]; then
        MAN_STD_ERROR="true"
    fi
    if [ -f "$PKG_DIR/man/coef_var.Rd" ]; then
        MAN_COEF_VAR="true"
    fi
fi

if [ -f "$TARBALL" ]; then
    TARBALL_EXISTS="true"
fi

if [ -f "$VERIFY_SCRIPT" ]; then
    VERIFY_SCRIPT_EXISTS="true"
fi

# Run an R script to test the package mathematically and structurally
cat > /tmp/test_pkg.R << 'EOF'
res_installed <- FALSE
res_se_partial <- FALSE
res_se_correct <- FALSE
res_cv_partial <- FALSE
res_cv_correct <- FALSE
res_data_exists <- FALSE

# Check if installed in standard library
if ("statkit" %in% rownames(installed.packages())) {
    res_installed <- TRUE
    suppressWarnings(library(statkit, character.only=TRUE))
} else if (file.exists("/home/ga/RProjects/statkit_0.1.0.tar.gz")) {
    tryCatch({
        install.packages("/home/ga/RProjects/statkit_0.1.0.tar.gz", repos=NULL, type="source", quiet=TRUE)
        res_installed <- TRUE
        suppressWarnings(library(statkit, character.only=TRUE))
    }, error=function(e) NULL)
}

# Fallback: source the R directory if package logic failed to install
if (!res_installed && file.exists("/home/ga/RProjects/statkit/R")) {
    files <- list.files("/home/ga/RProjects/statkit/R", full.names=TRUE, pattern="\\.[rR]$")
    for (f in files) {
        tryCatch(source(f), error=function(e) NULL)
    }
}

# Test standard error
tryCatch({
    if (exists("std_error")) {
        se <- std_error(c(1, 2, 3))
        res_se_partial <- isTRUE(abs(se - 0.5773503) < 0.01)
        
        # Test na.rm
        se_na <- std_error(c(1, 2, 3, NA), na.rm=TRUE)
        res_se_narm <- isTRUE(abs(se_na - 0.5773503) < 0.01)
        
        if (res_se_partial && res_se_narm) res_se_correct <- TRUE
    }
}, error=function(e) NULL)

# Test coefficient of variation
tryCatch({
    if (exists("coef_var")) {
        cv <- coef_var(c(1, 2, 3))
        res_cv_partial <- isTRUE(abs(cv - 50) < 0.1)
        
        # Test na.rm
        cv_na <- coef_var(c(1, 2, 3, NA), na.rm=TRUE)
        res_cv_narm <- isTRUE(abs(cv_na - 50) < 0.1)
        
        if (res_cv_partial && res_cv_narm) res_cv_correct <- TRUE
    }
}, error=function(e) NULL)

# Test dataset inclusion
tryCatch({
    # Try using data() if package is installed
    if (res_installed) {
        data("project_metrics", package="statkit", envir=environment())
    }
    
    # If not loaded, look for data file manually
    if (!exists("project_metrics")) {
        files <- list.files("/home/ga/RProjects/statkit/data", pattern="project_metrics\\.(rda|rdata)$", full.names=TRUE, ignore.case=TRUE)
        if (length(files) > 0) {
            load(files[1], envir=environment())
        }
    }
    
    if (exists("project_metrics") && is.data.frame(project_metrics)) {
        if (nrow(project_metrics) >= 10 && ncol(project_metrics) >= 3) {
            res_data_exists <- TRUE
        }
    }
}, error=function(e) NULL)

# Output results as JSON directly
cat(sprintf('{"pkg_installed": %s, "std_error_correct": %s, "std_error_partial": %s, "coef_var_correct": %s, "coef_var_partial": %s, "dataset_exists": %s}',
    ifelse(res_installed, 'true', 'false'),
    ifelse(res_se_correct, 'true', 'false'),
    ifelse(res_se_partial, 'true', 'false'),
    ifelse(res_cv_correct, 'true', 'false'),
    ifelse(res_cv_partial, 'true', 'false'),
    ifelse(res_data_exists, 'true', 'false')),
    file="/tmp/r_test_results.json")
EOF

sudo -u ga Rscript /tmp/test_pkg.R

R_TEST_RESULTS=$(cat /tmp/r_test_results.json 2>/dev/null || echo '{"pkg_installed": false, "std_error_correct": false, "std_error_partial": false, "coef_var_correct": false, "coef_var_partial": false, "dataset_exists": false}')

# Combine results into final JSON
TEMP_JSON=$(mktemp /tmp/result.XXXXXX.json)
cat > "$TEMP_JSON" << EOF
{
    "pkg_dir_exists": $PKG_DIR_EXISTS,
    "tarball_exists": $TARBALL_EXISTS,
    "verify_script_exists": $VERIFY_SCRIPT_EXISTS,
    "namespace_roxygen": $NAMESPACE_ROXYGEN,
    "description_correct": $DESCRIPTION_CORRECT,
    "man_std_error": $MAN_STD_ERROR,
    "man_coef_var": $MAN_COEF_VAR,
    "r_tests": $R_TEST_RESULTS,
    "timestamp": "$(date -Iseconds)"
}
EOF

rm -f /tmp/task_result.json 2>/dev/null || true
cp "$TEMP_JSON" /tmp/task_result.json
chmod 666 /tmp/task_result.json
rm -f "$TEMP_JSON"
echo "Export complete."