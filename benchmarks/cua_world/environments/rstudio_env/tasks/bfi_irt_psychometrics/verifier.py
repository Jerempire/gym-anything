#!/usr/bin/env python3
"""
Verifier for BFI IRT Psychometrics Task.

Criteria:
1. Reliability CSV: Exists, 5 rows, alphas within expected ranges. (20 pts)
2. IRT Parameters CSV: Exists, 25 rows, all discrimination (a) parameters > 0. (30 pts)
   * This checks the "reverse coding" trap.
3. Item Fit CSV: Exists. (15 pts)
4. Plot PNG: Exists, substantial size, VLM verified content. (25 pts)
5. Script modified. (10 pts)

Pass Threshold: 60/100 AND IRT parameters positive.
"""

import json
import os
import tempfile
import csv
import logging
from typing import Dict, Any, List

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

# Expected Alpha Ranges (from literature/ground truth)
EXPECTED_ALPHAS = {
    "A": (0.60, 0.85),
    "C": (0.70, 0.90),
    "E": (0.65, 0.85),
    "N": (0.75, 0.95),
    "O": (0.50, 0.80)
}

VLM_PROMPT = """You are verifying a psychometric analysis plot.
The image should contain multiple panels related to Item Response Theory (IRT) or reliability analysis.
Look for:
1. Test Information Function (curve peaking in the middle).
2. Item Characteristic Curves (ICCs) or Trace Lines (S-shaped curves).
3. Bar charts of parameters or fit statistics.

Does this image look like a valid statistical analysis plot generated by R?
Reply JSON with: {"is_valid_plot": true/false, "content_description": "..."}
"""

def verify_bfi_irt_psychometrics(traj, env_info, task_info):
    """Verify BFI psychometrics task."""
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    get_final_screenshot = env_info.get('get_final_screenshot')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # 1. Load Task Result JSON
    result = {}
    with tempfile.NamedTemporaryFile(suffix='.json') as tf:
        try:
            copy_from_env("/tmp/task_result.json", tf.name)
            tf.seek(0)
            result = json.load(tf)
        except Exception as e:
            return {"passed": False, "score": 0, "feedback": f"Failed to load result JSON: {e}"}

    score = 0
    feedback = []
    files = result.get("files", {})

    # =========================================================
    # Check 1: Reliability Analysis (20 pts)
    # =========================================================
    rel_info = files.get("reliability", {})
    if rel_info.get("new", False):
        try:
            with tempfile.NamedTemporaryFile(mode='w+', suffix='.csv') as tf:
                copy_from_env(rel_info["path"], tf.name)
                tf.seek(0)
                reader = csv.DictReader(tf)
                rows = list(reader)
                
                if len(rows) >= 5:
                    score += 5
                    feedback.append("Reliability CSV has sufficient rows.")
                    
                    # Check values
                    valid_alphas = 0
                    for row in rows:
                        # Flexible column matching
                        scale_val = next((v for k, v in row.items() if 'scale' in k.lower() or 'factor' in k.lower()), "")
                        alpha_val = next((v for k, v in row.items() if 'alpha' in k.lower() or 'raw' in k.lower()), "0")
                        
                        # Extract scale letter (A, C, E, N, O)
                        scale_char = scale_val[0].upper() if scale_val else ""
                        
                        try:
                            a = float(alpha_val)
                            expected = EXPECTED_ALPHAS.get(scale_char)
                            if expected and expected[0] <= a <= expected[1]:
                                valid_alphas += 1
                        except ValueError:
                            pass
                    
                    if valid_alphas >= 3:
                        score += 15
                        feedback.append(f"Alphas valid for {valid_alphas} scales (+15).")
                    else:
                        score += 5
                        feedback.append(f"Alphas valid for only {valid_alphas} scales (+5).")
                else:
                    feedback.append("Reliability CSV has fewer than 5 rows.")
        except Exception as e:
            feedback.append(f"Error checking reliability CSV: {e}")
    else:
        feedback.append("Reliability CSV missing or not new.")

    # =========================================================
    # Check 2: IRT Parameters & Reverse Coding (30 pts)
    # =========================================================
    param_info = files.get("parameters", {})
    reverse_coding_failed = False
    
    if param_info.get("new", False):
        try:
            with tempfile.NamedTemporaryFile(mode='w+', suffix='.csv') as tf:
                copy_from_env(param_info["path"], tf.name)
                tf.seek(0)
                reader = csv.DictReader(tf)
                rows = list(reader)
                
                if len(rows) >= 25:
                    score += 10
                    feedback.append("IRT Parameters CSV has correct number of items (+10).")
                    
                    # Check discrimination (a) parameters
                    # If reverse coding was skipped, negative items will have negative 'a'
                    neg_discrimination = 0
                    for row in rows:
                        # Find 'a' or 'a1' column
                        a_val = next((v for k, v in row.items() if k.lower() in ['a', 'a1', 'discrim', 'discrimination']), "0")
                        try:
                            if float(a_val) < 0:
                                neg_discrimination += 1
                        except ValueError:
                            pass
                            
                    if neg_discrimination == 0:
                        score += 20
                        feedback.append("All discrimination parameters are positive. Reverse coding likely correct (+20).")
                    else:
                        reverse_coding_failed = True
                        feedback.append(f"FAIL: Found {neg_discrimination} negative discrimination parameters. Reverse coding missed.")
                else:
                    feedback.append("IRT Parameters CSV incomplete.")
        except Exception as e:
            feedback.append(f"Error checking parameters CSV: {e}")
    else:
        feedback.append("IRT Parameters CSV missing.")

    # =========================================================
    # Check 3: Item Fit & Script (25 pts)
    # =========================================================
    fit_info = files.get("fit", {})
    if fit_info.get("new", False):
        score += 15
        feedback.append("Item Fit CSV created (+15).")
    
    if result.get("script_modified", False):
        score += 10
        feedback.append("Analysis script modified (+10).")

    # =========================================================
    # Check 4: Visualization (25 pts)
    # =========================================================
    plot_info = files.get("plot", {})
    if plot_info.get("new", False) and plot_info.get("size", 0) > 30000: # >30KB
        score += 10
        feedback.append("Plot file exists and is substantial (+10).")
        
        # VLM Check
        if query_vlm:
            try:
                # We need to get the image content. The framework usually handles VLM on trajectory
                # but here we specifically want to verify the *output file*.
                # Since we can't easily upload the file to VLM via this function's interface 
                # (unless query_vlm accepts bytes), we will fallback to checking the *final screenshot*
                # which should show the plot if the agent is doing it right.
                
                # Check final screenshot first
                final_ss = get_final_screenshot(traj)
                vlm_res = query_vlm(prompt=VLM_PROMPT, image=final_screenshot)
                
                if vlm_res.get("success") and vlm_res.get("parsed", {}).get("is_valid_plot"):
                    score += 15
                    feedback.append("VLM verified plot content (+15).")
                else:
                    # Fallback partial credit if file exists but VLM unsure
                    score += 5
                    feedback.append("VLM could not verify plot content clearly (+5).")
            except:
                score += 5 # Grace points
    elif plot_info.get("new", False):
         score += 5
         feedback.append("Plot file exists but is small (+5).")
         
    # =========================================================
    # Final Score Calculation
    # =========================================================
    
    # Critical failure condition: Negative discrimination parameters implies fundamental method error
    if reverse_coding_failed and score > 40:
        score = 40
        feedback.append("SCORE CAPPED: Fundamental error in reverse coding detected.")

    passed = score >= 60
    
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback)
    }