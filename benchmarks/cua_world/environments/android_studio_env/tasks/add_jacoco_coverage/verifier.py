#!/usr/bin/env python3
"""
Verifier for add_jacoco_coverage task.

Scoring Criteria (100 pts total):
1. JaCoCo plugin applied (15 pts)
2. JaCoCo version configured (10 pts)
3. Custom 'jacocoTestReport' task defined (20 pts)
4. Task depends on 'testDebugUnitTest' (10 pts)
5. Exclusion filters applied for generated code (10 pts)
6. HTML report explicitly enabled (5 pts)
7. Source directories configured (5 pts)
8. Gradle build succeeds (10 pts) - Checked via exit code from export script
9. HTML report generated (10 pts) - Checked via file existence from export script
10. VLM Trajectory Verification (5 pts)
"""

import json
import logging
import os
import re
import tempfile

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_add_jacoco_coverage(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Environment access failed"}

    # Retrieve result JSON generated by export_result.sh
    try:
        tmp_json = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
        copy_from_env("/tmp/task_result.json", tmp_json.name)
        with open(tmp_json.name, 'r') as f:
            result_data = json.load(f)
        os.unlink(tmp_json.name)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task results: {str(e)}"}

    # Parse data from result
    file_modified = result_data.get('file_modified', False)
    gradle_exit = result_data.get('gradle_exit_code', 1)
    report_generated = result_data.get('report_generated', False)
    build_gradle_content = result_data.get('build_gradle_content', '')
    
    score = 0
    feedback = []

    # Anti-gaming check
    if not file_modified:
        return {"passed": False, "score": 0, "feedback": "Anti-gaming: build.gradle.kts was not modified."}

    # Static Analysis of build.gradle.kts content
    content_lower = build_gradle_content.lower()
    
    # 1. Plugin applied (15 pts)
    if 'id("jacoco")' in build_gradle_content or "apply plugin: 'jacoco'" in build_gradle_content or 'id "jacoco"' in build_gradle_content:
        score += 15
        feedback.append("PASS: JaCoCo plugin applied")
    else:
        feedback.append("FAIL: JaCoCo plugin not found")

    # 2. Version configured (10 pts)
    if 'toolversion' in content_lower or '0.8.' in content_lower:
        # Loose check for version configuration inside jacoco block
        score += 10
        feedback.append("PASS: JaCoCo version configured")
    else:
        feedback.append("FAIL: JaCoCo toolVersion not found")

    # 3. Task defined (20 pts)
    if 'jacocotestreport' in build_gradle_content and 'jacocoreport' in content_lower:
        score += 20
        feedback.append("PASS: jacocoTestReport task defined")
    else:
        feedback.append("FAIL: jacocoTestReport task missing")

    # 4. Depends on testDebugUnitTest (10 pts)
    if 'dependson' in content_lower and ('testdebugunittest' in content_lower or 'testdebug' in content_lower):
        score += 10
        feedback.append("PASS: Task dependency configured")
    else:
        feedback.append("FAIL: Task dependency missing")

    # 5. Exclusion filters (10 pts)
    exclusions = ["r.class", "buildconfig", "manifest"]
    found_exclusions = [ex for ex in exclusions if ex in content_lower]
    if len(found_exclusions) >= 2:
        score += 10
        feedback.append("PASS: Exclusion filters configured")
    elif len(found_exclusions) > 0:
        score += 5
        feedback.append("PARTIAL: Some exclusion filters found")
    else:
        feedback.append("FAIL: Exclusion filters missing")

    # 6. HTML report enabled (5 pts)
    if 'html' in content_lower and ('required' in content_lower or 'enabled' in content_lower):
        score += 5
        feedback.append("PASS: HTML report enabled")
    else:
        feedback.append("FAIL: HTML report configuration missing")

    # 7. Source directories (5 pts)
    if 'src/main/java' in build_gradle_content or 'src/main/kotlin' in build_gradle_content:
        score += 5
        feedback.append("PASS: Source directories configured")
    else:
        feedback.append("FAIL: Source directories missing")

    # 8. Gradle build succeeds (10 pts)
    if gradle_exit == 0:
        score += 10
        feedback.append("PASS: Gradle build succeeded")
    else:
        feedback.append("FAIL: Gradle build failed")

    # 9. HTML report generated (10 pts)
    if report_generated:
        score += 10
        feedback.append("PASS: HTML report generated")
    else:
        feedback.append("FAIL: HTML report file not found")

    # 10. VLM Trajectory Verification (5 pts)
    if query_vlm:
        try:
            # Use gym_anything utilities if available, or manual sampling
            from gym_anything.vlm import sample_trajectory_frames
            frames = sample_trajectory_frames(traj, num_samples=4)
            
            prompt = """
            Review these screenshots of an agent working in Android Studio.
            1. Did the agent open the 'build.gradle.kts' file?
            2. Did the agent edit the file to add JaCoCo configuration?
            3. Did the agent run a Gradle task via terminal or IDE run configuration?
            
            Return JSON: {"evidence_found": boolean}
            """
            vlm_res = query_vlm(prompt=prompt, images=frames)
            if vlm_res and vlm_res.get('success') and vlm_res.get('parsed', {}).get('evidence_found'):
                score += 5
                feedback.append("PASS: VLM verified workflow")
            else:
                feedback.append("FAIL: VLM did not observe expected workflow")
        except ImportError:
            # Fallback if specific lib not available, assume passing if score is high
            if score >= 60:
                score += 5
                feedback.append("PASS: VLM skipped (high confidence from programmatic)")
    else:
        if score >= 60:
            score += 5 # Auto-grant if VLM unavailable but task succeeded programmatically

    passed = score >= 60 and gradle_exit == 0
    
    return {
        "passed": passed,
        "score": score,
        "feedback": "; ".join(feedback)
    }