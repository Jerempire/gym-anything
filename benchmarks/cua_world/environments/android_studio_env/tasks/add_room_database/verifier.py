#!/usr/bin/env python3
"""
Verifier for Add Room Database task.

Checks:
1. KSP plugin added to root build.gradle.kts
2. KSP plugin and Room dependencies added to app/build.gradle.kts
3. Task.kt entity created with correct fields and annotations
4. TaskDao.kt interface created with correct methods and annotations
5. AppDatabase.kt created extending RoomDatabase
6. Project compiles successfully
7. VLM verification of trajectory
"""

import json
import logging
import os
import re
import tempfile
from typing import Dict, Any

# Import VLM utils from the environment if available
try:
    from gym_anything.vlm import (
        sample_trajectory_frames,
        get_final_screenshot,
        get_first_screenshot,
    )
except ImportError:
    pass

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)


def _read_json_from_env(copy_from_env, container_path: str) -> dict:
    """Copy a JSON file out of the container and return parsed dict."""
    tmp = tempfile.NamedTemporaryFile(delete=False, suffix=".json")
    try:
        copy_from_env(container_path, tmp.name)
        with open(tmp.name, "r", encoding="utf-8") as fh:
            return json.load(fh)
    except Exception as exc:
        logger.debug("Could not read JSON %s: %s", container_path, exc)
        return {}
    finally:
        if os.path.exists(tmp.name):
            os.unlink(tmp.name)


def verify_via_vlm(traj: Dict[str, Any], query_vlm) -> Dict[str, Any]:
    """Perform VLM verification on trajectory frames."""
    if not query_vlm:
        return {"success": False, "score": 0, "feedback": "VLM unavailable"}

    try:
        frames = sample_trajectory_frames(traj, num_samples=4)
        if not frames:
            # Fallback to just final if trajectory sampling fails
            final = get_final_screenshot(traj)
            if final:
                frames = [final]
            else:
                return {"success": False, "score": 0, "feedback": "No frames available"}

        prompt = """You are verifying an Android Studio task: "Add Room Database".
        
        Review the screenshots to see if the user:
        1. Edited build.gradle files (dependencies/plugins).
        2. Created new Kotlin files (Task.kt, TaskDao.kt, AppDatabase.kt).
        3. Wrote code that looks like database entities/DAOs (annotations like @Entity, @Dao).
        4. Ran a build or sync that completed successfully (green checks, no red errors).
        
        Respond in JSON:
        {
            "files_created": true/false,
            "gradle_edited": true/false,
            "build_attempted": true/false,
            "errors_visible": true/false,
            "overall_impression": "positive"/"negative",
            "summary": "brief summary of what is seen"
        }
        """
        
        result = query_vlm(prompt=prompt, images=frames)
        if result and result.get("success"):
            parsed = result.get("parsed", {})
            score = 0
            if parsed.get("files_created"): score += 30
            if parsed.get("gradle_edited"): score += 30
            if parsed.get("build_attempted") and not parsed.get("errors_visible"): score += 40
            
            return {
                "success": True, 
                "score": score, 
                "feedback": parsed.get("summary", "VLM check complete")
            }
            
    except Exception as e:
        logger.warning(f"VLM check failed: {e}")
        
    return {"success": False, "score": 0, "feedback": "VLM check failed"}


def verify_add_room_database(traj, env_info, task_info):
    """Verify the Room Database task."""
    copy_from_env = env_info.get('copy_from_env')
    query_vlm = env_info.get('query_vlm')
    
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Read the result JSON generated by export_result.sh
    result = _read_json_from_env(copy_from_env, "/tmp/task_result.json")
    
    score = 0
    feedback_parts = []
    
    # 1. Check Root build.gradle (10 pts)
    # Looking for KSP plugin declaration
    root_gradle = result.get('project_build_gradle', '')
    if 'com.google.devtools.ksp' in root_gradle:
        score += 10
        feedback_parts.append("Root build.gradle: KSP plugin declared (10/10)")
    else:
        feedback_parts.append("Root build.gradle: KSP plugin missing (0/10)")

    # 2. Check App build.gradle (15 pts)
    # Looking for 'id("com.google.devtools.ksp")' and Room dependencies
    app_gradle = result.get('app_build_gradle', '')
    app_gradle_score = 0
    if 'com.google.devtools.ksp' in app_gradle:
        app_gradle_score += 5
    if 'androidx.room:room-runtime' in app_gradle:
        app_gradle_score += 5
    if 'androidx.room:room-compiler' in app_gradle and ('ksp(' in app_gradle or 'kapt' in app_gradle):
        app_gradle_score += 5
        
    score += app_gradle_score
    feedback_parts.append(f"App build.gradle dependencies: ({app_gradle_score}/15)")

    # 3. Check Task.kt (20 pts)
    task_kt = result.get('task_kt', '')
    task_score = 0
    if task_kt:
        if '@Entity' in task_kt: task_score += 5
        if 'tableName' in task_kt and 'tasks' in task_kt: task_score += 3
        if '@PrimaryKey' in task_kt: task_score += 4
        # Check fields
        if 'title' in task_kt and 'String' in task_kt: task_score += 2
        if 'isCompleted' in task_kt and 'Boolean' in task_kt: task_score += 2
        if 'createdAt' in task_kt and 'Long' in task_kt: task_score += 2
        if 'description' in task_kt: task_score += 2
    
    score += task_score
    if task_score > 0:
        feedback_parts.append(f"Task entity: ({task_score}/20)")
    else:
        feedback_parts.append("Task entity: file missing or empty (0/20)")

    # 4. Check TaskDao.kt (20 pts)
    dao_kt = result.get('task_dao_kt', '')
    dao_score = 0
    if dao_kt:
        if '@Dao' in dao_kt: dao_score += 5
        if '@Query' in dao_kt and 'SELECT' in dao_kt: dao_score += 5
        if '@Insert' in dao_kt: dao_score += 3
        if '@Delete' in dao_kt: dao_score += 3
        if '@Update' in dao_kt: dao_score += 4
        
    score += dao_score
    if dao_score > 0:
        feedback_parts.append(f"TaskDao: ({dao_score}/20)")
    else:
        feedback_parts.append("TaskDao: file missing or empty (0/20)")

    # 5. Check AppDatabase.kt (15 pts)
    db_kt = result.get('database_kt', '')
    db_score = 0
    if db_kt:
        if '@Database' in db_kt: db_score += 5
        if 'RoomDatabase' in db_kt: db_score += 5
        if 'abstract fun taskDao' in db_kt: db_score += 5
        
    score += db_score
    if db_score > 0:
        feedback_parts.append(f"AppDatabase: ({db_score}/15)")
    else:
        feedback_parts.append("AppDatabase: file missing or empty (0/15)")

    # 6. Build Success (20 pts)
    if result.get('build_success', False):
        score += 20
        feedback_parts.append("Build verification: Success (20/20)")
    else:
        feedback_parts.append("Build verification: Failed (0/20)")
        
    # 7. VLM check (Bonus/Correction)
    # If code looks perfect but build failed (maybe environment fluke), VLM can help confirm intent
    if score >= 60 and not result.get('build_success', False):
        vlm_res = verify_via_vlm(traj, query_vlm)
        if vlm_res['success'] and vlm_res['score'] > 70:
            score += 10
            feedback_parts.append("VLM Bonus: Visuals confirm work despite build failure (+10)")

    passed = score >= 60  # Pass threshold
    
    return {
        "passed": passed,
        "score": min(100, score),
        "feedback": " | ".join(feedback_parts)
    }