#!/usr/bin/env python3
"""
Verifier for configure_superdom task in NinjaTrader.

Scoring Criteria:
1. Workspace Saved (15 pts): Verify workspace XML files modified.
2. SuperDOM Window (25 pts): Verify 'SuperDom' or 'DynamicSuperDom' present in XML.
3. Instrument SPY (20 pts): Verify 'SPY' associated with SuperDOM.
4. Account Sim101 (15 pts): Verify 'Sim101' account selected.
5. Quantity 50 (15 pts): Verify default order quantity set to 50.
6. VLM Verification (10 pts): Visual check for SuperDOM UI using trajectory.

Pass Threshold: 70 points
"""

import json
import tempfile
import os
import logging
from gym_anything.vlm import sample_trajectory_frames, query_vlm

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_configure_superdom(traj, env_info, task_info):
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "Copy function not available"}

    # Define paths
    result_remote_path = task_info.get('metadata', {}).get(
        'result_path', 
        "C:\\Users\\Docker\\Desktop\\NinjaTraderTasks\\configure_superdom_result.json"
    )

    # 1. Load Programmatic Results
    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    try:
        # Note: copy_from_env usually handles path conversion if the container is Linux,
        # but for Windows containers, ensure the path format matches what the framework expects.
        # Assuming the framework handles the "C:\" path correctly or the agent mount.
        # We try copying the file generated by export_result.ps1
        copy_from_env(result_remote_path, temp_file.name)
        with open(temp_file.name, 'r') as f:
            result_data = json.load(f)
    except Exception as e:
        logger.error(f"Failed to load result file: {e}")
        result_data = {}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []

    # Criterion 1: Workspace Saved (15 pts)
    if result_data.get("workspace_saved", False):
        score += 15
        feedback_parts.append("Workspace saved (+15)")
    else:
        feedback_parts.append("Workspace NOT saved (0)")

    # Criterion 2: SuperDOM Window (25 pts)
    if result_data.get("has_superdom", False):
        score += 25
        feedback_parts.append("SuperDOM window detected (+25)")
    else:
        feedback_parts.append("SuperDOM window NOT found (0)")

    # Criterion 3: Instrument SPY (20 pts)
    if result_data.get("target_instrument_found", False):
        score += 20
        feedback_parts.append("Instrument SPY configured (+20)")
    else:
        feedback_parts.append("Instrument SPY NOT found (0)")

    # Criterion 4: Account Sim101 (15 pts)
    if result_data.get("target_account_found", False):
        score += 15
        feedback_parts.append("Account Sim101 selected (+15)")
    else:
        feedback_parts.append("Account Sim101 NOT found (0)")

    # Criterion 5: Quantity 50 (15 pts)
    if result_data.get("target_quantity_found", False):
        score += 15
        feedback_parts.append("Quantity set to 50 (+15)")
    else:
        feedback_parts.append("Quantity 50 NOT found (0)")

    # Criterion 6: VLM Verification (10 pts)
    # Use trajectory frames to confirm it looks like a SuperDOM (ladder) and not a Chart
    frames = sample_trajectory_frames(traj, n=3)
    
    vlm_passed = False
    vlm_reason = "VLM did not run"
    
    if frames:
        prompt = (
            "You are verifying a NinjaTrader task. The user should have opened a 'SuperDOM' (Dynamic Order Matrix) window.\n"
            "This window typically looks like a vertical ladder of prices with 'Buy' and 'Sell' columns, NOT a standard time-series chart.\n\n"
            "Look at the images provided.\n"
            "1. Is a SuperDOM/Price Ladder window visible?\n"
            "2. Is the instrument 'SPY' visible on it?\n"
            "3. Is the Quantity field set to '50'?\n\n"
            "Respond in JSON: {\"is_superdom_visible\": bool, \"instrument_spy_visible\": bool, \"quantity_50_visible\": bool, \"reasoning\": str}"
        )
        
        try:
            vlm_resp = query_vlm(images=frames, prompt=prompt)
            parsed = vlm_resp.get("parsed", {})
            
            is_superdom = parsed.get("is_superdom_visible", False)
            
            if is_superdom:
                score += 10
                vlm_passed = True
                feedback_parts.append("VLM confirmed SuperDOM UI (+10)")
            else:
                feedback_parts.append("VLM did NOT observe SuperDOM UI (0)")
                
            vlm_reason = parsed.get("reasoning", "No reasoning provided")
            
        except Exception as e:
            logger.error(f"VLM check failed: {e}")
            feedback_parts.append("VLM check failed (0)")

    # Final Evaluation
    # Must have saved workspace, created SuperDOM, and set SPY to pass basic threshold
    # Pass threshold: 70
    passed = score >= 70

    return {
        "passed": passed,
        "score": score,
        "feedback": " | ".join(feedback_parts),
        "details": {
            "vlm_reason": vlm_reason,
            "raw_score": score
        }
    }