#!/usr/bin/env python3
"""
Verifier for Pareto Revenue Analysis task.

Scoring (100 points total):
1. File saved & created during task: 15 pts
2. Page named "Pareto Analysis": 10 pts
3. Measure 'Rep_Revenue' created: 15 pts
4. Measure 'Cumulative_Pct' created: 25 pts (Hardest part)
5. Combo Chart (Line & Clustered Column) present: 20 pts
6. Card visual present: 15 pts

Pass threshold: 70 points
"""

import json
import os
import tempfile
import logging

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger(__name__)

def verify_pareto_analysis(traj, env_info, task_info):
    """
    Verify the Power BI Pareto analysis task.
    Reads the JSON result generated by the PowerShell export script.
    """
    
    # 1. Retrieve result from environment
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Verification failed: copy_from_env capability not available."
        }

    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    temp_file.close()
    
    try:
        # The export script saves to C:\Users\Docker\Desktop\pareto_result.json
        copy_from_env("C:/Users/Docker/Desktop/pareto_result.json", temp_file.name)
        
        with open(temp_file.name, 'r', encoding='utf-8-sig') as f:
            result = json.load(f)
    except Exception as e:
        logger.error(f"Failed to retrieve or parse result file: {e}")
        return {
            "passed": False, 
            "score": 0, 
            "feedback": "Could not verify task: Result file missing or invalid. Did you save the report correctly?"
        }
    finally:
        if os.path.exists(temp_file.name):
            try:
                os.unlink(temp_file.name)
            except:
                pass

    # 2. Evaluate Criteria
    score = 0
    feedback = []
    
    # Criterion 1: File Existence (15 pts)
    if result.get('file_exists') and result.get('file_created_during_task'):
        score += 15
        feedback.append("✅ Report saved successfully.")
    elif result.get('file_exists'):
        score += 5
        feedback.append("⚠️ Report exists but timestamp verification failed (modified before task start?).")
    else:
        feedback.append("❌ Report file 'Pareto_Analysis.pbix' not found on Desktop.")
        # If file missing, fail immediately
        return {"passed": False, "score": 0, "feedback": " | ".join(feedback)}

    # Criterion 2: Page Name (10 pts)
    if result.get('page_correct'):
        score += 10
        feedback.append("✅ Page renamed to 'Pareto Analysis'.")
    else:
        feedback.append("❌ Page name 'Pareto Analysis' not found.")

    # Criterion 3: Measure 'Rep_Revenue' (15 pts)
    if result.get('has_rep_revenue'):
        score += 15
        feedback.append("✅ DAX measure 'Rep_Revenue' found.")
    else:
        feedback.append("❌ DAX measure 'Rep_Revenue' missing.")

    # Criterion 4: Measure 'Cumulative_Pct' (25 pts)
    if result.get('has_cumulative_pct'):
        score += 25
        feedback.append("✅ DAX measure 'Cumulative_Pct' found.")
    else:
        feedback.append("❌ DAX measure 'Cumulative_Pct' missing.")

    # Criterion 5: Combo Chart Visual (20 pts)
    if result.get('has_combo_chart'):
        score += 20
        feedback.append("✅ Line and Clustered Column Chart visual found.")
    else:
        feedback.append("❌ Line and Clustered Column Chart visual missing.")

    # Criterion 6: Card Visual (15 pts)
    if result.get('has_card'):
        score += 15
        feedback.append("✅ Card visual found.")
    else:
        feedback.append("❌ Card visual missing.")

    # 3. Final Result
    passed = score >= 70
    feedback_str = " | ".join(feedback)
    
    return {
        "passed": passed,
        "score": score,
        "feedback": feedback_str,
        "details": result
    }