#!/usr/bin/env python3
"""
Verifier for Smart Home Telemetry ETL task.

Scoring (100 points total):
1. PBIX File Saved (15 pts) - File exists and is a valid archive
2. Data Flattening (40 pts) - "device_id", "timestamp", "temperature" columns present in model
3. Visual Creation (30 pts) - 'lineChart' visual type present in report layout
4. Anti-Gaming (15 pts) - File created during task window, reasonable size

Pass threshold: 70 points
"""

import json
import os
import tempfile
import logging
import shutil

logger = logging.getLogger(__name__)

def verify_smart_home_telemetry_etl(traj, env_info, task_info):
    """
    Verify the Power BI ETL task by inspecting the extracted PBIX metadata.
    """
    
    # 1. Setup access to file from container
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "System error: Copy function not available"}

    temp_dir = tempfile.mkdtemp()
    result_json_path = os.path.join(temp_dir, "task_result.json")
    
    try:
        # Copy result JSON generated by the powershell script
        # Windows path in container: C:\Windows\Temp\task_result.json
        # Docker/Container path maps to: /tmp/task_result.json or similar depending on implementation
        # The powershell script saved to $env:TEMP\task_result.json
        # We try standard locations.
        try:
            copy_from_env("C:/Windows/Temp/task_result.json", result_json_path)
        except:
            # Fallback for some env configs
            copy_from_env("/tmp/task_result.json", result_json_path)
            
        with open(result_json_path, 'r') as f:
            result = json.load(f)
            
    except Exception as e:
        shutil.rmtree(temp_dir)
        return {"passed": False, "score": 0, "feedback": f"Failed to retrieve task results: {str(e)}"}

    # 2. Evaluate Criteria
    score = 0
    feedback_parts = []
    
    # Criterion 1: File Existence (15 pts)
    output_exists = result.get('output_exists', False)
    file_created = result.get('file_created_during_task', False)
    file_size = result.get('output_size_bytes', 0)
    
    if output_exists:
        if file_size > 5000: # Empty PBIX is ~3-4KB, valid one usually >10KB
            score += 15
            feedback_parts.append("Report file saved successfully.")
        else:
            score += 5
            feedback_parts.append("Report file saved but seems empty.")
    else:
        feedback_parts.append("Report file not found.")
        # Critical failure
        shutil.rmtree(temp_dir)
        return {"passed": False, "score": 0, "feedback": "Report file not found."}

    # Criterion 2: Data Flattening (40 pts)
    # Check for column names in the model
    columns = [c.lower() for c in result.get('columns_found', [])]
    required_cols = ["device_id", "timestamp", "temperature"]
    found_cols = [c for c in required_cols if any(c in col for col in columns)]
    
    if len(found_cols) == 3:
        score += 40
        feedback_parts.append("Data flattening verified: All required columns found.")
    elif len(found_cols) > 0:
        partial = int(40 * (len(found_cols) / 3))
        score += partial
        feedback_parts.append(f"Partial data flattening: Found {found_cols}, missing others.")
    else:
        feedback_parts.append("Data flattening failed: Required columns not found in model.")

    # Criterion 3: Visual Creation (30 pts)
    visuals = result.get('visuals_found', [])
    # Look for lineChart
    has_line_chart = any('lineChart' in v for v in visuals)
    
    if has_line_chart:
        score += 30
        feedback_parts.append("Line Chart visual confirmed.")
    else:
        # Check for alternative valid charts (areaChart, etc)
        if any(t in str(visuals) for t in ['areaChart', 'lineClusteredColumnComboChart']):
             score += 15
             feedback_parts.append("Alternative time-series chart found (partial credit).")
        else:
             feedback_parts.append("No Line Chart found.")

    # Criterion 4: Anti-Gaming (15 pts)
    if file_created:
        score += 15
    else:
        feedback_parts.append("File timestamp indicates it wasn't created during this session.")

    # Clean up
    shutil.rmtree(temp_dir)

    passed = score >= 70
    return {
        "passed": passed,
        "score": score,
        "feedback": " ".join(feedback_parts)
    }