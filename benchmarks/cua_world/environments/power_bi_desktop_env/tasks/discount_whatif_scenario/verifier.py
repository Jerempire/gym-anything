#!/usr/bin/env python3
"""
Verifier for discount_whatif_scenario task.

Scoring (100 points total):
- File saved (15 pts): Discount_Scenario.pbix exists and created during task.
- Page Name (10 pts): "Scenario Analysis" page exists.
- Visuals (40 pts): Slicer (15), Card (10), Clustered Column Chart (15).
- Measures & Parameters (35 pts): Projected_Revenue (15), Baseline_Revenue (10), Discount_Rate table (10).

Pass threshold: 70 points
"""

import json
import os
import tempfile
import logging

logger = logging.getLogger(__name__)

def verify_discount_whatif_scenario(traj, env_info, task_info):
    """Verify the Power BI what-if scenario task."""

    # 1. Setup access to file from Windows VM
    copy_from_env = env_info.get('copy_from_env')
    if not copy_from_env:
        return {"passed": False, "score": 0, "feedback": "copy_from_env not available"}

    temp_file = tempfile.NamedTemporaryFile(delete=False, suffix='.json')
    temp_file.close()
    
    try:
        # Copy the JSON result generated by the PowerShell script
        copy_from_env("C:/Users/Docker/Desktop/whatif_result.json", temp_file.name)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Could not retrieve result file: {e}"}

    try:
        with open(temp_file.name, 'r', encoding='utf-8-sig', errors='replace') as f:
            result = json.load(f)
    except Exception as e:
        return {"passed": False, "score": 0, "feedback": f"Failed to parse result JSON: {e}"}
    finally:
        if os.path.exists(temp_file.name):
            os.unlink(temp_file.name)

    score = 0
    feedback_parts = []
    details = {}

    # --- Criterion 1: File Saved & Anti-Gaming (15 pts) ---
    file_exists = result.get('file_exists', False)
    created_fresh = result.get('file_created_after_start', False)
    
    if file_exists and created_fresh:
        score += 15
        feedback_parts.append("File saved successfully")
    elif file_exists:
        score += 5
        feedback_parts.append("File exists but timestamp check failed (anti-gaming warning)")
    else:
        feedback_parts.append("Discount_Scenario.pbix not found")
        # Critical failure, but we continue checking to give feedback if they did partial work
        # (Though without a file, other checks will likely fail too)

    # --- Criterion 2: Page Name (10 pts) ---
    page_names = result.get('page_names', [])
    # Case insensitive check
    has_scenario_page = any("scenario analysis" in p.lower() for p in page_names)
    
    if has_scenario_page:
        score += 10
        feedback_parts.append("Page 'Scenario Analysis' found")
    else:
        feedback_parts.append(f"Page 'Scenario Analysis' not found (Found: {page_names})")

    # --- Criterion 3: Visuals (40 pts) ---
    visual_types = result.get('visual_types', [])
    layout_dump = result.get('full_layout_search', '')
    
    # Slicer (15)
    has_slicer = "slicer" in visual_types or "slicer" in layout_dump.lower()
    if has_slicer:
        score += 15
        feedback_parts.append("Slicer visual found")
    else:
        feedback_parts.append("Slicer visual missing")

    # Card (10)
    has_card = "card" in visual_types or "card" in layout_dump.lower()
    if has_card:
        score += 10
        feedback_parts.append("Card visual found")
    else:
        feedback_parts.append("Card visual missing")

    # Clustered Column Chart (15)
    has_chart = ("clusteredColumnChart" in visual_types or 
                 "clusteredcolumn" in layout_dump.lower() or 
                 "columnchart" in layout_dump.lower())
    if has_chart:
        score += 15
        feedback_parts.append("Column Chart visual found")
    else:
        feedback_parts.append("Clustered Column Chart missing")

    # --- Criterion 4: Data Model / Measures (35 pts) ---
    model_terms = result.get('model_text_found', [])
    
    # Projected_Revenue (15)
    if "Projected_Revenue" in model_terms:
        score += 15
        feedback_parts.append("Projected_Revenue measure found")
    else:
        feedback_parts.append("Projected_Revenue measure missing in data model")

    # Baseline_Revenue (10)
    if "Baseline_Revenue" in model_terms:
        score += 10
        feedback_parts.append("Baseline_Revenue measure found")
    else:
        feedback_parts.append("Baseline_Revenue measure missing in data model")

    # Discount_Rate Parameter (10)
    # We check for "Discount_Rate" (table name) or "Discount Rate Value" (measure created by parameter)
    if "Discount_Rate" in model_terms or "Discount Rate Value" in model_terms:
        score += 10
        feedback_parts.append("What-if Parameter found")
    else:
        feedback_parts.append("What-if Parameter (Discount_Rate) not found")

    # Final tally
    passed = score >= 70
    
    return {
        "passed": passed,
        "score": score,
        "feedback": "; ".join(feedback_parts),
        "details": {
            "page_names": page_names,
            "visuals_found": visual_types,
            "model_terms": model_terms
        }
    }